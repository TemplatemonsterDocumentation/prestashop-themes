<h2 class="item6">Política de cookies</h2>
<p>Las cookies son pequeños archivos de texto que los sitios web colocan en los ordenadores de los visitantes. Normalmente se utilizan para identificar a ese visitante en particular y proporcionarles una mejor experiencia. Las cookies son una parte crucial de muchas funciones del sitio web que damos por sentado.</p>
<p>Por ejemplo, por lo general, son las cookies que permiten a las tiendas en línea recordar qué elementos que tiene en su carrito de compras. También se utilizan para mantenerlo conectado a un sitio web o para proporcionar valiosas estadísticas de uso e información a los propietarios de sitios web.</p>
<p>Si ejecuta un sitio web, casi seguro que utiliza cookies. Su función más probable es monitorear el número de visitantes y comportamientos a través de herramientas como Google Analytics. También pueden utilizarse para mostrar anuncios relevantes a los visitantes o, si vende online, para alimentar partes claves de su sistema de compras en línea.</p>
<p>26 de mayo de 2012 - El ICO lanzó un cambio a la aplicación, "consentimiento implícito" ahora está permitido, siempre y cuando se aclare dónde está su política de privacidad/cookie.</p>
