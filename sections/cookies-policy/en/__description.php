<h2 class="item6">Cookies Policy</h2>
<p>Cookies are small text files which websites place on visitors' computers. They're typically used to identify that particular visitor and provide them with a better experience. Cookies are a crucial part of many website functions which we take for granted.</p>
<p>For instance, it's usually cookies which allow online shops to remember what items you have in your shopping trolley. They're also used to keep you logged in to a website, or to provide valuable usage statistics and information to website owners.</p>
<p>If you run a website, it almost certainly uses cookies. Their most likely function is to monitor visitor numbers and behaviours through tools like Google Analytics. They may also be used to display relevant adverts to visitors, or - if you sell online - to power key parts of your online shopping system.</p>
<p>26th May 2012 - The ICO released a change to the enforcement, "implied consent" is now allowed as long as you make it clear where your privacy/cookie policy is.</p>
