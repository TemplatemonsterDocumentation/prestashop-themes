<h2 class="item6">Cookies Policy</h2>
<p>I cookie sono piccoli file di testo che i siti web posizionano sui computer dei visitatori. Di solito vengono utilizzati per identificare quel visitatore particolare e fornire loro una migliore esperienza. I cookie sono una parte fondamentale di molte funzioni del sito web che prendiamo per scontata.</p>
<p>Ad esempio, di solito sono i cookie che consentono ai negozi online di ricordare quali elementi si trovano nel carrello della spesa. Sono anche utilizzati per mantenere il login a un sito web o per fornire statistiche utili e informazioni utili ai proprietari di siti web.</p>
<p>Se si esegue un sito web, utilizza quasi certamente i cookie. La loro funzione più probabile è monitorare i numeri ei comportamenti dei visitatori tramite strumenti come Google Analytics. Può anche essere utilizzato per visualizzare annunci pertinenti ai visitatori o, se vendi online, per alimentare parti chiave del tuo sistema di acquisto online.</p>
<p>26 maggio 2012 - L'ICO ha rilasciato una modifica all'applicazione, ora è consentito il "consenso implicito" a condizione che sia chiaro dove sia la tua privacy/cookie policy.</p>
