<h2 class="item6">Politique des Cookies</h2>
<p>Les cookies sont de petits morceaux de texte que les sites Web placent sur les ordinateurs des visiteurs. Ils sont généralement utilisés pour identifier ce visiteur particulier et le fournir une meilleure expérience. Les cookies sont une partie essentielle de nombreuses fonctions de site Web que nous considérons comme acquises.</p>
<p>Par exemple, ce sont généralement des cookies qui permettnt de rappeler des articles que vous avez dans votre panier. Ils sont également utilisés pour vous garder connecté au site Web, ou de fournir des statistiques et des informations d'utilisation précieuses aux propriétaires de sites.</p>
<p>Si vous lancez un site Web, il utilise presque certainement des cookies. Leur fonction la plus importante est de surveiller le nombre de visiteurs et les comportements via des outils comme Google Analytics. Ils peuvent être utilisés aussi pour afficher des publicités pertinentes aux visiteurs, ou - si vous vendez en ligne - pour alimenter des éléments clés de votre système d'achats en ligne.</p>
<p>26 mai 2012 - L'ICO a publié un changement à l'application, "consentement implicite" est maintenant autorisé tant que vous précisez où votre politique de confidentialité/cookie est.</p>
