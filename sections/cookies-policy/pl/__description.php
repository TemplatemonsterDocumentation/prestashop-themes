<h2 class="item6"> Zasady Dotyczące Plików Cookie</h2>
<p>Pliki cookie to niewielkie pliki tekstowe zapisywane przez witryny internetowe na komputerach gości. Zazwyczaj
    wykorzystywane są dla potwierdzenia tożsamości konkretnego gościa oraz zapewnienia użytkownikowi lepszej całości
    wrażeń. Pliki cookie mają podstawowe znaczenie dla wsparcja wielu funkcji witryn internetowych które my uważamy za
    oczywiste.</p>
<p>Na przykład, zazwyczaj to pliki cookie pozwalają sklepom internetowym pamiętać jakie artykuły masz w swoim wozku na
    zakupy. Także wykorzystywane są po to, żebyś zostawał zalogowany na portalu internetowym lub dla dostarczania
    właścicielom portalu cennej informacji oraz danych statystycznych o użytkownikach.</p>
<p>Jeżeli prowadzisz witrynę internetową, to z całą pewnością wykorzystuje ona pliki cookie. Najczęściej wykorzystywaną
    funkcją tych plików jest monitorowanie liczby gości oraz zachowania użytkowników za pomocą specjalnych narzędzi, na
    przykład Google Analytics. Mogą one również być wykorzystywane dla wyświetlania istotnych reklam dla gości
    albo—jeżeli sprzedajesz online—dla zasilania kluczowych części twojego systemu zakupów online.</p>
<p>26 maja 2012 roku - ICO opublikował zmianę procedury wdrażania, na skutek czego aktualnie dozwolone jest stosowanie
    "domniemanej zgody" o ile jasno określisz swoją politykę prywatności/politykę w stosunku do plików cookie.</p>
