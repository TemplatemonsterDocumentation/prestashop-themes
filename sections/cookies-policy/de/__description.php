<h2 class="item6">Cookie-Richtlinien</h2>
<p>Cookie-Dateien sind kleine Textdateien, die von einer Website, die Sie besuchen, auf Ihrem Computer gespeichert werden. Normalerweise werden sie benutzt, um einen bestimmten Besucher zu identifizieren und ihm eine bessere Nutzererfahrung zu gewährleisten. Cookies sind ein wichtiger Teil vieler Website-Funktionen.</p>
<p>Dank der Cookie-Dateien werden normalerweise Artikel in dem Online-Shop gespeichert, die sich in Ihrem Warenkorb befinden. Sie werden auch benutzt, damit Sie in dem Webseitensystem sein können. Darüber hinaus stellen sie wichtige Nutzungsstatistiken und Informationen den Webseiteninhabern zur Verfügung.</p>
<p>Wenn Sie eine Website haben, verwendet sie höchstwahrscheinlich Cookie-Dateien. Sie verfolgen die Anzahl der Besucher und ihr Verhalten mit Hilfe von den Werkzeugen wie Google Analytics. Sie können auch verwendet werden, um relevante Anzeigen den Besuchern darzustellen. Wenn Sie Produkte online verkaufen, stellen sie die Schlüsselelemente des Online-Shopping-System sicher.</p>
<p>Am 26. Mai 2012 ICO hat die Änderungen zu den Rechtsvorschriften über Cookies veröffentlicht:  seitdem «konkludente Zustimmung» erlaubt wurde, müssen Sie unbedingt zeigen, wo sich auf der Webseite Ihre Datenschutzrichtlinie und Cookies befinden.</p>
