<h3>Uso del servidor local</h3>
<p>Puede ejecutar PrestaShop localmente en su ordenador usando el servidor local. Para crear un Servidor de alojamiento local, utilice
     las aplicaciones localshost, como WAMP, AppServ, XAMP, etc. Cualquiera de ellas puede instalarse fácilmente como cualquier otra
     y se utiliza para ejecutar PrestaShop.</p>
