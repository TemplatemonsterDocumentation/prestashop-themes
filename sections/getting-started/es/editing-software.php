<h3>Edición de software</h3>
<p>Para sentirse cómodo trabajando con su plantilla de PrestaShop, le recomendamos que descargue todas las aplicaciones necesarias. Puede ver la lista de software necesario en la página de vista previa de la plantilla. Los requisitos pueden variar dependiendo de la plantilla. Vamos a decirle en general lo que se necesita:</p>
<ol class="index-list">
    <li>
        <p>En primer lugar, necesita la aplicación para extraer el archivo de código fuente protegido por contraseña _ #########. zip. Puede utilizar WinZip 9+ (Windows) y Stuffit Expander 10+ (Mac).</p>
    </li>
    <li>
        <p>También puede necesitar la aplicación Adobe Photoshop. Se utiliza para editar archivos fuente .PSD y es necesario si desea editar diseño gráfico de plantilla e imágenes.</p>
    </li>
    <li>
        <p>Para editar los archivos de código fuente de la plantilla, necesita algún editor de código como Adobe Dreamweaver, Notepad++, Sublime Text, etc.</p>
    </li>
    <li>
        <p>Para cargar archivos en el Servidor de Alojamiento, es posible que necesite un Administrador de FTP como Total Commander, FileZilla, CuteFTP, etc.</p>
    </li>
</ol>
