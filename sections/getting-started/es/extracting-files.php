<h3>Extrayendo archivos</h3>
<p>Una vez descargado el paquete del motor de PrestaShop, necesitará extraer sus archivos y carpetas del archivo.</p>
<p>Esto se puede hacer con cualquier Administrador de archivos como <strong>WinZip (Windows)</strong>, <strong>StuffitExpander (Mac)</strong>, etc.</p>
<p>Compruebe los siguientes tutoriales sobre cómo extraer archivos de los archivos con
    <a target="_blank" href="/help/extract-archived-files-windows-os.html">WinZip (Windows)</a> y <a target="_blank" href="/help/extract-archived-files-mac-os.html">StuffitExpander (Mac)</a>.
</p>
<p class="alert alert-warning">Tenga en cuenta que si su panel de control de hosting tiene la opción de <strong>'Extraer'</strong> archivos, puede utilizarlo para extraer los archivos directamente en su servidor después de cargar el archivo .zip.
</p>
