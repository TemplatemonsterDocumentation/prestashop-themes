<h3>Requisitos del servidor</h3>
<p>Asegúrese de que su servidor de alojamiento cumpla con los requisitos de PrestaShop.:</p>
<ul class="marked-list">
    <li><strong>Sistema:</strong> Unix, Linux o Windows. Unix es muy recomendable.</li>
    <li><strong>Servidor web:</strong> Servidor Web Apache 1.3 o posterior, servidor Web IIS de Microsoft 6.0 o posterior y nginx 1.0 o posterior.
    </li>
    <li><strong>PHP 5.4</strong> o posterior.</li>
    <li><strong>MySQL 5.5</strong> o posterior.</li>
    <li>Por lo menos 128 Mb de RAM en su Servidor (64 Mb es cómodo, cuanto más mejor).
    </li>
</ul>
<p class="alert alert-info">PrestaShop funcionará con PHP 5.1 o posterior, pero las versiones inferiores a 5.4 tienen errores que pueden impedir que algunas funciones, no funcionen como se esperaba (como la zona horaria de fecha no válida).</p>