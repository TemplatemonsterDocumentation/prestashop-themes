<h3>Requisitos del servidor</h3>

<p>Por favor, aseg&uacute;rese de que su servidor de alojamiento cumple con los
    requisitos de PrestaShop:</p>

<ul class="marked-list">
    <li><strong>Sistema:</strong> Unix, Linux o Windows. Se recomienda Unix.</li>
    <li><strong>Servidor web:</strong> Apache 1.3 o superior, IIS 6.0 de Microsoft o
        superior, y nginx 1.0 o superior.
    </li>
    <li><strong>PHP 5.2</strong> o superior.
    </li>
    <li><strong>MySQL 5.0</strong> o superior.</li>
    <li>Al menos 32 Mb de memoria RAM en el servidor (64 Mb es mejor, en realidad,
        cuanto m&aacute;s mejor).
    </li>
</ul>
<p class="alert alert-info"> PrestaShop funcionar&aacute; con PHP 5.1 o superior, pero las versiones anteriores a la
    5.2 tienen errores que podr&iacute;an hacer que algunas caracter&iacute;sitcas no funcionen correctamente (como
    la validaci&oacute;n de fechas).</p>
