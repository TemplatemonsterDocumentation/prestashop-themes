<h3>Descargando</h3>
<p>Para instalar PrestaShop, debe descargar el paquete del motor de PrestaShop desde
     sitio web oficial en <a target="_blank" href="http://www.prestashop.com/en/downloads">www.prestashop.com/en/downloads</a>
</p>
<p>Simplemente inserta tu nombre y correo electrónico en la entrada del cuadro de descarga y haz clic en el botón <strong>'Download'</strong> . </p>
<p class="alert alert-warning">Asegúrese de que la versión del motor de PrestaShop coincida con los requisitos de la plantilla. Todas
     versiones de PrestaShop están disponibles en <a class="spec_link" target="_blank" href="http://sourceforge.net/projects/prestashop/files/prestashop%20v1/">SourceForge</a>.
</p>