<h3>Скачивание</h3>
<p>Для того чтобы установить PrestaShop, нужно скачать пакет файлов движка PrestaShop с официального сайта <a target="_blank" href="http://www.prestashop.com/en/downloads">www.prestashop.com/en/downloads</a>
</p>
<p>Просто введите ваше имя и адрес электронной почты в окно скачивания и нажмите на кнопку <strong>'Скачать'</strong> (Download). </p>
<p class="alert alert-warning">Убедитесь, что версия скачиваемого движка PrestaShop соответствует требованиям шаблона. Все версии PrestaShop доступны на <a class="spec_link" target="_blank" href="http://sourceforge.net/projects/prestashop/files/prestashop%20v1/">SourceForge</a>.
</p>
