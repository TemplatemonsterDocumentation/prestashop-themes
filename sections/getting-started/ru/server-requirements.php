<h3>Требования к серверу</h3>

<p>Пожалуйста, убедитесь, что Ваш хостинг сервер соответствует требованиям
    PrestaShop:</p>
<ul class="marked-list">
    <li><strong>Система:</strong> Unix, Linux или Windows. Рекомендуется Unix.</li>
    <li><strong>Веб-сервер:</strong> Apache Web server 1.3 или выше, Microsoft's IIS Web
        server 6.0 или выше, и nginx 1.0 или выше.
    </li>
    <li><strong>PHP 5.2</strong> или выше.</li>
    <li><strong>MySQL 5.0</strong> или выше.</li>
    <li>По крайней мере 32 Mb оперативной памяти на сервере (рекомендуется 64 Mb, чем
        больше, тем лучше).
    </li>
</ul>
<p class="alert alert-info"> PrestaShop работает с PHP 5.1 или выше, однако версии ниже 5.2 содержат ошибки, которые не
    позволяют некоторым функциям работать, как следует (например, неправильная дата часовых поясов).</p>
