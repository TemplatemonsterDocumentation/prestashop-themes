<h3>Использование локального сервера</h3>
<p>Вы можете запустить PrestaShop на вашем компьютере, используя локальный сервер. Для того чтобы создать локальный сервер, используйте программное обеспечение для создания локального сервера, такое как WAMP, AppServ, XAMP и т.д. Любая из этих программ может быть легко установлена, как и любая другая программа, и использоваться для запуска PrestaShop.</p>
