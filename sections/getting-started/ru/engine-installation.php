<h4> Системная совместимость </h4>
<p>Проверьте настройки PHP и привилегии для папок. Если есть какие-либо ошибки, обратитесь к вашему хостинг-провайдеру для дальнейшей помощи.</p>
<p>Если всё верно, нажмите на кнопку <strong>'Далее'</strong> (Next).</p>
<figure class="img-polaroid">
    <img src="img/engine-install-03.png" alt="">
</figure>
<h4>Настройка магазина </h4>
<p> На этом этапе Вы должны ввести настройки магазина, такие как название магазина, активность, страна, часовой пояс и т. д.</p>
<p> Все эти настройки могут быть изменены позже в админ панели PrestaShop.</p>
<figure class="img-polaroid">
    <img src="img/engine-install-04.png" alt="">
</figure>
<h4>Настройки системы</h4>
<p>Если у Вас уже есть база данных MySQL, введите детали доступа к вашей базе данных в соответствующие поля ниже.</p>
<p> В противном случае, Вы можете получить детали доступа к вашей базе данных из панели управления хостингом или связавшись с вашим хостинг-провайдером.</p>
<p>Закончив внесение изменений, нажмите на кнопку <strong>'Далее'</strong> (Next)..</p>
<figure class="img-polaroid">
    <img src="img/engine-install-05.png" alt="">
</figure>
<h4>Установка завершена</h4>
<p> Это заключительный этап установки.</p>
<p class="alert alert-danger"> По соображениям безопасности вам необходимо удалить папку <strong>/install/</strong> на вашем сервере.
</p>
<figure class="img-polaroid">
    <img src="img/engine-install-06.png" alt="">
</figure>
<p class="alert alert-info">Начиная с версии 1.5.4, PrestaShop сам переименовывает папку админа для Вас: она будет назваться "admin" + 4 случайные цифры, например, "admin8814." Переименование происходит, когда Вы впервые обращаетесь к папке "admin": PrestaShop переименовывает её, затем получает доступ к переименованной папке.<br/><br/>
    Войдите в вашу админ панель PrestaShop, получив доступ к переименованной папке "admin" и начните заполнение каталога данными.
</p>
