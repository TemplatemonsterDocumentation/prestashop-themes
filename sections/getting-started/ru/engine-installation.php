<h3>Установка движка PrestaShop</h3>
<p class="alert alert-warning">Убедитесь, что ваша версия движка PrestaShop соответствует версии, указанной на вкладке "Технические данные" как "Движок PrestaShop: 1.X.X.X".<br/>
    Также, убедитесь, что ваш префикс базы данных - это <strong>ps_</strong>. Устанавливайте Демо-контент, только если версия вашего движка PrestaShop соответствует указанной.</p>
<p>Завершив загрузку файлов, Вы готовы начать установку движка PrestaShop. Откройте ваш браузер и введите 'доменное имя/путь к вашей папке PrestaShop' в вашу адресную строку, затем, перезагрузите текущую страницу. Вы увидите экран приветствия. Следуйте инструкциям, приведённым ниже, для того чтобы установить PrestaShop.</p>
<h4>Помощник установки (Выберите ваш язык)</h4>
<h6>Выберите язык установки.</h6>
<p class="alert alert-warning">Обратите внимание: настройка языка установки не повлияет на язык магазина. По умолчанию, PrestaShop предоставляется на английском. Вы можете установить дополнительные пакеты локализации, для того чтобы изменить язык магазина.</p>
<p>Закончив внесение изменений, нажмите на кнопку <strong>'Далее'</strong> (Next).</p>
