<h3>Estrazione dei File</h3>
<p>Una volta scaricato il pacchetto motore PrestaShop, dovrai estrarre i file e le cartelle dall'archivio.</p>
<p>Questo può essere fatto con qualsiasi gestore di archivio come <strong>WinZip (Windows)</strong>, <strong>StuffitExpander (Mac)</strong>, ecc.</p>
<p>Controlli i seguenti tutorial su come estrarre i file dagli archivi
    <a target="_blank" href="/help/extract-archived-files-windows-os.html">WinZip (Windows)</a> and <a target="_blank" href="/help/extract-archived-files-mac-os.html">StuffitExpander (Mac)</a>.
</p>
<p class="alert alert-warning">Nota: se il tuo pannello di controllo ospita l'opzione <strong>'Extract'</strong> file, è possibile utilizzarlo per estrarre i file direttamente sul server dopo il file .zip è caricato.
</p>
