<h3>Utilizzo del server locale</h3>
<p>È possibile eseguire PrestaShop localmente sul computer utilizzando il server locale. Per creare un server di hosting locale, utilizza
    le applicazioni localhost, come WAMP, AppServ, XAMP, ecc. Qualsiasi di questi può essere facilmente installata come qualsiasi altra
    programma e utilizzato per eseguire PrestaShop.</p>
