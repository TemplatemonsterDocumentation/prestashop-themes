<h3>Modifica del software</h3>
<p>Per sentirti a proprio agio con il tuo modello PrestaShop, ti consigliamo di scaricare tutte le applicazioni necessarie. È possibile visualizzare l'elenco dei software richiesti nella pagina di anteprima del modello. I requisiti possono variare a seconda del modello. Vi racconteremo in generale ciò che è necessario:</p>
<ol class="index-list">
    <li>
        <p>Prima di tutto, hai bisogno di un'applicazione per estrarre la password protetta sources_#########.zip archivio. Puoi usare WinZip 9+ (Windows) e Stuffit Expander 10+ (Mac).</p>
    </li>
    <li>
        <p>Potrebbe anche essere necessaria un'applicazione Adobe Photoshop. È usata per modificare i file sorgente di PSP ed è necessario se si desidera modificare la grafica e le immagini di progettazione dei modelli.</p>
    </li>
    <li>
        <p>Per modificare i file di codice sorgente del modello, è necessario un editor di codice come Adobe Dreamweaver, Notepad ++, Sublime Text, ecc.</p>
    </li>
    <li>
        <p>Per caricare i file sul server di hosting, potrebbe essere necessario un gestore di FTP come Total Commander, FileZilla, CuteFTP, ecc.</p>
    </li>
</ol>
