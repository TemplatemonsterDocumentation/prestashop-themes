<h3>Requisiti del Server</h3>

<p>Verifica che il tuo server risponda ai requisiti minimi di PrestaShop:</p>
<ul class="marked-list">
    <li><strong>Sistema:</strong> Unix, Linux o Windows. Si consiglia vivamente Unix.
    </li>
    <li><strong>Web server:</strong> Apache Web server 1.3 o successivi, Microsoft IIS
        Web server 6.0 o successivi, e nginx 1.0 o successivi.
    </li>
    <li><strong>PHP 5.2</strong> o successivi.
    </li>
    <li><strong>MySQL 5.0</strong> o successivi.</li>
    <li>Almeno 32 Mb di RAM sul server (64 Mb è vanno bene, ma più ce n'è, meglio è).
    </li>
</ul>
<p class="alert alert-info"> PrestaShop funziona con PHP 5.1 o successivi, ma le versioni inferiori alla 5.2 hanno
    dei bug che potrebbero impedire il corretto funzionamento di alcune funzioni (ad esempio problemi di orari e
    fusi orari).</p>
