<h3>Requisiti del server</h3>
<p>Assicurati che il server di hosting soddisfi i requisiti di PrestaShop:</p>
<ul class="marked-list">
    <li><strong>Systema:</strong> Unix, Linux o Windows. Unix è altamente raccomandato.</li>
    <li><strong>Web server:</strong> Apache Web server 1.3 o versioni successive, Microsoft IIS Web server 6.0 o
        successivo e nginx 1.0 o successivo.
    </li>
    <li><strong>PHP 5.4</strong> o successiva.</li>
    <li><strong>MySQL 5.5</strong> o successiva.</li>
    <li>Almeno 128 Mb di RAM sul tuo server (64 Mb è comodo, tanto più è meglio).
    </li>
</ul>
<p class="alert alert-info">PrestaShop funzionerà con PHP 5.1 o successivo, ma le versioni inferiori a 5.4 hanno bug che
    potrebbero impedire che alcune funzioni funzionino come previsto (come fuso orario non valido).</p>
