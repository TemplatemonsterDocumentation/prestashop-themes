<h3>Utilisation du serveur local</h3>
<p>Vous pouvez exécuter PrestaShop localement sur votre ordinateur à l'aide du serveur local. Pour créer un serveur d'hébergement local,
il faut utiliser les applications localhost, telles que WAMP, AppServ, XAMP, etc. Toutes ces applications peuvent être facilement installées et utilisées comme tout autre programme.</p>
