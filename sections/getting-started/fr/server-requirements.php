<h3>Configuration requise du serveur</h3>
<p>Assurez-vous que votre serveur d'hébergement répond aux exigences PrestaShop:</p>
<ul class="marked-list">
    <li><strong>Système:</strong> Unix, Linux ou Windows. Unix est fortement recommandé.</li>
    <li><strong>Serveur Web:</strong> serveur Web Apache 1.3 ou plus récent, serveur Web IIS de Microsoft 6.0 ou plus
        récent, et nginx 1.0 ou plus récent.
    </li>
    <li><strong>PHP 5.4</strong> ou version ultérieure.</li>
    <li><strong>MySQL 5.5</strong> ou version ultérieure.</li>
    <li>Au moins 128Mb de RAM sur votre serveur (64Mb suffit, plus c'est meilleur).
    </li>
</ul>
<p class="alert alert-info">PrestaShop travaillera avec PHP 5.1 ou version ultérieure, mais les versions inférieures à
    5,4 ont des bugs qui pourrait empêcher certaines fonctions de fonctionner comme prévu (comme date erronée du fuseau
    horaire).</p>
