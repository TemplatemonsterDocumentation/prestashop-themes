<h3>Besoins côté serveur</h3>

<p>Veuillez vous assurer que votre serveur d'hébergement est conforme aux besoins de
    PrestaShop:</p>
<ul class="marked-list">
    <li><strong>Système:</strong> Unix, Linux ou Windows. Unix est fortement recommandé.
    </li>
    <li><strong>Serveur web:</strong> Serveur Web Apache 1.3 ou plus récent, serveur Web
        Microsoft IIS 6.0 ou plus récent, et nginx 1.0 ou plus récent.
    </li>
    <li><strong>PHP 5.2</strong> ou plus récent.
    </li>
    <li><strong>MySQL 5.0</strong> ou plus récent.</li>
    <li>Au moins 32 Mo de RAM sur votre serveur (64 Mb pour plus de confort. Plus il y
        en a, mieux c'est).
    </li>
</ul>
<p class="alert alert-info"> PrestaShop fonctionnera avec PHP 5.1 ou plus récent, mais les versions antérieures à la
    5.2 présentent des bugs pouvant empêcher certaines fonctions d'agir convenablement (comme un fuseau horaire
    invalide).</p>
