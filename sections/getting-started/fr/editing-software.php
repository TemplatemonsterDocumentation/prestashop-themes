`<h3>Logiciel d'édition</h3>
<p>Pour se sentir à l'aise avec le modèle PrestaShop, nous vous recommandons de télécharger toutes les applications requises. 
On peut voir la liste des logiciels requis sur la page d'aperçu du modèle. Les exigences peuvent varier selon le modèle. 
Nous allons vous dire en général ce qu'il faut:</p>
<ol class="index-list">
    <li>
        <p>Tout d'abord, vous avez besoin d'une application pour extraire l'archive sources_#########.zip protégée par mot de passe. Vous pouvez utiliser WinZip 9+ (Windows) et Stuffit Expander 10+ (Mac).</p>
    </li>
    <li>
        <p>Il vous faudra peut-être utiliser l'application Adobe Photoshop. Il est utilisé pour modifier les fichiers source .PSD et it's necessary il est nécessaire si vous souhaitez éditer des graphiques et des images modèle.</p>
    </li>
    <li>
        <p>Pour modifier les fichiers du code source du modèle, vous aurez besoin d'un éditeur de code comme Adobe Dreamweaver, Notepad++, Sublime Text, etc.</p>
    </li>
    <li>
        <p>Pour télécharger des fichiers sur le serveur d'hébergement, vous pourriez avoir besoin d'un gestionnaire FTP comme Total Commander, FileZilla, CuteFTP, etc.</p>
    </li>
</ol>
