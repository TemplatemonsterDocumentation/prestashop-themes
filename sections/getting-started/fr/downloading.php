<h3>Téléchargement</h3>
<p>Pour installer PrestaShop, vous devez télécharger le package de moteur PrestaShop à partir du site officiel <a target="_blank" href="http://www.prestashop.com/en/downloads">www.prestashop.com/en/downloads</a>
</p>
<p>Insérez simplement votre nom et votre e-mail dans la zone de saisie et cliquez sur le bouton <strong>'Télécharger'</strong>.</p>
<p class="alert alert-warning">Assurez-vous que votre version du moteur PrestaShop correspond aux exigences du modèle. 
Toutes les versions de PrestaShop sont disponibles à <a class="spec_link" target="_blank" href="http://sourceforge.net/projects/prestashop/files/prestashop%20v1/">SourceForge</a>.
</p>