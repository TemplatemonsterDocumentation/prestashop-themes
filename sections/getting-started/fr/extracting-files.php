<h3>Extraction des fichiers</h3>
<p>Une fois le package de moteur PrestaShop est téléchargé, vous devrez extraire ses fichiers et dossiers..</p>
<p>On peut le fiare avec un gestionnaire d'archives comme <strong>WinZip (Windows)</strong>, <strong>StuffitExpander (Mac)</strong>, etc.</p>
<p>Veuillez vérifier les tutoriels suivants sur la façon d'extraire des fichiers des archives avec 
    <a target="_blank" href="/help/extract-archived-files-windows-os.html">WinZip (Windows)</a> et <a target="_blank" href="/help/extract-archived-files-mac-os.html">StuffitExpander (Mac)</a>.
</p>
<p class="alert alert-warning">Remarque: si votre panneau de contrôle d'hébergement a la possibilité de <strong>'Extraire'</strong> des fichiers, vous pouvez l'utiliser pour extraire les fichiers directement sur votre serveur après l'importation du fichier .zip.
</p>
