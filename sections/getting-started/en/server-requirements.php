<h3>Server requirements</h3>

<p>Please make sure your hosting server meets the PrestaShop requirements:</p>
<ul class="marked-list">
    <li><strong>System:</strong> Unix, Linux or Windows. Unix is highly recommended.
    </li>
    <li><strong>Web server:</strong> Apache Web server 1.3 or later, Microsoft's IIS Web
        server 6.0 or later, and nginx 1.0 or later.
    </li>
    <li><strong>PHP 5.2</strong> or later.</li>
    <li><strong>MySQL 5.0</strong> or later.</li>
    <li>At least 32 Mb of RAM on your server (64 Mb is comfortable, the more the
        better).
    </li>
</ul>
<p class="alert alert-info">PrestaShop will work with PHP 5.1 or later, but the versions lower than 5.2 have bugs which
    might prevent some functions from working as expected (like invalid date timezone).</p>
