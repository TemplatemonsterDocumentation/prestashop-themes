<h3>Przesyłanie plinów silnika PrestaShop</h3>
<p>Jeżeli wcześniej już rozpakowywałeś te pliki na swój komputer, będziesz musiał ponownie przesłać ich na swój serwer
    hostingowy.</p>
<p>Można to zrobić, korzystając z Menadżera Plinów na hostingowym panelu sterowania lub za pomocą zewnętrznych
    menadżerów (klientów) FTP, na przykład FileZilla, TotalCommander, CuteFTP, etc.</p>
<p>Możesz zapoznać się z dokładniejszym samouczkiem: <a target="_blank" href="/help/how-upload-files-server-2.html">jak
        przesyłać pliki na serwer za pomocą menadżera FTP </a> oraz <a target="_blank"
                                                                       href="/help/how-to-upload-files-to-a-server-using-cpanel-webhost-manager-whm.html">jak
        przesyłać pliki na serwer za pomocą cPanel (WebHost Manager, WHM)</a>.</p>
