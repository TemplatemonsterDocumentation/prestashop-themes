<h3>Wymagania Serwera</h3>
<p>Upewnij się, że Twój serwer hostingowy spełnia wymagania PrestaShop:</p>
<ul class="marked-list">
    <li><strong>System:</strong> Unix, Linux albo Windows. Zdecydowanie zaleca się Unix.</li>
    <li><strong>Serwer Web:</strong> Apache Web server 1.3 lub nowsza wersja, Microsoft's IIS Web server 6.0 lub nowsza
        wersja, oraz nginx 1.0 lub nowsza wersja.
    </li>
    <li><strong>PHP 5.4</strong> lub nowsza wersja.</li>
    <li><strong>MySQL 5.5</strong> lub nowsza wersja.</li>
    <li>Przynajmniej 128 Mb pamięci RAM na Twoim serwerze (wygodnie jest mieć 64 Mb; im więcej tym lepiej).</li>
</ul>
<p class="alert alert-info">PrestaShop będzie działał z PHP 5.1 lub nowszą wersją, jednakże wersje poniżej 5.4 mają
    błędy które mogą uniemożliwić korzystanie z niektórych funkcji w oczekiwany sposób i z oczekiwaną wydajnością (jak
    na przykład nieważna data lub strefa czasowa).</p>