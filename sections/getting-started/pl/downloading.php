<h3>Pobieranie</h3>
<p>Aby zainstalować PrestaShop, trzeba pobrać pakiet silnika PrestaShop ze strony oficjalnej: <a target="_blank"
                                                                                                 href="http://www.prestashop.com/en/downloads">www.prestashop.com/en/downloads</a>
</p>
<p>Po prostu wpisz swoje imię i adres poczty elektronicznej w pole wprowadzenia danych dla Pobierania i wciśnij przycisk
    <strong>'Pobierz'</strong>. </p>
<p class="alert alert-warning">Upewnij się że Twoja wersja silnika PrestaShop odpowiada wymaganiom szablonu. Wszystkie
    wersje można pobrać pod tym adresem <a class="spec_link" target="_blank"
                                           href="http://sourceforge.net/projects/prestashop/files/prestashop%20v1/">SourceForge</a>.
</p>