<h4>Zgodność (Kompatybilność) Systemowa</h4>
<p>Sprawdź ustawienia kompatybilności (zgodności) PHP oraz uprawnienia do katalogów. W wypadku jakichkolwiek błędów
    skontaktuj się z dostawcą usług hostingowych dla otrzymania dalszej pomocy.</p>
<p>Jeżeli wszystko jest w porządku, wciśnij przycisk <strong>'Dalej'</strong>.</p>
<figure class="img-polaroid">
    <img src="img/engine-install-03.png" alt="">
</figure>
<h4>Konfiguracja Sklepu</h4>
<p>Dla dokonania tego kroku trzeba przejść do ustawień sklepu, szczególnie: nazwa sklepu, działalność, kraj, strefa
    czasowa etc.</p>
<p>Wszystkie te ustawienia można zmienić później na swoim panelu administracyjnym PrestaShop.</p>
<figure class="img-polaroid">
    <img src="img/engine-install-04.png" alt="">
</figure>
<h4>Konfiguracja Systemu</h4>
<p>Jeżeli już masz zainstalowaną bazę danych MySQL, wprowadź szczegóły bazy danych do odpowiednich pól poniżej.</p>
<p>Opcjonalnie możesz pobrać szczegóły swojej bazy danych ze swojego hostingowego panelu sterowania lub zwróciwszy się
    do swojego dostawcy usług hostingowych.</p>
<p>Po zakończeniu wciśnij przycisk <strong>'Dalej'</strong>.</p>
<figure class="img-polaroid">
    <img src="img/engine-install-05.png" alt="">
</figure>
<h4>Instalowanie zakończone</h4>
<p>To jest ostatni krok instalacji.</p>
<p class="alert alert-danger">Ze względów bezpieczeństwa należy skasować katalog <strong>/install/</strong> na swoim
    serwerze hostingowym. </p>
<figure class="img-polaroid">
    <img src="img/engine-install-06.png" alt="">
</figure>
<p class="alert alert-info">Począwszy od wersji 1.5.4, PrestaShop sam dba o to, żeby zmienić nazwę folderu
    administracyjnego dla Ciebie: otrzymuję nazwę "admin" plus losowo wybrane cyfry, na przykład "admin8814." Zmiana
    nazwy następuje jak tylko sięgniesz po folder "admin" po raz pierwszy: PrestaShop zmienia jego nazwę, po czym
    poprawnie uzyskuje dostęp do folderu ze zmienioną nazwą.<br/><br/>
    Zaloguj się do swojego PrestaShop po stronie serwera, przechodząc do swojego na nowo nazwanego folderu "admin", i
    zacznij zapełniać swój katalog swoimi danymi. </p>
<h6>W przypadku wystąpienia jakichś trudności, zapoznaj się z <a target="_blank"
                                                                 href="http://doc.prestashop.com/display/PS16/Getting+Started">oficjalną
        instrukcją instalacji PrestaShop</a>.</h6>
