<h3>Instalowanie silnika PrestaShop</h3>
<p class="alert alert-warning">Upewnij się, że Twoja wersja silnika PrestaShop odpowiada wersji zaznaczonej w zakładce
    "Szczegóły Techniczne", gdzie będzie ona sprecyzowana jak "Silnik PrestaShop (Prestashop Engine): wersja
    1.X.X.X".<br/>
    Ponadto, upewnij się że twoje tablicy bazy danych mają prefiks <strong>ps_</strong>. Instaluj przykładowe dane
    wyłącznie jeżeli twój silnik PrestaShop odpowiada podanej wersji.</p>
<p>Po zakończeniu przesyłania danych możesz zacząć instalowanie silnika PrestaShop. Otwórz swoją przeglądarkę
    internetową i wpisz 'nazwę domeny internetowej/ścieżkę do swojego katalogu PrestaShop' w pasek adresu, po czym
    odśwież bieżącą stronę. Zobaczysz ekran powitalny. Postępuj zgodnie z poniższą instrukcją aby zainstalować
    PrestaShop.</p>
<h4>Kreator Instalacji (Wybierz język)</h4>
<h6>Proszę wybrać język instalacji.</h6>
<p class="alert alert-warning">Proszę zauważyć że: opcja języka wykorzystywanego w trakcie instalacji nie wpływa na
    język twojego sklepu.
    Domyślnie PrestaShop proponowany jest w języku angielskim. Możesz zainstalować dodatkowe pakiety lokalizacyjne aby
    zmienić język sklepu.</p>
<p>Po zakończeniu wciśnij przycisk <strong>'Dalej'</strong>.</p>
