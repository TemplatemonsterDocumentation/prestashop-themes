<h3>Rozpakowywanie Plików</h3>
<p>Jak tylko pakiet silnika PrestaShop zostanie pobrany, należy rozpakować jego pliki i foldery z archiwum.</p>
<p>Można to zrobić za pomocą dowolnego menadżera archiwum, na przykłąd: <strong>WinZip (Windows)</strong>, <strong>StuffitExpander
        (Mac)</strong>, etc.</p>
<p>Proszę sprawdzić następujące samouczki na temat tego jak rozpakowywać pliki z archiwów za pomocą <a target="_blank"
                                                                                                       href="/help/extract-archived-files-windows-os.html">WinZip
        (Windows)</a> oraz <a target="_blank" href="/help/extract-archived-files-mac-os.html">StuffitExpander (Mac)</a>.
</p>
<p class="alert alert-warning">Proszę zauważyć że: Jeżeli panel sterowania twojego hostingu posiada opcję <strong>'Rozpakuj'</strong>
    pliki, możesz ją wykorzystać dla rozpakowywania plików bezpośrednio na serwer po tym jak plik .zip został przesłany.
</p>
