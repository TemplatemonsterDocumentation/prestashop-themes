<h3>Korzystanie z serweru lokalnego</h3>
<p>Możesz uruchomić PrestaShop lokalnie na swoim komputerze, korzystając z serweru lokalnego. Aby stworzyć serwer
    hostingowy, skorzystaj z aplikacji localhost, jak na przykład WAMP, AppServ, XAMP, itp. Każdą z tych aplikacji można
    w sposób prosty zainstalować jako każdy inny program i wykorzystywać ją dla uruchomienia PrestaShop.</p>
