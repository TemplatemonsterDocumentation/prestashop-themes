<h3>Programy dla Edytowania</h3>
<p>Abyś mógł wygodnie pracować ze swoim szablonem PrestaShop, zalecamy pobrać wszystkie wymagane aplikacje. Możesz
    zobaczyć listę wymaganych aplikacji na stronie podglądu szablonu. Wymagania mogą się różnić, zależnie od szablonu.
    Opowiemy Ci ogólnie co jest potrzebne:</p>
<ol class="index-list">
    <li>
        <p>Najpierw, będziesz potrzebował aplikacji do rozpakowywania chronionego hasłem archiwum sources_#########.zip.
            Możesz wykorzystać WinZip 9+ (Windows) albo Stuffit Expander 10+ (Mac).</p>
    </li>
    <li>
        <p>Prawdopodobnie będziesz również potrzebował aplikacji Adobe Photoshop. Wykorzystuje się do celów edytowania
            plików źródłowych .PSD i jest niezbędna jeżeli chcesz edytować projektowaną grafikę oraz obrazki.</p>
    </li>
    <li>
        <p>Aby edytować pliki źródłowe kodu szablonu, będziesz potrzebował edytora kodów, na przykład Adobe Dreamweaver,
            Notepad++, Sublime Text, etc.</p>
    </li>
    <li>
        <p>Dla przesłania plików na serwer hostingowy, prawdopodobnie będzie Ci potrzebny Menadżer FTP na kstałt Total
            Commander, FileZilla, CuteFTP, etc.</p>
    </li>
</ol>
