<h4> Systemkompatibilität</h4>
<p>Überprüfen Sie PHP Einstellungen und Berechtigungen für Ordner. Wenn es Fehler gibt, nehmen Sie den Kontakt mit Ihrem Hosting-Anbieter auf, um diese Fehler zu beheben.</p>
<p>Wenn es keine Fehler gibt, drücken Sie auf den Button <strong>'Weiter'</strong> (Next).</p>
<figure class="img-polaroid">
    <img src="img/engine-install-03.png" alt="">
</figure>
<h4>Shop-Einstellung </h4>
<p> Hier sollen Sie die Einstellungen Ihres Shops wie Name, Tätigkeit, Land, Zeitzone usw. eingeben.</p>
<p> Alle diesen Einstellungen können Sie später in dem PrestaShop Control Panel ändern.</p>
<figure class="img-polaroid">
    <img src="img/engine-install-04.png" alt="">
</figure>
<h4>Systemeinstellungen</h4>
<p>Wenn Sie die MySQL Datenbank schon haben, geben Sie die Datenbank-Zugangsdaten in die entsprechenden Felder unten ein.</p>
<p> Sie können auch die Datenbank-Zugangsdaten in dem Hosting Control Panel finden oder den Hosting-Anbieter fragen.</p>
<p>Nachdem Sie die Änderungen vorgenommen haben, drücken Sie auf den Button <strong>'Weiter'</strong> (Next).</p>
<figure class="img-polaroid">
    <img src="img/engine-install-05.png" alt="">
</figure>
<h4>Installation wird abgeschlossen</h4>
<p> Es ist der letzte Installationsschritt.</p>
<p class="alert alert-danger"> Aus Sicherheitsgründen sollen Sie den Ordner <strong>/install/</strong> auf Ihrem Server löschen.
</p>
<figure class="img-polaroid">
    <img src="img/engine-install-06.png" alt="">
</figure>
<p class="alert alert-info">Ab der Version 1.5.4 wird der Admin-Ordner automatisch für Sie umbenannt: der Ordner namens "admin" + 4 Zufallszahlen, zum Beispiel, "admin8814." Die Umbenennung wird durchgeführt, wenn Sie mit dem Ordner "admin" zum ersten Mal arbeiten: PrestaShop benennt diesen Ordner um, und dann erhält den Zugriff auf den umbenannten Ordner.<br/><br/>
    Loggen Sie sich ins PrestaShop Control Panel ein, nachdem Sie den Zugriff auf den umbenannten Ordner "admin" erhalten haben und füllen Sie den Katalog mit den Daten aus.
</p>
<h6>Wenn Sie irgendwelche Probleme haben, machen Sie sich mit der <a target="_blank" href="http://doc.prestashop.com/display/PS16/Getting+Started">offizielle Anleitung zur PrestaShop Installation bekannt</a>.</h6>
