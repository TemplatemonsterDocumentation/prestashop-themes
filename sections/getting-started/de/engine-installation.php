<h3>Installation der PrestaShop Engine</h3>
<p class="alert alert-warning">Stellen Sie sicher, dass die Version der PrestaShop Engine den Anforderungen im Tab "Technische Daten" wie "PrestaShop Engine: 1.X.X.X" entspricht.<br/>
Stellen Sie auch sicher, dass Ihr Datenbank-Präfix <strong>ps_</strong> ist. Installieren Sie den Demo-Inhalt nur un dem Fall, wenn die Version Ihrer PrestaShop Engine der angegebenen Version entspricht.</p>
<p>Nachdem Sie die Dateien hochgeladen haben, können Sie die Installation Ihrer PrestaShop starten. Öffnen Sie Ihren Browser und geben Sie den 'Domainnamen/Pfad zum Ihrem PrestaShop Ordner' in die Adressleiste ein, danach aktualisieren Sie die aktuelle Seite. Sie sehen den Begrüßungsbildschirm. Führen Sie die folgenden Schritte aus, um PrestaShop zu installieren.</p>
<h4>Installationsassistent (Wählen Sie Ihre Sprache aus)</h4>
<h6>Wählen Sie Installationssprache aus.</h6>
<p class="alert alert-warning">Achtung: Installationssprache ist mit der Sprache Ihres Shops nicht verbunden. Standardmäßig ist PrestaShop in Englisch verfügbar. Sie können zusätzliche Sprachpakete installieren, um die Sprache des Shops zu ändern.</p>
<p>Nachdem Sie die Änderungen vorgenommen haben, drücken Sie auf den Button <strong>'Weiter'</strong> (Next).</p>
<figure class="img-polaroid">
    <img src="img/engine-install-01.png" alt="">
</figure>
<h4>Installationsassistent (Lizenzvereinbarung)</h4>
<ol class="index-list">
    <li>Setzen Sie ein Häkchen, um zu bestätigen, dass Sie der Lizenzvereinbarung zustimmen.</li>
    <li>Danach drücken Sie auf den Button <strong>'Weiter'</strong> (Next).</li>
</ol>
