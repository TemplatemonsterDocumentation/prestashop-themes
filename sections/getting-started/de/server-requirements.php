<h3>Server-Anforderungen</h3>
<p>Stellen Sie sicher, dass Ihr Server den PrestaShop Anforderungen entspricht:</p>
<ul class="marked-list">
    <li><strong>System:</strong> Unix, Linux oder Windows. Unix ist dringend empfohlen.</li>
    <li><strong>Server:</strong> Webserver Apache 1.3 oder spätere Versionen, Webserver IIS von Windows 6.0 oder spätere
        Versionen, und nginx 1.0 oder spätere Versionen.
    </li>
    <li><strong>PHP 5.4</strong> oder spätere Versionen.</li>
    <li><strong>MySQL 5.5</strong> oder spätere Versionen.</li>
    <li>Mindestens 128 MB RAM auf dem Server (64 MB - genug, aber je mehr, desto besser).
    </li>
</ul>
<p class="alert alert-info">PrestaShop wird auf PHP 5.1 oder späteren Versionen funktionieren, aber Versionen niedriger
    als 5.4 haben Fehler, die einige Funktionen Ihrer Vorlage (zum Beispiel, falsche Zeitzone) stören können.</p>
