<h3>Bearbeitungssoftware</h3>
<p>Damit Sie mühelos mit einer PrestaShop Vorlage arbeiten können, laden Sie alle notwenidgen Anwendungen herunter. Sie finden die Liste der notwendigen Software auf der Seite Vorschau. Die Anforderungen können Sie sich abhängig von der Vorlage unterscheiden. Sie brauchen die folgende Software:</p>
<ol class="index-list">
    <li>
        <p>Vor allem brauchen Sie eine Anwendung zum Entpacken des passwortgeschützten Archivs sources_#########.zip. Sie können WinZip 9+ (Windows) und Stuffit Expander 10+ (Mac) verwenden.</p>
    </li>
    <li>
        <p>Sie brauchen auch die Anwendung Adobe Photoshop. Sie wird für die Bearbeitung der Quelldateien .PSD verwendet und ist in dem Fall nötig, wenn Sie das Grafikdesign der Vorlage oder eines Bildes ändern möchten.</p>
    </li>
    <li>
        <p>Um die Dateien des Quellcodes Ihrer Vorlage zu bearbeiten, sollen Sie den Code-Editor wie Adobe Dreamweaver, Notepad++, Sublime Text usw. nutzen.</p>
    </li>
    <li>
        <p>Um die Dateien auf dem Server hochzuladen, brauchen Sie die FTP Software wie Total Commander, FileZilla, CuteFTP usw.</p>
    </li>
</ol>
