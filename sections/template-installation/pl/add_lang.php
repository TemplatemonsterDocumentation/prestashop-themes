<h3>Dodawanie języków do sklepu internetowego</h3>
<p>Jak tylko zainstalujesz szablon wielojęzyczny, będziesz mógł dodawać języki dostarczane w komplecie z nim (angielski,
    francuski, hiszpański, niemiecki, rosyjski) do platformy sklepu internetowego. Można to zrobić w następujący
    sposób: </p>
<p>Przejdź do <strong>'Lokalizacja > Tłumaczenia'</strong> na panelu administracyjnym, znajdź <strong>'DODAJ/ODŚWIEŻ
        JĘZYK'</strong> oraz dodaj języki wybierając je z menu rozwijanego
    <small>(1)</small>
    oraz wciskając przycisk <strong>'Dodaj lub aktualizuj język'</strong>
    <small>(2)</small>
    .
</p>
<figure class="img-polaroid">
    <img alt="" src="img/01-language-update.png"/>
</figure>
<p>Jeżeli nie chcesz jakiegoś języku, możesz go usunąć
    <small>(2)</small>
    albo deaktywować
    <small>(1)</small>
    w menu <strong>'Lokalizacja > Języki'</strong> .
</p>
