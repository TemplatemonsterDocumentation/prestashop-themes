<h3>Automatyczna instalacja z danymi demonstracyjnymi (Instalator Przykładowych Danych)</h3>
<p>Aby Twój sklep PrestaShop wyglądał tak samo, jak demonstracja szablonu na żywo, będziesz musiał zainstalować <strong>dane
        przykładowe</strong>. Zawarte w nich będą wszystkie produkty demonstracyjne, kategorie, moduły oraz ustawienia
    sklepu.</p>
<p class="alert alert-danger">W żadnym razie nie stosuj dane przykładowe na swojej uruchomionej witrynie internetowej!
    Spowoduje to usunięcie wszystkich obecnych ustawień i produktów w twoim sklepie i zastąpienie ich danymi
    przykładowymi.</p>
<p class="alert alert-warning">Upewnij się, że Twoja wersja silnika PrestaShop odpowiada wersji zaznaczonej w zakładce
    &quot;Szczegóły Techniczne&quot;, gdzie będzie ona sprecyzowana jak &quot;Silnik PrestaShop (Prestashop Engine):
    wersja 1.X.X.X&quot;.<br/>
    Ponadto, upewnij się że twoje tablicy bazy danych mają prefiks <strong>ps_</strong>. Instaluj przykładowe dane
    wyłącznie jeżeli twój silnik PrestaShop odpowiada podanej wersji. </p>
<p>Aby zainstalować dane przykładowe za pomocą modułu instalowania danych przykładowych, prosimy postępować zgodnie z
    instrukcją począwszy od kroku 1 <a href="#themeinstaller">(Importuj Motyw)</a> w niniejszym podręczniku. Następnie,
    na panelu administracyjnym swojego sklepu zobaczysz moduł instalatora Danych Przykładowych.</p>
<p>Korzystając z tego modułu, możesz w sposób prosty zainstalować oraz zachować przykładowe dane sklepu w kilku prostych
    krokach. Prosimy wykonać następujące czynności:</p>
<ol class="index-list">
    <li>
        <p>W menu panelu administracyjnego wciśnij element <strong>"Instaluj Sklep Demonstracyjny"</strong>.</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-1.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Wybierz oraz prześlij pliki przykładowych danych z katalogu <strong>'sample_data'</strong> twojego pakietu
            szablonu. Możesz to zrobić, korzystając z przycisku <strong>"Przeglądaj lokalne pliki"</strong> lub
            przeciągając dane przykładowe do okna przeglądarki.</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-2.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Zaczekaj, aż pliki zainstalują się.</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-3.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Następnie wciśnij przycisk <strong>"Kontynuuj instalowanie"</strong>.</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-4.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Następnie instalator przykładowych danych automatycznie skonfiguruje Twój sklep oraz inne moduły. Może to
            zająć kilka minut.</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-5.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Po tym jak dane przykładowe zostaną pomyślnie zainstalowane, będziesz musiał wykonać ostatni krok -
            zregenerować obrazki. Wciśnij przycisk <strong>"Przejdź na Stronę Regenerowania"</strong>.</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-6.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Aby zregenerować obrazki, wciśnij przycisk <strong>"Zregeneruj miniaturki"</strong>.</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-7.png" alt=""/>
        </figure>
    </li>
</ol>
<p><strong>Gratulacje, Twój sklep demonstracyjny został zainstalowany.</strong></p>