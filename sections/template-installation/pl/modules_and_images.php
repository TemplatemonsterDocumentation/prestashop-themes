<h3>Instalowanie modułów</h3>
<p>Każdy szablon jest opatrzony w pewne konkretne ustawienia modułu. Aby zapewnić, żeby moduły mieli wygląd taki sam jak
    na podglądzie demonstracyjnym, musimy ich skonfigurować. Wszystkie moduły można skonfigurować na panelu
    administracyjnym PrestaShop.</p>
<p>Na swoim panelu administracyjnym PrestaShop, w głównym menu wybierz <strong>Moduły i Serwisy > Moduły i
        Serwisy</strong>.</p>
<p>Na stronie Moduły możesz zobaczyć listę dostępnych modułów PrestaShop. Możliwe jest że będziesz potrzebował
    zainstalować niektóre z nich.</p>
<p>Aby zainstalować moduł, odszukaj go i wciśnij przycisk <strong>'Instaluj'</strong>.</p>
<figure class="img-polaroid">
    <img src="img/06-modules.png" alt=""/>
</figure>
<p>Korzystając z linków modułu możesz je włączać/wyłączać (linki <strong>Włącz</strong> | <strong>Wyłącz</strong>),
    zresetować ich ustawienia, wracając do ustawień domyślnych (link<strong>Zresetuj</strong>), usunąć moduł (link
    <strong>Usuń</strong>) albo skonfigurować ich (link <strong>Skonfiguruj</strong>).</p>
<figure class="img-polaroid">
    <img src="img/08-modules.png" alt=""/>
</figure>
<p>Po <strong>aktywowaniu</strong> niezbędnych modułów będziesz musiał <strong>wyznaczyć dla nich prawidłowe
        pozycje.</strong></p>
<p>Na panelu administrowania PrestaShop, wybierz <strong>Moduły i Serwisy > Pozycje</strong> z górnej części menu. Aby
    umieścić moduł w prawidłową pozycję, po prostu przeciągnij go w górę lub w dół w liście modułów.</p>
