</figure>
<h3>Konfigurowanie obrazków</h3>
<p>W PrestaShop możesz zastosować predefiniowane rozmiary dla obrazków produktów, obrazków kategorii, wytwórców etc.</p>
<p>Na przykład, obrazki rozmiarów <strong>'strona domowa'</strong> są zwykle wykorzystywane jako zdjęcia produktów w
    zestawieniach kategorii, <strong>'duży rozmiar'</strong> jest wykorzystywany jako główne zdjęcie produktu na stronie
    z informacją o produkcie.</p>
<p>Aby skonfigurować rozmiary zdjęcia produktu, otwórz swój panel administracyjny PrestaShop, przejdź do <strong>Preferencje
        > Obrazki</strong> i ustaw takie rozmiary obrazku, jak opisane są na <strong>Stronie Konfiguracji</strong> w
    demonstracji na żywo szablonu.</p>