<h3>Importowanie motywu</h3>
<p class="alert alert-warning">Uwaga: ten metod nie przewiduje instalacji przykładowych danych.</p>
<h4>Przygotowywanie szablonu</h4>
<p>Zanim przejdziesz do następnych operacji, upewnij się że już masz pobrany szablon i rozpakowane pliki z pakietu
    szablonu. Możesz zapoznać się z dokładniejszymi samouczkami opisującymi procedurę rozpakowywania plików z pakietu
    szablonu wykorzystując <a target="_blank" href="/help/unzipping-your-template.html">WinZip</a> oraz <a
            target="_blank" href="/help/how-unzip-template-mac.html">StuffitExpander</a>. </p>
<p>Po rozpakowaniu plików możesz przystąpić do przesyłania szablonu.</p>
<h4>Instalacja</h4>
<p>Zaloguj się do swojego panelu administracyjnego PrestaShop oraz przejdź do <strong>Preferencje -> Motywy</strong>. W
    górnej części strony wciśnij przycisk <strong>Dodaj nowy motyw</strong> (1). </p>
<figure class="img-polaroid">
    <img src="img/theme-import-1.png" alt=""/>
</figure>
<p>Wciśnij przycisk <strong>Dodaj plik</strong> (1) w sekcji Importuj ze swojego komputera. Przejdź do wcześniej
    rozpakowanego pakietu szablonu oraz otwórz folder <strong>themeinstallator</strong>.</p>
<p>Wybierz swój plik theme .zip oraz wciśnij przycisk <strong>Zachowaj</strong> (2).</p>
<figure class="img-polaroid">
    <img src="img/theme-import-2.png" alt=""/>
</figure>
<p>Zacznie się proces przesyłania. Zobaczysz wiadomość informującą Ciebie o pomyślnym przesłaniu motywu. Podgląd Twojego
    nowego motywu pojawi się w liście zainstalowanych motywów.</p>
