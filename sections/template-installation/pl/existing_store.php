<h3>Instalowanie szablonu dla istnięjącego sklepu</h3>
<p class="alert alert-info">Możesz wykorzystać tą metodę instalacji jeżeli już masz uruchomioną witrynę PrestaShop i po
    prostu chcesz zainstalować nowy szablon.</p>
<p class="alert alert-info"><span>Tablicę konfiguracji modułów można znaleźć na stronie livedemo (demonstracji na żywo) szablonu. Znajdź link 'Sitemap' ('Mapa Witryny') w górnej lub w dolnej części menu i przejdź do Mapa Witryny -> Strony -> Konfiguracja Stron.</span>
</p>
<h4>Przesyłanie plików szablonu oraz aktywacja motywu</h4>
<ol class="index-list">
    <li>Otwórz pakiet szablonu.</li>
    <li>Następnie przejdź do folderu <strong>/theme/manual_install/</strong> .</li>
    <li>Prześlij foldery <strong>/modules/</strong> oraz <strong>/themes/</strong> do katalogu głównego PrestaShop.
    <li>
        <p>Na swoIm panelu administracyjnym PrestaShop przejdź do <strong>Preferencje > Motywy</strong>, wybierz
            zainstalowany motyw (możesz wykorzystać już aktywowany motyw; nie spowoduje żadnych problemów dla Twojego
            aktywowanego motywu). Wciśnij przycisk <strong>'Ustawienia Zaawansowane'</strong> (1).</p>
        <figure class="img-polaroid">
            <img alt="" src="img/existing-store-1.png"/>
        </figure>
    </li>
    <li>
        <p>Na ekranie który się pojawi, wybierz katalog zawierający nazwę twojego nowego motywu (1) i wciśnij przycisk
            <strong>'Zachowaj'</strong> (2).</p>
        <figure class="img-polaroid">
            <img alt="" src="img/existing-store-2.png"/>
        </figure>
    </li>
</ol>