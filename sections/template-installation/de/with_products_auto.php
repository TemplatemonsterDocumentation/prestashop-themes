<h3>Automatische Installation mit dem Demo-Inhalt (Installation des Demo-Inhalts)</h3>
<p> Damit der PrestaShop Online-Shop wie die Demoversion der Vorlage aussieht, sollen Sie den <strong>Demo-Inhalt</strong> installieren. Er wird alle Demo-Produkte, Kategorien, Module, Einstellungen enthalten.</p>
<p class="alert alert-danger"> Nutzen Sie bitte nicht den Demo-Inhalt für Ihren funktionierenden Online-Shop! Dies wird alle Ihren Produkte in Shop und Shop-Einstellungen ersetzt.</p>
<p class="alert alert-warning"> Stellen Sie sicher, dass die Version Ihrer PrestaShop Engine der Version entspricht, die im Tab «Technische Details» wie «PrestaShop Engine: 1.X.X.X» festgelegt ist.<br/>
Außerdem stellen Sie sicher, dass das Datenbank Tabellen Präfix <strong>ps_</strong> ist. Installieren Sie den Demo-Inhalt in dem Fall, wenn Ihre PrestaShop Engine der angegebenen Version entspricht. </p>
<p> Um die Demo-Daten mit Hilfe von dem Modul «Sample data installer» zu installieren, folgen Sie den Anweisungen aus dem Schritt 1 <a href="#themeinstaller">(Vorlagenimport)</a>. Danach sehen Sie in dem Shop Control Panel das Modul Sample data installer.</p>
<p> Mit Hilfe von diesem Modul können Sie ganz einfach den Demo-Inhalt des Shops in ein paar einfachen Schritten installieren. Führen Sie die folgenden Aktionen aus:</p>
<ol class="index-list">
    <li>
        <p>Im Control Panel drücken Sie <strong>"Demoversion des Shops installieren"</strong> (Install Demo Store).</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-1.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Hier sollen Sie die Dateien des Demo-Inhalts aus dem Ordner Выберите <strong>'sample_data'</strong> Ihres Vorlagenpakets auswählen und hochladen. Sie können dies auch machen, indem Sie <strong>"Vorschau der lokalen Dateien"</strong> (Browse local files) nutzen oder die Dateien des Demo-Inhalts ins Browserfenster verschieben.</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-2.png" alt=""/>
        </figure>
    </li>
    <li>
        <p> Warten Sie, bis die Dateien geladen sind</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-3.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Danach drücken Sie auf den Button <strong>"Installation fortfahren"</strong> (Continue Install).</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-4.png" alt=""/>
        </figure>
    </li>
    <li>
        <p> Danach stellt das Installationsprogramm des Demo-Inhalts automatisch Ihren Online-Shop und andere Module ein. Es kann ein paar Minuten dauern.</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-5.png" alt=""/>
        </figure>
    </li>
    <li>
        <p> Nachdem der Demo-Inhalt erfolgreich installiert wurde, sollen Sie den folgenden Schritt durchführen - generieren Sie Bilder neu. Drücken Sie auf den Button <strong>"Zur Generierungsseite"</strong> (Go To Regenerate Page).</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-6.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Um Bilder neu zu generieren, drücken Sie auf den Button <strong>"Vorschaubilder neu generieren"</strong> (Regenerate thumbnails).</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-7.png" alt=""/>
        </figure>
    </li>
</ol>
<p><strong> Gratulieren, Ihr Demo-Shop wurde installiert.</strong></p>
