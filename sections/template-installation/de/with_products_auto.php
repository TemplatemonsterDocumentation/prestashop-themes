<h3>Automatische Installation mit den Demodaten (Modul Sample data installer)</h3>
<p class="alert alert-info custom">Wenn Sie einiges an Zeit sparen möchten, <span class="label label-warning opener"
                                                                                  onclick="showChat()">bestellen</span>
    Sie den Installationsservice für Ihr Theme in unserem Online-Chat.</p>
<p>Nachdem Sie <strong>Demodaten</strong> installiert haben, wird Ihr Online-Shop
    PrestaShop wie auf der Demoversion aussehen. Er wird alle Produkte, Kategorien und
    Einstellungen für Module und den Online-Shop enthalten.</p>

<p class="alert alert-danger">Nutzen Sie, bitte, die Demodaten für aktuelle Webseite nicht, weil alle Produkte und
    Einstellungen überschrieben werden.</p>

<p class="alert alert-warning">Bitte überzeugen Sie sich, dass Sie die Version der PrestaShop Engine, die im Bereich
    "Notwendige Software" wie "PrestaShop 1.X.X.X" festgelegt wurde, verwenden. Überzeugen Sie sich auch davon, dass
    das Datenbanktabellen-Präfix <strong>ps_</strong> entspricht. Wenn die Version Ihrer PrestaShop Engine der
    festgelegten entspricht, installieren Sie Demodaten.</p>

<p>Um die Demodaten mit Hilfe vom Modul модуль Sample data installer zu installieren,
    folgen Sie der Anleitung aus dem Schritt 1 <a href="#themeinstaller">(Import des
        Themas)</a> dieser Dokumentation. Dann wird im Control Panel des Online-Shops
    das Modul Sample data installer angezeigt.</p>

<p>Mit Hilfe von diesem Modul kann man einfach die Demodaten installieren. Machen Sie
    das Folgende:</p>
<ol class="index-list">
    <li>
        <p>Im Control Panel drücken Sie <strong>"Install Demo Store (Demo-Shop
                installieren)"</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7.png" alt="">
        </figure>
    </li>
    <li>
        <p>Dann sollen Sie die Dateien von Demodaten aus dem Ordner <strong>'sample_data'</strong>
            markieren und hochladen. Dies können Sie anhand des Buttons <strong>"Browse
                local files (Dateien finden)"</strong> machen oder die Dateien in das
            Browserfenster verschieben.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_1.png" alt="">
        </figure>
    </li>
    <li>
        <p>Warten Sie bis das Hochladen von Dateien abgeschlossen wird.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_2.png" alt="">
        </figure>
    </li>
    <li>
        <p>Drücken Sie den Button <strong>"Continue Install (Installation
                fortfahren)"</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_3.png" alt="">
        </figure>
    </li>
    <li>
        <p>Danach wird das Modul Sample data installer automatisch den Online-Shop und
            andere Module einstellen. Es kann einige Minuten dauern.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_4.png" alt="">
        </figure>
    </li>
    <li>
        <p>Nachdem Sie die Demodaten installiert haben, müssen Sie noch eine Aktion
            durchführen. Regenerieren Sie die Bilder. Drücken Sie den Button <strong>"Go
                To Regenerate Page (Zur Regenerationsseite gelangen)"</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_5.png" alt="">
        </figure>
    </li>
    <li>
        <p>Um die Bilder zu regenerieren, klicken Sie den Button <strong>"Regenerate
                thumbnails (Vorschaubild regenerieren)"</strong>. </p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_6.png" alt="">
        </figure>
    </li>
</ol>
<p><strong>Wir gratulieren Ihnen, der Demo-Shop wurde erfolgreich installiert.</strong></p>