</figure>
<p> Wenn Sie einige Sprachen nicht hinzufügen möchten, können Sie sie <small>(2)</small> löschen oder <small>(1)</small> in dem Menüpunkt <strong>'Lokalisierung > Sprachen</strong> (Localization > Languages) deaktivieren.</p>
