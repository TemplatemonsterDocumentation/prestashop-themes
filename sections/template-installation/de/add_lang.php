<h3>Wie man Sprachen zum Online-Shop hinzufügt</h3>
<p> Nachdem Sie eine mehrsprachige Vorlage installiert haben, können Sie alle Sprachen hinzufügen, die zu ihr gehören (Englisch, Französisch, Spanisch, Deutsch, Russisch). Führen Sie die folgenden Schritte aus: </p>
<p>Finden Sie den Tab <strong>'Lokalisierung > Übersetzungen'</strong> (Localization > Translations) im Control Panel, finden Sie <strong>'ADD/UPDATE A LANGUAGE'</strong> und fügen Sie Sprachen hinzu, indem Sie sie aus dem DropDown Menü wählen <small>(1)</small> und drücken Sie auf den Button <strong>'Sprache hinzufügen oder aktualisieren'</strong> (Add or update a language) <small>(2)</small>.</p>
