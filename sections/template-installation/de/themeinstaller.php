<h3>Wie man eine Vorlage importiert</h3>
<p class="alert alert-warning">Achtung: Dieses Verfahren bietet keine Installation des Demo-Inhalts an.</p>
<h4>Vorbereitung</h4>
<p>Stellen Sie sicher, dass Sie die Vorlage installiert und die Dateien aus dem Vorlagenpaket entpackt haben. Sie können die ausführliche Anleitung zum Entpacken der Dateien aus dem Vorlagenpaket mit <a target="_blank" href="/help/unzipping-your-template.html">WinZip</a> und <a target="_blank" href="/help/how-unzip-template-mac.html">StuffitExpander nutzen</a>.
</p>
<p> Nachdem Sie die Dateien entpackt haben, können Sie Ihre Vorlage hochladen.</p>
<h4>Installation</h4>
<p>Loggen Sie sich ins PrestaShop Control Panel ein und finden Sie den Tab <strong>Präferenzen -> Vorlagen</strong> (Preferences -> Themes). Auf der Seite oben drücken Sie auf den Button <strong>Neue Vorlage hinzufügen</strong> (Add new theme) (1).
</p>
<figure class="img-polaroid">
    <img src="img/theme-import-1.png" alt=""/>
</figure>
<p>Drücken Sie auf den Button <strong>Datei hinzufügen</strong> (Add file) (1) im Bereich «Import von Ihrem Computer». Finden Sie die vorher entpackte Vorlage und öffnen Sie den Ordner <strong>themeinstallator</strong>.</p>
<p>Wählen Sie die .zip Datei Ihrer Vorlage und drücken Sie auf den Button <strong>Speichern</strong> (Save) (2).</p>
<figure class="img-polaroid">
    <img src="img/theme-import-2.png" alt=""/>
</figure>
<p>Der Downloadprozess wird gestartet. Sie sehen eine Mitteilung darüber, dass Ihre Vorlage erfolgreich hochgeladen wurde. Die Vorschau Ihrer neuen Vorlage ist jetzt in der Liste der installierten Vorlagen verfügbar.</p>

