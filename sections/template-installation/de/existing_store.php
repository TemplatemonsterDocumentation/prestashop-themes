<h3>Installation der Vorlage auf den bestehenden Online-Shop </h3>
<p class="alert alert-info"> Sie können dieses Installationsverfahren verwenden, wenn Sie eine  funktionierende PrestaShop Website haben und Ihre Vorlage installieren möchten.</p>
<p class="alert alert-info"><span> Die Konfigurationstabelle der Module können Sie auf der Seite der Demoversion Ihrer Vorlage finden. Finden Sie den Link «Site Map» im Header oder Footer und öffnen Sie die Seite Site Map -> Seiten -> Seiten.</span></p>
<h4> Wie man Dateien hochlädt und die Vorlage aktiviert </h4>
<ol class="index-list">
    <li> Öffnen Sie das Vorlagenpaket.</li>
    <li>Danach finden Sie den Ordner <strong>/theme/manual_install/</strong>.</li>
    <li>Laden Sie die Ordner <strong>/modules/</strong> und <strong>/themes/</strong> in den PrestaShop Stammordner hoch.
    <li>
        <p>In Ihrem PrestaShop Control Panel finden Sie den Tab <strong>Einstellungen > Vorlagen</strong> (Preferences > Themes), wählen Sie die Vorlage aus, die Sie (Sie können auch die beiliegende Vorlage verwenden, sie wird keine Probleme für die von Ihnen verwendete Vorlage bereiten) installiert haben. Drücken Sie auf den Button <strong>'Erweiterte Einstellungen'</strong> (Advanced settings) (1).</p>
        <figure class="img-polaroid">
            <img alt="" src="img/existing-store-1.png"/>
        </figure>
    </li>
    <li>
        <p> Auf dem angezeigten Bildschirm wählen Sie den Ordner mit dem neuen Namen des Themes (1) und drücken Sie auf den Button <strong>'Speichern'</strong> (Save) (2).</p>
        <figure class="img-polaroid">
            <img alt="" src="img/existing-store-2.png"/>
        </figure>
    </li>
</ol>

