<h3>Добавление языков в интернет-магазин</h3>
<p> После установки многоязычного шаблона Вы сможете добавить все языки, которые входят в него (английский, французский, испанский, немецкий, русский). Это можно сделать таким образом: </p>
<p>Перейдите на вкладку <strong>'Локализация > Переводы'</strong> (Localization > Translations) в админ панели, найдите <strong>'ADD/UPDATE A LANGUAGE'</strong> и добавьте языки, выбрав их из выпадающего меню <small>(1)</small> и нажав на кнопку <strong>'Добавить или обновить язык'</strong> (Add or update a language) <small>(2)</small>.</p>
<figure class="img-polaroid">
    <img alt="" src="img/01-language-update.png"/>
</figure>
<p> Если Вы не хотите добавлять какие-либо языки, Вы можете удалить их <small>(2)</small> или отключить их <small>(1)</small> в пункте меню <strong>'Локализация > Языки'</strong> (Localization > Languages).</p>
