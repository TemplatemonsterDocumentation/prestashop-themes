<h3>Автоматическая установка с Демо-контентом (Установка Демо-контента)</h3>
<p class="alert alert-info custom">Если Вы не хотите тратить Ваше время на установку шаблона, вы всегда можете <span class="label label-warning opener" onclick="showChat()">заказать</span> услугу установки шаблона используя наш чат.</p>
<p> Чтобы магазин PrestaShop выглядел так же, как Демо-версия шаблона, Вам необходимо установить <strong>Демо-контент</strong>. Он будет содержать все демонстрационные товары, категории, модули и настройки магазина.</p>
<p class="alert alert-danger"> Пожалуйста, не используйте Демо-контент для вашего рабочего магазина! Это заменит все ваши товары в магазине и настройки магазина.</p>
<p class="alert alert-warning"> Убедитесь, что ваша версия движка PrestaShop соответствует версии, указанной на вкладке «Технические детали» как «Движок PrestaShop: 1.X.X.X».<br/>
Кроме того, убедитесь, что ваш префикс таблиц базы данных – это <strong>ps_</strong>. Установите Демо-контент только в том случае, если ваш движок PrestaShop соответствует указанной версии. </p>
<p> Для того, чтобы установить Демо-данные с помощью модуля «Sample data installer», пожалуйста, следуйте инструкциям из шага 1 <a href="#themeinstaller">(Иморта шаблона)</a> этого руководства. Затем, в админ панели магазина Вы увидите модуль Sample data installer.</p>
<p> С помощью этого модуля Вы можете легко установить Демо-контент магазина в несколько простых шагов. Выполните следующие действия:</p>
<ol class="index-list">
    <li>
        <p>В меню админ панели нажмите на <strong>"Установить Демо-версию магазина"</strong> (Install Demo Store).</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-1.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Выберите и загрузите файлы Демо-контента из папки <strong>'sample_data'</strong> вашего пакета шаблона. Вы можете сделать это также используя <strong>"Обзор локальных файлов"</strong> (Browse local files) или перетянуть файлы Демо-контента в окно браузера.</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-2.png" alt=""/>
        </figure>
    </li>
    <li>
        <p> Подождите, пока файлы будут загружены.</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-3.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Затем, нажмите на кнопку <strong>"Продолжить установку"</strong> (Continue Install).</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-4.png" alt=""/>
        </figure>
    </li>
    <li>
        <p> Затем, программа установки Демо-контента автоматически настроит ваш магазин и другие модули. Это может занять несколько минут.</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-5.png" alt=""/>
        </figure>
    </li>
    <li>
        <p> Когда Демо-контент успешно установлен, Вам необходимо выполнить последний шаг - пересоздать изображения. Нажмите на кнопку <strong>"Перейдите на страницу пересоздания"</strong> (Go To Regenerate Page).</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-6.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Для того чтобы пересоздать изображения, нажмите на кнопку <strong>"Пересоздать миниатюры"</strong> (Regenerate thumbnails).</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-7.png" alt=""/>
        </figure>
    </li>
</ol>
<p><strong> Поздравляем, ваш Демо-магазин установлен.</strong></p>
