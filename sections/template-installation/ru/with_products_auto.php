<h3>Автоматическая установка с использованием демо-данных (модуль Sample data
    installer)</h3>
<p class="alert alert-info custom">Если Вы не хотите тратить Ваше время на установку шаблона, вы всегда можете <span
            class="label label-warning opener" onclick="showChat()">заказать</span> услугу установки шаблона используя
    наш чат.</p>
<p>После установки <strong>демо-данных</strong>, ваш магазин PrestaShop будет выглядеть
    точь-в-точь как на демо. Он будет содержать все образцы товаров, категорий,
    настройки модулей и магазина.</p>

<p class="alert alert-danger">Пожалуйста, не используйте демо-данные для действующего сайта! Это перезапишет все товары
    и настройки магазина.</p>
<p class="alert alert-warning">Пожалуйста, убедитесь, что вы используете версию движка PrestaShop, указанную в пункте
    "Необходимое программное обеспечение:" как "PrestaShop 1.X.X.X". Также убедитесь, что префикс таблиц базы данных
    соответствует <strong>ps_</strong>. Только если версия движка PrestaShop соответствует указанной, устанавливайте
    демонстрационные данные.</p>
<p>Для установки демо-данных через модуль Sample data installer, следуйте инструкции из
    шага 1 <a href="#themeinstaller">(Импорт темы)</a> данного руководства. Затем в
    панели управления магазина появится модуль Sample data installer.</p>

<p>При помощи этого модуля можно легко установить демо-данные за несколько простых
    действий. Выполните следующее:</p>
<ol class="index-list">
    <li>
        <p>В панели управления нажмите <strong>"Install Demo Store (Установить
                демо-магазин)"</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7.png" alt="">
        </figure>
    </li>
    <li>
        <p>Выделите и загрузите файлы демо-данных из папки
            <strong>'sample_data'</strong> шаблона. Это можно сделать при помощи кнопки
            <strong>"Browse local files (Найти файлы)"</strong> или перетянуть файлы
            демо-данных в окно браузера.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_1.png" alt="">
        </figure>
    </li>
    <li>
        <p>Дождитесь завершения загрузки файлов.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_2.png" alt="">
        </figure>
    </li>
    <li>
        <p>Нажмите кнопку <strong>"Continue Install (Продолжить установку)"</strong>.
        </p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_3.png" alt="">
        </figure>
    </li>
    <li>
        <p>После этого модуль Sample data installer автоматически настроит магазин и
            другие модули. Это может занять несколько минут.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_4.png" alt="">
        </figure>
    </li>
    <li>
        <p>По завершению установки демо-данных необходимо выполнить последнее действие -
            регенерировать картинки. Нажмите кнопку <strong>"Go To Regenerate Page
                (Перейти на страницу регенерации)"</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_5.png" alt="">
        </figure>
    </li>
    <li>
        <p>Чтобы регенерировать картинки, кликните кнопку <strong>"Regenerate thumbnails
                (Регенерировать миниатюры)"</strong>. </p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_6.png" alt="">
        </figure>
    </li>
</ol>
<p><strong>Поздравляем, демо-магазин установлен.</strong></p>