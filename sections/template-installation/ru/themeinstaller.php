<h3>Импорт шаблона</h3>
<p class="alert alert-warning">Обратите внимание: этот способ не обеспечивает установку Демо-контента.</p>
<h4>Подготовка шаблона</h4>
<p>Прежде чем продолжить, убедитесь, что Вы уже загрузили шаблон и извлекли файлы из пакета шаблона. Вы можете ознакомиться с детальным руководством по извлечению файлов из пакета шаблонов, используя <a target="_blank" href="/help/unzipping-your-template.html">WinZip</a> and <a target="_blank" href="/help/how-unzip-template-mac.html">StuffitExpander</a>.
</p>
<p> Когда файлы будут извлечены, Вы можете перейти к загрузке шаблона.</p>
<h4>Установка</h4>
<p>Войдите в вашу админ панель PrestaShop и перейдите на вкладку <strong>Предпочтения -> Шаблоны</strong> (Preferences -> Themes). Вверху страницы нажмите на кнопку <strong>Добавить новый шаблон</strong> (Add new theme) (1).
</p>
<figure class="img-polaroid">
    <img src="img/theme-import-1.png" alt=""/>
</figure>
<p>Нажмите на кнопку <strong>Добавить файл</strong> (Add file) (1) в разделе «Импорт с вашего компьютера». Перейдите в ранее разархивированный шаблон и откройте папку <strong>themeinstallator</strong>.</p>
<p>Выберите .zip файл шаблона и нажмите на кнопку <strong>Сохранить</strong> (Save) (2).</p>
<figure class="img-polaroid">
    <img src="img/theme-import-2.png" alt=""/>
</figure>
<p>Начнётся процесс загрузки. Вы увидите сообщение о том, что шаблон успешно загружен. Предварительный просмотр вашего нового шаблона теперь доступен в списке установленных шаблонов.</p>

