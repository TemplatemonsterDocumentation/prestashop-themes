<h3>Installazione automatica con dati demo (installatore dei dati di esempio)</h3>
<p class="alert alert-info custom">Se non vuoi spendere il tempo per l'installazione del modello, non esitare a contattarci <span class="label label-warning opener" onclick="showChat()">ordina</span> servizio di installaione utilizzando il nostro live-chat.</p>
<p>Per rendere il tuo negozio PrestaShop simile al demo live di modello, devi installare <strong>sample data</strong>. Conterrà tutti i prodotti demo, le categorie, i moduli e le impostazioni del negozio.</p>
<p class="alert alert-danger">Non utilizzare i dati di esempio per il tuo sito web in esecuzione! Ciò sostituirà tutti i prodotti del tuo negozio e memorizza le impostazioni.</p>
<p class="alert alert-warning">Assicurati che la tua versione del motore PrestaShop corrisponda alla versione specificata nella scheda "Dettagli tecnici", indicata come "PrestaShop Engine: 1.X.X.X".<br/>
    Inoltre, assicurati che il prefisso delle tabelle del database sia <strong>ps_</strong>. Installare i dati di esempio solo se il tuo motore PrestaShop corrisponde alla versione specificata. </p>
<p>Per installare i dati di esempio utilizzando il modulo di installazione dei dati di esempio, seguire le istruzioni del passaggio 1 <a href="#themeinstaller">(Theme import)</a> di questo manuale. Quindi nel pannello di amministrazione del negozio vedrai il modulo di installazione dei dati del campione.</p>
<p>Utilizzando questo modulo è possibile installare facilmente i dati del campione in pochi semplici passaggi. Per favore fai quanto segue:</p>
<ol class="index-list">
    <li>
        <p>Dal menu del pannello di amministrazione fare clic sul pulsante <strong>"Installa Demo Store"</strong> articolo.</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-1.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Seleziona e carica i file di dati di esempio da <strong>'sample_data'</strong> Directory del tuo pacchetto di modelli. Puoi farlo usando il comando <strong>"Esplora file locali"</strong> pulsante o trascinando i file di dati di esempio nella finestra del browse.</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-2.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Attendi finché i file non vengono caricati.</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-3.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Quindi fare clic su <strong>"Continua Installare"</strong> pulsante.</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-4.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Quindi il programma di installazione dei dati campione configurerà automaticamente il tuo archivio e altri moduli. Questo può richiedere alcuni minuti.</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-5.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Quando i dati di esempio sono installati correttamente, è necessario eseguire l'ultimo passo - rigenerare le immagini. Clic <strong>"Vai a Rigenerare la Pagina"</strong> pulsante.</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-6.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Fare clic su rigenerare le immagini <strong>"Rigenerare thumbnails"</strong> pulsante.</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-7.png" alt=""/>
        </figure>
    </li>
</ol>
<p><strong>Congratulazioni, il tuo negozio demo è installato.</strong></p>