<h3>Installazione automatica con dati campione (installer 'Sample data')</h3>
<p class="alert alert-info custom">Se non vuoi spendere il tempo per l'installazione del modello, non esitare a
    contattarci <span class="label label-warning opener" onclick="showChat()">ordina</span> servizio di installaione
    utilizzando il nostro live-chat.</p>
<p>Dopo aver installato i <strong>dati campione</strong>, il tuo negozio PrestaShop
    apparirà proprio come nella demo live del tuo template. Conterrà tutti i prodotti
    dimostrativi, le categorie, i moduli e le impostazioni dello store.</p>

<p class="alert alert-danger">Attenzione a non utilizzare i dati campioni per il tuo negozio già esistente! Così facendo
    sostituiresti tutti i prodotti e le impostazioni del tuo negozio.</p>
<p class="alert alert-warning">Assicurati che la tua versione di Prestashop corrisponda alla versione specificata nella
    scheda Technical Details, indicata come PrestaShop Engine: 1.X.X.X. Verifica anche che il prefisso del tuo database
    sia <strong>ps_</strong>. Installa i dati campione solo se il tuo motore PrestaShop corrisponde alla versione
    specificata. </p>
<p>Per installare i dati campione usando il modulo Sample data installer, segui le
    istruzioni a partire dalla Fase 1 <a href="#themeinstaller">(Theme import)</a> di
    questo manuale. Poi nel tuo pannello di amministrazione del negozio vedrai il modulo
    Sample data installer.</p>

<p>Usando questo modulo potrai facilmente installare i dati campione in pochi semplici
    passi. Fai come segue:</p>
<ol class="index-list">
    <li>
        <p>Dal menu del pannello di amministrazione clicca sull'elemento <strong>"Install
                Demo Store"</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7.png" alt="">
        </figure>
    </li>
    <li>
        <p>Seleziona e carica i file dei dati campione dalla directory <strong>'sample_data'</strong>
            del tuo pacchetto template. Puoi farlo utilizzando il pulsante <strong>"Browse
                local files"</strong> oppure semplicemnte trascinando i file dati
            campione sulla finestra del browser.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_1.png" alt="">
        </figure>
    </li>
    <li>
        <p>Attendi che i file vengano caricati.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_2.png" alt="">
        </figure>
    </li>
    <li>
        <p>Poi clicca su <strong>"Continue Install"</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_3.png" alt="">
        </figure>
    </li>
    <li>
        <p>Adesso il Sample data installer configurerà automaticamente il tuo negozio e
            gli altri moduli. Per queste operazioni potrebbero volerci alcuni
            minuti.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_4.png" alt="">
        </figure>
    </li>
    <li>
        <p>Una volta che i dati campione sono stati installati correttamente, dovrai
            completare un'ultima fase: rigenerare le imagini. Clicca su <strong>"Go To
                Regenerate Page"</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_5.png" alt="">
        </figure>
    </li>
    <li>
        <p>Per rigenerare le immagini, clicca su <strong>"Regenerate
                thumbnails"</strong>. </p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_6.png" alt="">
        </figure>
    </li>
</ol>
<p><strong>Congratulazioni, il tuo store dimostrativo è ora installato.</strong></p>