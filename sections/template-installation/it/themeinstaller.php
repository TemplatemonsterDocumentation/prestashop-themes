<h3>Importa tema</h3>
<p class="alert alert-warning">Nota: questo metodo non fornisce l'installazione di dati di esempio.</p>
<h4>Preparazione del modello</h4>
<p>Prima di procedere, assicurati di aver già scaricato il modello e di estrarre i file dal pacchetto modello. È possibile controllare esercitazioni dettagliate su come estrarre i file dal pacchetto modello utilizzando <a target="_blank" href="/help/unzipping-your-template.html">WinZip</a> e <a target="_blank" href="/help/how-unzip-template-mac.html">StuffitExpander</a>.
</p>
<p>Quando i file vengono estratti, puoi procedere al caricamento del modello.</p>
<h4>Installazione</h4>
<p>Accedi al tuo pannello di controllo di PrestaShop e vai verso <strong>Preferenze -> Temi</strong>. Nella parte superiore della pagina fare clic su <strong>Aggiungi nuovo tema</strong> pulsante (1).
</p>
<figure class="img-polaroid">
    <img src="img/theme-import-1.png" alt=""/>
</figure>
<p>Click <strong>Aggiungi file</strong> pulsante (1) In Importa dalla sezione del computer. Passare al pacchetto template precedentemente sbloccato e aprire <strong>themeinstallator</strong> cartella.</p>
<p>Seleziona il tuo file .zip tema e fai clic su <strong>Salva</strong> (2).</p>
<figure class="img-polaroid">
    <img src="img/theme-import-2.png" alt=""/>
</figure>
<p>Il processo di caricamento verrà avviato. Verrà visualizzato il messaggio che il tema è stato caricato correttamente. La nuova anteprima tema verrà visualizzata nell'elenco dei temi installati.</p>

