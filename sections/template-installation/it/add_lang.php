<h3>Aggiunta di lingue al negozio online</h3>
<p>Una volta installato il modello multilingue, potrai aggiungere tutte le lingue con esso (inglese, francese, spagnolo, tedesco, russo) alla piattaforma del negozio online. Può essere fatto in questo modo:</p>
<p>Go to <strong>'Localization > Translations'</strong> Nel pannello di amministrazione, trova <strong>'ADD/UPDATE A LANGUAGE'</strong> e aggiungete le lingue selezionandole dal menu a discesa <small>(1)</small> e cliccando sul pulsante <strong>'Aggiungi o aggiorna una lingua'</strong> pulsante <small>(2)</small>.</p>
<figure class="img-polaroid">
    <img alt="" src="img/01-language-update.png"/>
</figure>
<p>Se non si desidera avere alcune lingue, è possibile eliminarle <small>(2)</small> O disattivarli <small>(1)</small> in <strong>'Localization > Languages'</strong> menu.</p>
