<h3>Installazione del modello sopra l'archivio esistente</h3>
<p class="alert alert-info">È possibile utilizzare questo metodo di installazione se si dispone di un sito web PrestaShop in esecuzione e si desidera solo installare un nuovo modello.</p>
<p class="alert alert-info"><span>La tabella di configurazione dei moduli può essere trovata nella pagina del template livesemo. Trovate il link 'Sitemap' nel menu principale o in piè di pagina e scegliete la Sitemap -> Pagine -> Configurazione pagine.</span></p>
<h4>Caricamento di file di template e attivazione di temi</h4>
<ol class="index-list">
    <li>Aprire il pacchetto modello.</li>
    <li>Poi andate alla <strong>/theme/manual_install/</strong> cartella.</li>
    <li>Caricare <strong>/modules/</strong> e <strong>/themes/</strong> cartelle nella directory principale di PrestaShop.
    <li>
        <p>Entra nel tuo pannello di controllo di PrestaShop <strong>Preferences > Themes</strong>, seleziona il tema che hai installato (puoi anche utilizzare il tema abilitato, non causerà Problema al tuo tema abilitato). Click <strong>'Advanced settings'</strong> pulsante (1).</p>
        <figure class="img-polaroid">
            <img alt="" src="img/existing-store-1.png"/>
        </figure>
    </li>
    <li>
        <p>Nella schermata apparsa seleziona la directory con il nuovo nome di tema (1) e fai clic su <strong>'Salva'</strong> pulsante (2).</p>
        <figure class="img-polaroid">
            <img alt="" src="img/existing-store-2.png"/>
        </figure>
    </li>
</ol>