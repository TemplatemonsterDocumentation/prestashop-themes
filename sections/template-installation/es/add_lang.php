<h3>Adición de idiomas a la tienda online</h3>
<p>Una vez instalada la plantilla multilingüe, podrá agregar todos los idiomas proporcionados (Inglés, Francés, Español, Alemán, Ruso) a la plataforma de la tienda en línea. Se puede hacer de esta manera: </p>
<p>Vaya a <strong>'Localización> Traducciones'</strong> en el panel de administración, encuentre <strong>'AÑADIR/ACTUALIZAR UN IDIOMA'</strong> y agregue idiomas eligiéndolos en el menú desplegable <small>(1)</small> y haciendo clic en el botón <strong>'Agregar o actualizar un idioma'</strong> <small>(2)</small>.</p>
<figure class="img-polaroid">
    <img alt="" src="img/01-language-update.png"/>
</figure>
<p>Si no desea tener algunos idiomas, puede eliminarlos <small>(2)</small> o desactivarlos <small>(1)</small> en el menú <strong>'Localización> Idiomas'</strong>.</p>
