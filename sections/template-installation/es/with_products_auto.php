<h3>Instalaci&oacute;n autom&aacute;tica con los datos de demostraci&oacute;n
    (instalador de datos de muestra)</h3>
<p class="alert alert-info custom">Si no quieres pasar el tiempo instalando la plantilla, no dudes en <span
            class="label label-warning opener" onclick="showChat()">solicitar</span> el servicio de instalación
    utilizando nuestro chat en vivo.</p>l
<p>Despu&eacute;s de instalar los <strong>datos de demostraci&oacute;n</strong>, tendr&aacute;s
    tu tienda de PrestaShop justo igual que en la plantilla de demostraci&oacute;n en l&iacute;nea.
    Contendr&aacute; todos los productos de demostraci&oacute;n, categor&iacute;as, m&oacute;dulos
    y configuraci&oacute;n de la tienda.</p>

<p class="alert alert-danger">Por favor, no use los de datos de muestra para su sitio web en funcionamiento! Esto
    reemplazar&aacute;
    todos sus productos y ajustes de la tienda.</p>

<p class="alert alert-warning">Por favor, asegúrese que la versión del motor de PrestaShop coincide con la versión
    especificada en la pestaña Technical Details como PrestaShop Engine: 1.X.X.X
    También, asegúrese que el prefijo de la base de datos es <strong>ps _</strong>. Sólo si el motor de PrestaShop
    coincide con la versión especificada, instalar los datos de ejemplo.</p>

<p>Para instalar los datos de muestra utilizando el instalador de los datos de
    demostraci&oacute;n, por favor, siga las instrucciones del paso 1 <a
            href="#themeinstaller">(Importaci&oacute;n del tema)</a> de este manual.
    Luego, en el panel de administraci&oacute;n de la tienda ver&aacute;s el m&oacute;dulo
    para instalar los datos de demostraci&oacute;n.</p>

<p>Usando este m&oacute;dulo puede instalar f&aacute;cilmente los datos de muestra de la
    tienda en unos pocos y sencillos pasos. Por favor, haga lo siguiente:</p>

<ol class="index-list">
    <li>
        <p>En el men&uacute; del panel de administraci&oacute;n haga clic en la opci&oacute;n
            <strong>"Instalar Tienda de Demostraci&oacute;n"</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7.png" alt="">
        </figure>
    </li>
    <li>
        <p>Seleccione y cargue los archivos de datos de ejemplo desde la carpeta
            <strong>'sample_data'</strong> que se encuentra en el paquete de la
            plantilla. Puede hacerlo utilizando el bot&oacute;n <strong>"Examinar
                archivos locales"</strong> o arrastrando los archivos de datos de
            muestra a la ventana del navegador.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_1.png" alt="">
        </figure>
    </li>
    <li>
        <p>Espere hasta que los archivos se carguen.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_2.png" alt="">
        </figure>
    </li>
    <li>
        <p>Luego haga clic en el bot&oacute;n <strong>"Continuar
                Instalaci&oacute;n"</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_3.png" alt="">
        </figure>
    </li>
    <li>
        <p>Then Sample data installer will automatically configure your store and other
            modules. This can take several minutes.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_4.png" alt="">
        </figure>
    </li>
    <li>
        <p>Cuando los datos de la demostraci&oacute;n se hayan instalado correctamente,
            es necesario realizar el &uacute;ltimo paso - regenerar las im&aacute;genes.
            Haga clic en el bot&oacute;n <strong>"Ir a la p&aacute;gina
                Regenerar"</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_5.png" alt="">
        </figure>
    </li>
    <li>
        <p>Para volver a generar las im&aacute;genes haga clic en el bot&oacute;n
            <strong>"Regenerar miniaturas"</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_6.png" alt="">
        </figure>
    </li>
</ol>
<p><strong>Enhorabuena, se ha instalado su tienda de demostraci&oacute;n.</strong></p>