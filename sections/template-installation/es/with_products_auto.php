<h3>Instalación automática con datos de demo (Instalador de datos de muestra)</h3>
<p class="alert alert-info custom">Si no quieres pasar el tiempo instalando la plantilla, no dudes en <span class="label label-warning opener" onclick="showChat()">solicitar</span> el servicio de instalación utilizando nuestro chat en vivo.</p>
<p>Para que su tienda PrestaShop aparezca igual que la demostración en vivo de la plantilla, debe instalar <strong>sample data</strong>. Contendrá todos los productos de la demo, categorías, módulos y configuraciones de tienda.</p>
<p class="alert alert-danger">Por favor, no utilice los datos de muestra para su sitio web ya en ejecución! Esto reemplazará todos los productos de su tienda y los ajustes de la tienda.</p>
<p class="alert alert-warning">Asegúrese de que su versión del motor PrestaShop coincida con la versión especificada en la pestaña "Detalles técnicos", mencionada como "PrestaShop Engine: 1.X.X.X".<br/>
    Además, asegúrese de que su prefijo de tablas de Base de Datos sea <strong>ps_</strong>. Instale los datos de ejemplo sólo si su motor de PrestaShop coincide con la versión especificada. </p>
<p>Para instalar los datos de muestra utilizando el módulo de instalación de datos de muestra, siga las instrucciones del paso 1 <a href="#themeinstaller">(Importación de tema)</a> de este manual. A continuación, en el panel de Administración de la tienda, verá el módulo Instalador de datos de muestra.</p>
<p>Utilizando este módulo, puede instalar fácilmente datos de ejemplo de tienda en pocos pasos sencillos. Por favor haga lo siguiente:</p>
<ol class="index-list">
    <li>
        <p>Desde el panel de administración, haga clic en el item <strong>"Install Demo Store"</strong> .</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-1.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Seleccione y cargue archivos de datos de muestra desde el directorio <strong>'sample_data'</strong> de su paquete de plantillas. Puede hacerlo utilizando el botón <strong>"Browse local files"</strong> arrastrar archivos de datos de muestra a la ventana del navegador.</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-2.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Espere hasta que se carguen los archivos.</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-3.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>A continuación, haga clic en el botón <strong>"Continuar Instalar"</strong> .</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-4.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Entonces el Instalador de datos de muestra configurará automáticamente su tienda y otros módulos. Esto puede tardar varios minutos.</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-5.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Cuando los datos de ejemplo se instalan correctamente, debe realizar el último paso: regenerar imágenes. Haga click en el botón <strong>"Ir a la página de regeneración"</strong> .</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-6.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Para regenerar imágenes, haga clic en el botón <strong>"Regenerar miniaturas"</strong> .</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-7.png" alt=""/>
        </figure>
    </li>
</ol>
<p><strong>Enhorabuena, su tienda de muestra está instalada.</strong></p>