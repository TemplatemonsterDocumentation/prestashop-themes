<h3>Importar Tema</h3>
<p class="alert alert-warning">Nota: este método no proporciona instalación de datos de muestra.</p>
<h4>Preparación de la plantilla</h4>
<p>Antes de continuar, asegúrese de haber descargado la plantilla y extraído los archivos del paquete de plantilla. Puede consultar tutoriales detallados sobre cómo extraer archivos del paquete de plantilla mediante <a target="_blank" href="/help/unzipping-your-template.html">WinZip</a> y <a target="_blank" href="/help/how-unzip-template-mac.html">StuffitExpander</a>.
</p>
<p>Cuando se extraen los archivos, puede proceder a cargar la plantilla.</p>
<h4>Instalación</h4>
<p>Inicie sesión en el panel de Administración de PrestaShop y vaya hasta <strong>Preferencias -> Temas</strong>. En la parte superior de la página, haga clic en el botón <strong>Añadir nuevo tema</strong> (1).
</p>
<figure class="img-polaroid">
    <img src="img/theme-import-1.png" alt=""/>
</figure>
<p>Haga click en el botón <strong>Agregar archivo</strong> (1) en la sección Importar desde su ordenador. Navegue hasta el paquete de plantilla previamente descomprimido y abra la carpeta <strong>themeinstallator</strong> .</p>
<p>Seleccione su archivo .zip de tema y haga clic en <strong>Guardar</strong> (2).</p>
<figure class="img-polaroid">
    <img src="img/theme-import-2.png" alt=""/>
</figure>
<p>Se iniciará el proceso de carga. Verá el mensaje de que el tema se ha cargado correctamente. Su nueva vista previa de tema aparecerá en la lista de temas instalados.</p>

