<h3>Instalación de la plantilla en una tienda existente</h3>
<p class="alert alert-info">Puede utilizar este método de instalación si tiene un sitio web PrestaShop en ejecución y sólo desea instalar una nueva plantilla.</p>
<p class="alert alert-info"><span>Tabla de Configuración de los módulos se puede encontrar de la página de demostración en vivo. Encuentre el enlace 'Sitemap' en el menú superior o en el pie de página y vaya al Sitemap -> Páginas -> Configuración de Páginas.</span></p>
<h4>Carga de archivos de plantilla y activación de temas</h4>
<ol class="index-list">
    <li>Abra el paquete de plantillas.</li>
    <li>Luego vaya a la carpeta <strong>/theme/manual_install/</strong>.</li>
    <li>Suba carpetas <strong>/modules/</strong> y <strong>/themes/</strong> al directorio raíz de PrestaShop.
    <li>
        <p>En tu panel de administración de PrestaShop, acceda a <strong>Preferencias> Temas</strong>, seleccione el tema que ha instalado (también puede utilizar el tema activado, no causará ningún problema para su tema activado). Haga click en botón <strong>'Ajustes avanzados'</strong> (1).</p>
        <figure class="img-polaroid">
            <img alt="" src="img/existing-store-1.png"/>
        </figure>
    </li>
    <li>
        <p>En la pantalla que aparece, seleccione el directorio con su nuevo nombre de tema (1) y haga clic en el botón <strong>'Guardar'</strong> (2).</p>
        <figure class="img-polaroid">
            <img alt="" src="img/existing-store-2.png"/>
        </figure>
    </li>
</ol>