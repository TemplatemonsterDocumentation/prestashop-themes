<h3>Ajout de langues au magasin en ligne</h3>
<p>Une fois le modèle multilingue est installé, vous pourrez ajouter toutes les langues fournies (Anglais, Français, Espagnol, Allemand, Russe) à la plateforme web. On peut le faire de cette façon: </p>
<p>Allez à <strong>'Localisation> Traductions'</strong> dans le panneau d'administration, trouvez <strong>'AJOUTER/METTRE A JOUR UNE LANGUE'</strong> et ajouter des langues en les choisissant dans le menu déroulant <small>(1)</small> et en cliquant sur le bouton <strong>'Ajouter ou Mettre à jour une langue'</strong><small>(2)</small>.</p>
<figure class="img-polaroid">
    <img alt="" src="img/01-language-update.png"/>
</figure>
<p>Si vous ne souhaitez pas avoir certaines langues, vous pouvez les supprimer <small>(2)</small> ou les désactiver <small>(1)</small> dans le menu <strong>'Localisation> Langues'</strong>.</p>
