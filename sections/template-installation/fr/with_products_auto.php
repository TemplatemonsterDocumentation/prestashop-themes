<h3>Installation automatique avec des données de démo (Installeur de données
    d'exemple)</h3>

<p>Après avoir installé les <strong>données d'exemple</strong>, votre boutique
    PrestaShop aura le même aspect que la live demo du template. Elle contiendra les
    produits de démo, les catégories, les modules et les paramètres de boutique.</p>

<p class="alert alert-danger">N'utilisez pas les données sur votre site en production! Ceci remplacera tous les produits
    et paramètres de votre boutique.</p>
<p class="alert alert-warning">Veuillez vous assurer que votre version de l'engine PrestaShop correspond à la version
    spécifiée dans l'onglet de "Détails Techniques", mentionné comme "PrestaShop Engine: 1.X.X.X".
    <br/>Aussi, assurez-vous que votre préfixe de la base de données est <strong>ps_</strong>. Seulement si votre
    version de l'engine PrestaShop correspond à la version spécifiée, veuillez installer les données d'exemple. </p>
<p>Pour installer les données d'exemple avec le module d'installation de données
    d'exemple, veuillez suivre les instruction de l'étape 1 <a href="#themeinstaller">(Theme
        import)</a> de ce manuel. Vous verrez ensuite le module d'installation de
    données d'exemple dans le panneau d'administration de votre boutique.</p>

<p>Avec ce module, vous pouvez facilement installer les données d'exemple en quelques
    étapes simples:</p>
<ol class="index-list">
    <li>
        <p>Depuis le menu du panneau d'administration, cliquez sur l'élément <strong>"Install
                Demo Store"</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7.png" alt="">
        </figure>
    </li>
    <li>
        <p>Sélectionnez et uploadez les fichiers des données d'exemple du dossier
            <strong>'sample_data'</strong> du package de votre template. Vous pouvez le
            faire avec le bouton <strong>"Browse local files"</strong> ou en faisant
            glisser les fichiers de données d'exemple dans la fenêtre du navigateur.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_1.png" alt="">
        </figure>
    </li>
    <li>
        <p>Attendez que les fichiers soient uploadés.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_2.png" alt="">
        </figure>
    </li>
    <li>
        <p>Puis cliquez sur le bouton <strong>"Continue Install"</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_3.png" alt="">
        </figure>
    </li>
    <li>
        <p>L'installeur de données d'exemple configurera automatiquement votre boutique
            et les autres modules. Ceci peut prendre plusieurs minutes.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_4.png" alt="">
        </figure>
    </li>
    <li>
        <p>Une fois les données d'exemple correctement installées, vous devez passer à
            la dernière étape - regénerer les images. Cliquez sur le bouton <strong>"Go
                To Regenerate Page"</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_5.png" alt="">
        </figure>
    </li>
    <li>
        <p>Pour régénérer les images, cliquez sur le bouton <strong>"Regenerate
                thumbnails"</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_6.png" alt="">
        </figure>
    </li>
</ol>
<p><strong>Félicitations, votre boutique de démo est installée.</strong></p>