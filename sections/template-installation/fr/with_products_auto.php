<h3>Installation automatique avec données de échantillon (Sample data installer)</h3>
<p class="alert alert-info custom">Si vous ne voulez pas passer votre temps à installer le template, n'hésitez pas à <span class="label label-warning opener" onclick="showChat()">commander</span> le service d'installation via notre chat en direct.</p>
<p>Pour faire apparaître votre boutique Prestashop comme sur la démonstration en direct du modèle, vous devez installer des <strong>données exemples</strong>. Cela contient  tous les produits de démonstration, catégories, modules et paramètres de magasin.</p>
<p class="alert alert-danger">N'utilisez pas les exemples de données pour votre site Web en cours! Cela remplacera tous les produits et les paramètres de votre magasin.</p>
<p class="alert alert-warning">Assurez-vous que votre version du moteur PrestaShop correspond à la version spécifiée dans l'onglet "Détails techniques", mentionné comme  "PrestaShop Engine: 1.X.X.X".<br/> 
De plus, assurez-vous que le préfixe de tables de base de données est <strong>ps_</strong>. Installez les données d'échantillons uniquement si votre moteur PrestaShop correspond à la version spécifiée.</p>
<p>Pour installer des exemples de données à l'aide du module Sample Data Installer, suivez les instructions de l'étape 1 <a href="#themeinstaller">(Importation de thème)</a> de ce guide. Ensuite, dans le panneau d'administration de votre magasin, vous verrez le module Sample Data Installer.</p>
<p>À l'aide de ce module, vous pouvez facilement installer des données d'échantillons en quelques étapes. Veuillez faire ce qui suit:</p>
<ol class="index-list">
    <li>
        <p>Dans le menu du panneau d'administration, cliquez sur <strong>"Installer Demo Store"</strong>.</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-1.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Sélectionnez et téléchargez des fichiers de données exemples à partir du répertoire<strong>'sample_data'</strong> ocde votre package de modèle. Le bouton <strong>Parcourir<strong> permet de choisir le fichier local à importer ou faites glisser-déposez des exemples de données dans la fenêtre du navigateur.</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-2.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Attendez que les fichiers soient téléchargés.</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-3.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Ensuite, appuyez sur le bouton <strong>"Continuer installation"</strong> .</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-4.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>L'installateur de données d'échantillonnage (Sample data installer) configurera automatiquement votre magasin et d'autres modules. Cela peut durer plusieurs minutes.</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-5.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Lorsque les données d'échantillons sont installées avec succès, Vous devez effectuer la dernière étape - régénérer les images. Cliquez sur le bouton <strong>"Aller à la Page Régénérer"</strong>.</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-6.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Pour régénérer les images, cliquez sur le bouton <strong>"Regénérer Miniatures"</strong>.</p>
        <figure class="img-polaroid">
            <img src="img/auto-install-7.png" alt=""/>
        </figure>
    </li>
</ol>
<p><strong>Félicitations, votre magasin est installé.</strong></p>