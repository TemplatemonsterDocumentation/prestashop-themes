<h3>Installation du modèle sur le magasin existant</h3>
<p class="alert alert-info">Vous pouvez utiliser cette méthode d'installation si vous avez déjà un site PrestaShop en ligne et souhaitez simplement installer un nouveau modèle.</p>
<p class="alert alert-info"><span>Le tableau de configuration des modules peut être trouvé sur la page de démonstration du modèle. 
Trouvez le lien 'Sitemap' dans le menu supérieur ou en bas de page et allez vers Sitemap -> Pages -> Configuration des pages.</span></p>
<h4>Téléchargement de fichiers de modèle et activation de thème</h4>
<ol class="index-list">
    <li>Ouvrez le package modèle.</li>
    <li>Ensuite, allez dans le dossier <strong>/theme/manual_install/</strong>.</li>
    <li>Téléchargez les dossiers <strong>/modules/</strong> et <strong>/themes/</strong> dans le répertoire racine de PrestaShop.</li>
    <li>
        <p>Dans votre panneau d'administration PrestaShop, accédez à <strong>Préférences> Thèmes</strong>, sélectionnez le thème que vous avez installé (vous pouvez utiliser le thème activé aussi, il ne causera aucun problème pour votre thème activé). Cliquez sur le bouton <strong>'Paramètres avancés'</strong>(1).</p>
        <figure class="img-polaroid">
            <img alt="" src="img/existing-store-1.png"/>
        </figure>
    </li>
    <li>
        <p>Sur l'écran apparaissant, sélectionnez le répertoire avec votre nouveau nom de thème (1) cliquez sur le bouton <strong>'Enregistrer'</strong>(2).</p>
        <figure class="img-polaroid">
            <img alt="" src="img/existing-store-2.png"/>
        </figure>
    </li>
</ol>