<h3>Thème d'importation</h3>
<p class="alert alert-warning">Remarque: cette méthode ne fournit pas des données de l'échantillon.</p>
<h4>Préparation du modèle</h4>
<p>Avant de procéder, assurez-vous d'avoir déjà téléchargé le modèle et extrait les fichiers du package. Vous pouvez vérifier les tutoriels détaillés sur comment extraire des fichiers du package modèle en utilisant <a target="_blank" href="/help/unzipping-your-template.html">WinZip</a> et <a target="_blank" href="/help/how-unzip-template-mac.html">StuffitExpander</a>.
</p>
<p>Lorsque les fichiers sont extraits, vous pouvez passer à télécharger le modèle.</p>
<h4>Installation</h4>
<p>Connectez-vous à votre panneau d'administration PrestaShop et navigez à <strong>Préférences -> Thèmes</strong>. En haut de la page, appuyez sur le bouton <strong>Ajouter nouveau thème</strong>(1).
</p>
<figure class="img-polaroid">
    <img src="img/theme-import-1.png" alt=""/>
</figure>
<p>Cliquez sur le bouton <strong>Ajouter fichier</strong> (1) dans la zone Importer à partir de votre ordinateur. Accédez au package de modèle décompressé auparavant et ouvrez le dossier <strong>themeinstallator</strong>.</p>
<p>Sélectionnez le fichier .zip de votre thème et cliquez sur <strong>Sauvegarder</strong> (2).</p>
<figure class="img-polaroid">
    <img src="img/theme-import-2.png" alt=""/>
</figure>
<p>Le processus de téléchargement sera démarré. ous verrez le message que le thème a été téléchargé avec succès. Votre nouvel thème apparaîtra dans la liste des thèmes installés.</p>

