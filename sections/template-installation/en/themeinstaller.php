<h3>Import theme</h3>
<p class="alert alert-warning">Note: this method does not provide sample data installation.</p>
<h4>Template preparation</h4>
<p>Before you proceed, please make sure you have already downloaded the template and extracted the files from the template package. You can check detailed tutorials on how to extract files from the template package using <a target="_blank" href="/help/unzipping-your-template.html">WinZip</a> and <a target="_blank" href="/help/how-unzip-template-mac.html">StuffitExpander</a>.
</p>
<p>When the files are extracted, you can proceed to uploading the template.</p>
<h4>Installation</h4>
<p>Log into your PrestaShop admin panel and navigate to <strong>Preferences -> Themes</strong>. At the top of the page click <strong>Add new theme</strong> button (1).
</p>
<figure class="img-polaroid">
    <img src="img/theme-import-1.png" alt=""/>
</figure>
<p>Click <strong>Add file</strong> button (1) in Import from your computer section. Navigate to previously unzipped template package and open <strong>themeinstallator</strong> folder.</p>
<p>Select your theme .zip file and click <strong>Save</strong> (2).</p>
<figure class="img-polaroid">
    <img src="img/theme-import-2.png" alt=""/>
</figure>
<p>The uploading process will be started. You will see the message that the theme has been successfully uploaded. Your new theme preview will appear in the list of installed themes.</p>

