<h3>Automatic installation with demo data (Sample data installer)</h3>
<p class="alert alert-info custom">If you don't want to spend your time installing the template, don't hesitate to <span
            class="label label-warning opener" onclick="showChat()">order</span> installation service using our
    live-chat.</p>
<p>After installing <strong>sample data</strong>, you'll get your PrestaShop store
    appear the same as at the template live demo. It will contain all demo products,
    categories, modules and store settings.</p>

<p class="alert alert-danger">Please do not use the sample data for your running website! This will replace all your
    store products and store settings.</p>

<p class="alert alert-warning">Please make sure your version of PrestaShop engine matches the specified version in
    "Technical Details" tab, mentioned as "PrestaShop Engine: 1.X.X.X".
    <br/>Also, make sure your database tables prefix is <strong>ps_</strong>. Only if your PrestaShop engine matches
    the specified version, install sample data. </p>

<p>In order to install sample data using the Sample data installer module, please follow
    instruction from the step 1 <a href="#themeinstaller">(Theme import)</a> of this
    manual. Then in your store admin panel you'll see Sample data installer module.</p>

<p>Using this module you can easily install store sample data in few easy steps. Please
    do the following:</p>
<ol class="index-list">
    <li>
        <p>From the admin panel menu click <strong>"Install Demo Store"</strong> item.
        </p>

        <figure class="img-polaroid">
            <img src="img/05-install-7.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Select and upload sample data files from <strong>'sample_data'</strong>
            directory of your template package. You can do this using the <strong>"Browse
                local files"</strong> button or dragging sample data files to the
            browser window.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_1.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Wait till files are uploaded.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_2.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Then click <strong>"Continue Install"</strong> button.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_3.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Then Sample data installer will automatically configure your store and other
            modules. This can take several minutes.</p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_4.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>When sample data is installed successfully, you need to perform the last step
            - regenerate images. Click <strong>"Go To Regenerate Page"</strong> button.
        </p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_5.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>To regenerate images click <strong>"Regenerate thumbnails"</strong> button.
        </p>

        <figure class="img-polaroid">
            <img src="img/05-install-7_6.png" alt=""/>
        </figure>
    </li>
</ol>
<p><strong>Congratulations, your demo store is installed.</strong></p>