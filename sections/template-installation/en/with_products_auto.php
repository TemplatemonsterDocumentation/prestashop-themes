<h3>Automatic installation with demo data (Sample data installer)</h3>
<p class="alert alert-info custom">If you don't want to spend your time installing the template, don't hesitate to <span class="label label-warning opener" onclick="showChat()">order</span> installation service using our live-chat.</p>
<p>To make your PrestaShop store appear the same as template live demo, you need to install <strong>sample data</strong>.
    It will contain all demo products, categories, modules and store settings.</p>
<p class="alert alert-danger">Please do not use the sample data for your running website! This will replace all your
    store products and store settings.</p>
<p class="alert alert-warning">Please make sure your version of PrestaShop engine matches the specified version in
    "Technical Details" tab, mentioned as "PrestaShop Engine: 1.X.X.X".<br/>
    Also, make sure your database tables prefix is <strong>ps_</strong>. Install sample data only if your PrestaShop
    engine matches the specified version. </p>
<p>In order to install sample data using the Sample data installer module, please follow instruction from the step 1
    (Theme import) of this manual. Then in your store admin panel you'll see Sample data installer module.</p>
<p>Using this module you can easily install store sample data in few easy steps. Please do the following:</p>
<ol class="index-list">
    <li>
        <p>From the admin panel menu click <strong>"Install Demo Store"</strong> item.</p>
        <figure class="img-polaroid">
            <img src="img/sample-data-install-1.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Select and upload sample data files from <strong>'sample_data'</strong> directory of your template package.
            You can do this using the <strong>"Browse local files"</strong> button or dragging sample data files to the
            browser window.</p>
        <figure class="img-polaroid">
            <img src="img/sample-data-install-2.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>You will see the pop-up window offering different types of sample data installation. By default it's set to
            'Load all content' type (1). This means that all sample data will be imported to your store. If that's what
            you need, click the 'Confirm' button.</p>
        <figure class="img-polaroid">
            <img src="img/sample-data-install-3.png" alt=""/>
        </figure>
        <p>If you do not need to install all sample data, uncheck 'Load all content' (1), choose from the required
            options below by checking options the same way (2) and confirm your choice (3).</p>
        <figure class="img-polaroid">
            <img src="img/sample-data-install-4.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Wait until the files are uploaded.</p>
        <figure class="img-polaroid">
            <img src="img/sample-data-install-5.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Then click <strong>"Continue Install"</strong> button.</p>
        <figure class="img-polaroid">
            <img src="img/sample-data-install-6.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Then Sample data installer will automatically configure your store and other modules. This can take several
            minutes.</p>
        <figure class="img-polaroid">
            <img src="img/sample-data-install-7.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>When sample data is installed successfully, you need to perform the last step - regenerate images.</p>
        <figure class="img-polaroid">
            <img src="img/sample-data-install-8.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Click <strong>"Go To Regenerate Page"</strong> button at the bottom of the page.</p>
        <figure class="img-polaroid">
            <img src="img/sample-data-install-9.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>To regenerate images click <strong>"Regenerate thumbnails"</strong> button.</p>
        <figure class="img-polaroid">
            <img src="img/sample-data-install-10.png" alt=""/>
        </figure>
    </li>
</ol>
<p><strong>Congratulations, your demo store is installed.</strong></p>