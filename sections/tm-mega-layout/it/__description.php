<h5>Ottimizzazione di stili e script</h5>
<p>L'ottimizzazione degli stili e degli script aggiunge esclusioni per i moduli nella sezione Intestazione, in base al loro utilizzo nei preset. Ciò consente di accelerare la velocità di caricamento della pagina. L'ottimizzazione funziona in modalità di prova.</p>
<p>Attivazione dell'ottimizzazione:</p>
<ul class="index-list">
    <li>
        <p>Per attivare l'ottimizzazione, aprire la scheda 'Strumenti' (1) e selezionare la scheda 'Opzioni' (2).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-optimization-1.png" alt=""></figure>
    </li>
    <li>
        <p>Nella finestra apparente, è possibile attivare l'ottimizzazione (3).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-optimization-2.png" alt=""></figure>
    </li>
</ul>
<p>Se l'ottimizzazione è abilitata quando si modifica ed abilita i preset, l'ottimizzazione viene eliminata, ma vedrai un'offerta per l'esecuzione dell'ottimizzazione. Non è necessario ottimizzare gli script e gli stili ogni volta che si cambiano i preset. È meglio ottimizzarli quando hai finito di modificare.</p>
<figure class="img-polaroid"><img src="img/tmmegalayout100-optimization-3.png" alt=""></figure>

<h5>Messa in piega</h5>
<ul class="index-list">
    <li>
