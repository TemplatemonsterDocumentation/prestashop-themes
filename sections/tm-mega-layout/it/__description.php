            <li>ripetizione di sfondo (ripetizione di sfondo) - tutte le opzioni disponibili sono elencate nei pulsanti di scelta rapida.</li>
            <li>background-position (background posizione) - tutte le opzioni disponibili sono elencate nell'elenco a discesa.</li>
            <li>background-size (background dimensione) - tutte le opzioni disponibili sono elencate nell'elenco a discesa.</li>
            <li>background-origin (background regione di posizionamento dell'immagine) - tutte le opzioni disponibili sono elencate nell'elenco a discesa.</li>
