<h4>Modul</h4>
<p>Dieses Modul gehört zu diesem Hook.</p>
<h5>Wie man das Modul hinzufügt</h5>
<p class="alert alert-warning"><span>Seien Sie vorsichtig, wenn Sie das Modul zur Voreinstellung hinzufügen, einige Module müssen nicht das dargestellte Layout (zum Beispiel, das Modul 'Data mining for statistics') ändern.</span></p>
<ul class="index-list">
    <li>
        <p>Um das Modul zur Voreinstellung im Block Spalten hinzuzufügen, öffnen Sie das Menü (das Zeichen 'Auslassungspunkte') (1).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-add-module-1.png" alt=""></figure>
    </li>
    <li>
        <p>Drücken Sie '+ Modul hinzufügen' (2) in der Liste der verfügbaren Einstellungen.</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-add-module-2.png" alt=""></figure>
    </li>
    <li>
        <p>Wenn es nötig ist, können Sie eine benutzerdefinierte Klasse (3) in das DropDown Fenster eingeben und das Modul (4) wählen. Danach drücken Sie auf den Button 'Bestätigen' (5).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-add-module-3.png" alt=""></figure>
    </li>
    <li>
        <p>Das Modul wurde erfolgreich hinzugefügt.</p>
    </li>
</ul>

<h5>Wie man das Modul löscht</h5>
<p class="alert alert-warning"><span>Wenn Sie das Modul löschen, werden auch seine geordneten Elemente und Stile gelöscht.</span></p>
<ul class="index-list">
    <li>
        <p>Um das Modul zu löschen, öffnen Sie das Menü (das Zeichen 'Auslassungspunkte') (1) in dem Block Modul.</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-delete-module-1.png" alt=""></figure>
    </li>
    <li>
        <p>Drücken Sie auf den Button 'Löschen' (2) in der Liste der verfügbaren Einstellungen.</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-delete-module-2.png" alt=""></figure>
    </li>
    <li>
        <p>Das Modul wurde gelöscht.</p>
    </li>
</ul>

<h5>Wie man eine benutzerdefinierte Klasse zum Modul hinzufügt</h5>
<p>Indem Sie eine benutzerdefinierte Klasse hinzufügen, können Sie Stile des Moduls mit den CSS-Regeln erstellen.</p>
<ul class="index-list">
    <li>
        <p>Um eine benutzerdefinierte Klasse im Modul festzulegen, öffnen Sie das Menü im Block Modul (das Zeichen 'Auslassungspunkte') (1).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-module-add-class-1.png" alt=""></figure>
    </li>
    <li>
        <p>Drücken Sie auf den Button 'Einstellungen ändern' (2).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-module-add-class-2.png" alt=""></figure>
    </li>
    <li>
        <p>Legen Sie die benutzerdefinierte Klasse (3) in dem DropDown Fenster fest und drücken Sie auf den Button 'Bestätigen' (4).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-module-add-class-3.png" alt=""></figure>
    </li>
    <li>
        <p>Die benutzerdefinierte Klasse wurde hinzugefügt.</p>
    </li>
</ul>

<h5>Logo und Urheberrechtserklärung</h5>
<p>Die Blöcke Logo und Urheberrechtserklärung werden genauso wie Module geändert. Aber es gibt eine Ausnahme, das Logo kann nur entweder im Header oder im Footer platziert werden und der Block Urheberrechtserklärung kann nur im Footer platziert werden.</p>

<h5>Wie man Voreinstellungen exportiert</h5>
<p> Das Exportwerkzeug ermöglicht es Ihnen, Voreinstellungen für den Import in verschiedenen Shops zu exportieren.</p>
<ul class="index-list">
    <li>
        <p>Finden Sie den Tab 'Werkzeuge' (1) im Haupteinstellungsfenster.</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-export-preset-1.png" alt=""></figure>
    </li>
    <li>
        <p>Wählen Sie 'Exportieren' (2) im Formular.</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-export-preset-2.png" alt=""></figure>
    </li>
    <li>
        <p> Sie sehen die Liste der bestehenden Voreinstellungen.</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-export-preset-3.png" alt=""></figure>
    </li>
    <li>
        <p>Um eine Voreinstellung vorher zu sehen, drücken Sie auf den Button 'Vorschau des Layouts' (3) neben der gewünschten Voreinstellung. Um sie zu exportieren, drücken Sie auf den Button 'Layout exportieren' (4).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-export-preset-4.png" alt=""></figure>
    </li>
    <li>
        <p> Danach können Sie diese Voreinstellung als Zip-Archiv hochladen.</p>
    </li>
</ul>

<h5>Wie man eine Voreinstellung importiert</h5>
<p class="alert alert-warning"><span> Das Importwerkzeug unterstützt nur * .zip Dateien der Voreinstellungen, die vorher mit Hilfe von demselben Modul «Export» erzeugt wurden.</span></p>
<ul class="index-list">
    <li>
        <p>Um Ihre Voreinstellung zu importieren, öffnen Sie den Tab <strong>Werkzeuge (1)</strong> (Tools (1)) und wählen Sie <strong>'Import' (2)</strong> ('Import' (2)).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-import-preset-1.png" alt="" /></figure>
    </li>
    <li>
        <p>Danach drücken Sie auf den Button <strong>Datei hinzufügen (3)</strong> (Add file (3)) und wählen Sie die Zip Datei der exportierten Voreinstellung aus. Wenn dieses Archiv als eine exportierte Voreinstellung vorgestellt ist, sehen Sie die Vorschau dieser Voreinstellung.</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-import-preset-2.png" alt="" /></figure>
    </li>
    <li>
        <p>Wenn Sie diese Voreinstellung brauchen, drücken Sie <strong>'Import' (4)</strong> ('Import' (4)). Die Voreinstellung wird importiert und zur Liste der verfügbaren Voreinstellungen hinzugefügt.</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-import-preset-3.png" alt="" /></figure>
    </li>
</ul>

<h5>Zurücksetzen</h5>
<p>«Zurücksetzen» setzt alle Voreinstellungen in ursprünglichen Zustand zurück (wie bei der Installation der Vorlage), und löscht alle erstellten Voreinstellungen.</p>
<p class="alert alert-warning"><span> Achtung! Wenn Sie die Voreinstellungen auf ihren Standardzustand zurücksetzen, werden alle benutzerdefinierten Voreinstellungen gelöscht!</span></p>
<ul class="index-list">
    <li>
        <p>Um die Voreinstellungen zurückzusetzen, finden Sie den Tab 'Werkzeuge' (1). In dem angezeigten Block drücken Sie auf den Button 'Einstellungen' (2).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-reset-1.png" alt=""></figure>
    </li>
    <li>
        <p>Drücken Sie auf den Button 'Zurücksetzen'.</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-reset-2.png" alt=""></figure>
    </li>
    <li>
        <p>Das war's! Alle Voreinstellungen wurden auf ihren Standardzustand zurückgesetzt.</p>
    </li>
</ul>

