<h5>Wie man Stile und Skripte optimiert</h5>
<p> Indem Sie Stile und Skripte optimieren, fügen Sie Ausnahmen zu Modulen im Bereich Header abhängig von ihrer Verwendung in einer Voreinstellung hinzu. Dadurch wird die Seite schneller geladen. Die Optimierung funktioniert im im Testmodus.</p>
<p>Wie man die Optimierung aktiviert:</p>
<ul class="index-list">
    <li>
        <p>Um die Optimierung zu aktivieren, öffnen Sie den Tab 'Werkzeuge' (1) und wählen Sie den Tab 'Einstellungen' (2).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-optimization-1.png" alt=""></figure>
    </li>
    <li>
        <p> In dem DropDown Fenster können Sie die Optimierung aktivieren (3).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-optimization-2.png" alt=""></figure>
    </li>
</ul>
<p>Wenn die Optimierung bei der Bearbeitung und Aktivierung der Voreinstellung aktiviert ist, wird die Optimierung zurückgesetzt. Aber Sie sehen ein Fenster zur Durchführung der Optimierung. Es gibt keine Notwendigkeit, Skripte und Stile jedesmal zu optimieren, wenn Sie Voreinstellungen ändern. Optimieren Sie sie besser nach der Bearbeitung.</p>
<figure class="img-polaroid"><img src="img/tmmegalayout100-optimization-3.png" alt=""></figure>

