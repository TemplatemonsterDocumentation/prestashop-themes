<h4>Module</h4>
<p>Module affecté à ce crochet.</p>
<h5>Ajout du module</h5>
<p class="alert alert-warning"><span>Soyez prudent lors de l'ajout de modules au préréglage, certains des modules sont souhaitables à utiliser s'ils ne changent pas leur disposition visible, par ex., 
le module 'Récupération des données statistiques'.</span></p>
<ul class="index-list">
    <li>
        <p>Pour ajouter le module au préréglage dans un bloc, ouvrez le menu (l'icône 'ellipse') (1).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-add-module-1.png" alt=""></figure>
    </li>
    <li>
        <p>Appuyez sur '+ Ajouter Module' (2) dans la liste des options disponibles.</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-add-module-2.png" alt=""></figure>
    </li>
    <li>
        <p>Si nécessaire, vous pouvez entrer la classe personnalisée (3) dans la fenêtre contextuelle et sélectionnez le module (4), ensuite, appuyez sur 'Confirmer' (5).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-add-module-3.png" alt=""></figure>
    </li>
    <li>
        <p>Le module a été ajouté avec succès.</p>
    </li>
</ul>

<h5>Suppression du module</h5>
<p class="alert alert-warning"><span>L'élimination de l'élément préréglé causera la suppression de ses objets enfants et de leurs styles.</span></p>
<ul class="index-list">
    <li>
        <p>Pour supprimer le module, ouvrez le menu (l'icône 'ellipse') (1) dans le bloc de module.</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-delete-module-1.png" alt=""></figure>
    </li>
    <li>
        <p>Appuyez sur 'Supprimer' (2) dans la liste des options disponibles.</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-delete-module-2.png" alt=""></figure>
    </li>
    <li>
        <p>Le module est retiré.</p>
    </li>
</ul>

<h5>Ajout de classe personnalisée au module</h5>
<p>La classe personnalisée permet de créer un style de module avec les règles CSS.</p>
<ul class="index-list">
    <li>
        <p>Pour spécifier une classe personnalisée pour le module, ouvrez le menu dans le bloc de module(l'icône 'ellipse') (1).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-module-add-class-1.png" alt=""></figure>
    </li>
    <li>
        <p>Ensuite, appuyez sur 'Modifier Paramètres' (2).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-module-add-class-2.png" alt=""></figure>
    </li>
    <li>
        <p>Spécifiez une classe personnalisée (3) dans la fenêtre contextuelle et appuyez sur le bouton 'Confirmer' (4).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-module-add-class-3.png" alt=""></figure>
    </li>
    <li>
        <p>Une classe personnalisée a été ajoutée.</p>
    </li>
</ul>

<h5>Logo, Droits D'auteur</h5>
<p>Les blocs Logo et Droit D'auteur sont édités de la même manière que les modules, sauf que Logo peut être placé dans la position de l'en-tête ou du pied de page, et le bloc Droit D'auteur  peut être placé uniquement en position de pied de page.</p>

<h5>Exportation de Préréglages</h5>
<p>L'outil Export vous permet d'exporter des préréglages pour l'importation supplémentaire dans différents magasins.</p>
<ul class="index-list">
    <li>
        <p>Accédez à l'onglet 'Outils'(1) dans le panneau des configurations principales.</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-export-preset-1.png" alt=""></figure>
    </li>
    <li>
        <p>Choisissez 'Exporter' (2) dans la forme.</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-export-preset-2.png" alt=""></figure>
    </li>
    <li>
        <p>Vous verrez la liste des préréglages existants dans les crochets de site.</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-export-preset-3.png" alt=""></figure>
    </li>
    <li>
        <p>Pour prévisualiser le préréglage, appuyez sur le bouton 'Prévisualisation' (3) à côté du préréglage dont vous avez besoin, pour exporter, appuyez sur le bouton 'Exporter Mise en Page' (4).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-export-preset-4.png" alt=""></figure>
    </li>
    <li>
        <p>Ensuite, vous pourrez télécharger ce préréglage sous forme d'archive zip.</p>
    </li>
</ul>

<h5>Importation de Préréglages</h5>
<p class="alert alert-warning"><span>L'outil d'importation n'accepte que les préréglages * .zip qui ont déjà été générés avec l'outil Export du même module.</span></p>
<ul class="index-list">
    <li>
        <p>Pour importer votre préréglage, vous devez ouvrir l'onglet <strong>Outils (1)</strong> et choisissez <strong>'Importer' (2)</strong>.</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-import-preset-1.png" alt="" /></figure>
    </li>
    <li>
        <p>Ensuite, appuyez sur le bouton <strong>Ajouter Fichier (3)</strong> et sélectionnez le fichier zip de votre préréglage exporté. Si cette archive est le préréglage exporté, vous verrez cet aperçu prédéfini.</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-import-preset-2.png" alt="" /></figure>
    </li>
    <li>
        <p>Si ce préréglage répond à vos besoins, appuyez sur <strong>'Import' (4)</strong>. Le préréglage sera importé et ajouté à la liste des préréglages disponibles.</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-import-preset-3.png" alt="" /></figure>
    </li>
</ul>

<h5>Réinitialisation</h5>
<p>'Réinitialisation' revient tous les préréglages à leur état initial (à la manière dont ils sont venus avec le modèle), et supprime tous les préréglages créés.</p>
<p class="alert alert-warning"><span>Attention! Après un retour à l'état par défaut, tous les préréglages personnalisés seront éliminés!</span></p>
<ul class="index-list">
    <li>
        <p>Pour réinitialiser les préréglages, accédez à l'onglet 'Outils' (1). Dans le bloc apparu, ouvrez l'onglet 'Options' (2).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-reset-1.png" alt=""></figure>
    </li>
    <li>
        <p>Appuyez sur le bouton 'Réinitialiser'.</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-reset-2.png" alt=""></figure>
    </li>
    <li>
        <p>C'est tout! Tous les préréglages sont réinitialisés à l'état initial.</p>
    </li>
</ul>

