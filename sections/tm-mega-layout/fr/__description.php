<h5>Optimisation des styles et des scripts</h5>
<p>Optimisation des Styles et des Scripts ajoute des exclusions pour les modules dans la section En-tête, en fonction de leur utilisation dans les préréglages. Cela permet d'accélérer la vitesse de chargement de la page. L'optimisation fonctionne en mode test.</p>
<p>Activation de l'optimisation:</p>
<ul class="index-list">
    <li>
        <p>Pour activer l'optimisation, ouvrez l'onglet 'Outils' (1) et sélectionnez l'onglet 'Options' (2).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-optimization-1.png" alt=""></figure>
    </li>
    <li>
        <p>Dans la fenêtre apparue, vous pouvez activer l'optimisation (3).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-optimization-2.png" alt=""></figure>
    </li>
</ul>
<p>Si l'optimisation est activée lors de l'édition et de l'activation des préréglages, l'optimisation  est rejetée, mais vous verrez une fenêtre offrant pour lancer l'optimisation. Il n'y a pas besoin d'optimiser les scripts et les styles chaque fois vous modifiez les préréglages. Il est préférable de les optimiser quand vous avez terminé l'édition.</p>
<figure class="img-polaroid"><img src="img/tmmegalayout100-optimization-3.png" alt=""></figure>

<h5>Styles</h5>
<ul class="index-list">
    <li>
        <p>Styles de fond. Description des champs:</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-styles-1.png" alt="" /></figure>
        <ul class="marked-list">
            <li>background-image (mage de fond) - l'URL de l'image ou la valeur "none". L'image est choisie dans le dossier ~/images/cms/.</li>
            <li>background-color (couleur de fond) - la valeur peut être définie dans différents formats de couleur (rgb, hex, etc.). Vous pouvez choisir la couleur avec l'outil Pipette à couleurs.</li>
            <li>background-repeat (répétition du fond) - toutes les options disponibles sont listées dans les boutons radio.</li>
            <li>background-position (position de fond) - toutes les options disponibles sont listées dans la liste déroulante.</li>
            <li>background-size (taille de fond) - toutes les options disponibles sont listées dans la liste déroulante.</li>
            <li>background-origin (région de positionnement de fond) - toutes les options disponibles sont listées dans la liste déroulante.</li>
        </ul>
    </li>
    <li><p>Styles de bordure.</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-styles-2.png" alt="" /></figure>
        <ul class="marked-list">
            <li>border-top-style, border-right-style, border-left-style, border-bottom-style (styles de bordure) - toutes les options disponibles sont listées dans la liste déroulante.</li>
            <li>border-top-width, border-right-width, border-left-width, border-bottom-width (largeur des bordures) - sevrait être configuré en pixels.</li>
            <li>border-top-сolor, border-right-color, border-left-color, border-bottom-color (couleur des bordures) - la valeur peut être définie dans différents formats de couleur (rgb, hex, etc.). Vous pouvez choisir la couleur avec l'outil Pipette à couleurs.</li>
            <li>border-top-right-radius, border-top-left-radius, border-bottom-right-radius, border-bottom-left-radius (rayon de bordure) - sevrait être configuré en pixels.</li>
        </ul>
    </li>
    <li><p>Autres styles.</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-styles-3.png" alt="" /></figure>
        <ul class="marked-list">
            <li>box-shadow (ombre de la boîte) - exemple:0px 0px 0px 0px rgba(0,0,0,0.75).</li>
        </ul>
    </li>
</ul>

