<h5>Optimización de estilos y secuencias de comandos</h5>
<p>La optimización de estilos y secuencias agrega exclusiones para los módulos en la sección Encabezado, en función de su uso en preajustes. Esto permite acelerar la velocidad de carga de la página. La optimización funciona en modo de prueba.</p>
<p>Activación de la optimización:</p>
<ul class="index-list">
    <li>
        <p>Para activar la optimización, abra la pestaña 'Herramientas' (1) y seleccione la pestaña 'Opciones' (2).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-optimization-1.png" alt=""></figure>
    </li>
    <li>
        <p>En la ventana que aparece, puede activar la optimización (3).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-optimization-2.png" alt=""></figure>
    </li>
</ul>
<p>Si la optimización está habilitada al editar y habilitar presets, la optimización se descarta, pero verá una ventana que ofrece ejecutar la optimización. No hay necesidad de optimizar los scripts y los estilos cada vez que cambie presets. Es mejor optimizarlos cuando termines de editar.</p>
<figure class="img-polaroid"><img src="img/tmmegalayout100-optimization-3.png" alt=""></figure>

