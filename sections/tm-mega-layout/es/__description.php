<h5>Logo, Copyright</h5>
<p>Los bloques de Logotipo y de Copyright se editan de la misma forma que los módulos, excepto por el hecho de que el logotipo sólo se puede colocar en la posición del encabezado o pie de página y el bloque de Copyright sólo se puede colocar en la posición de pie de página.</p>

