<h4>Модуль</h4>
<p>Модуль, закреплённый за этим хуком.</p>
<h5>Добавление модуля</h5>
<p class="alert alert-warning"><span>Будьте осторожны при добавлении модуля в пресет, некоторые модули не должны изменять отображаемый макет, например, модуль 'Data mining for statistics'.</span></p>
<ul class="index-list">
    <li>
        <p>Для того чтобы добавить модуль в пресет в блоке колонки, откройте меню (значок 'многоточие') (1).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-add-module-1.png" alt=""></figure>
    </li>
    <li>
        <p>Нажмите на '+ Добавить модуль' (2) в списке доступных настроек.</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-add-module-2.png" alt=""></figure>
    </li>
    <li>
        <p>Если нужно, Вы можете ввести пользовательский класс (3) во всплывающее окно и выбрать модуль (4), затем, нажмите на кнопку 'Подтвердить' (5).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-add-module-3.png" alt=""></figure>
    </li>
    <li>
        <p>Модуль был успешно добавлен.</p>
    </li>
</ul>

<h5>Удаление модуля</h5>
<p class="alert alert-warning"><span>Удаление элемента пресета повлечёт также удаление его дочерних элементов и их стилей.</span></p>
<ul class="index-list">
    <li>
        <p>Для того чтобы удалить модуль, откройте меню (значок 'многоточие') (1) в блоке модуля.</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-delete-module-1.png" alt=""></figure>
    </li>
    <li>
        <p>Нажмите на кнопку 'Удалить' (2) в списке доступных настроек.</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-delete-module-2.png" alt=""></figure>
    </li>
    <li>
        <p>Модуль удалён.</p>
    </li>
</ul>

<h5>Добавление пользовательского класса в модуль</h5>
<p>Добавление пользовательского класса позволяет создать стили модуля, используя правила CSS.</p>
<ul class="index-list">
    <li>
        <p>Для того чтобы указать пользовательский класс модуля, откройте меню в блоке модуля (значок 'многоточие') (1).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-module-add-class-1.png" alt=""></figure>
    </li>
    <li>
        <p>Нажмите на кнопку 'Изменить настройки' (2).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-module-add-class-2.png" alt=""></figure>
    </li>
    <li>
        <p>Укажите пользовательский класс (3) во всплывающем окне и нажмите на кнопку 'Подтвердить' (4).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-module-add-class-3.png" alt=""></figure>
    </li>
    <li>
        <p>Пользовательский класс добавлен.</p>
    </li>
</ul>

<h5>Логотип, Сообщение об авторских правах</h5>
<p>Блоки Логотип и Сообщение об авторских правах изменяются так же, как модули, за исключением факта, что Логотип можно разместить только в позициях Хедера и Футера, а блок Сообщения об авторских правах можно разместить только в Футере.</p>

<h5>Экспорт пресетов</h5>
<p> Инструмент экспорта позволяет Вам экспортировать пресеты для дальнейшего импорта в разные магазины.</p>
<ul class="index-list">
    <li>
        <p>Перейдите на вкладку 'Инструменты' (1) в основной панели настроек.</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-export-preset-1.png" alt=""></figure>
    </li>
    <li>
        <p>Выберите 'Экспорт' (2) в форме.</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-export-preset-2.png" alt=""></figure>
    </li>
    <li>
        <p> Вы увидите список существующих пресетов в хуках сайтов.</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-export-preset-3.png" alt=""></figure>
    </li>
    <li>
        <p>Для того чтобы предварительно просмотреть пресет, нажмите на кнопку 'Предварительный просмотр макета' (3) рядом с нужным Вам пресетом. Для того чтобы экспортировать, нажмите на кнопку 'Экспортировать макет' (4).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-export-preset-4.png" alt=""></figure>
    </li>
    <li>
        <p> Затем, Вы сможете загрузить этот пресет в виде zip-архива.</p>
    </li>
</ul>

<h5>Импорт пресета</h5>
<p class="alert alert-warning"><span> Инструмент импорта принимает только * .zip файлы пресетов, которые ранее были сгенерированы с помощью такого же модуля «Экспорт».</span></p>
<ul class="index-list">
    <li>
        <p>Для того чтобы импортировать ваш пресет, откройте вкладку <strong>Инструменты (1)</strong> (Tools (1)) и выберите <strong>'Импорт' (2)</strong> ('Import' (2)).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-import-preset-1.png" alt="" /></figure>
    </li>
    <li>
        <p>Затем, нажмите на кнопку <strong>Добавить файл (3)</strong> (Add file (3)) и выберите zip файл экспортированного пресета. Если этот архив представляет собой экспортированный пресет, Вы увидите предварительный  просмотр пресета.</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-import-preset-2.png" alt="" /></figure>
    </li>
    <li>
        <p>Если этот пресет Вам подходит, нажмите на <strong>'Импорт' (4)</strong> ('Import' (4)). Пресет будет импортирован и добавлен в список доступных пресетов.</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-import-preset-3.png" alt="" /></figure>
    </li>
</ul>

<h5>Сброс</h5>
<p>«Сброс» возвращает все пресеты в исходное состояние (в то, в котором они были при установке шаблона), и удаляет все созданные пресеты.</p>
<p class="alert alert-warning"><span> Внимание! После возврата пресетов в состояние по умолчанию все пользовательские пресеты будут удалены!</span></p>
<ul class="index-list">
    <li>
        <p>Для того чтобы сбросить пресеты, перейдите на вкладку 'Инструменты' (1). В отобразившемся блоке нажмите на кнопку 'Настройки' (2).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-reset-1.png" alt=""></figure>
    </li>
    <li>
        <p>Нажмите на кнопку 'Сброс'.</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-reset-2.png" alt=""></figure>
    </li>
    <li>
        <p> Вот и всё! Все пресеты сброшены до исходного состояния.</p>
    </li>
</ul>

