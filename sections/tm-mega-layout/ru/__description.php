<h5> Оптимизация стилей и скриптов </h5>
<p> Оптимизация стилей и скриптов добавляет исключения для модулей в разделе Хедера в зависимости от их использования в пресете. Это позволяет ускорить загрузку страницы. Оптимизация работает в тестовом режиме.</p>
<p>Включение оптимизации:</p>
<ul class="index-list">
    <li>
        <p>Для того чтобы включить оптимизацию, откройте вкладку 'Инструменты' (1) и выберите вкладку 'Настройки' (2).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-optimization-1.png" alt=""></figure>
    </li>
    <li>
        <p> В открывшемся окне, Вы можете включить оптимизацию (3).</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-optimization-2.png" alt=""></figure>
    </li>
</ul>
<p>Если оптимизация включена при редактировании и включении пресетов, оптимизация будет отброшена, но Вы увидите окно, предлагающее выполнить оптимизацию. Нет необходимости оптимизировать скрипты и стили каждый раз, когда Вы меняете пресеты. Оптимизировать их лучше, когда Вы закончите редактирование.</p>
<figure class="img-polaroid"><img src="img/tmmegalayout100-optimization-3.png" alt=""></figure>

<h5>Стили</h5>
<ul class="index-list">
    <li>
        <p> Фоновые стили. Описание полей:</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-styles-1.png" alt="" /></figure>
        <ul class="marked-list">
            <li>background-image (фоновое изображение) – ссылка изображения или значение 'нет'. Выберите изображение из папки ~/images/cms/.</li>
            <li>background-color (фоновый цвет) – задайте значение в любом формате цвета (rgb, hex, и т.д.) Вы можете выбрать цвет, используя инструмент colorpicker.</li>
            <li>background-repeat (повторение фона) - все доступные опции перечислены во флаговых кнопках.</li>
            <li>background-position (позиция фона) - все доступные опции перечислены в выпадающем списке.</li>
            <li>background-size (размер фона) - все доступные опции перечислены в выпадающем списке.</li>
            <li>background-origin (область размещения фонового изображения) - все доступные опции перечислены в выпадающем списке.</li>
        </ul>
    </li>
    <li><p>Стили границы.</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-styles-2.png" alt="" /></figure>
        <ul class="marked-list">
            <li>border-top-style, border-right-style, border-left-style, border-bottom-style (стили границ) – все доступные настройки перечислены в выпадающем списке.</li>
            <li>border-top-width, border-right-width, border-left-width, border-bottom-width (ширина границ) – укажите значения в пикселях.</li>
            <li>border-top-сolor, border-right-color, border-left-color, border-bottom-color (цвета границ) – задайте значения в любом формате цвета (rgb, hex, и т.д.) Выберите цвет, используя инструмент colorpicker.</li>
            <li>border-top-right-radius, border-top-left-radius, border-bottom-right-radius, border-bottom-left-radius (радиус границы) – задайте значение в пикселях.</li>
        </ul>
    </li>
    <li><p>Прочие стили.</p>
        <figure class="img-polaroid"><img src="img/tmmegalayout100-styles-3.png" alt="" /></figure>
        <ul class="marked-list">
            <li>box-shadow (тень блока) - например: 0px 0px 0px 0px rgba(0,0,0,0.75).</li>
        </ul>
    </li>
</ul>

