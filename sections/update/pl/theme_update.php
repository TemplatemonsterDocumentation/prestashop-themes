<h3>Jak zaktualizować Eveprest</h3>
<p class="alert alert-warning">Zmiany których dokonujesz mogą wpłynąć na Twój istniejący sklep, a więc zdecydowanie
    zalecamy sporządzić kopię zapasową swojej witryny internetowej przed przystąpieniem do jej aktualizacji.</p>
<p>Aby pobrać zaktualizowaną wersję Eveprest, należy przywrócić link do pobrania. Następnie, pobierz ponownie archiwum
    Eveprest.</p>
<p>Aby zastosować nowy motyw, należy go dodać, korzystając z instrukcji zawartych w sekcji instalowania Szablonu.</p>
<p>Aby dodać i wykorzystywać nowe moduły, otwórz folder aktualizacji w archiwum regulowania, skopiuj odpowiednie foldery
    do swojego folderu /modules oraz zainstaluj ich z panelu administracyjnego.</p>
