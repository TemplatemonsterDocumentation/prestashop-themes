<h3>Wie man Eveprest aktualisiert</h3>
<p class="alert alert-warning">Die vorgenommenen von Ihnen Änderungen können auf den bestehenden Online-Shop wirken, aus diesem Grund ist es dringed empfohlen, die Sicherungskopie Ihrer Website zu erstellen, bevor Sie sie aktualisieren.</p>
<p> Um die aktualisierte Eveprest Version zu erhalten, sollen Sie den Download-Link wiederherstellen. Danach laden Sie das Eveprest Archiv noch einmal hoch.</p>
<p>Um ein neues Theme nutzen zu können, sollen Sie es hinzufügen, indem Sie die Anleitungen im Bereich «Installation» nutzen. </p>
<p> Um neue Module hinzuzufügen und zu nutzen, öffnen Sie den Ordner Aktualisierung im Archiv flagman, kopieren Sie entsprechende Ordner in den Ordner /modules und installieren Sie sie im Control Panel.</p>


