<h3>TM Manufacturers Block</h3>

<p>This module shows up manufacturers available in your store. The list of manufacturers
    may consist of the image and title. The list can also show up as a carousel.</p>
<h5>Installing and Deleting </h5>

<p>The module is installed and deleted like any other Prestashop module.</p>
<h5>Module configuration</h5>

<p>After installation you can change its settings on the module configuration page.</p>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Display name</dt>
            <dd>show the manufacturer's name.</dd>
        </dl></li>
    <li>
        <dl class="inline-term">
            <dt>Display image</dt>
            <dd>show the image of the manufacturer.</dd>
        </dl></li>
    <li>
        <dl class="inline-term">
            <dt>Number of elements to display</dt>
            <dd>select the number of elements displayed in the module.</dd>
        </dl></li>
    <li>
        <dl class="inline-term">
            <dt>Image Type</dt>
            <dd>select the image type (you can select the image from the images related to the manufacturer).</dd>
        </dl></li>
    <li>
        <dl class="inline-term">
            <dt>Use carousel</dt>
            <dd>show up the list as a carousel.</dd>
        </dl></li>
    <li>
        <dl class="inline-term">
            <dt>* Visible items</dt>
            <dd>number of visible manufacturers.</dd>
        </dl></li>
    <li>
        <dl class="inline-term">
            <dt>* Items scroll</dt>
            <dd>number of manufacturers shown in one carousel scroll.</dd>
        </dl></li>
    <li>
        <dl class="inline-term">
            <dt>* Slide Width</dt>
            <dd>the width of the element in the carousel.</dd>
        </dl></li>
    <li>
        <dl class="inline-term">
            <dt>* Slide Margin</dt>
            <dd>the margin of the element in the carousel.</dd>
        </dl></li>
    <li>
        <dl class="inline-term">
            <dt>* Auto scroll</dt>
            <dd>automatic scrolling.</dd>
        </dl></li>
    <li>
        <dl class="inline-term">
            <dt>* Carousel speed</dt>
            <dd>speed of the carousel.</dd>
        </dl></li>
    <li>
        <dl class="inline-term">
            <dt>* Pause</dt>
            <dd>pause between scrolls(if "Auto scroll" is enabled).</dd>
        </dl></li>
    <li>
        <dl class="inline-term">
            <dt>* Random</dt>
            <dd>carousel start from the random element.</dd>
        </dl></li>
    <li>
        <dl class="inline-term">
            <dt>* Carousel loop</dt>
            <dd>carousel returns back to the beginning when all elements have been scrolled.</dd>
        </dl></li>
    <li>
        <dl class="inline-term">
            <dt>* Hide control on end</dt>
            <dd>hide next/previous buttons.</dd>
        </dl></li>
    <li>
        <dl class="inline-term">
            <dt>* Pager</dt>
            <dd>show pagination.</dd>
        </dl></li>
    <li>
        <dl class="inline-term">
            <dt>* Control</dt>
            <dd>show next/previous buttons.</dd>
        </dl></li>
    <li>
        <dl class="inline-term">
            <dt>* Auto control</dt>
            <dd>show play/pause buttons.</dd>
        </dl></li>
    <li>
        <dl class="inline-term">
            <dt>* Auto hover</dt>
            <dd>pausing when hovering over carousel elements.</dd>
        </dl></li>
</ol>
<p><strong>
        <small>* - settings are available only if carousel is enabled.</small>
    </strong></p>
<p>The module supports multi-stores.</p>
