<h2 class="item1">Introduzione</h2>
<h3>Che cosa è il commercio elettronico di PrestaShop?</h3>
<p>PrestaShop è una soluzione di e-commerce che è gratuita per il kernel di base e l'open source. Sostiene i gateway di pagamento
    come il checkout di Google, paypal o pagamenti pro tramite API.</p>
<p>PrestaShop è disponibile sotto la licenza Open Software e lanciato ufficialmente nell'agosto 2007 per gg a medie dimensioni
    Imprese. Il software, basato sul motore di modello Smarty, è utilizzato da diverse migliaia di negozi
    in tutto il mondo.</p>
<h3>Che cosa è il modello PrestaShop?</h3>
<p>Il modello PrestaShop è un tema per la soluzione di e-commerce PrestaShop. Fornisce una vasta gamma di personalizzazioni
    opportunità e consente di creare un negozio online che soddisfi pienamente le tue esigenze.</p>
