<h2 class="item1">Introduction</h2>
<h3>Qu'est-ce que PrestaShop e-commerce?</h3>
<p>PrestaShop est une solution de commerce électronique gratuite pour kernel de base et open source. Il soutient les passerelles de paiement tels que Google checkout, paypal ou paiements pro via API.</p>
<p>PrestaShop est disponible sous Licence Logicielle Open Sourc (Open Software License) et officiellement lancé en août 2007 pour les petites et moyennes entreprises. Ce logiciel, basé sur le moteur de modèle Smarty, est utilisé par milliers de magasins dans le monde entier.</p>
<h3>Qu'est-ce que le modèle PrestaShop?</h3>
<p>Le modèle PrestaShop est un thème pour la solution de commerce électronique. Il vous offre une vaste variété de possibilités de personnalisation et permet de créer une boutique en ligne qui répond parfaitement à vos besoins.</p>
