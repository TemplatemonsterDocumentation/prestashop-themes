<h2 class="item1">Wstęp</h2>
<h3>Czym jest handel elektroniczny PrestaShop?</h3>
<p>PrestaShop — to rozwiązanie, wspierające handel elektroniczny, bezpłatne do celów stosowania podstawowego jądra oraz
    o otwartym dostępie do kodu źródłowego. Wspiera różne systemy płatności, w tym Google checkout, PayPal oraz/lub
    płatności za pośrednictwem API.</p>
<p>PrestaShop można nabyć w oparciu o licencję Open Software License; oficjalnie została uruchomiona w sierpniu 2007
    roku dla małych (dd) oraz średnich przedsiębiorstw. To oprogramowanie stworzone na podstawie systemu szablonów
    Smarty, jest wykorzystywane przez kilka tysięcy sklepów na całym świecie.</p>
<h3>Czym jest szablon?</h3>
<p>Szablon PrestaShop jest motywem PrestaShop dla rozwiązań w sferze handlu elektronicznego. Oferuje szeroki zakres
    różnych możliwości dostosowania i pozwala Ci stworzyć sklep internetowy który najlepiej spełni Twoje wymagania.</p>
