<h2 class="item1">Einführung</h2>
<h3>Was ist eigentlich die PrestaShop Emgine?</h3>
<p> PrestaShop ist eine E-Commerce-Lösung, die kostenlos auf einer grundlegenden Ebene mit Open-Source ist. Sie unterstüzt Zahlungssysteme wie Google Checkout, PayPal oder Payments Pro über API.</p>
<p>PrestaShop ist für die Software-Lizenz mit Open-Source verfügbar und wurde offiziell im August 2007 für kleine und mittlere Unternehmen gestartet. Die basierende auf der Engine der Smarty Vorlagen Software wird von mehreren tausend Shops weltweit verwendet.</p>
<h3>Was ist ein PrestaShop Vorlage?</h3>
<p>Eine PrestaShop Vorlage ist eine Vorlage zur Erstellung eines PrestaShop Online-Shops. Sie gibt Ihnen die Möglichkeit, Ihren Online-Shop einzustellen und einen eigenen Online-Shop ganz nach Ihren Bedürfnissen zu erstellen.</p>
