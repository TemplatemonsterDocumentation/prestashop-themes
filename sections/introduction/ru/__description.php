<h2 class="item1">Вступление</h2>
<h3>Что такое движок электронной коммерции PrestaShop?</h3>
<p> PrestaShop - это решение для электронной коммерции, бесплатное на базовом уровне с открытым исходным кодом. Он поддерживает платежные системы, такие как Google Checkout, PayPal или Payments Pro через API.</p>
<p>PrestaShop доступен под лицензией программного обеспечения с открытым исходным кодом и официально запущен в августе 2007 года для компаний среднего и малого бизнеса. Программное обеспечение, основанное на движке шаблонов Smarty, используется несколькими тысячами магазинов по всему миру.</p>
<h3>Что такое шаблон PrestaShop?</h3>
<p>Шаблон PrestaShop - это шаблон для магазина электронной коммерции PrestaShop. Он предоставляет Вам множество возможностей настройки и позволяет Вам создать интернет-магазин, который полностью отвечает вашим потребностям.</p>
