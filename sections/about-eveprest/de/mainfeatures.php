<h3>Wichtige Eigenschaften</h3>
<ul class="marked-list-type-2">
    <li>
        <p class="big"><strong>Wichtige Funktionen</strong></p>
        <p>Jede Vorlage hat besondere Funktionen, die von ihrem Ziel abhängen. Zum Beispiel, die Vorlage "Ersatzteile" hat Erweiterte Suche (nach dem Typ, Modell, Marke, Hersteller usw.), für "Elektronik" gibt es ein einzigartiges Layout für Artikel, für "Kleidung" und "Möbel" - wird das Widget LookBook verwendet.</p>
    </li>
    <li>
        <p class="big"><strong>Mehrere Widgets</strong></p>
        <p>Eveprest verfügt über mehr als 20 Widgets, die die Funktionalität Ihrer Vorlage erweitern.</p>
    </li>
    <li>
        <p class="big"><strong>Mehrere Layouts</strong></p>
        <p>Jede Eveprest Vorlage hat 4 Layouts (drei Standard-Layouts für jede Vorlage aus dem Satz +1 Speziell, der dem  Anwendungsbereich entspricht) und mehrere Farbpaletten, die viele verfügbare Kombinationen für die Startseite jeder Vorlage anbieten. </p>
    </li>
    <li>
        <p class="big"><strong>Adaptiv und Retina Ready</strong></p>
        <p>Mit Eveprest wird Ihr Online-Shop auf jedem Endgerät optimal dargestellt. Ihr Layout passt sich automatisch jeder Bildschirmauflösung an.</p>
    </li>
    <li>
        <p class="big"><strong>Logos</strong></p>
        <p>Jede Vorlage hat ein eigenes einzigartiges Logo.</p>
    </li>
