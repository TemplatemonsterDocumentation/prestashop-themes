<h3>Main Caratteristiche</h3>
<ul class="marked-list-type-2">
    <li>
        <p class="big"><strong>Funzionalità Specifiche</strong></p>
        <p>Ogni tema ha funzionalità specifiche a seconda del tema. Ad esempio, parti di ricambio comprende avanzata
            Ricerca (per tipo parti, modello, marca, produttore, ecc), per l'elettronica c'è un layout di prodotti unici,
            per indumenti e mobili - viene utilizzato il modulo LookBook.</p>
    </li>
    <li>
        <p class="big"><strong>Un Sacco di Moduli</strong></p>
        <p>Più di 20 moduli espandono le funzionalità del tema sono compresi nel Eveprest.</p>
    </li>
    <li>
        <p class="big"><strong>Multiple Layouts</strong></p>
        <p>Ogni tema Eveprest comprende 4 layout (tre layout standard per ciascun tema dal fascio +1 specifico,
            regimi soggetto corrispondente layout) e colore multiplo che forniscono decine di combinazioni possibili per la
            pagina principale di ogni modello.</p>
    </li>
    <li>
        <p class="big"><strong>Responsive & Retina-Ready</strong></p>
        <p>Con Eveprest, i clienti possono navigare il negozio sia in movimento e da casa. Il suo tracciato si adatta allo schermo
            di qualsiasi risoluzione automaticamente.</p>
    </li>
    <li>
        <p class="big"><strong>Logotypes</strong></p>
        <p>Ogni tema ha il suo logo unico.</p>
    </li>
