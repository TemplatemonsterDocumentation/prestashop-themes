<h3>Caractéristiques Principales</h3>
<ul class="marked-list-type-2">
    <li>
        <p class="big"><strong>Fonctionnalité Spécifique</strong></p>
        <p>Chaque thème a des fonctionnalités spécifiques en fonction du sujet. Par exemple, Spare Parts comprend la Recherche 
		avancée (par type de pièces, le modèle, le fabricant etc.), il y a une disposition de produits uniques pour Electronics,
        pour Clothes & Furniture - le module LookBook est utilisé.</p>
    </li>
    <li>
        <p class="big"><strong>Beaucoup de Modules</strong></p>
        <p>Plus de 20 modules développant la fonctionnalité du thème sont inclus dans Eveprest.</p>
    </li>
    <li>
        <p class="big"><strong>Grand choix de Présentations</strong></p>
        <p>Chaque thème Eveprest comprend 4 mises en page (trois schémas standard pour chaque thème à partir du paquet +1 schéma spécifique, 
		correspondant au sujet)et multiples combinaisons de couleurs qui fournissent des dizaines de combinaisons possibles 
		pour la page principale de chaque modèle. </p>
    </li>
    <li>
        <p class="big"><strong>Adaptable & Prêt à la Réine</strong></p>
        <p>Avec Eveprest, les clients peuvent parcourir votre magasin sur la route et à la maison.
		Une interface de site web adaptable est compatible avec tous les appareils.</p>
    </li>
    <li>
        <p class="big"><strong>Types de Logo</strong></p>
        <p>Chaque thème a son propre logo unique.</p>
    </li>
