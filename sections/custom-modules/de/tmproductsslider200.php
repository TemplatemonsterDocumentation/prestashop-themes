<h4>Einführung</h4>
<p>Mit diesem Modul können Sie Galerien basierend auf Produktdaten erstellen und sie als Slides in der Schnittstelle der Website darzustellen. Dieses Modul bietet 4 Darstellungsarten der Galerien mit  individuellen Einstellungen und Aussehen. Jede Art verfügt über die vorgefertigte Funktionalität. Es gibt auch die erweiterte Einstellung. Darüber hinaus kann dieses Modul unbegrenzte Anzahl an Produkten mit verschiedenen Informationen darstellen.</p>
<h4>Wie man das Modul installiert und löscht</h4>
<p class="alert alert-warning">
    Wenn in Ihrer PrestaShop Engine die Version 1.n dieses Moduls installiert ist, sollen Sie nicht es auf die letzte Version aktualisieren. Vor allem sollen Sie die alte Version des Moduls löschen, damit das Modul korrekt funktionieren kann.
</p>
<p> Dieses Modul wird ein anderes PrestaShop Modul installiert und gelöscht.</p>
<p class="alert alert-info">
    <span> Wenn Sie dieses Modul löschen oder noch einmal installieren, werden alle Einstellungen und Filter zurückgesetzt.</span>
</p>
<h4> Wie man ein Slide hinzufügt /löscht / bearbeitet </h4>
<h5>Wie man ein Slide hinzufügt</h5>
<p>Um ein neues Produkt zur Galerie hinzuzufügen, finden Sie ein notwendiges Produkt im Bereich <strong>Katalog -> Produkte</strong> (Catalog  -> Products). Danach drücken Sie auf den Button <strong>'Bearbeiten'</strong> (Edit) (1) neben dem notwendigen Produkt.</p>
<figure class="img-polaroid"><img src="img/tmproductsslider2-1.png" alt=""/></figure>
<p> In dem DropDown Fenster zur Bearbeitung der Produktinformationen finden Sie den Tab <strong>TM Products Slider</strong> (2) und setzen Sie ein Häkchen (3), um das Produkt im Slider darzustellen.</p>
<figure class="img-polaroid"><img src="img/tmproductsslider2-2.png" alt=""/></figure>
<p>Speichern Sie die Änderungen, indem Sie auf den Button <strong>'Speichern und bleiben'</strong> (Save and stay) (4) oder <strong>'Speichern'</strong> (Save) (5) drücken.</p>
<h5>Wie man ein Slide löscht</h5>
<p> Es gibt zwei Wege ein Slide zu löschen.</p>
<ul class="index-list">
    <li>
        <p>Um ein Produkt aus der Galerie zu löschen, finden Sie ein notwendiges Produkt in dem Bereich <strong>Katalog -> Produkte </strong> (Catalog -> Products). Danach drücken Sie auf den Button <strong>'Bearbeiten'</strong> (Edit) (1) neben dem notwendigen Produkt.</p>
        <figure class="img-polaroid"><img src="img/tmproductsslider2-1.png" alt=""/></figure>
        <p>In dem DropDown Fenster zur Bearbeitung der Produktinformationen finden Sie den Tab <strong>TM Products Slider</strong> (2) und setzen Sie kein Häkchen (3), das bedeutet, dass Sie nicht möchten, Produkte im Slider darstellen:</p>
        <figure class="img-polaroid"><img src="img/tmproductsslider2-3.png" alt=""/></figure>
        <p>Speichern Sie die Änderungen, indem Sie auf den Button <strong>'Speichern und bleiben'</strong> (Save and stay) (4) oder <strong>'Speichern'</strong> (Save)(5) drücken.
        </p>
    </li>
    <li>
        <p> Der zweite Weg - öffnen Sie die Seite Moduleinstellungen, finden Sie ein notwendiges Produkt in der <strong>Liste der Slides</strong> (Slides list) und drücken Sie auf den Button <strong>Löschen</strong> (Delete) (1) daneben.</p>
