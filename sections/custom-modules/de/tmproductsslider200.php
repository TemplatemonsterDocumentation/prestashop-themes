<h4>Erweiterte Einstellungen</h4>
<p>Um den Zugriff auf die erweiterten Einstellungen zu bekommen, verschieben Sie den Schieber <strong>Erweiterte Einstellungen</strong> (Extended Settings) in die Position "Ja".</p>
<figure class="img-polaroid"><img src="img/tmproductsslider2-11.png" alt=""/></figure>
<ul class="marked-list">
    <li><strong>Typ der Galerie</strong> (Gallery Type) – aktiver Typ der Galerie</li>
    <li><strong>Breite der Galerie</strong> (Gallery Width) – aktive Breite der Galerie</li>
    <li><strong>Höhe der Galerie </strong>(Gallery Height) – aktive Höhe der Galerie </li>
    <li><strong> Erweiterte Einstellungen</strong> (Extended Settings) - erweiterte Einstellungen; wenn diese Option den Wert "Nein" hat, werden alle zusätzlichen Einstellungen gespeichert und Sie können sie jederzeit wiederherstellen. Und der Slider wird standardmäßig dargestellt.    </li>
    <li><strong>Geschwindigkeit des Sliders</strong> (Slider Duration) – die Geschwindigkeitswechsel der Slides</li>
    <li><strong>Navigation verwenden</strong> (Use navigation) – Buttons "Zurück"/"Weiter"  für die Umschaltung von Slides darstellen </li>
    <li><strong>Vorschaubilder verwenden</strong> (Use Thumbnails) – Vorschaubilder mit zusätzlichen Informationen über Slides für die Umschaltung des aktuellen Slides verwenden    </li>
    <li><strong>Seitennummerierung verwenden</strong> (Use Pagination) – die Seitennummerierung als Element zur Umschaltung des aktuellen Slides verwenden </li>
    <li><strong>Bildergalerie verwenden</strong> (Use Image Gallery) - zusätzliche Produktbilder in Slides verwenden </li>
    <li><strong>Automatischen Start aktivieren </strong> (Allow autoplay) – automatische Umschaltung der Slides aktivieren </li>
    <li><strong> Slider Intervall</strong> (Slider Interval) - der Zeitraum, nach dem das Slide auf ein weiteres Slide (es ist verfügbar, wenn die Einstellung "Automatischen Start aktivieren" aktiv ist) umgeschaltet wird    </li>
    <li><strong>Loop aktivieren</strong> (Allow Loop) – die Wiederholung von Slides (nachdem alle Slides angezeigt sind) aktivieren</li>
</ul>
<p class="alert alert-info">
    <span> Wenn Sie die Galerie umschalten, werden alle Einstellungen der vorher verwendeten Galerie gespeichert.</span>
</p>
<p class="alert alert-info">
    <span> Wenn Sie zusätzliche Einstellungen aktivieren/deaktivieren, werden die Einstellungen gespeichert und Sie können sie jederzeit nutzen.</span>
</p>
