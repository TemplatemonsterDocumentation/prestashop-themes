<h3>TM Slider</h3>
<p>Dieses Modul wurde speziell für perfekte Inhaltsdarstellung entwickelt. Es hat alles, was Sie brauchen, um Ihren Inhalt zu präsentieren und einen professionell aussehenden Slider zu erstellen. Dieses Modul verfügt über mehrere Einstellungen, deswegen können Sie den Slider ganz einfach einstellen. </p>
<h4>Wie man das Modul einstellt</h4>
<p> Dieses Modul wird wie ein anderes PrestaShop Modul installiert und gelöscht.</p>
<h4>Wie man das Modul einstellt</h4>
<p> Nachdem Sie das Modul installiert haben, können Sie seine Einstellungen ändern.</p>
<p> Um die notwendigen Daten für die Moduleinstellung zu erhalten, sollen Sie eine entsprechende Anwendung auf einer notwendigen Ressource erstellen.</p>
<p>  Es gibt keine Notwendigkeit zwei Varianten zum Einloggen ins System (Google, Facebook) gleichzeitig zu nutzen, Sie können nur eine nutzen.</p>
<h4>Facebook Feld</h4>
<ul class="marked-list">
    <li> ID Ihrer Anwendung </li>
    <li> Geheimschlüssel Ihrer Anwendung</li></ul>
<h4>Google Feld </h4>
<ul class="marked-list">
    <li> ID Ihrer Anwendung </li>
    <li> Geheimschlüssel Ihrer Anwendung</li> 
   <li> Der Umleitungspfad nach dem Einloggen ins System<strong> (die Adresse Ihres Online-Shops+index.php?fc=module&module=tmsociallogin&controller=googlelogin)</strong></li>
</ul>
<h4>VK Feld </h4>
<ul class="marked-list">
    <li> ID Ihrer Anwendung </li>
    <li> Geheimschlüssel Ihrer Anwendung</li>
    <li>Der Umleitungspfad nach dem Einloggen ins System<strong> (die Adresse Ihres Online-Shops+index.php?fc=module&module=tmsociallogin&controller=vklogin)</strong></li>
</ul>
<p class="alert alert-info"> Erhalten Sie ausführliche Informationen, indem Sie diesen Links folgen: <a href="https://developers.facebook.com/docs/apps/register" target="_blank">Facebook App</a>, <a href="https://support.google.com/cloud/answer/6158849?hl=en&ref_topic=6262490" target="_blank">Google App</a> und <a href="https://vk.com/dev/web_how_to_start">VK App</a>.</p>
<p class="alert alert-warning">
    Der Umleitungspfad muss gleich mit demjenigen sein, den Sie bei der Erstellung der Anwendung Google und VK erstellt haben.
</p>
