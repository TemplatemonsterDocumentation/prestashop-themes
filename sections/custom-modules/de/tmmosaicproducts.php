<p>Mit diesem Modul können Sie Produktkategorien, Banner, Videos (Youtube, Vimeo und ein benutzerdefiniertes Video), HTML Inhalt, Slider mit einer unterschiedlichen Anzahl der Slides und Bildgrößen in jeder Zeile auf der Startseite Ihrer Website darstellen.
</p>
<h5>Wie man das Modul installiert und löscht</h5>
<p>Dieses Modul wird wie ein anderes PrestaShop Modul installiert und gelöscht.</p>
<h5>Wie man das Modul einstellt</h5>
<p>Nachdem Sie dieses Modul installiert haben, können Sie notwendige Blöcke hinzufügen mit Produktkategorien, vorher hinzugefügte Banner, Videos und HTML Inhalt hinzufügen.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-1.png" alt="" /></figure>
<h5>Wie man Blöcke hinzufügt/löscht/bearbeitet</h5>
<p>Um eine neue Kategorie hinzuzufügen, drücken Sie auf den Button "Neu hinzufügen".</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-2.png" alt="" /></figure>
<ul class="marked-list">
    <li><strong>Kategorie wählen</strong> (Select category) - wählen Sie eine Kategorie aus, zu der Sie Produkte, Banner, Videos und HTML Inhalt hinzufügen möchten.</li>
    <li><strong>Einen benutzerdefinierten Namen verwenden</strong> (Use custom name) - der benutzerdefinierte Status der Kategorie.</li>
    <li><strong>Benutzerdefinierter Name</strong> (Custom name) - ein Kategorienname.</li>
    <li><strong>Status</strong> (Status) - der Status des Elementes.</li>
    <p>Um eine neue Zeile hinzuzufügen, drücken Sie auf den Button "Zeile hinzufügen". Danach wählen Sie ein Layout aus, das in dem folgenden Fester angezeigt ist.</p>
</ul>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-3.png" alt="" /></figure>
<p>Um ein Layout hinzuzufügen, drücken Sie auf eine von neun Varianten, um es zu wählen. Dadurch wird es zum Formular hinzugefügt.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-4.png" alt="" /></figure>
<p>Um Elemente hinzuzufügen, drücken Sie auf ein von den Fensters des Layouts. Sie sehen fünf Buttons, in denen verfügbare Elemente einer bestimmten Art vorgestellt sind.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-5.png" alt="" /></figure>
<p>Indem Sie auf einen Button drücken, sehen Sie das Formular, in dem Sie das Element der ausgewählten Art wählen können.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-6.png" alt="" /></figure>
<p>Indem Sie auf ein Element drücken, fügen Sie es zur Layoutzelle hinzu. Dadruch können Sie das Layout mit verschiedenen Blöcken füllen.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-7.png" alt="" /></figure>
<p>Nachdem Sie die Kategorie mit den ausgewählten Layout speichert haben, sehen Sie sie in dem Formular der hinzugefügten Kategorien.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-8.png" alt="" /></figure>
<p>Um ein Element zu löschen, drücken Sie auf den Button 'Löschen'.</p>
<h5>Wie man ein Banner hinzufügt/löscht/bearbeitet</h5>
<p>Um ein neues Banner hinzuzufügen, drücken Sie auf den Button 'Neues Banner hinzufügen'. Danach füllen Sie notwendige Felder in dem DropDown Formular aus.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-9.png" alt="" /></figure>
<ul class="marked-list">
    <li><strong>Datei wählen</strong> (Select a file) - wählen Sie eine Datei aus, es ist ein Pflichtfeld (zumindest für die Standardsprache).</li>
    <li><strong>Titel</strong> (Title) - der Titel des Banners, es ist ein Pflichtfeld (zumindest für die Standardsprache).</li>
    <li><strong>Url</strong> - der Link, es ist ein Pflichtfeld (zumindest für die Standardsprache).</li>
    <li><strong>Beschreibung</strong> (Description) - die Beschreibung des Banners, es ist ein Pflichtfeld (zumindest für die Standardsprache).</li>
    <li><strong>Spezifische Klasse</strong> (Specific class) - CSS Klasse des Blocks.</li>
</ul>
<h5>Wie mn ein Video hinzufügt/löscht/bearbeitet</h5>
<p>Um ein neues Video hinzuzufügen, drücken Sie auf den Button 'Neues Video hinzufügen'. Danach füllen Sie notwendige Felder in dem DropDown Formular aus.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-10.png" alt="" /></figure>
<ul class="marked-list">
    <li><strong>Namen des Videos eingeben</strong> (Enter Video name) - der Name des Videos, es ist ein Pflichtfeld (zumindest für die Standardsprache).</li>
    <li><strong>Link/Pfad zum Video</strong> - der Link zum Video (YouTube, Vimeo oder ein benutzerdefiniertes Video), es ist ein Pflichtfeld (zumindest für die Standardsprache).</li>
    <li><strong>Automatische Wiedergabe aktivieren</strong> (Allow autoplay) - hier können Sie festlegen, ob die Wiedergabe des Videos nach dem Laden des Players automatisch gestartet wird.</li>
    <li><strong>Automatische Wiedergabe</strong> (Allow controls) - hier können Sie festlegen, ob Bedienelemente des Players angezeigt werden.</li>
    <li><strong>Loop</strong> - hier können Sie festlegen, ob das Video von Anfang an dargestellt wird, nachdem es abgespielt wurde.</li>
</ul>
<p>Um ein benutzerdefiniertes Videos hinzuzfügen, drücken Sie auf den Button 'Datei'. Neben können Sie ein Video mit Hilfe von dem Dateimanager hinzufügen.</p>
<h5>Wie man den HTML Inhalt hinzufügt/löscht/bearbeitet</h5>
<p>Um eine neuen HTML Inhalt hinzuzufügen, drücken Sie auf den Button 'Neuen HTML Inhalt hinzufügen'. Danach füllen Sie notwendige Felder in dem DropDown Formular aus.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-11.png" alt="" /></figure>
<ul class="marked-list">
    <li><strong>Namen des HTML Elementes eingeben </strong> (Enter HTML item name) - der Name des HTML Inhalts, es ist ein Pflichtfeld (zumindest für die Standardsprache).</li>
    <li><strong>Spezifische Klasse</strong> (Specific class) - CSS Klasse des Blocks.</li>
    <li><strong>HTML Inhalt</strong> (HTML content) - der Inhalt.</li>
</ul>
<h5>Wie man einen Slider hinzufügt/löscht/bearbeitet</h5>
<p>Um einen neuen Slider hinzuzufügen, drücken Sie auf den Button 'Neuen Slider hinzufügen'. Danach füllen Sie notwendige Felder in dem DropDown Formular aus.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-12.png" alt="" /></figure>
<ul class="marked-list">
    <li><strong>Den Namen des Sliders eingeben</strong> (Enter Slider item name) - der Name des Sliders, es ist ein Pflichtfeld (zumindest für die Standardsprache).</li>
    <li><strong>Bedienelemente</strong> (Control) - die Slider-Navigation anzeigen/ausblenden.</li>
    <li><strong>Seitennummerierung</strong> (Pager) - die Seitennummerierung des Sliders anzeigen/ausblenden.</li>
    <li><strong>Automatisches Scrollen</strong> (Auto scroll) - automatische Wiedergabe der Slides aktivieren/deaktivieren.</li>
    <li><strong>Spezifische Klasse</strong> (Specific class) - CSS Klasse des Blocks.</li>
    <li><strong>Karussell Geschwindigkeit</strong> (Carousel speed) - Übergangsdauer der Slides (in Millisekunden), es ist ein Pflichtfeld.</li>
    <li><strong>Pause</strong> - der Zeitraum zwischen Umschaltungen der Slides (in Millisekunden), es ist ein Pflichtfeld.</li>
</ul>
<h5>Wie man Slides hinzufügt/löscht/bearbeitet</h5>
<p>Um ein neues Slide hinzuzufügen, drücken Sie auf den Button 'Ansicht' in dem Formular des Sliders und drücken Sie auf den Button 'Neu hinzufügen' in dem DropDown Fenster. Danach füllen Sie notwendige Felder in dem DropDown Formular aus.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-13.png" alt="" /></figure>
<ul class="marked-list">
    <li><strong>Typ</strong> (Type) - wählen Sie einen Typ des Sliders aus, es ist ein Pflichtfeld.</li>
    <li><strong>Liste der Banner/Videos/Html</strong> (Banner/Video/Html list) - wählen Sie das Element des vorher ausgewählten Typs aus, es ist ein Pflichtfeld.</li>
    <li><strong>Anzeigereihenfolge </strong> (Sort order) - die Platzierung des Slides.</li>
    <li><strong>Status des Elementes</strong> (Item status) - der Status des Elementes.</li>
</ul>
