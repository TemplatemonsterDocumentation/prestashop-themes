<h3>TM Category Products 0.1.3</h3>
<p>Dieses Modul stellt Produktkategorien in Hooks displayHome und displayHomeTabContent dar. Mit diesem Modul können Sie einzelne Produkte, Produkte aus Kategorien oder alle Produkte darstellen. Außerdem gibt es in diesem Modul ein Karussell, das funktioniert, wenn das Modul in der Position displayHome platziert ist.</p>
<h4>Wie man das Modul installiert</h4>
<p>Um dieses modul zu installieren, wählen Sie den Tab Control Panel -> Module und Dienste -> Module und Dienste und finden Sie das Modul 'TM Category Products' in Frontend-Eigenschaften, danach drücken Sie auf den Button 'Installieren' (1).</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryproducts013-1.png" alt=""/>
</figure>
<h4>Wie man das Modul einstellt</h4>
<ul class="index-list">
    <li>
        <p>Nachdem Sie das Modul erfolgreich installiert haben, sehen Sie den Button 'Einstellen' (1).</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts013-2.png" alt=""/>
        </figure>
    </li>
    <li>
        <p> Auf der Hauptseite der Moduleinstellungen gibt es die Liste der Tabs und Blöcke (1) (hier können Sie die erstellten Elemente sehen) und zwei Buttons - 'Einstellungen' (2) und 'Element hinzufügen', (3).</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts013-3.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Drücken Sie auf den Button 'Einstellungen' und wählen Moduleinstellungen nach Ihren Bedürfnissen.</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts013-4.png" alt=""/>
        </figure>
        <ul class="marked-list">
            <li><p><strong>Sichtbare Elemente</strong> (Visible items) – die Anzahl der sichtbaren Elemente.</p></li>
            <li><p><strong>Scrollen der Elemente</strong> (Items scroll) – die Anzahl der Elemente, die bei einem Scrollen in dem Karussell angezeigt werden.</p></li>
            <li><p><strong>Breite des Slides</strong> (Slide Width) – die Breite des Slides in der Liste des Karussells.</p></li>
            <li><p><strong>Feld des Slides</strong> (Slide Margin) - das Feld des Slides in der Liste des Karussells.</p></li>
            <li><p><strong>Automatisches Scrollen</strong> (Auto scroll) – automatisches Scrollen der Elemente in dem Karussell.</p></li>
            <li><p><strong>Geschwindigkeit des Karussells</strong> (Carousel speed) – Geschwindigkeit, wie die Elemente nacheinander ersetzt werden.</p></li>
            <li><p><strong>Pause</strong> (Pause) – Pause zwischen dem Scrollen (wenn "Automatisches Scrollen" aktiviert ist).</p></li>
            <li><p><strong>Zufällig</strong> (Random) – das Karussell beginnt mit einem zufälligen Element.</p></li>
            <li><p><strong>Loop des Karussells</strong> (Carousel loop) - Loop: nachdem alle Elemente angezeigt sind, das Karussell beginnt von Anfang an.</p></li>
            <li><p><strong>Bedienelemente am Ende ausblenden</strong> (Hide control at the end) – Bedienelemente (Zurück/Weiter) ausblenden.</p></li>
            <li><p><strong>Seitennumerierung</strong> (Pager) – die Darstellung der Seitennumerierung.</p></li>
            <li><p><strong>Bedienelemente</strong> (Control) – Bedienelemente (Zurück/Weiter) darstellen.</p></li>
            <li><p><strong> Automatische Verwaltung</strong> (Auto control) - Bedienelemente (Wiedergabe/Pause) darstellen.</p></li>
            <li><p><strong>Automatisches Hover </strong> (Auto hover) - die Pause im Karussell, sobald der Nutzer mit dem Mauszeiger über ein Element aus der Liste fährt.</p></li>
        </ul>
    </li>
</ul>
<h4>Wie man einen neuen Tab/Block hinzugefügt</h4>
<p> Der Tab ist ein Block, der Produkte in der Position displayHomeTabContent darstellt.</p>
<p>Dank diesem Block können Sie Produkte in der Position displayHome wählen.</p>
<ul>
    <li>
        <p> Um einen neuen Tab oder Block hinzuzufügen, drücken Sie auf den Button 'Element hinzufügen'.</p>
        <p> Sie sehen einen Bildschirm, auf dem Sie die folgenden Einstellungen wählen können:</p>
        <ul class="marked-list">
            <li><p><strong>Kategorie wählen</strong> (Select category) – wählen Sie eine Kategorie aus, deren Produkte dargestellt werden.</p>
                <figure class="img-polaroid">
                    <img src="img/tmcategoryproducts013-5.png" alt=""/>
                </figure>
            </li>
            <li>
                <p><strong>Die Anzahl der dargestellten Produkte</strong> (Number of products to display) – die Anzahl der dargestellten Produkte.</p>
                <figure class="img-polaroid">
                    <img src="img/tmcategoryproducts013-6.png" alt=""/>
                </figure>
            </li>
            <li>
                <p><strong>Modus</strong> (Mode) – der Modus des Tabs/Blocks.</p>
                <figure class="img-polaroid">
                    <img src="img/tmcategoryproducts013-7.png" alt=""/>
                </figure>
            </li>
            <li>
                <p><strong>Status</strong> (Status) – Sie können Tabs oder Blöcke aktivieren/deaktivieren.</p>
                <figure class="img-polaroid">
                    <img src="img/tmcategoryproducts013-8.png" alt=""/>
                </figure>
            </li>
        </ul>
        <p> Nachdem Sie die Optionen gewählt haben, drücken Sie auf den Button "Speichern". Der Tab/Block wird gespeichert und in der Liste der Tabs/Blöcke angezeigt.</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts013-9.png" alt=""/>
        </figure>
    </li>
</ul>
<h4>Wie man Tabs/Blöcke bearbeitet</h4>
<p>Um einen Tab oder Block zu ändern, finden Sie ihn in der Liste und drücken Sie auf den Button 'Bearbeiten' (1). <br>
 Die Einstellungen sind gleich wie zur Erstellung eines Blocks/Tabs.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryproducts013-10.png" alt=""/>
</figure>
<h4>Wie man Tabs/Blöcke löscht</h4>
<p>Um einen Tab oder Block zu ändern, finden Sie den Button 'Löschen' unter dem Button 'Bearbeiten'.</p>
