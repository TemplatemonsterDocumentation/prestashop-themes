<h3>TM Header Account</h3>
<p>Dieses Modul stellt das Anmeldungsformular ins System einige verfügbare Elemente des Benutzerkontos im Header dar. Dadurch kann man sich ins Konto einloggen und auf der aktuellen Seite bleiben. Dieses Modul ist ein DropDown Block.</p>
<h5> Wie man das Modul installiert und löscht </h5>
<p> Dieses Modul wird wie ein anderes PrestaShop Modul installiert und gelöscht.</p>
<h5> Wie man das Modul einstellt </h5>
<p> Dieses Modul hat keine zusätzlichen Einstellungen.</p>
