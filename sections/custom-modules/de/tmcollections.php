<h6> Das Modul TM Collections wurde erstellt, damit Besucher des Online-Shops Produkte zu Sammlungen hinzufügen und diese Details auf Facebook teilen können.</h6>
<h4>Wie man das Modul installiert</h4>
<p>Um dieses Modul zu installieren, finden Sie den Tab Control Panel -> Module und Dienste -> Module und Dienste (1), finden Sie das Modul TM Collections (2) und drücken Sie auf den Button 'Installieren' (3).</p>
<figure class="img-polaroid">
    <img src="img/tmcollections1.png" alt=""/>
</figure>
<h4>Wie man das Modul einstellt</h4>
<p>Nachdem Sie das Modul installiert haben, sehen Sie die Seite 'Einstellungen'.<br>
  Fügen Sie den Facebook Profil-Identifier in ein entsprechendes Feld (1) hinzu und drücken Sie auf den Button 'Speichern' (2).<br>
    Hier erfahren Sie, wie man den Facebook Profil-Identifier erhalten kann: <a
        href="https://developers.facebook.com/docs/apps/register" target="blank">Wie man einen Identifier erstellt</a>.</p>
<figure class="img-polaroid">
    <img src="img/tmcollections2.png" alt=""/>
</figure>
<h4>Wie man das Modul TM Collections verwendet</h4>
<h5>Wie man ein Produkt zur Sammlung hinzufügt</h5>
<p class="alert alert-info">Webseitenbesucher müssen sich ins System einloggen, um Elemente zur Sammlung hinzuzufügen.</p>
<ul class="index-list">
    <li>
        <p>Nachdem das Modul installiert ist, sehen Sie den Button 'Zur Sammlung hinzufügen' auf jeder Produktseite. Sie wird wie auf dem Bildschirmfoto (1) aussehen.</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections3.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Mit dem Klick auf diesen Button sehen die Webseitenbesucher die Mitteilung: 'Produkt wurde erfolgreich zur Sammlung hinzugefügt.' Diese Mitteilung kann mit dem Klick auf den Button 'Meine Sammlungen' (2) geschlossen oder übersprungen werden.</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections4.png" alt=""/>
        </figure>
    </li>
</ul>
<h5>Vorschau der Sammlung</h5>
<ul class="index-list">
    <li>
        <p>Auf der Seite 'Meine Sammlungen' kann der Kunde eine neue Sammlung клиент может добавить новую коллекцию (1) hinzufügen oder die Liste der erstellten Produkte ansehen (2).</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections5.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Drücken Sie auf den Button 'Sammlung ansehen' (1) und wird die Sammlungsseite geöffnet. Der Button 'Produkte ansehen' (2) ermöglicht es, die Liste der Produkte aus dieser Sammlung auf der aktuellen Seite anzusehen. <br>
        Der Titel der Sammlung kann geändert (3) oder gelöscht werden (4). <br>
          Um eine Sammlung zu teilen, drücken Sie auf den Button 'Teilen' (5).</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections6.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Beim Anschauen der Elemente aus der Sammlung kann jeder Element aus der Liste gelöscht werden, indem Sie auf den Button 'Produkte anschauen' drücken (1).</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections7.png" alt=""/>
        </figure>
    </li>
</ul>
<h5>Sammlungen teilen </h5>
<p>Um eine Sammlung zu teilen, soll der Besucher auf den Button "Teilen" drücken. Vor der Veröffentlichung der Sammlung müssen einige einfache Schritte durchgeführt werden. Aber Sie können immer zurückkehren und "Teilen" Optionen ändern.</p>
<ul class="index-list">
    <li>
        <p> Der erste Schritt - wählen Sie das Layout der Sammlung, die veröffentlicht wird.</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections8.png" alt=""/>
        </figure>
    </li>
    <li>
        <p> Danach sollen Sie Produkte hinzufügen. Jede Zelle mit dem Zeichen "+" ist ein Button zum Hinzufügen eines Produktbildes.</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections9.png" alt=""/>
        </figure>
        <p> Wählen Sie ein Bild für jedes Produkt:</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections10.png" alt=""/>
        </figure>
    </li>
    <li>
        <p> Nachdem alle Bilder hinzugefügt sind, soll der Besucher auf den Button 'Teilen' (1) teilen.</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections11.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>In einem kleinem Fenster sehen Sie Standardeinstellungen für die Veröffentlichung und ein Feld zum Hinzufügen der Textnachricht (1). Mit dem Klick auf den Button "Auf Facebook veröffentlichen" (2) wird die Nachricht veröffentlicht.</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections12.png" alt=""/>
        </figure>
    </li>
</ul>
<h5>Statistik</h5>
<p>Mit Hilfe von diesem Modul können Sie Statistiken erhalten, die gesammelt werden, wann Kunden Produkte zu Sammlungen hinzufügen. Dadurch werden Sie wissen, welche Produkte derzeit beliebt sind. Diese Informationen können in dem Fall nützlich sein, wenn Sie Rabatte anbieten. <br>
 Sie können sich die Statistiken anschauen, indem Sie auf den Button Control Panel -> Statistik -> Statistik -> TM Collections drücken. <br>
</p>
<figure class="img-polaroid">
    <img src="img/tmcollections13.png" alt=""/>
</figure>
<ul class="marked-list">
    <li> Sie können einen bestimmten Zeitraum wählen und sich die Statistik anschauen (1).</li>
    <li> In der ersten Spalte sehen Sie die Anzahl der hinzugefügten Produkte (2).</li>
    <li> Die zweite Spalte wurde zur Bequemlichkeit erstellt. Sie können sie verwenden, um die Anzahl der verkauften Produkte zu erfahren, die vorher hinzugefügt wurden (3).
    </li>
</ul>
