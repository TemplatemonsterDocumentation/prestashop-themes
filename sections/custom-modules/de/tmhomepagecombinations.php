<h4>TM Homepage Combinations Hauptpanel</h4>
<p> Es ist das Hauptpanel des Moduls, das es Ihnen ermöglicht, Elemente hinzuzufügen, zu bearbeiten und zu löschen. Um ein neues Element zu erstellen, drücken Sie auf den Button 'Neu hinzufügen' (1) im Menü des Hauptpanels.</p>
<figure class="img-polaroid">
	<img src="img/tmhomepagecombinations-1.png" alt=""/>
</figure>
<p> Füllen Sie die Felder in dem DropDown Formular aus.</p>
<figure class="img-polaroid">
	<img src="img/tmhomepagecombinations-2.png" alt=""/>
</figure>
<ul class="marked-list">
	<li><strong>Produkt</strong> (Product) – wählen Sie ein Produkt aus.</li>
	<li><strong>Hook</strong> (Hook) – wählen Sie die Position des Elementes aus.</li>
	<li><strong>Titel</strong> (Title) – legen Sie den Titel des Elementes fest.</li>
	<li><strong>Status des Titels</strong> (Title status) – wenn diese Option deaktiviert ist, wird der Titel des Elementes nicht dargestellt.</li>
	<li><strong>Status des Elementes</strong> (Item status) – wenn diese Option deaktiviert ist, wird das Element in der Position nicht angezeigt.</li>
	<li><strong>Anzeigereihenfolge</strong> (Sort order) – Anzeigereihenfolge des Elementes.</li>
</ul>
<p>Drücken Sie auf den Button 'Speichern und bleiben', nachdem Sie alle Felder ausgefüllt haben. Warten Sie bis die Seite neu geladen wird und drücken Sie auf den Button 'Neue Kombination hinzufügen'.</p>
<p>Wählen Sie eine notwendige Kombination aus der Liste aus und drücken Sie auf den Button 'Hinzufügen' (1).</p>
<figure class="img-polaroid">
	<img src="img/tmhomepagecombinations-3.png" alt=""/>
</figure>
<p> Füllen Sie die Felder in dem DropDown Formular aus.</p>
<figure class="img-polaroid">
	<img src="img/tmhomepagecombinations-4.png" alt=""/>
</figure>
<ul class="marked-list">
	<li><strong>Bild wählen</strong> (Select image) – wählen Sie ein notwendiges Bild aus.</li>
	<li><strong>Spezifische Klasse</strong> (Specific class) – eine benutzerdewfinierte Klasse, die Stile der Elemente (CSS Klasse) definiert.</li>
	<li><strong>Anzeigereihenfolge</strong> (Sort order) – Anzeigereihenfolge des Elementes.</li>
</ul>
<p>Drücken Sie auf den Button 'Speichern', nachdem Sie alle Felder ausgefüllt haben.</p>
