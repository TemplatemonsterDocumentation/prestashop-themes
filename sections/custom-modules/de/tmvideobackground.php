<h3>Video Background Module</h3>
<p> Dieses Modul ermöglicht es Ihnen, ein Hintergrund-Video zu jedem Block auf Ihrer HTML Seite (legen Sie eine ID oder ein einzigartiges Tag fest) hinzuzufügen.</p>
<h6> Um ein Hintergrund-Video hinzuzufügen, führen Sie die folgenden Schritte aus:</h6>
<ul class="index-list">
    <li>Laden Sie die Video-Dateien des Vorschaubildes in den Ordner modules/tmvideobackground/video (oder in einen anderen Ordner auf dem Server) per FTP hoch. Laden Sie das Video in drei Formaten hoch: .mp4, .ogv, .webm, alle Dateien müssen die gleichen Namen (ocean.mp4, ocean.ogv, ocean.webm) haben. Die Vorschaubilder müssen auch in drei Formaten sein: .jpg, .gif, .png und müssen die gleichen Namen (ocean.jpg, ocean.gif, ocean.png) haben.
    </li>
    <li><p>In Ihrem Control Panel finden Sie den Tab <strong>Video background module</strong> (Module -> Frontend-Eigenschaften -> Video background module) und legen Sie notwendige Einstellungen fest.</p>
        <h6><strong>Einstellungen:</strong></h6>
        <ul class="marked-list">
            <li>
                <dl class="inline-term">
                    <dt>Videowähler</dt>
                    <dd> Das Element der HTML Seite, wo das Hintergrund-Video hinzugefügt wird (eine ID oder ein einzigartiges Tag (Body, Header, Footer usw.))
                    </dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Pfad zu Video-Dateien</dt>
                    <dd> Der Pfad zu Ihren Video-Dateien (der Pfad muss '/' am Ende haben), Dateien können in jedem Ordner auf dem Server platziert sein, aber wir empfehlen Ihnen, sie in die folgenden Ordner <strong> modules/tmvideobackground/video/</strong> hochzuladen.
                    </dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Name der Video-Dateien</dt>
                    <dd> Der Name Ihrer Video-Dateien. Hier muss nur der Name (es gibt keine Notwendigkeit, ein Format festzulegen) hinzugefügt. Zum Beispiel, «Ozean».
                    </dd>
                </dl>
            </li>
            <li>Drücken Sie auf den Button <strong>'Speichern'</strong> (Save).</li>
        </ul>
    </li>
</ul>
