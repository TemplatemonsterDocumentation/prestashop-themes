<h3>TM Infinite Scroll</h3>
<p>Mit Hilfe von diesem Modul können Sie AJAX Produkte zu Seiten mit dem Block Produktliste hinzufügen. Darüber hinaus können Sie automatisches Laden der Produkte beim Scrollen oder mit dem Klick auf den Button «Mehr anzeigen» einstellen. Dieses Modul unterstützt PrestaShop Produktfilter standardmäßig.</p>
