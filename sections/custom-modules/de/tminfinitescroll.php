<h5>Moduleinstellungen</h5>
<ul class="index-list">
    <li> Seiten - Seiten, auf denen das Modul funktionieren muss.</li>
    <li> Automatisches Laden - wenn diese Einstellung aktiviert ist, werden Produkte atomatisch beim Scrollen geladen. Wenn diese Option nicht aktiviert ist, sehen Sie den Button «Mehr anzeigen», und Produkte werden geladen, wenn Sie auf diesen Button drücken.</li>
    <li>Einzug – diese Option wird dargestellt, wenn 'Automatisches Laden' aktiviert ist. Diese Option ermöglicht es Ihnen, den Einzug vor dem Downloadbereich des Produktes (oder nach ihm - wenn der Wert negativ ist) zu bestimmen.</li>
    <li> Seitennummerierung - diese Option wird dargestellt, wenn 'Automatisches Laden' deaktiviert ist. Dank dieser Option können Sie diee Seitennummerierung zur Produktseite hinzufügen.</li>
    <li>Den Button 'Alles anzeigen' darstellen – diese Option ist verfügbar, nur wenn 'Automatisches Laden' deaktiviert ist. Mit dem Klick auf diesen Button werden alle Produkte angezeigt.</li>
    <li> Preloader - in diesem Feld können Sie das Bild für den Preloader wählen, das während dem Laden der Produkte angezeigt wird.</li>
</ul>
