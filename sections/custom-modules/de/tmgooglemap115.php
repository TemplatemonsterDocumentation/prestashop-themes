<h3>TM Google Map 1.1.5</h3>
<p>Mit diesem Modul können Sie Google Map mit den Standorten Ihrer Shops und kurze Beschreibungen zur Startseite hinzufügen. Auf der Karte werden die Adressen aller Shops angezeigt, die mit Ihrem Online-Shop verbunden sind.</p>
<h5> Wie man das Modul installiert und löscht </h5>
<p> Dieses Modul wird wie ein anderes Prestashop Modul installiert und gelöscht.</p>
<h5> Wie man das Modul einstellt </h5>
<p> Nachdem Sie das Modul installiert haben, können Sie Shops zur Karte hinzufügen. Indem Sie auf den Button 'Neuen Shop hinzufügen' (1) drücken, wird der Tab 'Kontaktdaten des Shops' geöffnet, wo Sie Ihre Shops ändern können.</p>
<p class="img-polaroid"><img src="img/tmgooglemap115-1.png" alt="" /></p>
<p>Um einen neuen Shop hinzuzufügen, drücken Sie auf den Button 'Neu hinzufügen'.</p>
<p class="img-polaroid"><img src="img/tmgooglemap115-2.png" alt="" /></p>
<ul class="marked-list">
    <li><strong>Shop wählen</strong> (Select a store) - wählen Sie einen Shop aus, den Sie zur Karte hinzufügen möchten.</li>
    <li><strong>Status</strong> (Status) – der Status des Elementes.</li>
    <li><strong>Marker</strong> (Marker) – Hinzufügen des benutzerdefinierten Markers.</li>
    <li><strong>benutzerdefinierter Text</strong> (Custom text) – Hinzufügen des benutzerdefinierten Textes.</li>
</ul>
<h5>Google Maps Darstellungsoptionen</h5>
<p class="img-polaroid"><img src="img/tmgooglemap115-3.png" alt="" /></p>
<ul class="marked-list">
    <li><strong>Stile der Karte</strong> (Map Style) – wählen Sie Darstellungsstile der Karte.</li>
    <li><strong>Kartentyp</strong> (Map Type) – wählen Sie Darstellungsart der Karte.</li>
    <li><strong>Zoom-Ebene</strong> (Zoom Level) – legen Sie eine Zoom-Ebene der Karte (von 1 bis 17) fest.</li>
    <li><strong>Zoom beim Scrollen</strong> (Zoom on scroll) - Zoom beim Scrollen aktivieren/deaktivieren.</li>
    <li><strong> Bedienelemente der Karte</strong> (Map Controls) - Bedienelemente der Karte aktivieren/deaktivieren.</li>
    <li><strong> Straßenansicht</strong> (Street view) - Straßenansicht aktivieren/deaktivieren.</li>
    <li><strong> Animation-Marker </strong> (Animation marker) - Animation-Marker aktivieren/deaktivieren.</li>
    <li><strong>DropDown Fenster</strong> (Popup) - DropDown Fenster mit Informationen über den Shop aktivieren/deaktivieren.</li>
</ul>
