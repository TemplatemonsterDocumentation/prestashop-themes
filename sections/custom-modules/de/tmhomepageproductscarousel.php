<h3>TM Homepage Products Carousel</h3>
<p> Mit diesem Modul können Sie das Karussell zu Tabs auf der Startseite hinzufügen.</p>
<h5>Wie man das Modul installiert und löscht</h5>
<p> Dieses Modul wird ein anderes PrestaShop Modul installiert und gelöscht.</p>
<h5>Wie man das Modul einstellt</h5>
<p>Die Einstellungen des installierten Moduls können Sie auf der Seite Moduleinstellungen ändern.</p>
