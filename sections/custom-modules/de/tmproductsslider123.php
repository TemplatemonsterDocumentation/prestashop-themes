<h3>TM Products Slider 1.2.3</h3>
<p>Dieses Modul stellt Produkte des Online-Shops im Slider dar.</p>
<h4>Wie man das Modul installiert und löscht</h4>
<p> Dieses Modul wird ein anderes PrestaShop Modul installiert und gelöscht.</p>
<p> Nachdem Sie das Modul installiert haben, können Sie seine Einstellungen ändern.</p>
<p>Sie können die Reihenfolge der Produktdarstellung im Slider festlegen, indem Sie Produkte verschieben. Die können auch die Produktdarstelling im Slider aktivieren/deaktivieren.</p>
<p> Sie können die Breit, Übergangseffekt, Geschwindigkeit, Pause, automatische Wiedergabe, Pause (sobald der Mauszeiger über ein Element im Karussell fährt ), Bedienelemente, Buttons "Zurück"/"Weiter", Buttons "Wiedergabe"/"Pause" für den Slider festlegen.</p>
<figure class="img-polaroid">
    <img src="img/tmproductsslider-1.png" alt="">
</figure>
<p>Nachdem Sie das Modul installiert haben, sehen Sie einen neuen Tab auf der Seote Produkteinstellungen (Katalog -> Produkt -> Produkt wählen -> TM Products Slider).</p>
<p> Hier können Sie festlegen, ob das Produkt im Slider dargestellt oder nicht dargestellt wird. Nachdem Sie die Änderungen vorgenommen haben, drücken Sie auf den Button Speichern/Speichern und Bleiben, um die Änderungen zu speichern.</p>
<p> Dieses unterstützt den Multishop. Sie können Einstellungen für jeden Shop, für jede Gruppe von Shops oder für alle Shops gleichzeitig ändern.</p>
