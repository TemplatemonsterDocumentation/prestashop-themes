<h3>TM Categories Badges</h3>
<p>Dieses Modul ermöglicht es Ihnen, Etiketten zu Kategorien in der Seitenleiste des Standardmoduls Prestashop Categories («Blockcategories») hinzuzufügen. Etiketten können zur Kategorie jeder Ebene und in jeder Sprache hinzugefügt werden. Das Textfeld der Etikette ist ein Pflichtfeld und muss in allen Sprachen ausgefüllt werden. Wenn dieses Feld in einer Sprache leer ist, wird dann für es die Standardsprache automatisch festgelegt.</p>
<h5>Installation und Löschung</h5>
<p>Dieses Modul wird genauso wie ein anderes Prestashop Modul installiert und gelöscht.</p>
<h5>Moduleinstellung</h5>
<p>Nachdem Sie dieses Modul installiert haben, können Sie seine Einstellungen auf der Seite Einstellungen ändern.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoriesbadges-1.png" alt=""/>
</figure>
<p> Sie können Etiketten zu jeder Kategorie hinzufügen, indem Sie sie aus der DropDown Liste wählen.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoriesbadges-2.png" alt=""/>
</figure>
<p class="alert alert-warning"><span>Achtung! Sie können nur eine Etikette zu einer Kategorie hinzufügen.</span></p>
<p> Danach können Sie einen Text hinzufügen, der in der Etikette angezeigt wird, indem Sie den Stil aus der DropDown Liste (Stile können sich in verschiedenen Vorlagen unterscheiden) wählen.</p>
<p>Danach drücken Sie auf den Button 'Speichern' (1).</p>
<figure class="img-polaroid">
    <img src="img/tmcategoriesbadges-3.png" alt=""/>
</figure>

<p> Unten finden Sie die Liste der bestehenden Etiketten. Hier können Sie die Etiketten mit Hilfe von entsprechenden Buttons bearbeiten oder löschen.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoriesbadges-4.png" alt=""/>
</figure>
<p> Dieses Modul ist mehrsprachig und unterstüzt mehrere Shops. Sie können Etiketten zu Gruppen der Shops oder zu allen Shops auf einmal hinzufügen. Sie werden in der Liste als einzelne Elemente dargestellt.</p>
