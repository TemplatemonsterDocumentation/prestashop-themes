<h3>TM Category Image Slider</h3>
<p> Mit diesem Modul können Sie mehrsprachige Slides für jede Kategorie des Shops erstellen. Der Slider wird auf den Kategorienseiten dargestellt.</p>
<h4> Installation und Löschung </h4>
<p> Dieses Modul wird genauso wie ein anderes Prestashop Modul installiert und gelöscht.</p>
<h4> Moduleinstellung </h4>
<p> Nachdem Sie dieses Modul installiert haben, können Sie gewünschte Einstellungen auf der Seite Einstellungen ändern oder festlegen.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryimagesslider-1.png" alt=""/>
</figure>
<ul class="marked-list">
    <li><strong>Modus</strong> (Mode) - ein Animationstyp (Dämpfung, vertikal, horizontal).</li>
    <li><strong>Geschwindigkeit</strong> (Speed) – die Animationsgeschwindigkeit.</li>
    <li><strong>Breite</strong> (Width) - die Breite der Slides (für die richtige Anzeige ist es empfohlen die tatsächliche Breite des Bildes zu verwenden).
    </li>
    <li><strong>Höhe</strong> (Height) - Höhe der Slides (für die richtige Anzeige ist es empfohlen die tatsächliche Höhe des Bildes zu verwenden).
    </li>
    <li><strong>Automatisch</strong> (Auto) – automatische Wiedergabe beim Öffnen der Seite.</li>
    <li><strong>Pause</strong> (Pause) - die Zeit bie zum Übergang auf das nächste Slide.</li>
    <li><strong>Auto Hover</strong> (Auto Hover) – die Pause, sobald die Mauszeiger darüber fährt.</li>
    <li><strong>Bedienelemente</strong> (Controls) – die Buttons "Weiter"/"Zurück".</li>
    <li><strong>Text "Weiter"</strong> (Next Text) – der Text auf dem Button "Weiter".</li>
    <li><strong> Text "Zurück"</strong> (Prev Text) - der Text auf dem Button "Zurück".</li>
    <li><strong>Automatische Wiedergabe verwalten</strong> (Auto Controls) – die Buttons "Wiedergabe"/"Pause".</li>
    <li><strong>Seitennumerierung</strong> (Pager) - Seitennumerierung.</li>
</ul>
<h5> Wie man Slides hinzufügt / löscht / bearbeitet </h5>
<p> Nachdem Sie das Modul installiert haben, sehen Sie ein neues Feld "Slides" in Kategorieeinstellungen.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryimagesslider-2.png" alt=""/>
</figure>
<p> Um ein Slide hinzuzufügen, drücken Sie auf den Button "Neues Slide hinzufügen". Füllen Sie die notwendigen Felder im Formular aus.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryimagesslider-3.png" alt=""/>
</figure>
<ul class="marked-list">
    <li><strong>Datei wählen</strong> (Select a file) - wählen Sie eine Datei aus. Sie können verschiedene Bilder für verschiedene Sprachen hochladen. Wenn kein Bild für eine bestimmte Sprache ausgewählt wurde, wird ein Bild automatisch hochgeladen, das für die Standardsprache festgelegt wurde.
    </li>
    <li><strong>Titel des Slides eingeben</strong> (Enter Slide title) – geben Sie einen Title für das Slide ein.</li>
    <li><strong>Link zum Slide eingeben</strong> (Enter Slide Url) – geben Sie den Link zum Slide ein.</li>
    <li><strong> Text des Slides eingeben</strong> (Enter Slide caption) – geben Sie den Text für das Slide ein.</li>
    <li><strong>In einem neuen Fenster öffnen </strong> (Open in new window) - in einem neuen Fenster öffnen.</li>
    <li><strong>Status</strong> (Status) - ein Slide aktivieren/deaktivieren.</li>
</ul>
<p> Nachdem das Slide gespeichert wurde, wird es in der Liste der Slides der Kategorie angezeigt.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryimagesslider-4.png" alt=""/>
</figure>
<p>Um die Änderungen an dem Slide vorzunehmen, drücken Sie auf den Button <strong>'Bearbeiten'</strong> (Edit).</p>
<p>Um ein Slide zu löschen, drücken Sie auf den Button <strong>'Löschen'</strong> (Delete) unter dem Button <strong>'Bearbeiten'</strong> (Edit).</p>
<div class="alert alert-info">
    <span> Die Slides können nur zu gespeicherten Kategorien hinzugefügt werden.</span>
</div>
