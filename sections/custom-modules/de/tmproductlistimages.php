<h3>TM Product List Images</h3>
<p> Dieses Modul stellt die Bildergalerie aller verfügbaren Produkten auf Produktlistenseiten.</p>
<h5>Wie man das Modul installiert und löscht</h5>
<p> Dieses Modul wird wie ein anderes PrestaShop Modul installiert und gelöscht.</p>
<h5>Wie man das Modul einstellt</h5>
<p>Dieses Modul hat keine zusätzlichen Einstellungen.</p>
<h5>Zusätzliche Aktionen</h5>
<p> Um die Funktionalität dieses Moduls zu aktivieren, fügen Sie die folgende Codezeile:</p>
<p><code>{hook h='displayProductListGallery' product=$product}</code></p>
<p>zur Datei <strong>product-list.tpl</strong> in dem Stammordner Ihrer Vorlage nach dem geschlossenen Tag <code>&lt;/a&gt;</code> mit der Klasse <code>.product_img_link</code> hinzu.</p>

