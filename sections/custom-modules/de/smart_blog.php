<h4> RSS Smart Blog Feed</h4>
<h6>Das Modul zur Erstellung des RSS-Feed für Ihren Blog.</h6>
<p>Dieses Modul hat einige Einstellungen in dem Control Panel:</p>
<ul class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Aktualisierungsrate</dt>
            <dd>Sie können Beiträge des bestimmten Zeitraums (Stunde/Tag/Woche/Monat/Jahr) darstellen.            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Aktualisierungshäufigkeit </dt>
            <dd> Sie können die Aktualisierungshäufigkeit festlegen (1 Woche, 2 Stunden usw.) </dd>
        </dl>
    </li>
</ul>
<p> Oben gibt es den Pfad zur Datei, wo Nachrichten erzeugt werden. Er besteht aus dem Link zur Website + /modules/smartblogfeed/rss.php.</p>
