<h3>Smart Blog</h3>
<p><strong>Smart blog</strong> ist ein Satz der Module, die es Ihnen ermöglichen, einen Blog in Ihrem Shop zu erstellen. Er besteht aus den folgenden Modulen:</p>
<ul class="marked-list">
    <li>Smart Blog</li>
    <li>RSS Smart Blog Feed</li>
    <li>Smart Blog Archive</li>
    <li>Smart Blog Categories</li>
    <li>Smart Blog Home Latest</li>
    <li>Smart Blog Latest Comments</li>
    <li>Smart Blog AddThisModule</li>
    <li>Smart Blog Popular Posts</li>
    <li>Smart Blog Recent Posts</li>
    <li>Smart Blog Related Post</li>
    <li>Smart Blog Search</li>
    <li>Smart Blog Tags</li>
</ul>
<p> Dieses Modul hat auch eigene Hooks, in denen Blogmodule auf der Website dargestellt werden können. Sie können auch in Standardhooks (nur Spalten) dargestellt werden.</p>
<h4>Smart Blog</h4>
<p>Es ist ein Hauptmodul, das mehrere Parameter für die Arbeit mit Blogs enthält. Nach der Installation wird der Tab «Blog» in dem Hauptmenü Ihres Control Panels angezeigt, wo Sie Beiträge/Kategorien hinzufügen / löschen / bearbeiten oder einen Bildtyp wählen können, der in den Mitteilungen verwendet wird. Darüber hinaus können Sie passende Größen (Typen und Größen der Beitragsbilder unterscheiden sich von den Einstellungen der Produktbilder in Ihrem Shop) wählen. Um Parameter des Frontend-Moduls hinzuzufügen, finden Sie den Tab <strong>Smart Blog (Module -> Frontend-Eigenschaften -> Smart Blog) </ strong> und fügen Sie alle Parameter hinzu.</p>
<h6>Parametereigenschaften:</h6>
<ul class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Meta-Name</dt>
            <dd> Der Name Ihres Blogs, der auf allen seinen Seiten dargestellt wird.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Meta-Schlüsselwörter</dt>
            <dd> Schlüsselwörter, die von Suchmaschinen für eine bessere SEO-Optimierung benutzt werden.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Meta-Beschreibung</dt>
            <dd> Die Beschreibung Ihres Blogs, die von Suchmaschinen für eine bessere SEO-Optimierung benutzt wird.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Hauptlink des Blogs</dt>
            <dd>Der Link zu Ihrem Blog.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Verwendung der .html mit SEO URLs</dt>
            <dd> Sie können das Hinzufügen .html zu Links der Blogseiten mit Hilfe von SEO URLS aktivieren / deaktivieren.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Die Anzahl der Beiträge auf einer Seite</dt>
            <dd>die Anzahl der Beiträge, die auf den Seiten (Kategorien / Archiv-Seiten usw.) dargestellt werden</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Kommentare automatisch bestätigen</dt>
            <dd> Automatische Bestätigung von Nutzerkommentare aktivieren / deaktivieren. Wenn diese Option aktiviert ist, werden Kommentare automatisch verfügbar.          </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Captcha aktivieren</dt>
            <dd>Captcha im Bereich Kommentar hinterlassen auf Produktseiten aktivieren / deaktivieren.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Kommentar aktivieren</dt>
            <dd> Nutzerkommentare aktivieren / deaktivieren.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Namen des Autors darstellen</dt>
            <dd> Die Darstellung von Mitteilungen auf Blogseiten und Seiten der einzelnen Beiträgen aktivieren / deaktivieren </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Die Anzahl der Beitragsansichten darstellen</dt>
            <dd>Die Anzahl der Beitragsansichten aktivieren / deaktivieren. Diese Anzahl wird unter dem Beitrag auf der Blogseite und auf den einzelnen Beitragsseiten dargestellt.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Den Stil des Namens des Autors darstellen</dt>
            <dd> Das Format des Namens des Autors wird ändert.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>AVATAR Bild</dt>
            <dd>Hinzufügen des ursprünglichen Avatar Bildes für alle Nutzer.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>«Kein Bild» darstellen</dt>
            <dd>Ein Bild aktivieren / deaktivieren. «Kein Bild» wird dargestellt, wenn es ein Bild für den Beitrag / die Kategorie usw. fehlt.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Kategorien darstellen</dt>
            <dd>Das Bild der Kategorie und die Beschreibung auf Kategorienseiten des Blogs aktivieren / deaktivieren.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Einstellungen für Spalten der Blogseite</dt>
            <dd> Die Einstellung zur Darstellung der Module, die mit Blogs verbunden sind. Sie benötigen häufig zusätzliche Einstellungen, deswegen ist es empfohlen, zusätzliche Module wie PrestaShop Standardmodule zu installieren.            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Benutzerdefinierte CSS</dt>
            <dd> Sie können benutzerdefinierte Stile in diesem Feld hinzufügen. Achtung: wegen der Verwendugn der Klassen, die schon in der Vorlage verwendet, können Fehler auftretten.            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Alte Thumblr löschen</dt>
            <dd> Es wird benutzt, um Bilder des Blogs (wenn Bildgrößen geändert oder eine neue Art der Bilder hinzugefügt wird) neu zu erstellen. Um die Bilder neu zu erstellen, wählen Sie <strong>'Ja'</strong> (Yes) und drücken Sie auf den Button <strong>'Neu erstellen'</strong> (Regenerate).
            </dd>
        </dl>
    </li>
</ul>
