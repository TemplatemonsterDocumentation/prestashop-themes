<h4>Verfügbare Optionen</h4>
<h5>Information</h5>
<p>Dieser Bereich ermöglicht es Ihnen, die folgenden Optionen zu ändern:</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-10.png" alt=""/></figure>
<ul class="marked-list">
    <li><strong>Produkte aktivieren/deaktivieren</strong> (Enable/Disable products) - Ihr Produkt aktivieren/deaktivieren.</li>
    <li><strong>Für Bestellung verfügbar</strong> (Available for order) – hier können Sie Produkte für Bestellung verfügbar oder nicht verfügbar machen.
        <p class="alert alert-warning small">
            <span>Achtung: wenn für die Option 'Für Bestellung verfügbar' der Wert 'Ja' für Produkte mit versteckten Preisen festgelegt ist, werden ihre Preise automatisch sichtbar.</span>
        </p>
    </li>
    <li><strong>Preis darstellen</strong> (Show price) – den Produktpreis darstellen/verstecken.
        <p class="alert alert-warning small">
            <span>Achtung:  wenn für die Option 'Preis darstellen' der Wert 'Neu' für Produkte, die für Bestellung verfügbar sind, festgelegt ist, werden sie automatisch nicht verfügbar.</span>
        </p>
    </li>
    <li><strong>Nur Online</strong> (Online only) – legen Sie den Wert 'Только Online' für ausgewählte Elemente fest.</li>
    <li><strong>Zustand</strong> (Condition) – ändern Sie den Zustand einiger Produkte.</li>
    <li><strong>Kurze Beschreibung</strong> (Short description) – fügen Sie eine kurze Beschreibung hinzu, die auf alle ausgewählten Produkte verwendet wird.</li>
    <li><strong>Volle Beschreibung</strong> (Full description) – fügen Sie die Beschreibung zu ausgewählten Produkten hinzu, um sie dann vor der Hauptbeschreibung zu platzieren, oder ersetzen Sie diese beschreibung durch eine neue.</li>
    <li><strong>Tags</strong> (Tags) – fügen Sie mehr Tags zu ausgewählten Produkten hinzu.</li>
</ul>

<h5>Preis</h5>
<p>In diesem Bereich können Sie die Einstellungen des Produktpreises ändern.</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-11.png" alt=""/></figure>
<ul class="marked-list">
    <li><strong>"Sonderangebot" darstellen</strong> (Display "On sale") – ausgewählte Elemente mit dem Zeichen 'Sonderangebot' darstellen.</li>
    <li><strong>Steuervorschriften ändern</strong> (Change tax rules) – wählen Sie Steuervorschriften  aus, die auf mehrere Produkte verwendet werden.</li>
    <li><strong>Neuen spezifischen Preis hinzufügen</strong> (Add new specific price) – legen Sie einen bestimmten Preis für ausgewählte Produkte fest, um dann den Zeitraum der Preisaktivierung hinzuzufügen.</li>
</ul>

<h5>Assoziationen</h5>
<p> Hier können Sie Optionen ändern, die mit Produkte verbunden sind.</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-12.png" alt=""/></figure>
<ul class="marked-list">
    <li><strong>Marke ändern</strong> (Change brand) – hier können Sie die Marke der ausgewählten Elemente ändern.</li>
    <li><strong>Standardkategorie ändern</strong> (Change default category) – wählen Sie eine Kategorie aus, die Sie als Standardkategorie für einige Produkte festlegen möchten.
        <p class="alert alert-warning small">
            <span>Achtung: Sie können die Kategorie nur standardmäßig wählen. Ihre Standardkategorie wird durch eine neue ersetzt.</span>
        </p>
    </li>
    <li><strong>Verwandte Kategorien ändern</strong> (Change related categories) – legen Sie verwandte Kategorien für mehrere Produkte fest, um dann alte Assoziationen zu speichern oder zu löschen.</li>
    <li><strong>Accessoires hinzufügen</strong> (Add accessories) – fügen Sie Accessoires hinzu, die für das ausgewählte Produkt geeignet sind.</li>
</ul>

<h5>Anzahl</h5>
<p> Diese Einstellung ermöglicht es Ihnen, die Anzahl der Produkte festzulegen oder aktuelle Anzahl zu erhöhen/zu verringern.</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-13.png" alt=""/></figure>
<ul class="marked-list">
    <li><strong>Anzahl ändern</strong> (Change quantity) – legen Sie eine notwendige Anzahl der Produkte fest.</li>
</ul>
