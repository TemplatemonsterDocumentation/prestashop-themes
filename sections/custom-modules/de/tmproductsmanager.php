<h3>TM Products Manager</h3>
<h4>Einführung</h4>
<p> Dieses Modul wurde für die Arbeit mit Online-Shops erstellt, besonders mit Online-Shops mit einer großen Anzahl von Produkten. Indem Sie dieses Modul nutzen, können Sie Ihre Produkte ganz einfach filtern und verschiedene Aktionen durchführen.</p>

<h4>Wie man das Modul installiert und löscht </h4>
<p>Dieses Modul wird ein anderes Prestashop Modul installiert und gelöscht.</p>
<p class="alert alert-info">
    <span> Wenn Sie dieses Modul löschen oder noch einmal installieren, werden die vorgenommenen Änderungen an diesem Modul nicht verloren.</span>
</p>
<p>Wenn Sie das Modul erfolgreich installiert haben, finden Sie den Tab Katalog -> TM Products Manager (1), um das Hauptmenü des Moduls zu sehen.</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-1.png" alt=""/></figure>
<h4>
</h4>
<p>Das Hauptmenü des Moduls ist in zwei Hauptteile geteilt: 'Bereich wählen' (1) und 'Einstellung wählen' (2).</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-2.png" alt=""/></figure>
<p>Der erste Teil wurde als Tabs erstellt - Information, Preis, Assoziationen und Anzahl.</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-3.png" alt=""/></figure>
<ul class="marked-list">
    <li><strong>Information</strong> (Information) – in diesem Tab finden Sie Informationen über die Produktverfügbarkeit und Produktbeschreibung.</li>
    <li><strong>Preis</strong> (Price) – Preisanpassung und Steuervorschriften .</li>
    <li><strong>Assoziationen</strong> (Associations) – hier können Sie die Marke, Kategorie oder Accessoires einiger Produkte ändern.</li>
    <li><strong>Anzahl</strong> (Quantity) – hier können Sie die Anzahl der Produkte ändern.</li>
</ul>
<ul class="index-list">
