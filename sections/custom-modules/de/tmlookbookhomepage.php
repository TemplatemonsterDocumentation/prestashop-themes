<p class="alert alert-warning"> Um dieses Modul nutzen zu können, muss das Modul TM Look Book installiert werden.</p>
<p>TM Look Book Homepage wird verwendet, um das Modul TM Look Book auf der Startseite darzustellen. Mit diesem Modul können Sie Lookbook und seine Seiten darstellen.</p>
<h5>Anforderungen:</h5>
<ul class="marked-list">
    <li>PHP 5.4+</li>
    <li>PrestaShop 1.6</li>
    <li>Modul TM Look Book</li>
</ul>
<h5>Moduleigenschaften:</h5>
<ul class="marked-list">
    <li>Das Modul kann in zwei Positionen dargestellt werden: displayHome und displayTopColumn.</li>
    <li>TM Look Book Homepage – ein Child-Modul TM Look Book, deswegen können Sie es ohne es verwenden.</li>
    <li> Das Modul stellt Lookbook und seine Seiten dar.</li>
</ul>
<h5>Hauptpanel TM Look Book</h5>
<p> In dem Hauptpanel des Moduls können Sie Blöcke hinzufügen, löschen und bearbeiten, die auf der Startseite dargestellt werden. </p>
<figure class="img-polaroid">
    <img src="img/tmlookbookhomepage-main-panel.png" alt=""/>
</figure>
<h5>Wie man einen neuen Block hinzufügt</h5>
<ul class="index-list">
    <li>
        <p>Um einen neuen Block hinzuzufügen, loggen Sie sich ins Panel des Moduls ein und drücken Sie auf '+' (1) in der Tabelle mit dem notwendigen Hook.</p>
        <figure class="img-polaroid">
            <img src="img/tmlookbookhomepage-add-block-1.png" alt=""/>
        </figure>
    </li>
    <li>
        <p> In dem angezeigten Formular stellen Sie die folgenden Optionen ein:</p>
        <ul class="marked-list">
            <li><strong>Status</strong> (Status) – der status des Blocks. Diese Option ermöglicht es Ihnen, die Darstellung des Block zu aktivieren oder zu deaktivieren.</li>
            <li><strong>Typ</strong> (Type) – hier können Sie den Typ des Blocks wählen.</li>
            <li><strong>Lookbook</strong> (Lookbook) – hier können Sie das vorher ausgewählte Look wählen.</li>
        </ul>
    </li>
    <li>
        <p>Nachdem Sie Werte für alle Optionen festgelegt  haben, drücken Sie auf den Button 'Speichern' (2).</p>
        <figure class="img-polaroid">
            <img src="img/tmlookbookhomepage-add-block-2.png" alt=""/>
        </figure>
    </li>
</ul>
<p> Nachdem Sie den neuen Block erstellt haben, wird er auf der Startseite dargestellt.</p>
