<h5>Wie man das Media Parallax hinzufügt </h5>
<ul class="index-list">
    <li>
        <p> Sie sollen eine ID oder eine Klasse zum Inhalt Ihrer HTML-Seite hinzufügen. Sie können auch ein einzigartiges Tag verwenden, aber dieses Element muss nicht wiederholt werden.</p>
    </li>

    <li>
        <p> Erstellen Sie ein Bild mit der notwendigen Größe (das Bild muss größer als Block sein, zu dem der Effekt hinzugefügt wird). Wenn Sie ein Video hinzufügen möchten, muss es in drei Formaten sein: .mp4, .webm und .ogv. Sie sollen auch ein Bild für die Vorschau mit derselben Größe (.png, .jpg und .gif) hinzufügen.</p>
    </li>
    <li>
        <p>Drücken Sie auf den Button <strong>'Container hinzufügen'</strong> (Add container) im Bereich <strong>'Neuen Block hinzufügen'</strong> (Add new block).</p>
        <figure class="img-polaroid">
            <img src="img/parallax-1.png" alt=""/>
        </figure>
    </li>
    <li>
        <p> In dem DropDown Fenster legen Sie notwendige Werte für Einstellungen fest.</p>
        <h6>
            <strong>Einstellungen:</strong>
        </h6>
        <ul class="index-list">
            <li>
                <dl class="inline-term">
                    <dt>Selektor</dt>
                    <dd> es ist ein Element, zu dem der Effekt (ID oder ein einzigartiges Tag) hinzugefügt wird.</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Typ des Parallax Effektes</dt>
                    <dd> Typ des Effektes, der (ein Bild oder ein Video) verwendet wird.</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Mediadateien</dt>
                    <dd>laden Sie alle notwendigen Dateien hoch.</dd>
                </dl>
            </li>
            <li>
                <p>Drücken Sie auf den Button <strong>'Speichern'</strong>.</p>
            </li>
        </ul>
        <figure class="img-polaroid">
            <img src="img/parallax-2.png" alt=""/>
        </figure>
    </li>
</ul>
