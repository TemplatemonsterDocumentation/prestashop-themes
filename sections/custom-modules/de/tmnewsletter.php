<ul class="index-list">
    <li><strong> Live-Modus</strong> (Live mode) - das Modul aktivieren/deaktivieren.</li>
    <li><strong> Möchten Sie Bestätigungen per E-Mail-Adresse senden, nachdem der Nutzer den Newsletter abonniert hat?</strong> (Would you like to send a verification email after subscription?) - Senden von Bestätigungen per E-Mail-Adresse aktivieren/deaktivieren.
    </li>
    <li><strong> Timeout für Gäste </strong> (Timeout for guests) - das Timeout für DropDown Erinnerungen über das Abonnement für Gäste festlegen.    </li>
    <li><strong> Timeout für Kunden </strong> (Timeout for customers) - das Timeout für DropDown Erinnerungen über das Abonnement für Kunden festlegen.
    </li>
</ul>
