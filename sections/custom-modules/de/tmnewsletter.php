<p>Dieses Modul stellt eine Benachrichtigung dar, in der es angeboten wird, den Newsletter in dem DropDown Fenster zu abonnieren. Die Überprüfung der E-Mail-Adresse kann in den Moduleinstellung (durch eine Bestätigung per E-Mail) aktiviert werden. Sie können auch das Timeout zwischen Erinnerungen für registrierte und nicht registrierte Benutzer einstellen. Die registrierten Nutzer können auf Erinnerungen für immer verzichten.</p>
<h5>Wie man das Modul installiert und löscht</h5>
<p>Dieses Modul wird wie ein anderes PrestaShop Modul installiert und gelöscht.</p>
<h5>Wie man das Modul einstellt</h5>
<p> Nachdem Sie das Modul installiert haben, können Sie seine Einstellungen ändern.</p>
