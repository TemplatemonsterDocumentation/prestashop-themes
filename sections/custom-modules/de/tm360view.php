<p>Dieses Modul stellt Bilder mit 3D Effekt dar und ermöglicht es Nutzern, Produkte um 360 Grad zu drehen. Die Rotation kann automatisch beim Laden der Seite gestartet werden oder der Kunde kann 'Bild' manuell drehen, indem er auf den linken Maustaste drückt.</p>
<p>Darüber hinaus können Sie dieses Modul nutzen, um die Panoramaaussicht (oder einige Panoramaaussichten) zur Startseite hinzuzufügen.</p>
<p>Der Modul 360°-Ansicht ist auch auf Produktseiten verfügbar.</p>

<h4>Installation</h4>
<p>Finden Sie den Tab Module und Einstellungen -> Module und Einstellungen (1) in Ihrem Control Panel und finden Sie das Modul TM 360 Ansicht (2). Danach drücken Sie auf den Button 'Installieren' (3).</p>
<figure class="img-polaroid">
    <img src="img/360view-1.png" alt=""/>
</figure>
<h4>Wie man das Modul einstellt</h4>
<p>Nachdem das Modul erfolgreich installiert wurde, sehen Sie die Seite 'Einstellung' mit den Standardeinstellungen des Moduls.</p>
<p>Hier finden Sie die Einstellungen für die 360°-Ansicht (1) und Panoramaaussicht (2).</p>
<figure class="img-polaroid">
    <img src="img/360view-2.png" alt=""/>
</figure>
<h4>360°-Ansicht</h4>
<p>Die Einstellungen sind unter dem Bildschirmfoto beschrieben. Wählen Sie Werte entsprechend Ihren Anforderungen und drücken Sie auf den Button 'Speichern' (1).</p>
<figure class="img-polaroid">
    <img src="img/360view-3.png" alt=""/>
</figure>
<ul class="marked-list">
    <li><strong>Auf der Startseite aktivieren</strong> (Enable on Home) – hier aktivieren Sie die 360°-Ansicht des Produktes auf der Startseite.</li>
    <li><strong>Ein Produkt wählen</strong> (Select a product) (es wird dargestellt, wenn für 'Auf der Startseite aktivieren' der Wert 'Ja' festgelegt wurde) – wählen Sie ein Produkt aus, das Sie in der 360°-Ansicht darstellen möchten.</li>
    <li><strong>Animieren</strong> (Animate) – dank dieser Option wird das Produkt automatisch beim Laden der Seite gedreht die Option zur Produktdarstellung.</li>
    <li><strong>Frame Zeit</strong> (Frame time) (es wird dargestellt, wenn die Option 'Animieren' den Wert 'Ja' hat) – das Zeitinterval, bevor ein Bild durch ein anderes ersetzt wird; geben Sie den Wert in Millisekunden ein.</li>
</ul>
<h5>Wie man Bilder hinzufügt/löscht</h5>
<ul class="index-list">
    <li>
        <p>Finden Sie den Tab Katalog -> Produkte in Ihrem Control Panel (1), wählen Sie ein Produkt und drücken Sie auf den Button 'Bearbeiten' (2).</p>
        <figure class="img-polaroid">
            <img src="img/360view-4.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Um Bilder hinzuzfügen, nutzen Sie den Tab  'TM 360 view' (1) in Produkteinstellungen und drücken Sie auf den Button 'Dateien hinzufügen' (2).</p>
        <figure class="img-polaroid">
            <img src="img/360view-5.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Danach drücken Sie auf den Button 'Dateien hochladen' (1).</p>
        <figure class="img-polaroid">
            <img src="img/360view-6.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Nachdem dem erfolgreichen Hochladen der Dateien, drücken Sie auf den Button 'Speichern' (1).</p>
        <figure class="img-polaroid">
            <img src="img/360view-7.png" alt=""/>
        </figure>
        <p class="alert alert-warning">Für die glatte Rotation fügen Sie mehr Bilder hinzu und ändert ein wenig die Bildposition, wenn Sie Fotos aufnehmen.</p>
    </li>
    <li>
        <p>Nachdem Sie die Bilder hinzugefügt haben, sehen Sie die 360°-Ansicht der Produkte auf der Startseite *. <br>
            *<small>Sie wird auch auf der Produktseite nach der Beschreibung dargestellt.</small></p>
    </li>
    <li>
        <p>Um die 360°-Ansicht des Produktes auf der Startseite zu deaktivieren, öffnen Sie die Einstellungen der 360°-Ansicht und ersetzen Sie den Wert der Option 'Auf der Startseite aktivieren' durch 'Nein'.</p>
    </li>
    <li>
        <p>Um die 360°-Ansicht  auf der Produktseite zu deaktivieren, öffnen Sie den Tab 'TM 360 view' in den Produkteinstellungen und löschen Sie alle Bilder.</p>
    </li>
</ul>
<h4>Panorama</h4>
<p>Dieses Modul wird auch benutzt, um die Panoramaansicht zur Startseite hinzuzufügen.</p>
<p class="alert alert-warning">Sie können einige Panorama-Blöcke hinzufügen.</p>
<h5>Wie man das Modul Panorama hinzufügt</h5>
<ul class="index-list">
    <li>
        <p>Um ein neues Panorama hinzuzufügen, öffnen Sie die Seite Moduleinstellungen und drücken Sie auf den Button '+' ('Neues Element hinzufügen') (1).</p>
        <figure class="img-polaroid">
            <img src="img/360view-8.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Hier sehen Sie die Einstellungen für das Panorama, die unten beschrieben sind.</p>
        <figure class="img-polaroid">
            <img src="img/360view-9.png" alt=""/>
        </figure>
        <ul class="marked-list">
            <li><strong>Eine Datei wählen</strong> (Select a file) - wählen Sie eine Datei für das Panorama aus; es ist ein Pflichtfeld (zumindest für die Standardsprache).</li>
            <li><strong>Titel</strong> (Title) – geben Sie einen Titel für das Panorama ein; es ist ein Pflichtfeld (zumindest für die Standardsprache).</li>
            <li><strong>Breite</strong> (Width) – es ist die Breite des Panorama-Blocks; es ist ein Pflichtfeld.</li>
            <li><strong>Höhe</strong> (Height) - es ist die Höhe des Panorama-Blocks; es ist ein Pflichtfeld.</li>
            <li><strong>Status</strong> (Status) - ein Panorama-Element auf der Startseite aktivieren/deaktivieren.</li>
            <li><strong>Loop</strong> (Loop) – die Rotation noch einmal wiederholen.</li>
            <li><strong>Animationseffekt</strong> (Animate) – das Panorama wird automatisch beim Laden der Seite gedreht.</li>
                <li><strong>Frame-Zeit</strong> (Frame time) (es wird dargestellt, wenn die Option  'Animieren' den Wert 'Ja' hat) – der Zeitraum, wann das Panorama einen Vollkreis macht; geben Sie den Wert in Millisekunden ein; es ist ein Pflichtfeld.</li>
        </ul>
    </li>
    <li>
        <p>Drücken Sie auf den Button 'Speichern' und Ihr Panorama-Bild wird auf der Startseite dargestellt.</p>
    </li>
</ul>
<h5>Wie man das Panorama löscht</h5>
