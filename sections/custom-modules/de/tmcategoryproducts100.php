<p>Dieses Modul stellt Produktkategorien in Hooks displayHome und displayHomeTabContent dar. Dieses Modul ermöglicht es Ihnen, einzelne Produkte, Produkte aus Kategorien oder alle Produkte darzustellen. Außerdem gibt es in diesem Modul Karussell, das funktioniert, wenn das Modul in der Position displayHome platziert ist.</p>
<h5>Das Hauptpanel TM Category Products</h5>
<p>Das Hauptpanel. Hier können Sie das Modul einstellen. </p>
<figure class="img-polaroid">
    <img src="img/tmcategoryproducts-1.png" alt=""/>
</figure>
<h5>Wie man einen Tab hinzufügt</h5>
<p>Ein Tab ist ein Block, der Produkte in der Position displayHomeTabContent darstellt.</p>
<ul class="index-list">
    <li><p>Öffnen Sie das Hauptpanel des Moduls und drücken Sie auf 'Plus' (1) in der Liste der Tabs.</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts-2.png" alt=""/>
        </figure>
    </li>
    <li><p>In dem DropDown Formular finden Sie die folgenden Einstellungen:</p>
        <ul class="marked-list">
            <li>Status – diese Einstellung ermöglicht es Ihnen, den Tab zu aktivieren/zu deaktivieren.</li>
            <li>Kategorie wählen - die Kategorie, deren Produkte angezeigt werden.            </li>
            <li>Einen benutzerdefinierten Namen verwenden - diese Einstellung ermöglicht es Ihnen, den dargestellten Kategoriennamen zu ändern. Wenn diese Einstellung aktiviert ist, wird das folgende Formular angezeigt:
                <ul class="marked-list">
                    <li> Name - der benutzerdefinierte Name des Tabs, der auf der Website dargestellt wird.</li>
                </ul>
            </li>
            <li> Produkte zur Darstellung wählen - wenn diese Einstellung aktiviert ist, wird das Feld 'Anzahl der dargestellten Produkte' dargestellt, das die Anzahl der dargestellten Produkte festlegt. Wenn diese Einstellung aktiviert ist, wird der Button 'Produkte hinzufügen' dargestellt, der es Ihnen ermöglicht, Produkte zur Darstellung im Tab zu wählen.            </li>
        </ul>
    </li>
    <li><p>Nachdem Sie die Einstellungen geändert haben, drücken Sie auf den Button 'Speichern' (2).</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts-3.png" alt=""/>
        </figure>
    </li>
</ul>
<h5>Wie man einen Block hinzufügt</h5>
<p>Dieser Block ermöglicht es Ihnen, Produkte in der Position displayHome zu wählen.</p>
<ul class="index-list">
    <li><p>Öffnen Sie das Hauptpanel des Moduls und drücken Sie auf den Tab 'Plus' (1) in der Liste der Blöcke.</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts-4.png" alt=""/>
        </figure>
    </li>
    <li><p> In dem DropDown Formular finden Sie die folgenden Einstellungen:</p>
        <ul class="marked-list">
            <li><p>Status – diese Einstellung ermöglicht es Ihnen, den Block zu aktivieren/zu deaktivieren.</p></li>
            <li><p>Kategorie wählen - die Kategorie, deren Produkte angezeigt werden.</p></li>
            <li>
                <p>Einen benutzerdefinierten Namen verwenden - diese Einstellung ermöglicht es Ihnen, den dargestellten Kategoriennamen zu ändern. Wenn diese Einstellung aktiviert ist, wird das folgende Formular angezeigt:</p>
                <ul class="marked-list">
                    <li><p>Name - der benutzerdefinierte Name des Tabs, der auf der Website dargestellt wird.</p></li>
                </ul>
            </li>
            <li>
                <p>Produkte zur Darstellung wählen – wenn diese Einstellung aktiviert ist, wird das Feld 'Anzahl der dargestellten Produkte' dargestellt, das die Anzahl der dargestellten Produkte festlegt. Wenn diese Einstellung aktiviert ist, wird der Button 'Produkte hinzufügen' dargestellt, der es Ihnen ermöglicht, Produkte zur Darstellung im Tab zu wählen.</p>
            </li>
            <li>
                <p> Karussell verwenden - diese Einstellung ermöglicht es Ihnen, das Karussell für Produkte in Blöcken zu aktivieren/zu deaktivieren. Nachdem Sie diese Einstellung aktiviert haben, sehen Sie die folgenden Einstellungen:</p>
                <ul class="marked-list">
                   <li><p><strong>Angezeigte Elemente</strong> (Visible items) – die Anzahl der angezeigten Elemente.</p></li>
                    <li><p><strong>Elemente scrollen</strong> (Items scroll) – die Anzahl der Elemente, die bei einem Scroll im Karussell geändert werden.</p></li>
                     <li><p><strong>Breite des Slides</strong> (Slide Width) – die Breite des Elementes in dem Karussell.</p></li>
                     <li><p><strong>Feld des Slides</strong> (Slide Margin) - das Feld des Elementes in dem Karussell.</p></li>
                     <li><p><strong>Automatisches Scrollen</strong> (Auto scroll) – automatisches Scrollen der Elemente im Karussell.</p></li>
                      <li><p><strong>Geschwindigkeit des Karussells</strong> (Carousel speed) – der Drehzahlwechsel der Elemente.</p></li>
                   <li><p><strong>Pause</strong> (Pause) – die pause zwischen dem Scrollen (wenn "Automatisches Scrollen" aktiviert ist).</p></li>
                     <li><p><strong>Zufällig</strong> (Random) – das Karussell wird mit einem zufälligen Element gestartet.</p></li>
                    <li><p><strong>Loop des Karussells</strong> (Carousel loop) - Loop: nachdem alle Elemente des Karussells angezeigt wurden, wird das Karussell von Anfang an gestartet.</p></li>
                    <li><p><strong>Bedienelemente am Ende ausblenden</strong> (Hide control at the end) – Bedienelemente (Zurück/Weiter) ausblenden.</p></li>
                    <li><p><strong>Seitennummerierung</strong> (Pager) – die Seitennummerierung darstellen.</p></li>
                    <li><p><strong>Bedienelemente </strong> (Control) – Bedienelemente (Zurück/Weiter) darstellen.</p></li>
                   <li><p><strong> Automatische Verwaltung</strong> (Auto control) - Bedienelemente (Wiedergabe/Pause) darstellen.</p></li>
            <li><p><strong>Automatisches Hover </strong> (Auto hover) - die Pause im Karussell, wenn der Nutzer mit dem Mauszeiger über das Element der Liste fährt.</p></li>
                </ul>
            </li>
        </ul>
    </li>
    <li>
        <p>После изменения всех необходимых настроек, нажмите на кнопку 'Сохранить' (2).</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts-5.png" alt=""/>
        </figure>
    </li>
</ul>
