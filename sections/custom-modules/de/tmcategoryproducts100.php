<h5>Produkte wählen</h5>
<p> Wenn Sie ein element erstellen und die Einstellung «Produkte zur Darstellung wählen» aktivieren, können Sie Produkte zur Darstellung wählen. Führen Sie die folgenden Aktionen durch:</p>
<ul class="index-list">
    <li><p>Drücken Sie auf den Button 'Produkte hinzufügen' (1):</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts-7.png" alt=""/>
        </figure>
    </li>
    <li><p>In dem DropDown Fenster wählen Sie ein notwendiges Produkt (2) und drücken Sie auf den Button 'Hinzufügen' (3):</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts-8.png" alt=""/>
        </figure>
    </li>
    <li><p> Jetzt können Sie die hinzugefügte Produkte an einer gewünschten Stelle platzieren, indem Sie sie verschieben (4):</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts-9.png" alt=""/>
