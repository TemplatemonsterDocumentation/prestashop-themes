<h5>Wie man einen neuen Slider hinzufügt</h5>
<p>Nachdem Sie auf den Button 'Slider hinzufügen' gedrückt haben, sehen Sie die Einstellung, die es Ihnen ermöglicht, 'Benutzerdefinierten Slider' oder 'Produkt-Slider' zu wählen.</p>
<figure class="img-polaroid">
    <img src="img/mpslider03_slider_add.png" alt=""/>
</figure>
<p>Der Unterschied zwischen diesen zwei Slidern besteht darin: der Produkt-Slider stellt Produkte Ihres Online-Shops dar, und zum benutzerdefinierten Slider können Sie Slides unbegrenzt hinzufügen und ihren Code nach Ihren Bedürfnissen bearbeiten.</p>
<p> Einstellungen für den benutzerdefinierten Slider und Produkt-Slider sind einbisschen anders. <br>
    Ihre Unterschiede haben wir unten beschrieben.</p>

<h4>Wie man das Modul einstellt</h4>
<h5>Slider-Einstellungen</h5>
<p>Einstellungen des Hauptsliders. Ausführliche Beschreibung der Einstellungen befindet sich unter dem Bildschirmfoto.</p>
<figure class="img-polaroid">
    <img src="img/mpslider04_slider_slider_settings.png" alt=""/>
</figure>
<ul class="marked-list">
    <li><strong>Name des Sliders</strong> (Slider title) - der Name des Sliders (zum Beispiel: Slider1); Sie müssen unbedingt einen Namen eingeben. Wenn Sie mehrere Sliders haben, müssen Sie sich unterscheiden.
    </li>
    <li><strong>Alias des Sliders</strong> (Slider alias) - Alias, das in den Shortcodes für die Hinzufügung des Sliders (zum Beispiel: slider1) verwendet wird; es muss einzigartig sein.
    </li>
    <li><strong>Shortcode des Sliders </strong> (Slider shortcode) - kopieren Sie diesen Shortcode und fügen Sie in Ihre Seite ein.</li>
    <li><strong>Erzwungene volle Breite</strong> (Force full width) - aktivieren Sie diese Einstellung, um den Slider über die gesamte Breite zu ziehen.</li>
    <li><strong>Erzwungene volle Höhe </strong> (Force full height) - aktivieren Sie diese Einstellung, um den Slider über die gesamte Höhe zu ziehen.</li>
    <li><strong>Grid in voller Größe (erzwungen)</strong> (Force full size grid) – diese Einstellung wird benutzt, um das Grid in dem gesamten Parent Container (Sie sollen sowieso die Breite und die Höhe für das Grid festlegen) zu platzieren. 
    </li>
    <li><strong>Größe der Grid-Schichten</strong> (Layers grid size) - Sie können die ursprüngliche Breite und Höhe der Schichten festlegen.</li>
    <li><strong>Slideshow aktivieren</strong> (Enable slideshow) - Slideshow aktivieren/deaktivieren.</li>
    <li><strong>Slideshow verzögern</strong> (Slideshow delay) - die Anzeigezeit des Slides auf dem Bildschirm in Millisekunden. </li>
    <li><strong>Slideshow Animation</strong> (Slideshow animation) - wählen Sie den Animationseffekt aus, wie Slides gewechselt werden.</li>
    <li><strong>Dauer des Slideshows</strong> (Slideshow duration) - die Dauer des Animationseffektes in Millisekunden.</li>
    <li><strong>Verfärbung des Slideshows</strong> (Slideshow easings) - die Verfärbung bei der Umschaltung zwischen den Slides.</li>
    <li><strong>Mit den Slide beginnen</strong> (Start with slide) - die ID des ersten Slides.</li>
</ul>
<h5>Bedienelemente</h5>
<p>Einstellungen der Bedienelemente bei der Umschaltung der Slides.</p>
<figure class="img-polaroid">
    <img src="img/mpslider05_slider_controls.png" alt=""/>
</figure>
<ul class="marked-list">
    <li><strong>Pfeile darstellen</strong> (Show arrows) - Pfeile darstellen/nicht darstellen.</li>
    <li><strong>Punkte darstellen </strong> (Show bullets) - Punkte darstellen/nicht darstellen.</li>
    <li><strong>Timer des Slideshows darstellen</strong> (Show slideshow timer) - Timer des Slideshows darstellen/nicht darstellen. </li>
    <li><strong>Den Button Wiedergabe/Pause darstellen</strong> (Show slideshow play/pause button) – Den Button Wiedergabe/Pause darstellen/nicht darstellen.</li>
    <li><strong>Bedienelemente nicht darstellen, wenn man mit dem Mauszeiger über den Slider nicht fährt </strong> (Hide controls when mouse leaves slider) – Bedienelemente nicht darstellen, wenn der Mauszeiger außer dem Slider-Bereich ist.</li>
    <li><strong>Pause bei Hover</strong> (Pause on hover) – Sie können die Slideshow bei Hover des Sliders stoppen.</li>
    <li><strong>Umgekehrte Reihenfolge des Slides aktivieren</strong> (Reverse order of the slides) – Slides in der umgekehrten Reihenfolge animmieren.</li>
    <li><strong>Zähler anzeigen</strong> (Show counter) – die Darstellung der Anzahl der Slides aktivieren.</li>
    <li><strong>Swipe aktivieren</strong> (Enable swipe) – Swipe auf dem  Display aktivieren.</li>
</ul>
<h5>Aussehen</h5>
<p>Einstellungen, die zur Darstellung des Slides gehören.</p>
<figure class="img-polaroid">
    <img src="img/mpslider06_slider_appearance.png" alt=""/>
</figure>
<ul class="marked-list">
    <li><strong>Darstellen auf</strong> (Display on) – Hooks, auf denen der Slider dargestellt werden muss.</li>
    <li><strong>Sichtbar</strong> (Visible) – minimale und / oder maximale Bildschirmauflösung, auf der der Slider (Sie können dies festlegen, indem Sie Voreinstellungen nutzen: Smartphone / Tablet / PC) sichtbar wird.
    </li>
    <li><strong>Initialisierungsverzögerung</strong> (Initialization delay) – die Pause von der Initialisierung des Sliders (legen Sie den Wert in Millisekunden fest).
    </li>
    <li><strong>Slider beim Scrollen initialisieren</strong> (Initialize slider on scroll) – legen Sie fest, ob Sie den Slider beim Scrollen initialisieren möchten.</li>
    <li><strong>Benutzerdefinierter Name der Slider-Klasse</strong> (Slider custom class name) – der Name der benutzerdefinierten Klasse, der für den Slider verwendet wird.</li>
    <li><strong>Benutzerdefinierte Stile des Sliders</strong> (Slider custom styles) – benutzerdefinierte CSS Stile können direkt zum Textbereich hinzugefügt werden.</li>
</ul>
<h5>Inhalt</h5>
<p>Dieses Menü wird nur den Produkt-Slider dargestellt. <br>
    Nachdem Sie das Modul eingestellt haben, drücken Sie auf den Button 'Erstellen' (1).</p>
<figure class="img-polaroid">
    <img src="img/mpslider07_slider_content1.png" alt=""/>
</figure>
<ul class="marked-list">
    <li><strong>Slide auf die Produktseite verlinken</strong> (Link slides to product page) – aktivieren Sie diese Einstellung, wenn Sie möchten, damit beim Klick auf das Slide die Seite des entsprechenden Produktes geöffnet wird.    </li>
    <li><strong>In einem neuen Fenster öffnen</strong> (Open in new window) – aktivieren Sie diese Einstellung, um die Produktseite in einem neuen Fenster zu öffnen.</li>
    <li><strong>Kategorien</strong> (Categories) – Produktkategorien, die in dem Slider (Ctrl + drücken Sie auf die linke Maustaste, um mehrere Kategorien zu wählen) dargestellt werden.
    </li>
    <li><strong>Tags</strong> (Tags) – notwendige Tags.</li>
    <li><strong>Produkte darstellen, die auf Lager sind</strong> (Only display in-stock products) – Produkte auf Lager werden dargestellt.</li>
    <li><strong>Nur Sonderangebote darstellen</strong> (Only display on sale products) – nur Sonderangebote werden dargestellt.</li>
    <li><strong>Produkte ausschließen</strong> (Exclude products) – fügen Sie die Produkt-ID durch Komma hinzu.</li>
    <li><strong>Produkte einschließen</strong> (Include products) - fügen Sie die Produkt-ID durch Komma hinzu.</li>
    <li><strong>Anzahl der Produkte</strong> (Products number) – die Anzahl der angezeigten Produkte.</li>
    <li><strong>Anzahl der ersten Ergebnisse, die übersprungen werden müssen (verzögerter Start)</strong> (Number of first results to skip (offset)) – die Anzahl der ersten Slides, die nicht dargestellt werden.
    </li>
    <li><strong>Sortieren nach</strong> (Order by) – die Reihenfolge, die verwendet wird.</li>
    <li><strong>Sortierrichtung</strong> (Order direction) – die Sortierrichtung entweder nach unten oder nach oben.</li>
</ul>
