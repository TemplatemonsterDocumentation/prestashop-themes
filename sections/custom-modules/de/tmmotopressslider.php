<h4>Der Inhalt des Slides </h4>
<p>Hier können Sie Texte, Bilder, Buttons und Videos zu jedem Slide hinzufügen. <br>
    Der Bereich ist in zwei Unterbereichen getrennt: 'Schichteinstellungen ' (1) und 'Schichtreihenfolge' (2).
   Über den Bereich 'Schichteinstellungen' sehen Sie vier Tabs zur Darstellung des notwendigen Inhalts (3). Nachdem Sie ein Element ausgewählt haben, können Sie es einstellen. <br>
Dieser Inhalt wird wie einzelne Schichten hinzugefügt, deswegen können Sie sie nach unten und nach oben ziehen, um die Reihenfolge in dem Bereich 'Schichtreihenfolge' (2) festzulegen.</p>
<p>Sie können entweder eine Schicht oder (4) oder alle Schichten zusammen löschen (5). </p>
<figure class="img-polaroid">
    <img src="img/mpslider19_slide_add_text.png" alt=""/>
</figure>
<h5>Schichteinstellungen</h5>
<h6>Wie man einen Text hinzufügt</h6>
<p>Mit Hilfe von diesem Tab können Sie den Text im Slide hinzufügen, bearbeiten oder platzieren.</p>
<figure class="img-polaroid">
    <img src="img/mpslider19_slide_add_text_2.png" alt=""/>
</figure>
<ul class="marked-list">
    <li>Indem Sie graue Kreise nutzen, können Sie die Textposition festlegen (1).</li>
    <li>Um diese Position einzustellen, fügen Sie die Werte 'X' und 'Y' in Pixel hinzu und legen Sie die Breite dieses Elementes fest (2).</li>
    <li>Wenn 'Leerraum' den Wert 'Standard' hat, wird der Text normal eingewickelt. Wenn Sie den Wert 'Nicht einwickeln' ausgewählt haben, wird der Text nicht eingewickelt (3).
    </li>
    <li>In der zweiten Spalte können Sie Animationseffekte des Elementes verwalten – wie, wie lange und wann er relativ zur Darstellungsdauer des Slides begonnen und beendet werden muss(4). Sie wird unten für Ihre Bequemlichkeit angezeigt (5); um sie zu ändern, finden Sie den Tab Slider Einstellungen -> Slideshow Paus. Sie können auch den Animationseffekt im Fenster ansehen, das angezeigt wird, wenn Sie auf den Button 'Bearbeiten' drücken. Wählen Sie Dauer, Verfärbung, Animationseffekt und drücken Sie auf den Button 'Wiedergeben' für die Vorschau des Slides. Danach drücken Sie auf den Button  'Anwenden', wenn das Slide Ihren Bedürfnissen entspricht.
    </li>
</ul>
<p>Die oben beschriebenen Einstellungen sind für Elemente gleich, deswegen erklären wir weiter nur Einstellungen aus der dritten Spalte.</p>
<figure class="img-polaroid">
    <img src="img/mpslider19_slide_add_text_3.png" alt=""/>
</figure>
<ul class="marked-list">
    <li>Der Text kann zum Textbereich hinzugefügt werden (6).</li>
    <li>Die Stile des Textes können Sie aus den Voreinstellungen in der DropDown Liste (7) wählen, die Sie ändern können, indem Sie auf den Button 'Bearbeiten' (8) drücken. Darüber hinaus können Sie sie deaktivieren, indem Sie auf den Button 'Löschen' (9) drücken. Nachdem Sie die Stile des Textes geändert haben, drücken Sie auf den Button 'Stil anwenden'. Wenn Sie einen bestehenden Stil hinzufügen oder speichern möchten, drücken Sie auf den Button 'Duplizieren', ändern Sie und speichern Sie ihn. Um einen Stil von Grund auf zu erstellen, drücken Sie auf den Button 'Neu Voreinstellung erstellen' und nehmen Sie gewünschte Änderungen vor.
    </li>
    <li>Sie können auch eine benutzerdefinierte Klasse zu jedem Element hinzufügen (10).</li>
</ul>
