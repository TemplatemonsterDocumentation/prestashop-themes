<h4>Wie man das Modul installiert</h4>
<p>Dank diesem Modul können Sie MotoPress Slider zu Ihrer HTML Seite hinzufügen. Es kann wie ein anderes PrestaShop Modul installiert werden:</p>
<ul class="index-list">
    <li><p>Laden Sie das Modul in den Ordner /modules hoch.</p></li>
    <li><p>Öffnen Sie das Control Panel  -> Module und Services -> Module und Services (1), finden Sie das Modul (2) und drücken Sie auf den Button 'Installieren' (3).</p>
        <figure class="img-polaroid">
            <img src="img/mpslider00_install.png" alt=""/>
        </figure>
    </li>
    <li><p>Nachden Sie das Modul erfolgreich installiert haben, sehen Sie das Menü 'MotoPress Slider' unter dem Panel (1).<br>
            Drücken Sie auf den Button 'MotoPress Slider' in diesem Menü (2).
        </p>
        <figure class="img-polaroid">
            <img src="img/mpslider01_open.png" alt=""/>
        </figure>
    </li>
</ul>

