<h3>TM Product Custom Tab</h3>
<p>Mit diesem Modul könnt ihr einen zusätzlichen Tab auf der Produktseite erstellen und dadurch zusätzliche Informationen darstellen. Dank diesem Modul könnt ihr Bilder, Texte, Links usw. hinzufügen.</p>
<h4> Wie man das Modul installiert und löscht</h4>
<p> Dieses Modul wird ein anderes PrestaShop Modul installiert und gelöscht.</p>
<h4>Wie man das Modul einstellt</h4>
<p> Nachdem Sie das Modul installiert haben, können Sie neue Tabs zu Produkten auf der Seite Einstellungen und auf der Bearbeitungsseite des Produktes hinzufügen.</p>
<h5> Wie man Tabs über Moduleinstellungen hinzufügt:</h5>
<p>Um einen neuen Tab hinzuzufügen, drücken Sie auf den Button 'Neu hinzufügen' (1).</p>
<figure class="img-polaroid">
    <img src="img/tmcustomproducttab-1.png" alt=""/>
</figure>
<ul class="index-list">
    <li>
        <p> Wie man einen Tab zu einem Produkt oder mehreren Produkten für die vorher ausgewählte Kategorie hinzufügt.</p>
        <figure class="img-polaroid">
            <img src="img/tmcustomproducttab-4.png" alt=""/>
        </figure>
        <ul class="marked-list">
            <li><strong>Name des Tabs</strong> (Tab Heading) – der Name des Tab ist ein Pflichtfeld.</li>
            <li><strong>Beschreibung des Tabs</strong> (Tab Description) - Informationen über den Tab: Bilder, Texte, Videos, Links usw.</li>
            <li><strong>Status</strong> (Status) – der Status des Elementes.</li>
            <li><strong>Zusätzliche Einstellungen</strong> (Additional settings) - zusätzliche Einstellungen aktivieren/deaktivieren.</li>
            <li><strong>Kategorie wählen</strong> (Select category) - wählen Sie eine Kategorie aus, in der der Tab angezeigt werden muss.</li>
            <li><strong>Angezeigte Produkte</strong> (Products to display) - wählen Sie Produkte aus der vorher ausgewählten Kategorien aus.</li>
        </ul>
