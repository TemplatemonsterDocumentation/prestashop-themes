<h5> Wie man die Darstellungsposition des Tabs ändert </h5>
<p> Verschieben Sie den notwendigen Tab, um seine Position zu ändern.</p>
<figure class="img-polaroid">
    <img src="img/tmcustomproducttab-5.png" alt=""/>
</figure>
<h5> Wie man Tabs über Produkteinstellungen hinzufügt:</h5>
<p>Öffnen Sie die Produktseite und drücken Sie auf den Link 'TM Product Custom Tab' im Menü links.</p>
<figure class="img-polaroid">
    <img src="img/tmcustomproducttab-6.png" alt=""/>
</figure>
<p>Wenn 'Zusätzliche Einstellungen' deaktiviert sind, wird der Tab zum Produkt hinzugefügt, das vorher geändert wurde. Sie können auch den Tab zu Produkten hinzufügen, die zu den ausgewählten Kategorien gehören. Darüber hinaus können Sie den Tab zu mehreren Produkten hinzufügen, die zur vorher ausgewählten Kategorie gehören.</p>
<p> Um die Darstellungsposition der Tabs zu ändern, verschieben Sie Blöcke. Sie können auch Tabs bearbeiten oder löschen.</p>
