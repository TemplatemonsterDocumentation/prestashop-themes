<p> Mit diesem Modul können Sie eine unbegrenzte Anzahl der Videos zu jedem Produkt in jeder Sprache und zu jedem Shop hinzufügen.</p>
<h5>Wie man das Modul installiert und löscht</h5>
<p> Dieses Modul wird wie ein anderes PrestaShop Modul installiert und gelöscht. Diese Modul hat keine Einstellungsseite und unterstützt den Multishop.</p>
<p> Nachdem Sie das Modul installiert haben, sehen Sie einen neuen Tab auf der Seite Produkteinstellungen (Katalog -> Produkt -> anderes Produkt wählen - TM Product Videos).</p>
<p> Um ein Video zum Produkt hinzuzufügen, füllen Sie notwendige Felder (das Feld Link ist ein Pflichtfeld) aus. Dieses Modul ist mehrsprachig, deswegen sollen Sie Felder aller Sprachen ausfüllen, mit dem dieses Video verbunden ist. Wenn das Feld «Link» für eine Sprache nicht ausgefüllt ist, wird das Video für diese Sprache nicht hinzugefügt. Dieses Modul stellt Videos von Youtube und Vimeo dar, deswegen sollen Sie Links von diesen Ressourcen hinzufügen. Nachdem Sie die Änderungen vorgenommen haben, drücken Sie auf den Button <strong>'Speichern'</strong> (Save)/<strong>'Speichern und bleiben'</strong> (Save and Stay), um die Änderungen zu speichern.</p>
