<h6>Beispiele der Links</h6>
<p> Um einen Link zum Video richtig hinzuzufügen, muss der Link wie folgt aussehen:</p>
<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Youtube</dt>
            <dd><code>https://www.youtube.com/embed/K6VcTyaym90</code></dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Vimeo</dt>
            <dd><code>http://player.vimeo.com/video/66140585</code></dd>
        </dl>
    </li>
</ul>
<p> Wenn das Video schon Videos, die mit der ausgewählten Sprache verbunden sind, hat, sehen Sie ihre Liste unten.</p>
<p> Um die Liste zu speichern, zu löschen oder zu ändern, sollen Sie nicht auf die Buttons <strong>'Speichern'</strong> (Save)/<strong>'Speichern und bleiben'</strong> (Save and Stay) drücken. Nutzen Sie die Buttons aus den Video-Blöcken. Um die Video-Reihenfolge zu ändern, verschieben Sie sie, wie Sie es wollen. Um ein Video zu aktivieren / zu deaktivieren, drücken Sie auf den Button neben ihm. Um ein Video zu bearbeiten, füllen Sie die Felder mit den notwendigen Daten aus und drücken Sie auf den Button <strong>'Aktualisieren'</strong> (Update).</p>
<p> Um ein Video aus der Liste zu löschen, drücken Sie auf den Button <strong>'Video löschen'</strong> (Remove video).</p>
<p> Bei der Sprachumschaltung wird nur die Liste der Videos dargestellt, die mit dieser Sprache verbunden sind.</p>
<p> Um ein Video in einem erweiterten Format anzusehen, drücken Sie auf den Button <strong>'Zoom'</strong> (Zoom) unter dem Video.</p>
