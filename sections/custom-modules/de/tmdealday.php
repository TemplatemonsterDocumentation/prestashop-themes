<p> Dieses Modul ermöglicht es Ihnen, die Produktliste mit dem Countdown-Timer für Rabatte darzustellen. Sie können auch den Timer zu Produkten in Tabs auf der Startseite, Kategorienseiten und auf einzelnen Produktseiten hinzufügen.</p>
<h5>Wie man das Modul installiert und löscht</h5>
<p> Dieses Modul wird genauso wie ein anderes Prestashop Modul installiert und gelöscht.</p>
<h5>Moduleinstellungen</h5>
<p> Nachdem Sie das Modul installiert haben, können Sie einen Rabatt aufs Produkt mit dem Countdown-Timer hinzufügen.</p>
<figure class="img-polaroid"><img src="img/tmdaydeal-scr-1.png" alt="" /></figure>
<h5> Wie man Blöcke hinzufügt / löscht / bearbeitet </h5>
<ul class="index-list">
    <li>
        <p>Um ein neues Produkt hinzuzufügen, drücken Sie auf den Button 'Neu hinzufügen'.</p>
        <figure class="img-polaroid"><img src="img/tmdaydeal-scr-2.png" alt="" /></figure>
        <ul class="marked-list">
            <li><strong>Produkt wählen</strong> (Select a product) - wählen Sie ein Produkt aus, auf das Sie den Rabatt mit dem Countdown-Timer verweden möchten.</li>
            <li><strong>Status</strong> (Status) – der Status des Elementes.</li>
            <li><strong>Startdatum </strong> (Start Date) - Startdatum, ab wann der Rabatt aufs Produkt gelten wird.</li>
            <li><strong>Enddatum</strong> (End Date) – Startdatum, wann der Rabatt aufs Produkt nicht mehr gültig wird.</li>
            <li><strong>Etikett</strong> (Label) – Produktetikett.</li>
            <li><strong>Aktionspreis</strong> (Discount price) - Aktionspreis (absoluter oder prozentualer Wert).</li>
        </ul>
    </li>
    <li>
        <p>Nach der Speicherung wird das Element in der Liste der Elemente dieses Typs dargestellt.</p>
        <figure class="img-polaroid"><img src="img/tmdaydeal-scr-3.png" alt="" /></figure>
    </li>
    <li>
        <p>Um die Änderungen an diesem Element vorzunehmen, drücken Sie auf den Button <strong>'Bearbeiten'</strong> (Edit).</p>
    </li>
    <li>
        <p>Um dieses Element zu löschen, drücken Sie auf den Button <strong>'Löschen'</strong> (Delete).</p>
    </li>
    <li>
        <p> Wenn für das Produkt ein Sonderpreis vorher festgelegt wurde, der für einen bestimmten Zeitraum gilt, kann dieses Produkt zur Liste der Elemente dieses Moduls hinzugefügt werden.</p>
        <figure class="img-polaroid"><img src="img/tmdaydeal-scr-4.png" alt="" /></figure>
    </li>
</ul>
<h5>Wie man die Produktdarstellung einstellt</h5>
<ul class="index-list">
    <li>
        <p>Um den Modul der Produktdarstellung zu ändern, drücken Sie auf den Button <strong>'Einstellen'</strong> (Configuration).</p>
        <figure class="img-polaroid"><img src="img/tmdaydeal-scr-5.png" alt="" /></figure>
    </li>
    <li>
        <p> Danach werden die Einstellungen zur Produktdarstellung angezeigt.</p>
        <figure class="img-polaroid"><img src="img/tmdaydeal-scr-6.png" alt="" /></figure>
        <ul class="marked-list">
            <li><strong>Dargestellte Produkte</strong> (Products to display) - die Anzahl der Produkte, die im Block auf der Startseite dargestellt werden.</li>
            <li><strong>Zufällige Produkte</strong> (Random products) – die Darstellung der zufälligen Produkte.</li>
        </ul>
    </li>
</ul>

