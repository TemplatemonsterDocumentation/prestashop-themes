<p> Mit diesem Modul können Sie Feeds der sozialen Netzwerke auf der Website hochladen. Dadurch sind Ihre Webseitenbesucher stets auf dem Laufenden rund um die letzten Nachrichten, die Sie in den sozialen Netzwerken veröffentlicht haben.</p>
<h4>Wie man das Modul installiert und löscht</h4>
<p>Dieses Modul wird wie ein anderes PrestaShop Modul installiert und gelöscht .</p>
<h4>Wie man das Modul einstellt</h4>
<p> Nachdem Sie das Modul installiert haben, sehen Sie seine Einstellungen auf der Einstellungsseite.</p>
<p> In dem Formular oben fügen Sie globale Einstellungen zu jedem sozialen Netzwerk hinzu. Verschiedene soziale Netzwerke haben verschiedene Einstellungen.</p>
