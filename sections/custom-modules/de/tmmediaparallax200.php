<p>Dieses Modul wurde erstellt, damit Sie den Parallax Effekt zu Elementen der DOM Seite hinzufügen können. Mit diesem Modul können Sie einen mehrschichtigen Parallax Effekt erstellen. Sie können auch ein benutzerdefiniertes Hintergrund-Video (Sie können es von Ihrem Computer auf den Server hochladen) oder ein Video von YouTube hinzufügen.</p>
