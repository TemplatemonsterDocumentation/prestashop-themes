<h4> Warnungen und Empfehlungen </h4>
<ul class="index-list">
    <li> Für mobile Geräte (Smartphones, Tablets) ist der Parallax Effekt standardmäßig deaktiviert und die obere Ebene des Bildblocks wird als Hintergrund verwendet.</li>
    <li>Erzwungene Breite des Parallax Blocks wirkt nicht mehr auf die Ladegeschwindigkeit.</li>
    <li> Je kleiner das hochgeladene Video ist, desto besser.</li>
</ul>
