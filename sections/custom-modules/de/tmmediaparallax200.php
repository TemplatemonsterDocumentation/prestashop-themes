<h4>Wie man ein neuen Parallax Effekt erstellt</h4>
<p>Um einen neuen Parallax Effekt zu erstellen, drücken Sie auf das Zeichen 'Plus' (1), in der oberen rechten Ecke des Hauptpanels.</p>
<figure class="img-polaroid">
    <img src="img/main-panel-add-item.jpg" alt="" />
</figure>
<p>Sie sehen das folgende Formular.</p>
<figure class="img-polaroid">
    <img src="img/add-item.jpg" alt="" />
</figure>
<h4>Formulareinstellungen für die Hinzufügung des Parallax Effektes</h4>
<ul class="index-list">
    <li>Selektor – der Selektor des CSS Elementes, auf das das Parallax Effekt verwendet ist. Zum Beispiel, '#header .nav'</li>
    <li>Status des Elementes – der Status des Elementes (Parallax Effekt wird nicht dargestellt, wenn er deaktiviert ist)</li>
    <li>Geschwindigkeit – der Digitalwert von 0 bis 2, der die Geschwindigkeit des Hauptblocks Prallax definiert </li>
    <li>Einzug – der Einzug der Basisschicht auf der y-Achse </li>
    <li>Zurück – die Parallax Richtung</li>
    <li>Verfärbung - diese Option ermöglicht es Ihnen, die Verfärbung des Hauptblocks beim Scrollen der Seite einzustellen </li>
    <li> Erzwungene volle Breite – erzwungene volle Breite des Parallax Blocks </li>
</ul>
<p>Drücken Sie auf den Button 'Speichern', nachdem Sie alle notwendigen Felder ausgefüllt haben.</p>
