<h5> Wie man Elementeinstellungen hinzufügt/ändert </h5>
<div class="alert alert-info">Achtung: Kategorienname, Beschreibung und Bild werden in den Kategorieeigenschaften hochgeladen und es gibt keine Notwendigkeit sie manuell hinzuzufügen. Um diese Optionen zu ändern, öffnen Sie Admin Panel -> Katalog -> Kategorien -> Kategorie wählen -> Ändern.</div>
<p> Nachdem Sie ein neues Element zum Modul (oder ein Element aus der Liste geändert haben) hinzugefügt haben, werden einige Optionen verfügbar. Eine ausführliche Beschreibung der Optionen finden Sie unter dem Bildschirmfoto. Nachdem Sie alle Werte festgelegt haben, drücken Sie auf den Button «Speichern» (1).</p>
<figure class="img-polaroid">
    <img src="img/tmhomepagecategorygallery-4.png" alt=""/>
</figure>
<ul class="marked-list">
	<li><strong>Kategorie auswählen</strong> (Select category) -  hier können Sie eine Kategorie aus der DropDown Liste auswählen, die auf Ihrer Seite angezeigt wird.</li>
	<li><strong>Spezifische Klasse</strong> (Specific class) - zum Element des Moduls kann eine spezifische Klasse hinzugefügt werden.</li>
	<li><strong>Namen darstellen</strong> (Display name) – der Kategoriename kann dargestellt oder nicht dargestellt werden.</li>
	<li><strong>Länge des Kategorienamens</strong> (Category name length) – die maximale Anzahl der  Zeichen, die in dem Kategorienamen dargestellt werden. Wenn der Name länger ist, wird das Zeichen '..' nach der festgelegten Anzahl der Zeichen dargestellt.</li>
	<li><strong>Beschreibung darstellen</strong> (Display description) – diese Option ermöglicht es Ihnen, die Kategoriebeschreibung entweder darzustellen oder nicht darzustellen.</li>
	<li><strong>Beschreibungslänge</strong> (Description length) – die maximale Anzahl der Zeichen zur Darstellung in der Beschreibung.  Wenn die Beschreibung länger ist, wird das Zeichen '..' nach der festgelegten Anzahl der Zeichen dargestellt.</li>
	<li><strong>Anzeigereihenfolge</strong> (Sort order) - die Anzeigereihenfolge der Elemente in der Galerie der Kategorie.</li>
	<li><strong>Button darstellen</strong> (Display button) – den Button 'Jetzt kaufen' (der Text auf dem Button kann sich abhängig von der Vorlage unterscheiden) darstellen oder nicht darstellen.</li>
	<li><strong>Status</strong> (Status) - die Kategorie darstellen oder nicht darstellen.</li>
	<li><strong>HTML Inhalt</strong> (HTML content) - ein Feld, zu dem der HTML Inhalt hinzugefügt werden kann.</li>
</ul>
