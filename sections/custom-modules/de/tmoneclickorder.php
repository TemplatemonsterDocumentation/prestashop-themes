<p> Dieses Modul wird benutzt, um den Bestellprozess zu beschleunigen und zu vereinfachen.<br>
    Mit diesem Modul der Bestellprozess mit einem Schritt und mühelos durchgeführt. Dadurch können Ihre Kunden viel Zeit sparen.<br>
Mit Hilfe von diesem Modul können Sie die Anzahl der Spontankäufe erhöhen und dadurch den Umsatz steigern.</p>
<h4>Installation</h4>
<p>Finden Sie den Tab Module und Einstellungen -> Module und Einstellungen (1) in Ihrem Control Panel und finden Sie das Modul TM One Click Order (2). Danach drücken Sie auf den Button 'Installieren' (3).</p>
<figure class="img-polaroid">
    <img src="img/tmoneclick-1.png" alt=""/>
</figure>
<h4>Wie man das Modul einstellt</h4>
<p>Nachdem Sie das Modul erfolgreich installiert haben, sehen Sie die Seite 'Einstellen' mit den Moduleinstellungen. <br>
   Sie sehen auch das neue Menü-Element 'Schnelle Bestellungen' (1) und in dem Navigationspanel wird ein neues Zeichen angezeigt(2).
</p>
<figure class="img-polaroid">
    <img src="img/tmoneclick-2.png" alt=""/>
</figure>
<p>Einstellungen sind in drei Tabs geteilt – Vorlage der Vorbestellung (1), Benachrichtigung über erfolgreiche Vorbestellung (2) und Einstellungen (3).</p>
<figure class="img-polaroid">
    <img src="img/tmoneclick-3.png" alt=""/>
</figure>
<h5>Vorlage der Vorbestellung</h5>
<ul class="index-list-type-2">
    <li><p> Hier können Sie ein neues Feld hinzufügen, indem Sie auf den entsprechenden Button drücken (1):</p>
        <figure class="img-polaroid">
            <img src="img/tmoneclick-4.png" alt=""/>
        </figure>
        <p> Wählen Sie Optionen für das neue Feld aus und drücken Sie auf den Button 'Speichern' (1):</p>
