    </li>
    <li>
        <p>Wie man ein Feld aus dem Formular löscht.<br>
       Drücken Sie auf den Button 'Löschen' (1) in der oberen rechten Ecke des Feldes.</p>
        <figure class="img-polaroid">
            <img src="img/tmoneclick-7.png" alt=""/>
