        </figure>
        <ul class="marked-list">
            <li><strong>Typ</strong> (Type) – wählen Sie den Typ für das Feld aus.</li>
            <li><strong>Spezifische Klasse</strong> (Specific class) – fügen Sie eine benutzerdefinierte Klasse für das aktuelle Feld hinzu.</li>
            <li><strong>Name</strong> (Name) – der Name des Feldes.</li>
            <li><strong>Beschreibung</strong> (Description) – die Beschreibung des Feldes; sie wird angezeigt, wenn Sie Informationen eingegeben haben.</li>
        </ul>
    </li>
    <li>
        <p>Ändern Sie das aktuelle Feld.</p>

        <figure class="img-polaroid">
