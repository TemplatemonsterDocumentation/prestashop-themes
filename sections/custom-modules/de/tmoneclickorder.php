    </li>
</ul>
<h5>Benachrichtigung über erfolgreiche Vorbestellung</h5>
<p> Dieser Tab ermöglicht es Ihnen, die Benachrichtigung über erfolgreiche Vorbestellung zu bearbeiten. Diese Benachrichtigung wird angezeigt, nachdem der Nutzer auf den Button 'Senden' in dem Formular Schnelle Bestellung gedrückt hat.</p>
<p>Fügen Sie einen Text/Button hinzu, die Sie darstellen möchten und drücken Sie auf den Button 'Speichern' (1).</p>
<figure class="img-polaroid">
    <img src="img/tmoneclick-8.png" alt=""/>
</figure>
<h5>Wie man das Modul einstellt</h5>
<p>Dieser Tab ermöglicht es Ihnen, die Eistellung 'Ajax Bestellung' zu aktivieren/zu deaktivieren .</p>
<p>Diese Einstellung hilft dem Webseitenadministrator, neue schnelle Bestellungen (während aktuelle Bestellungen bearbeitet werden) zu sehen.</p>
<figure class="img-polaroid">
    <img src="img/tmoneclick-9.png" alt=""/>
</figure>
<p>Wenn diese Einstellung aktiviert ist, sehen Sie eine zusätzliche Einstellung 'Timeout der Ajax Bestellung'. <br>
    Der Wert wird in Millisekunden festgelegt. Es ist die Zeit zwischen jeder Überprüfung einer neuen schnellen Bestellung.
</p>
<figure class="img-polaroid">
    <img src="img/tmoneclick-10.png" alt=""/>
</figure>
<p> Diese Option ermöglicht es Ihnen, neue Vorbestellungen zu öffnen, ohne die Seite neu zu laden. Um sie in der Liste schneller Bestellungen anzusehen, drücken Sie auf den Link 'Neue Bestellungen hochladen' (1) im Menü Bestellungen -> Schnelle Bestellungen.</p>
<figure class="img-polaroid">
    <img src="img/tmoneclick-11.png" alt=""/>
</figure>
<h4>Wie man eine Bestellung bearbeitet</h4>
<p>Finden Sie den Tab Bestellungen -> Schnelle Bestellungen (1) in Ihrem Control Panel.</p>
<figure class="img-polaroid">
    <img src="img/tmoneclick-12.png" alt=""/>
</figure>
<p>Hier gibt es vier Tabs - Neu, Erstellt, Gelöscht und Suche.</p>
<ul class="marked-list">
    <li>
        <p><strong>Neu</strong> (New) – neue Vorbestellungen Ihrer Kunden.</p>
        <p>Links gibt es einen Button für die Erstellung einer neuen Vorbestellung (1) und die Liste der Vorbestellungen (2).</p>
        <p>Rechts sehen Sie Details der Vorbestellung (3), die in Tabs (Information über die Vorbestellung, Kunden, Sprachen und Währungen, Warenkorb, Gutscheine, Adressen, Lieferung, Kurzangaben) geteilt sind.</p>
        <figure class="img-polaroid">
            <img src="img/tmoneclick-13.png" alt=""/>
        </figure>
        <p> Wenn eine Bestellung gerade gemacht wurde, sehen Sie nur drei Tabs : Information über die Vorbestellung, Kunden, Sprachen und Währungen.</p>
        <figure class="img-polaroid">
            <img src="img/tmoneclick-14.png" alt=""/>
        </figure>
        <p> Nachdem Sie Informationen über Ihren Kunden ausgefüllt haben, werden noch drei Tabs angezeigt - Warenkorb, Gutscheine, Adressen. Nachdem die Andresse hinzugefügt wird, werden die Tabs Lieferung, Kurzangaben angezeigt. Die Tabs, die mit notwendigen Informationen erfolgreich ausgefüllt sind, werden grün markiert. Für gelbe Tabs sind zusätzliche Informationen über die Bestellung erforderlich. Blaue Tabs sind Informations-Tabs, sie werden automatisch ausgefüllt.<br>
Nachdem der Tab Kurzangaben angezeigt ist, wird der Button 'Bestellung erstellen' im Tab Kurzangaben angezeigt(1).</p>
        <figure class="img-polaroid">
            <img src="img/tmoneclick-15.png" alt=""/>
        </figure>
        <p>Indem Sie auf diesen Button drücken, verschieben Sie die Vorbestellung aus der Liste 'Neu' und die Liste der Vorbestellungen 'Erstellt' (1).</p>
        <figure class="img-polaroid">
            <img src="img/tmoneclick-16.png" alt=""/>
        </figure>
        <p> Wenn Sie eine Bestellung löschen möchten, sehen Sie in der oberen rechten Ecke das Warenkorb-Zeichen (1).</p>
        <p>Drücken Sie auf es und die Vorbestellung wird in den Tab 'Gelöscht' verschoben.</p>
    </li>
    <li>
        <p><strong>Erstellt</strong> (Created) – Vorbestellungen, die von dem Webseitenadministrator bearbeitet wurden.</p>
        <p> Wählen Sie eine Bestellung auf der linken Seite und drücken Sie auf sie, um ausführliche Informationen in der rechten Seite zu sehen.</p>
        <p>Sie können auch auf den Butoon 'Anzeigen' drücken, um ausführliche Informationen über den Kunden (1), Bestellung (2) und Bearbeitung zu sehen (3).</p>
        <figure class="img-polaroid">
            <img src="img/tmoneclick-17.png" alt=""/>
        </figure>
    </li>
    <li>
        <p><strong>Gelöscht</strong> (Removed) – Vorbestellungen, die storniert wurden.</p>
        <p>Die Vorbestellung in diesem Tab kann wiederhergestellt werden, indem man auf den Button '+' (1) drückt. Danach wird sie in den Tab 'Neu' verschoben.</p>
        <figure class="img-polaroid">
            <img src="img/tmoneclick-18.png" alt=""/>
        </figure>
    </li>

    <li>
        <p><strong>Suche</strong> (Search) – die Suche der Vorbestellung. Suchen Sie nach (Kundeninformationen, Mitarbeiterinformation, Bestellungs-ID, Vorbestellungs-ID); Sie können auch ein bestimmtes Datum/Zeitraum eingeben, wann die Vorbestellung erstellt wurde.</p>
        <figure class="img-polaroid">
            <img src="img/tmoneclick-19.png" alt=""/>
        </figure>
    </li>
</ul>
