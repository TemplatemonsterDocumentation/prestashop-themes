<p>Este módulo carga los feeds de las redes sociales en su sitio web. Permite a los visitantes del sitio conocer las últimas
     noticias y publicaciones compartidas en las redes sociales.</p>
<h4>Instalación y eliminación</h4>
<p>El módulo se instala y se elimina como cualquier otro módulo de Prestashop.</p>
<h4>Configuración del módulo</h4>
<p>Después de la instalación, puede cambiar su configuración en la página de configuración del módulo.</p>
<p>En la parte superior, debe agregar ajustes globales para cada red social. Diferentes redes sociales tienen diferentes
     ajustes.</p>
