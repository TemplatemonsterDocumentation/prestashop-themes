<p class="alert alert-warning">Para utilizar el módulo, debe tener instalado el módulo TM Look Book.</p>
<p>La página de inicio de TM Look Book se utiliza para mostrar el bloque de módulo TM Look Book en la página principal. Este módulo permite mostrar tanto las portadas de lookbook como las páginas de lookbook.</p>
<h5>Requisitos:</h5>
<ul class="marked-list">
    <li>PHP 5.4+</li>
    <li>PrestaShop 1.6</li>
    <li>Módulo TM Look Book</li>
</ul>
<h5>Funciones del módulo:</h5>
<ul class="marked-list">
    <li>El módulo se puede visualizar en 2 posiciones: displayHome y displayTopColumn.</li>
    <li>TM Look Book Homepage es un módulo hijo de TM Look Book, por eso no se puede usarlo sin él.</li>
    <li>El módulo puede mostrar la portada del lookbook o sus páginas.</li>
</ul>
<h5>Panel principal de TM Look Book</h5>
<p>En el panel principal del módulo, puede agregar, eliminar y editar bloques, que el módulo mostrará en la página principal. </p>
<figure class="img-polaroid">
    <img src="img/tmlookbookhomepage-main-panel.png" alt=""/>
</figure>
<h5>Añadir un bloque nuevo</h5>
<ul class="index-list">
    <li>
        <p>Para agregar un nuevo bloque, ingrese al panel principal del módulo y haga clic en el '+' (1) de la tabla con el gancho necesario.</p>
        <figure class="img-polaroid">
            <img src="img/tmlookbookhomepage-add-block-1.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>En el formulario aparecido, configure las siguientes opciones:</p>
        <ul class="marked-list">
            <li><strong>Estado</strong> - el estado del bloque. Este ajuste permite activar o desactivar la visualización del bloque.</li>
            <li><strong>Tipo</strong> - permite seleccionar el tipo de bloque.</li>
            <li><strong>Lookbook</strong> - permite seleccionar un aspecto previamente seleccionado.</li>
        </ul>
    </li>
    <li>
        <p>Después de configurar todas las opciones, presione el botón 'Guardar' (2).</p>
        <figure class="img-polaroid">
            <img src="img/tmlookbookhomepage-add-block-2.png" alt=""/>
        </figure>
    </li>
</ul>
<p>Después de crear el nuevo bloque, se mostrará en la página principal.</p>