<h3>TM Google Map</h3>
<p>Este módulo permite agregar el mapa de Google con las ubicaciones de sus tiendas y su breve descripción en su página principal. El mapa mostrará las direcciones de todas las tiendas relacionadas con su tienda en línea. 
    Puede editar los detalles de sus tiendas en la pestaña Preferencias -> Contactos de la Tienda.</p>
<h5>Instalación y Eliminación</h5>
<p>El módulo se instala y elimina como cualquier otro módulo de Prestashop.</p>
<h5>Configuración del módulo</h5>
<p>Después de la instalación puede cambiar su configuración en la página de configuración del módulo.</p>
<p>Puede configurar el estilo y los elementos de su mapa.</p>
<ul class="index-list">
    <li>Seleccione el estilo de mapa en el primer campo (todos los estilos se toman de <a
        target="_blank" href="https://snazzymaps.com/">Snazzymaps</a>). También puede agregar 
		el estilo de este sitio web. Encontrarás cómo agregarlos al final de esta
		instrucción.</li>
    <li>Seleccione el tipo de vista en el segundo campo (vista de satélite o vista de calle).</li>
    <li>También puede seleccionar la proporción de zoom de su mapa de 1 a 17.</li>
    <li>Opción de zoom mientras se desplaza en el área del mapa.</li>
    <li>Puede desactivar el control del mapa y la capacidad de ver las calles.</li>
    <li>El módulo soporta múltiples tiendas. Puede cambiar el estilo de mapa para 
	   cada tienda.</li>
</ul>
