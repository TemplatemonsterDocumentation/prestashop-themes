<h5>Adición de estilos personalizados</h5>
<p>Puede agregar cualquier estilo personalizado de <a target="_blank" href="https://snazzymaps.com/">Snazzymaps</a>
    A su módulo. Para ello, debe seleccionar el estilo del sitio web, copiar
 JavaScript Style Array al archivo .js y guárdelo en la carpeta del módulo /js/styles/ (para
 el nombre de archivo utiliza el nombre de estilo reemplazando los espacios con guiones bajos y quitando
 todos los caracteres especiales. Por ejemplo: para el estilo Subtle Grayscale -
 archivo subtle_grayscale.js, para el estilo Apple Maps-esque - archivo apple_maps-esque.js).
 A continuación, vaya a la página de configuración del módulo y seleccione el estilo deseado de
 la lista desplegable de estilos.</p>