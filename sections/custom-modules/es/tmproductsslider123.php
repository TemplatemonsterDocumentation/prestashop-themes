<h3>TM Products Slider 1.2.3</h3>
<p>El módulo muestra los productos del almacén en el slider.</p>
<h4>Instalación y eliminación</h4>
<p>El módulo se instala y elimina como cualquier otro módulo de Prestashop.</p>
<p>Después de la instalación, puede cambiar su configuración en la página de configuración del módulo.</p>
<p>Puede establecer el orden de los productos en el control deslizante mediante el método de arrastrar y soltar. También puede 
    activar/desactivar la visualización del producto en el slider.</p>
<p>Puede configurar el ancho del slider, el efecto deslizante, la velocidad del slider, la pausa, la reproducción automática,
    la pausa al voltear sobre los elementos del carrusel, el control del deslizador, los botones siguientes/anteriores, 
	los botones de reproducción/pausa.</p>
<figure class="img-polaroid">
    <img src="img/tmproductsslider-1.png" alt="">
</figure>
<p>Después de la instalación del módulo, verá la nueva pestaña en la página de configuración del producto (Catálogo -> Producto -> seleccione cualquier producto -> TM Products Slider).</p>
<p>Aquí puede establecer si el producto se mostrará en el control deslizante o no. Después de realizar 
    los cambios, haga clic en Guardar/Guardar y quedarse para guardarlos.</p>
<p>El módulo soporta múltiples almacenes. Puede cambiar la configuración de cada tienda, grupo de
    tiendas o todas las tiendas a la vez.</p>
