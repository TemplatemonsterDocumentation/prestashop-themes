<p>Este módulo muestra los productos de la categoría en los ganchos displayHome y displayHomeTabContent. El módulo permite mostrar productos
     únicos, productos de una categoría o todos los productos. Además, el módulo viene con un carrusel que funciona, 
	 cuando el módulo ocupa la posición displayHome.</p>
<h5>Panel Principal de TM Category Products</h5>
<p>El panel principal. Aquí puede configurar el módulo. </p>
<figure class="img-polaroid">
    <img src="img/tmcategoryproducts-1.png" alt=""/>
</figure>
<h5>Agregar una Pestaña</h5>
<p>Pestaña es un bloque que muestra los productos en la posición displayHomeTabContent.</p>
<ul class="index-list">
    <li><p>Navegue hasta el panel principal del módulo y presione el botón 'más' (1) en la lista de pestañas.</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts-2.png" alt=""/>
        </figure>
    </li>
    <li><p>En el formulario que aparece, verá las siguientes opciones:</p>
        <ul class="marked-list">
            <li>Estado: esta opción permite activar / desactivar la pestaña.</li>
            <li>Seleccione categoría: la categoría, productos de los que
    			  se mostrarán.
            </li>
            <li>Usar nombre personalizado: esta opción permite cambiar el nombre de la categoría que se muestra. Si está activado,
                  se mostrará el siguiente formulario:
                <ul class="marked-list">
                    <li>Nombre - el nombre de la pestaña personalizada, que se muestra en la página web.</li>
                </ul>
            </li>
            <li>Seleccionar los productos a mostrar - si está deshabilitado, muestra el campo 
			     "Número de productos a mostrar", que establece el número de productos que se mostrarán. Si está activado, muestra el botón "Agregar productos" 
			      que permite seleccionar los productos que se mostrarán en la pestaña.
            </li>
        </ul>
    </li>
    <li><p>Después de configurar todas las opciones necesarias, pulse el botón 'Guardar' (2).</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts-3.png" alt=""/>
        </figure>
    </li>
</ul>
<h5>Añadir un bloque</h5>
<p>Este bloque permite seleccionar productos en la posición displayHome.</p>
<ul class="index-list">
    <li><p>Acceda al panel principal del módulo y presione el botón 'más' (1) en la lista de bloques.</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts-4.png" alt=""/>
        </figure>
    </li>
    <li><p>En el formulario que aparece, encontrará las siguientes opciones:</p>
        <ul class="marked-list">
            <li><p>Estado: esta opción permite activar/desactivar el bloque.</p></li>
            <li><p>Seleccione categoría: la categoría, productos de los que se mostrarán.</p></li>
            <li>
                <p>Usar nombre personalizado: esta opción permite cambiar el nombre de la categoría que se muestra. Si está activado, muestra el 
				   siguiente formulario:</p>
                <ul class="marked-list">
                    <li><p>Nombre: el nombre de la pestaña personalizada, que se muestra en la página web.</p></li>
                </ul>
            </li>
            <li>
                <p>Seleccionar productos para mostrar - si está desactivado, muestra el campo 'Número de productos a mostrar', 
				   que establece el número
				   de productos a mostrar. Si está desactivado, muestra el botón "Añadir productos" que permite seleccionar los productos que se mostrarán en la pestaña.</p>
            </li>
            <li>
                <p>Usar carrusel - esta opción permite activar / desactivar el carrusel para los productos de bloque. Después de
                     activar la opción,
                     verá las siguientes configuraciones:</p>
                <ul class="marked-list">
                    <li><p>Elementos visibles: el número de elementos visibles.</p></li>
                    <li><p>Desplazar elementos - el número de elementos cambiados con un rollo de carrusel.</p></li>
                    <li><p>Ancho de la Diapositiva: el ancho del elemento de la lista del carrusel.</p></li>
                    <li><p>Margen de Diapositiva: el margen del elemento de la lista de carrusel.</p></li>
                    <li><p>Desplazamiento Automático: desplazamiento automático de elementos del carrusel.</p></li>
                    <li><p>Velocidad del carrusel - velocidad de cambio de elementos.</p></li>
                    <li><p>Pausa - pausa entre desplazamientos (si "Desplazamiento automático" está activado).</p></li>
                    <li><p>Aleatorio - el carrusel comienza con un elemento aleatorio.</p></li>
                    <li><p>Bucle de carrusel - el bucle: cuando todos los elementos 
					       del carrusel 
					       se han mostrado, el carrusel comienza desde el principio.</p></li>
                    <li><p>Ocultar control en los botones de control de fin - ocultar (Siguiente/Anterior).</p></li>
                    <li><p>Paginador - mostrar paginación.</p></li>
                    <li><p>Control - mostrar controles (Siguiente/Anterior).</p></li>
                    <li><p>Controles Automáticos - mostrar controles (Reproducir/Pausa).</p></li>
                    <li><p>Auto hover - carrusel se detiene, si el usuario coloca un elemento de lista.</p></li>
                </ul>
            </li>
        </ul>
    </li>
    <li>
        <p>Después de configurar todas las opciones necesarias, pulse el botón 'Guardar' (2).</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts-5.png" alt=""/>
        </figure>
    </li>
</ul>
<h5>Clasificación de Bloques/Pestañas</h5>
<p>Puede ordenar los bloques del módulo con la ayuda de arrastrar y soltar. Esto permite cambiar posiciones de bloques y pestañas
     rápidamente.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryproducts-6.png" alt=""/>
</figure>
<h5>Selección de Productos</h5>
<p>Si crea un elemento y habilita la opción "Seleccionar productos para mostrar", puede elegir los productos que desea
     mostrar. Para hacer esto:</p>
<ul class="index-list">
    <li><p>Pulse el botón 'Añadir productos' (1):</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts-7.png" alt=""/>
        </figure>
    </li>
    <li><p>En la ventana emergente aparece, seleccione los productos necesarios (2) y pulse el botón 'Añadir' (3):</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts-8.png" alt=""/>
        </figure>
    </li>
    <li><p>Ahora, puede colocar los productos agregados donde quiera con la ayuda de arrastrar y soltar (4):</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts-9.png" alt=""/>
        </figure>
    </li>
    <li><p>Cuando todos los productos necesarios sean agregados y dispuestos en el orden deseado, presione el botón 'Guardar'.</p>
    </li>
</ul>
<h5>Eliminación de Bloques/Pestañas</h5>
<p>Para eliminar elementos, pulse el menú desplegable situado junto al bloque/ficha (1) que desee y pulse borrar (2). El elemento 
     se eliminará.</p>
