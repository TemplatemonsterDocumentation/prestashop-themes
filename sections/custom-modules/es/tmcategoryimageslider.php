<h3>TM Category Image Slider</h3>
<p>Módulo permite crear diapositivas multilingües para cualquier categoría de la tienda. El control deslizante se mostrará en las
    páginas de categoría.</p>
<h4>IInstalación y Extracción</h4>
<p>El procedimiento de instalación es estándar, el módulo se puede instalar y eliminar como un módulo PrestaShop regular.</p>
<h4>Configuración del módulo</h4>
<p>Después de la instalación del módulo, puede configurar o cambiar las opciones necesarias en la página de configuración del módulo.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryimagesslider-1.png" alt=""/>
</figure>
<ul class="marked-list">
    <li><strong>Modo</strong> - tipo de animación (desvanecimiento, vertical, horizontal).</li>
    <li><strong>Velocidad</strong> - velocidad de animación.</li>
    <li><strong>Anchura</strong> - ancho de diapositivas (para la vista correcta, se recomienda usar el ancho de imagen real).
    </li>
    <li><strong>Altura</strong> - altura de los diapositivas (para la vista correcta, se recomienda usar la altura real de la imagen).
    </li>
    <li><strong>Auto</strong> - reproducción automática cuando se abre la página.</li>
    <li><strong>Pausa</strong> - tiempo antes de la transición a la siguiente diapositiva.</li>
    <li><strong>Auto Hover</strong> - pausa en pasr el ratón.</li>
    <li><strong>Controles</strong> - Botones "Siguiente"/"Anterior".</li>
    <li><strong>Texto Siguiente</strong> - "Siguiente" texto del botón.</li>
    <li><strong>Texto Anterior</strong> -"Anterior" botón de texto.</li>
    <li><strong>Controles Automáticos</strong> - "Reproducción"/"Pausa" botones.</li>
    <li><strong>Paginador</strong> - paginación.</li>
</ul>
<h5>Adición/eliminación/edición de diapositivas</h5>
<p>Después de la instalación del módulo, verá el nuevo campo 'Diapositivas' en la configuración de categoría.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryimagesslider-2.png" alt=""/>
</figure>
<p>Para añadir una diapositiva, haga clic en el botón "Añadir nueva diapositiva". Rellene los campos necesarios en el formulario.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryimagesslider-3.png" alt=""/>
</figure>
<ul class="marked-list">
    <li><strong>Seleccione un archivo</strong> - Seleccione su archivo. Puede subir diferentes imágenes para cada idioma. En caso 
	de que no haya ninguna imagen seleccionada para el idioma específico, las imágenes configuradas para el idioma predeterminado se cargarán
    </li>
    <li><strong>Introduzca el Título de la página</strong> - escriba el título de la diapositiva.</li>
    <li><strong>Ingresar la URL de la diapositiva</strong> - proporcione la URL de la diapositiva.</li>
    <li><strong>Introducir Subtítulos de Diapositivas</strong> - escriba la leyenda de diapositivas.</li>
    <li><strong>Abrir en nueva ventana</strong> - abrir en Nueva ventana.</li>
    <li><strong>Estado</strong> - activar/desactivar la diapositiva.</li>
</ul>
<p>Cuando se guarda la diapositiva, aparecerá en una lista de diapositivas de categorías.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryimagesslider-4.png" alt=""/>
</figure>
<p>Para realizar cambios en la diapositiva, haga clic en el botón <strong>'Editar'</strong>.</p>
<p>Para eliminar la diapositiva, haga clic en el botón <strong>'Eliminar'</strong> bajo el botón <strong>'Editar'</strong>.</p>
<div class="alert alert-info">
    <span>Sólo se pueden agregar diapositivas a las categorías guardadas.</span>
</div>