<h6>Ejemplos de enlaces</h6>
<p>Para mostrar vídeo correctamente, los enlaces deben tener el siguiente formato:</p>
<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Youtube</dt>
            <dd><code>https://www.youtube.com/embed/K6VcTyaym90</code></dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Vimeo</dt>
            <dd><code>http://player.vimeo.com/video/66140585</code></dd>
        </dl>
    </li>
</ul>
<p>Si el producto tiene un vídeo relacionado con este idioma, verá la lista de videos de abajo.</p>
<p>Para guardar, eliminar o editar la lista, no es necesario hacer clic en botón <strong>'Guardar'</strong>/<strong>'Guardar y Quedarse'</strong>. Utilice los botones situados
     en los bloques de vídeo. Para cambiar el orden de los videos, utilice el método de arrastrar y soltar para cambiar su posición. Para activar/desactivar
     el vídeo, utilice el botón situado junto al vídeo. Para editar vídeos, rellene los campos con los datos necesarios y haga clic en el boton <strong>'Actualizar'</strong>.</p>
<p>Para eliminar el video de la lista, haga clic en el botón <strong>'Eliminar vídeo'</strong> .</p>
<p>Mientras cambia de idioma, sólo aparecerá la lista de videos relacionados con el idioma.</p>
<p>Para ver el video en formato ampliado, haga clic en el botón <strong>'Zoom'</strong> bajo este video.</p>
