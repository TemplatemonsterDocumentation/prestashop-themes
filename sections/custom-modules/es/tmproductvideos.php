<p>Este módulo permite añadir un número ilimitado de videos a cualquier producto en cualquier idioma y tienda.</p>
<h5>Instalación y eliminación</h5>
<p>El módulo se instala y suprime como cualquier otro módulo de Prestashop. No tiene página de configuración y soporta
     tiendas múltiples.</p>
<p>Después de la instalación del módulo, verá la nueva pestaña en la página de configuración del producto (Catálogo -> Producto -> seleccione cualquier
     producto - TM Product Videos).</p>
<p>Para añadir un video a cualquier producto, debe rellenar los campos requeridos (el campo de enlace es obligatorio). El módulo es
     multilingüe, por lo que tendrá que rellenar los campos para todos los idiomas a los que este video está relacionado. Si el campo Enlace
     no está lleno para ningún idioma, entonces el video no se agregará a este idioma. Se supone que el módulo mostrará videos de Youtube y Vimeo, por lo que necesita agregar enlaces sólo de estos recursos. Después de hacer los cambios,
     hacer clic en el botón <strong>'Guardar'</strong>/<strong>'Guardar y Quedarse'</strong> para guardarlos.</p>
