<h3>TM Product Custom Tab</h3>
<p>El módulo sirve para crear una pestaña adicional en la página del producto para mostrar información adicional.
     El módulo tiene una ventaja sobre la funcionalidad existente, debido a la capacidad de agregar imágenes, texto, video, enlaces, etc.</p>
<h4>Instalación y extracción</h4>
<p>El módulo se instala y se retira como cualquier otro módulo de PrestaShop.</p>
<h4>Configuración del módulo</h4>
<p>Después de instalar el módulo, puede agregar nuevas pestañas para los productos en la página de configuración del módulo, así como en la página de edición del producto.</p>
<h5>Agregar pestañas a través de la configuración del módulo:</h5>
<p>Para agregar una nueva pestaña, presione el botón 'Añadir nuevo' (1).</p>
<figure class="img-polaroid">
    <img src="img/tmcustomproducttab-1.png" alt=""/>
</figure>
<ul class="index-list">
    <li>
        <p>Agregar una pestaña a uno o varios productos de una categoría previamente seleccionada.</p>
        <figure class="img-polaroid">
            <img src="img/tmcustomproducttab-4.png" alt=""/>
        </figure>
        <ul class="marked-list">
            <li><strong>Pestaña del Encabezado</strong> - nombre de la pestaña, requeridod.</li>
            <li><strong>Descripción de la pestaña</strong> - información sobre la ficha, puede incluir datos en el formato de una imagen, texto, vídeo, enlaces, etc.</li>
            <li><strong>Estado</strong> - el estado del elemento.</li>
            <li><strong>Ajustes adicionales</strong> - activar/desactivar configuraciones adicionales.</li>
            <li><strong>Seleccione una categoría</strong> - elegir las categorías para las que se debe mostrar la pestaña.</li>
            <li><strong>Productos a mostrar</strong> - seleccionar los productos de las categorías previamente seleccionadas.</li>
        </ul>
    </li>
    <li>
        <p>Agregar la pestaña a los productos que pertenecen a categorías seleccionadas.</p>
        <figure class="img-polaroid">
            <img src="img/tmcustomproducttab-3.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Para agregar una pestaña a todos los productos, desactive la opción "Configuración adicional".</p>
        <figure class="img-polaroid">
            <img src="img/tmcustomproducttab-2.png" alt=""/>
        </figure>
    </li>
</ul>
<h5>Cambio de la posición de visualización de la ficha</h5>
<p>Arrastre y suelte la pestaña necesaria para cambiar su posición.</p>
<figure class="img-polaroid">
    <img src="img/tmcustomproducttab-5.png" alt=""/>
</figure>
<h5>Agregar pestañas a través de la configuración del producto:</h5>
<p>Vaya a la página del producto y haga clic en el vínculo 'TM Product Custom Tab' en el menú de la izquierda.</p>
<figure class="img-polaroid">
    <img src="img/tmcustomproducttab-6.png" alt=""/>
</figure>
<p>Si la opción 'Ajustes adicionales' está deshabilitada, la pestaña se agregará al producto que se editó anteriormente. También puede agregar la pestaña a los productos que pertenecen a categorías seleccionadas o puede agregarla a varios productos pertenecientes a la categoría previamente seleccionada.</p>
<p>Para cambiar la posición de visualización de las pestañas, arrastre y suelte los bloques. También puede editar o eliminar pestañas.</p>
