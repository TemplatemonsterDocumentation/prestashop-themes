<h5>Adición de módulo de paralaje de medios</h5>
<ul class="index-list">
    <li>
        <p>Es necesario agregar el ID o la clase en el contenido de su página HTML. También puede utilizar una etiqueta única, 
		   este elemento no debe repetirse.</p>
    </li>

    <li>
        <p>Crear la imagen del tamaño requerido (es deseable crear la imagen de mayor tamaño que el bloque donde
 		   se agregará el efecto). En caso de que quiera agregar el video, tendrás que prepararlo en 3 formatos:
             .mp4, .webm y .ogv, y también para agregar la imagen de vista previa del mismo tamaño (.png, .jpg y .gif)</p>
    </li>
    <li>
        <p>Haga click en el botón <strong>'Añadir contenedor'</strong>en el <strong>'Añadir nuevo bloque'</strong>.</p>
        <figure class="img-polaroid">
            <img src="img/parallax-1.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>En la pantalla que aparece, agregue todos los ajustes necesarios.</p>
        <h6>
            <strong>Ajustes:</strong>
        </h6>
        <ul class="index-list">
            <li>
                <dl class="inline-term">
                    <dt>Selector</dt>
                    <dd>elemento donde se agregará el efecto (id o etiqueta única).</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Tipo de Paralaje</dt>
                    <dd>tipo de efecto que se utilizará (imagen o video).</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Archivos multimedia</dt>
                    <dd>cargar todos los archivos necesarios.</dd>
                </dl>
            </li>
            <li>
                <p>Hacer clic en el botón <strong>'Guardar'</strong> .</p>
            </li>
        </ul>
        <figure class="img-polaroid">
            <img src="img/parallax-2.png" alt=""/>
        </figure>
    </li>
</ul>
