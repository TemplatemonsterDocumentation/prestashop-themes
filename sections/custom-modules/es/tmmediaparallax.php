<h3>Media Parallax Module</h3>
<h4>¿Qué es el efecto de paralaje?</h4>
<p>Parallax scrolling es una técnica de desplazamiento especial en gráficos por ordenador, en la que las imágenes de fondo se mueven por la 
   cámara más lentamente que las imágenes en primer plano, creando una ilusión de profundidad en un videojuego 2D y añadiéndose a la inmersión. 
   Los diseñadores Web comenzaron a incorporar el desplazamiento de paralaje en 2011, utilizando HTML5 y CSS3. Los sitios con antecedentes de paralaje
   se están convirtiendo en una estrategia cada vez más popular, ya que los defensores argumentan que es una manera sencilla de aceptar la fluidez de la Web.
   Además, los proponentes usan fondos de paralaje como una herramienta para mejorar la participación de los usuarios y mejorar la experiencia general que ofrece
   un sitio web. El desplazamiento de Parallax permite la creación de una experiencia de usuario dinámica e interactiva que tiene el potencial de mejorar el atractivo visual.</p>
<p>Este módulo permite añadir efectos de paralaje a su sitio web sin ningún tipo de habilidades y esfuerzo adicionales. Es posible
     agregar tanto imágenes como video de fondo.</p>
