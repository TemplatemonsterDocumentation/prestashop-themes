<h4>RSS Smart Blog Feed</h4>
<h6>El módulo que genera el nuevo feed para tu Blog.</h6>
<p>Tiene varios ajustes en el panel de Administración:</p>
<ul class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Período de actualización</dt>
            <dd>Permite mostrar mensajes de un período de tiempo
                 específico (hora/día/semana/mes/año).
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Frecuencia de Actualización</dt>
            <dd>Permite establecer la frecuencia de actualización (1
                semana, 2 horas, etc.).
            </dd>
        </dl>
    </li>
</ul>
<p>En la parte superior hay una ruta de archivo donde se generarán las noticias. Consiste en
    la URL del sitio web + /modules/smartblogfeed/rss.php.</p>
