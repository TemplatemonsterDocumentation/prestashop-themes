<h3>Smart Blog</h3>
<p><strong>Smart blog -</strong> un conjunto de módulos que permite crear un blog en su
     tienda. Consta de los siguientes módulos:</p>
<ul class="marked-list">
    <li>Smart Blog</li>
    <li>RSS Smart Blog Feed</li>
    <li>Smart Blog Archive</li>
    <li>Smart Blog Categories</li>
    <li>Smart Blog Home Latest</li>
    <li>Smart Blog Latest Comments</li>
    <li>Smart Blog AddThisModule</li>
    <li>Smart Blog Popular Posts</li>
    <li>Smart Blog Recent Posts</li>
    <li>Smart Blog Related Post</li>
    <li>Smart Blog Search</li>
    <li>Smart Blog Tags</li>
</ul>
<p>También tiene sus ganchos donde los módulos del blog pueden aparecer
     en el sitio, también pueden aparecer en los ganchos estándar (sólo columnas).
</p>
<h4>Smart Blog</h4>
<p>Este es el módulo principal que contiene muchas configuraciones para la funcionalidad del blog. Después
     instalación de la pestaña del menú Blog aparecerá en el menú principal de su panel de Administrador
     donde puede agregar/quitar/editar las publicaciones/categorías o seleccionar el tipo de
     imágenes y sus dimensiones utilizadas en las entradas (tipos y dimensiones de la publicación
     las imágenes son diferentes de las configuraciones de imágenes del producto en su tienda). Para agregar el
     del módulo, vaya a <strong>Smart Blog
        (Modulos -> Funciones de Front Office -> Smart Blog)</strong>  y agregar todos 
		los parámetros.</p>
<h6>Descripción de los parámetros:</h6>
<ul class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Meta Title</dt>
            <dd>el título de tu blog que aparecerá en todas sus páginas.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Meta Keyword</dt>
            <dd>palabras clave que serán utilizados por los motores de búsqueda para una mejor optimización de SEO.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Meta Description</dt>
            <dd>descripción de su blog utilizado por los motores de búsqueda para una mejor optimización de SEO.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>URL principal del blog</dt>
            <dd>la URL de tu blog.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Utilice .html con URL amigable</dt>
            <dd>Activar/desactivar la adición de .html a las URL de las páginas del blog mediante URL amigable.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Número de publicaciones por página</dt>
            <dd>número de publicaciones que aparecen por página (categorías/páginas de archivos, etc.).</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Auto aceptación del comentario</dt>
            <dd>Activar/desactivar los comentarios de usuario aceptados automáticamente; Si está activado, los comentarios estarán disponibles sin
                 administración
                 moderación.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Activar Captcha</dt>
            <dd>activar/desactivar Captcha en el formulario de dejar comentarios en las páginas de productos.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Activar comentario</dt>
            <dd>activar/desactivar dejar comentarios de los usuarios.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mostrar nombre del autor</dt>
            <dd>activar/desactivar mostrando autor de publicaciones en las páginas del blog y publicar páginas individuales</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mostrar publicaciones Vistas</dt>
            <dd>activar/desactivar el número de veces que los usuarios vieron la publicación. Se mostrará debajo del poste
                 en
                 la
                 páginas de blogs y páginas individuales de blogs.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mostrar estilo del Nombre de Autor</dt>
            <dd>cambiar el formato del nombre del autor.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Imagen de  AVATAR</dt>
            <dd>agregando la imagen de avatar estándar para todos los usuarios.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>No mostrar imagen</dt>
            <dd>activar/desactivar la imagen. No se establece la imagen si no hay imagen para la publicación/categoría, etc..</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mostrar categoría</dt>
            <dd>Activar/desactivar la imagen de categoría y la descripción en las páginas de categoría de blog.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Configuración de Columna de Página de Blog</dt>
            <dd>establecer la visualización de los módulos relacionados con el blog. A menudo requiere ajustes adicionales, por lo tanto,
                 se recomienda que instale módulos adicionales de la misma forma que los módulos estándar de PrestaShop
                 están instalados.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>CSS personalizado</dt>
            <dd>Puede agregar estilos adicionales en este campo. Tenga cuidado al utilizar las clases que ya se utilizan
                 en el tema, ya que algunos conflictos pueden ocurrir
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Eliminar Old Tumblr</dt>
            <dd>Se utiliza para regenerar imágenes de blog en caso de que las dimensiones de la imagen se hayan cambiado o el nuevo tipo de imagen
                fue añadido. Para regenerar, seleccione <strong>'Si'</strong> y haga click en <strong>'Regenerar'</strong>.
            </dd>
        </dl>
    </li>
</ul>
