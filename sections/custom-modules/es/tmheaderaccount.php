<h3>TM Header Account</h3>
<p>Este módulo muestra el formulario de inicio de sesión y algunos elementos
    de cuenta de usuario disponibles en el encabezado del sitio. Permite acceder a su cuenta permaneciendo en
  la página activa. El módulo sirve como  
  un bloque desplegable. </p>
<h5>Instalación y Eliminación</h5>
<p>El módulo se puede instalar y eliminar como cualquier otro módulo de PrestaShop.</p>
<h5>Configuración del módulo</h5>
<p>El módulo no tiene ninguna configuración adicional.</p>
