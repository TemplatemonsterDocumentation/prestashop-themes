<h3>TM Slider</h3>
<p>El módulo está especialmente diseñado para demostrar su contenido de una manera eficiente. Ofrece todo lo que necesita para mostrar su contenido y hacer un sldier de aspecto profesional. El módulo tiene un montón de herramientas de personalización, que hacen que al slider super-fácil de ajustar. </p>

<h4>Instalación del Módulo</h4>
<p>Este módulo se puede instalar/desinstalar como cualquier otro módulo de PrestaShop.</p>


<h4>Configuración del módulo</h4>
<p>Después de instalar el módulo, puede configurarlo en la configuración del módulo
     página.</p>
<p>Para obtener los datos necesarios para la configuración del módulo, debe crear
     aplicación correspondiente en el recurso necesario.</p>
<p>No es necesario utilizar ambas variantes de inicio de sesión (Google, Facebook)
     simultáneamente, puede utilizar una de ellas sólo.</p>
<h4>Campos de Facebook</h4>
<ul class="marked-list">
    <li>ID de tu Aplicación</li>
    <li>Clave secreta de tu Aplicación</li>
</ul>
<h4>Campos de Google</h4>
<ul class="marked-list">
    <li>ID de tu Aplicación</li>
    <li>Clave secreta de tu Aplicación</li>
    <li>Redirigir ruta después de iniciar sesión <strong>(your shop
            URL+index.php?fc=module&module=tmsociallogin&controller=googlelogin)</strong></li>
</ul>
<h4>Campos VK</h4>
<ul class="marked-list">
    <li>ID de tu Aplicación</li>
    <li>Clave secreta de tu Aplicación</li>
    <li>Redirigir ruta después de iniciar sesión <strong>(your shop
            URL+index.php?fc=module&module=tmsociallogin&controller=vklogin)</strong></li>
</ul>
<p class="alert alert-info">Información más detallada usted puede conseguir siguiendo los enlaces: <a href="https://developers.facebook.com/docs/apps/register" target="_blank">Facebook App</a>, <a href="https://support.google.com/cloud/answer/6158849?hl=en&ref_topic=6262490" target="_blank">Google App</a> y <a href="https://vk.com/dev/web_how_to_start">VK App</a>.</p>
<p class="alert alert-warning">
    La ruta de redireccionamiento debe ser la misma que se ha indicado durante la creación de Google App o VK App.
</p>
