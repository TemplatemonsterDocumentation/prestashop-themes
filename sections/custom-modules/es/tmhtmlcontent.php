<p>Este módulo permite mostrar cualquier contenido (imagen, HTML, enlaces, etc.) en las siguientes posiciones: displayNav, home,
   top, topColumn, left y right (displayLeftColumn/displayRightColumn), footer. Se utiliza para ampliar las oportunidades de diseño
   y agregar bloques de anuncios/información a su tienda. Es un módulo multilenguaje y también puede trabajar 
   en el sitio web de múltiples tiendas.</p>
