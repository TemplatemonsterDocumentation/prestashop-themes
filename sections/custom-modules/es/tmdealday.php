<p>Este módulo le permite mostrar la lista de productos con el contador de cuenta atrás de descuento. También puede añadir un temporizador a los productos en las fichas de la página principal, 
    en las páginas de categorías y en las páginas de un solo producto.
</p>
<h5>Instalación y Eliminación</h5>
<p>El módulo se instala y se retira del mismo modo que cualquier otro módulo de PrestaShop.</p>
<h5>Configuración del módulo</h5>
<p>Después de la instalación del módulo, puede añadir un descuento del producto con el período de visualización y un temporizador de cuenta atrás con descuento.</p>
