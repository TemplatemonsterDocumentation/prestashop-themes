<h6>Módulo TM Collections se crea para dar la posibilidad a los visitantes de la tienda en línea para agregar productos a las colecciones y compartir estas colecciones en Facebook.</h6>
<h4>Instalación del módulo</h4>
<p>Módulos y Servicios -> Módulos y Servicios (1), busque el módulo TM Collections (2) 
    y pulse el botón 'Instalar' (3).</p>
<figure class="img-polaroid">
    <img src="img/tmcollections1.png" alt=""/>
</figure>
<h4>Configuración del Módulo</h4>
<p>Una vez instalado el módulo, aparecerá la página "Configuración".<br>
    Agregue el Id de la aplicación de Facebook al campo requerido (1) y pulse el botón "Guardar" (2).<br>
    La instrucción de cómo obtener el Id de aplicación de Facebook se puede encontrar aquí: <a
        href="https://developers.facebook.com/docs/apps/register" target="blank">Creación de un ID de aplicación</a>.</p>
<figure class="img-polaroid">
    <img src="img/tmcollections2.png" alt=""/>
</figure>
<h4>Uso del Módulo TM Collections</h4>
<h5>Agregar producto a la colección</h5>
<p class="alert alert-info">Los visitantes de la tienda deben estar conectados para poder agregar los artículos a las colecciones.</p>
<ul class="index-list">
    <li>
        <p>Una vez instalado el módulo, aparecerá el botón "Añadir a la colección" en cada página del producto. Debería verse 
		    como en la captura de pantalla de abajo (1).</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections3.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Después de hacer clic en este botón, un visitante de tienda verá una notificación: 'El producto se agregó correctamente a su colección.' Se puede cerrar o saltar 
		    a la vista colecciones haciendo clic en el botón "Mis colecciones"
            (2).</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections4.png" alt=""/>
        </figure>
    </li>
</ul>
<h5>Cómo buscar colecciones</h5>
<ul class="index-list">
    <li>
        <p>En la página "Mis colecciones", un cliente puede agregar la nueva colección (1) o ver la lista de las creadas (2).</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections5.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Al hacer clic en el botón 'Ver colección' (1) se abrirá la página de recopilación, el botón 'Ver productos' (2) permite ver
     		la lista de productos de esta colección en la página actual. <br>
            El título de la colección puede ser editado (3) o suprimido (4). <br>
            Para compartir la colección, el visitante debe presionar el botón 'Compartir' (5).</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections6.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Mientras mira a través de los elementos de la colección después de hacer clic en el botón "Ver productos", cada elemento puede ser eliminado
    		de la lista (1).</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections7.png" alt=""/>
        </figure>
    </li>
</ul>
<h5>Compartir colecciones</h5>
<p>Para compartir la colección, el visitante debe presionar el botón 'Compartir'. Habrá algunos pasos fáciles de seguir antes 
   de publicar la colección. Durante cada paso hay una capacidad para volver atrás y cambiar las opciones de uso compartido.</p>
<ul class="index-list">
    <li>
        <p>El primer paso es elegir el diseño de la colección que se publicará.</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections8.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>A continuación, los productos deben añadirse. Cada celda con un '+' es un botón para agregar la imagen del producto.</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections9.png" alt=""/>
        </figure>
        <p>Selección de las imágenes de cada producto:</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections10.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Cuando se agreguen todas las imágenes, el visitante debe presionar el botón "Compartir"(1).</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections11.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>En una pequeña ventana que luego aparece, habrá algunas opciones de publicación estándar para elegir y la posibilidad de añadir algún texto a la publicación (1). Después de hacer clic en el botón "Publicar en Facebook" (2), se publicará la publicación.</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections12.png" alt=""/>
        </figure>
    </li>
</ul>
