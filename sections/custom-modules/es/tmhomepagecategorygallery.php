<p>Cuando agrega un nuevo elemento al módulo (o edita uno de la lista de elementos), tiene algunas opciones para elegir. La descripción detallada de las opciones que puedes ver debajo de la captura de pantalla. Después de configurar todas las opciones, haga clic en el botón "Guardar" (1).</p>
