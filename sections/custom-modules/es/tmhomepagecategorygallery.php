<ul class="marked-list">
	<li><strong>Selecciona una categoría</strong> - aquí usted elige de la lista desplegable la categoría que debe ser exhibida en su página.</li>
	<li><strong>Clase específica</strong> - la clase específica se puede agregar al elemento del módulo.</li>
	<li><strong>Nombre para mostrar</strong> - opción para mostrar u ocultar el nombre de la categoría.</li>
	<li><strong>Longitud del nombre de la categoría</strong> - número máximo de símbolos que se mostrarán en el nombre de la categoría. Si el nombre es más largo - el símbolo '..' se añadirá después del número de símbolos que se hat.</li>
	<li><strong>Longitud del nombre de la categoría</strong> - Opción para mostrar u ocultar la descripción de la categoría.</li>
	<li><strong>Longitud de la descripción</strong> - número máximo de símbolos que se mostrarán en la descripción. De la descripción es más larga - el símbolo '..' se añadirá después de que el número de símbolos que se ha establecido.</li>
	<li><strong>rden de clasificación</strong> - el orden del elemento en la galería de categorías.</li>
	<li><strong>Botón de visualización</strong> - la opción de mostrar u ocultar el botón 'Comprar ahora' (el texto del botón puede ser diferente dependiendo de la plantilla).</li>
	<li><strong>Estado</strong> - la opción de mostrar u ocultar la categoría</li>
	<li><strong>HTML content</strong> - el campo donde se puede agregar algún contenido HTML.</li>
</ul> 