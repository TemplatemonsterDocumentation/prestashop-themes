<h3>TM Category Products 0.1.3</h3>
<p>Este módulo muestra los productos de la categoría en los ganchos displayHome y displayHomeTabContent. El módulo permite mostrar 
    productos únicos, productos de una categoría o todos los productos. Además, el módulo viene con un carrusel que funciona, 
	cuando el módulo ocupa la posición displayHome.</p>
<h4>Instalación del Módulo</h4>
<p>Para instalar el módulo, vaya a Panel de administración -> Módulos y servicios -> Módulos y Servicios y busque 'TM Category Products'
    en Front Office Features, luego haga clic en el botón "Instalar" (1).</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryproducts013-1.png" alt=""/>
</figure>
<h4>Configuración del Módulo</h4>
<ul class="index-list">
    <li>
        <p>Cuando el módulo se ha instalado correctamente, aparecerá el botón 'Configurar' (1).</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts013-2.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>En la página de Configuración del Módulo principal hay listas de pestañas y bloques (1) (donde se pueden ver los 
		     elementos creados) y dos botones -'Ajustes'(2) y 'Agregar elemento'(3).</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts013-3.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Haga clic en el botón "Configuración" y elija las opciones del módulo según sus necesidades.</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts013-4.png" alt=""/>
        </figure>
        <ul class="marked-list">
            <li><p><strong>Elementos visibles</strong> - el número de elementos visibles.</p></li>
            <li><p><strong>Desplazar elementos</strong> - el número de elementos cambió con un rollo de carrusel.</p></li>
            <li><p><strong>Ancho de la Diapositiva</strong> - el ancho del elemento de la lista del carrusel.</p></li>
            <li><p><strong>Margen de Diapositivas</strong> - el margen del elemento de la lista del carrusel.</p></li>
            <li><p><strong>Desplazamiento automático</strong> - desplazamiento automático de elementos de carrusel.</p></li>
            <li><p><strong>Velocidad del Carrusel</strong> - velocidad de cambio de artículos.</p></li>
            <li><p><strong>Pausa</strong> - pausa entre desplazamientos (si está activado "Desplazamiento automático").</p></li>
            <li><p><strong>Aleatorio</strong> - el carrusel comienza con un elemento aleatorio.</p></li>
            <li><p><strong>Bucle de carrusel</strong> - el bucle: cuando todos los ítems del carrusel han sido mostrados, el carrusel
      			comienza desde el principio.</p></li>
            <li><p><strong>Ocultar el control al final</strong> - ocultar botones de control (Siguiente/Anterior).</p></li>
            <li><p><strong>Paginador</strong> - mostrar paginación.</p></li>
            <li><p><strong>Control</strong> - mostrar controles (Siguiente/Anterior).</p></li>
            <li><p><strong>Auto control</strong> - mostrar controles (Reproducir/Pausa).</p></li>
            <li><p><strong>Auto hover</strong> - carrusel se detiene, si el usuario coloca un elemento de lista.</p></li>
        </ul>
    </li>
</ul>
<h4>Agregar nueva pestaña/bloque</h4>
<p>Pestaña es un bloque que muestra los productos en la posición displayHomeTabContent.</p>
<p>Este bloque permite seleccionar productos en la posición displayHome.</p>
<ul>
    <li>
        <p>Para añadir una nueva pestaña o bloque, haga clic en el botón "Agregar elemento".</p>
        <p>Verá la pantalla donde puede elegir las siguientes opciones:</p>
        <ul class="marked-list">
            <li><p><strong>Selecciona una categoría</strong> - elegir una categoría, cuyos productos 
			  se mostrarán.</p>
                <figure class="img-polaroid">
                    <img src="img/tmcategoryproducts013-5.png" alt=""/>
                </figure>
            </li>
            <li>
                <p><strong>Número de productos a mostrar</strong> - número de elementos a mostrar.</p>
                <figure class="img-polaroid">
                    <img src="img/tmcategoryproducts013-6.png" alt=""/>
                </figure>
            </li>
            <li>
                <p><strong>Modo</strong> - Ficha/bloque de modo.</p>
                <figure class="img-polaroid">
                    <img src="img/tmcategoryproducts013-7.png" alt=""/>
                </figure>
            </li>
            <li>
                <p><strong>Estado</strong> - permite activar/desactivar la pestaña o bloque.</p>
                <figure class="img-polaroid">
                    <img src="img/tmcategoryproducts013-8.png" alt=""/>
                </figure>
            </li>
        </ul>
        <p>Después de elegir los parámetros, haga clic en el botón "Guardar". La pestaña/bloque se guardará y aparecerá en la
       		lista de pestañas/bloques.</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts013-9.png" alt=""/>
        </figure>
    </li>
</ul>
<h4>Edición de pestañas/bloques</h4>
<p>Para editar la pestaña o el bloque, busque en la lista y presione el botón 'Editar' (1). <br>
    Las opciones son las mismas que para crear un bloque o una pestaña.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryproducts013-10.png" alt=""/>
</figure>
<h4>Eliminación de ficha/bloque</h4>
<p>Para quitar la pestaña o el bloque, busque el botón "Borrar" debajo de "Editar".</p>
