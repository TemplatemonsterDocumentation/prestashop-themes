<h3>TM Categories Badges</h3>
<p>Este módulo permite añadir insignias a las categorías de la barra lateral del módulo de
    categorías de Prestashop predeterminado ("Blockcategories"). Las insignias se pueden
	agregar a la categoría de cualquier nivel y en cualquier idioma. El campo de texto de la etiqueta
	es obligatorio y debe rellenarse en todos los idiomas. Si este campo se deja en blanco 
	en cualquier idioma, automáticamente tomará el texto del idioma predeterminado.</p>
<h5>Instalación y eliminación</h5>
<p>El módulo se instala y suprime como cualquier otro módulo de Prestashop.</p>
<h5>Configuración del módulo</h5>
<p>Después de la instalación, puede cambiar su configuración en la página de configuración del módulo.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoriesbadges-1.png" alt=""/>
</figure>
<p>Puede agregar insignias a cualquier categoría después de seleccionarlo en la lista desplegable.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoriesbadges-2.png" alt=""/>
</figure>
<p class="alert alert-warning"><span>¡Atención! Sólo puede agregar una insignia para cada categoría.</span></p>
<p>A continuación, puede agregar texto que aparecerá en la insignia y seleccionar el estilo de la lista desplegable (los estilos
    pueden diferir en diferentes plantillas).</p>
<p>A continuación, pulse el botón "Guardar" (1).</p>
<figure class="img-polaroid">
    <img src="img/tmcategoriesbadges-3.png" alt=""/>
</figure>

<p>Puede ver la lista de distintivos existentes a continuación. Aquí puede editar o borrar insignias 
    con la ayuda de los botones correspondientes.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoriesbadges-4.png" alt=""/>
</figure>
<p>El módulo es multilingüe y soporta múltiples almacenes. Puede agregar insignias para 
    el grupo de tiendas o todas las tiendas a la vez. Aparecerán en la lista como
 	elementos separados.</p>
