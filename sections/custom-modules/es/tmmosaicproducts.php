<p>Este módulo le permite mostrar categorías con productos, banners, video (Youtube, Vimeo y video personalizado), contenido HTML, varios deslizadores con diferentes números de diapositivas y dimensiones en cualquier fila en la página principal del sitio.
</p>
<h5>Instalación y eliminación</h5>
<p>El módulo se instala y suprime como cualquier otro módulo de PrestaShop.</p>
<h5>Configuración del módulo</h5>
<p>Una vez instalado el módulo, puede agregar los bloques necesarios con productos de categoría, banners agregados anteriormente, contenido de vídeo y HTML.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-1.png" alt="" /></figure>
<h5>Adición/eliminación/edición de Bloques</h5>
<p>Para agregar una nueva categoría, presione el botón "Añadir nuevo".</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-2.png" alt="" /></figure>
<ul class="marked-list">
    <li><strong>Selecciona una categoría</strong> - seleccione una categoría para agregar productos, banners, videos y contenido HTML.</li>
    <li><strong>Usar nombre personalizado</strong> - estado de la categoría personalizada.</li>
    <li><strong>Nombre personalizado</strong> - título de categoría.</li>
    <li><strong>Estado</strong> - Estado del artículo.</li>
    <p>Para agregar la nueva fila, presione el botón "Agregar fila". A continuación, elija uno de los ocho diseños presentados en la siguiente ventana.</p>
</ul>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-3.png" alt="" /></figure>
<p>Para añadir el diseño, haga clic en una de las ocho opciones para seleccionarlo y se agregará al formulario.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-4.png" alt="" /></figure>
<p>Para añadir elementos, haga clic en una de las celdas de diseño. Verá cinco botones que enumeran los elementos disponibles de cierto tipo.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-5.png" alt="" /></figure>
<p>Después de pulsar uno de los botones, verá el formulario para seleccionar el elemento del tipo elegido.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-6.png" alt="" /></figure>
<p>Después de pulsar uno de los botones, verá el formulario para seleccionar el elemento del tipo elegido.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-7.png" alt="" /></figure>
<p>Después de guardar la categoría con los diseños elegidos, se mostrará en la forma de las categorías añadidas.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-8.png" alt="" /></figure>
<p>Para eliminar el elemento, pulse el botón 'Eliminar'.</p>
<h5>Adición/eliminación/edición de Banners</h5>
<p>Para agregar un nuevo banner, haga clic en el botón "Añadir nuevo banner". A continuación, rellene los campos necesarios en el formulario que aparece.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-9.png" alt="" /></figure>
<ul class="marked-list">
    <li><strong>Seleccione un archivo</strong> - elija un archivo, este campo es obligatorio (al menos para el idioma predeterminado).</li>
    <li><strong>Título</strong> - título del banner, este campo es obligatorio (al menos para el idioma predeterminado).</li>
    <li><strong>Url</strong> - enlace, este campo es obligatorio (al menos para el idioma predeterminado).</li>
    <li><strong>Descripción</strong> - descripción del banner (al menos para el idioma predeterminado).</li>
    <li><strong>Clase específica</strong> - Clase CSS del bloque.</li>
</ul>
<h5>Adición/eliminación/edición de vídeo</h5>
<p>Para agregar un nuevo video, haz clic en el botón "Añadir nuevo video". A continuación, rellene los campos necesarios en el formulario que aparece.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-10.png" alt="" /></figure>
<ul class="marked-list">
    <li><strong>Introduce el nombre del video</strong> - video title, this field is required (at least for the default language).</li>
    <li><strong>Enlace/Ruta de vídeo</strong> - enlace al video (youtube, vimeo o video personalizado), este campo es obligatorio (al menos para el idioma predeterminado).</li>
    <li><strong>Permitir reproducción automática</strong> - define si el video se inicia automáticamente justo después de cargar el reproductor.</li>
    <li><strong>Permitir controles</strong> - define si se muestran los controles del reproductor.</li>
    <li><strong>Bucle</strong> - define si la reproducción de vídeo está en el bucle infinito.</li>
</ul>
<p>Para agregar video personalizado, presione el botón 'file'. A continuación, podrá agregar un video a través del administrador de archivos.</p>
<h5>Adición/eliminación/edición de contenido HTML</h5>
<p>Para agregar un nuevo contenido HTML, presione el botón 'Añadir nuevo HTML'. A continuación, rellene los campos necesarios en el formulario que aparece.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-11.png" alt="" /></figure>
<ul class="marked-list">
    <li><strong>Introduzca el nombre del elemento HTML</strong> - título de contenido HTML, este campo es obligatorio (al menos para el idioma predeterminado).</li>
    <li><strong>Clase específica</strong> - bloque de la clase CSS.</li>
    <li><strong>Contenido HTML</strong> - el contenido.</li>
</ul>
<h5>Adición/eliminación/edición del Slider</h5>
<p>Para añadir un nuevo slider, pulse el botón 'Añadir nuevo slider'. A continuación, rellene los campos necesarios en el formulario que aparece.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-12.png" alt="" /></figure>
<ul class="marked-list">
    <li><strong>Introduzca el nombre del elemento Slider</strong> - título del slider, este campo es obligatorio (al menos para el idioma predeterminado).</li>
    <li><strong>Control</strong> - mostrar/ocultar navegación del slider.</li>
    <li><strong>Paginador</strong> - mostrar/ocultar la paginación del slider.</li>
    <li><strong>Desplazamiento automático</strong> - activa/desactiva el deslizador autoplay.</li>
    <li><strong>Clase específica</strong> - clase CSS del bloque.</li>
    <li><strong>Velocidad del carrusel</strong> - duración de transición de diapositivas (en ms), este campo es obligatorio.</li>
    <li><strong>Pausa</strong> - el período entre conmutación de diapositivas (en ms), este campo se requiere.</li>
</ul>
<h5>Adición/eliminación/edición de diapositivass</h5>
<p>Para agregar una nueva diapositiva, haga clic en el botón "Ver" en el control deslizante y luego presione el botón "Añadir nuevo" en una ventana que aparece. A continuación, rellene los campos requeridos.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-13.png" alt="" /></figure>
<ul class="marked-list">
    <li><strong>Tipo</strong> - seleccione el tipo de diapositiva, este campo es obligatorio.</li>
    <li><strong>Banner/Video/Lista de HTML</strong> - elija el elemento del tipo previamente seleccionado, este campo es obligatorio.</li>
    <li><strong>Orden de clasificación</strong> - posición de diapositiva.</li>
    <li><strong>Estado del artículo</strong> - estado del elemento.</li>
</ul>