<h3>TM Homepage Products Carousel</h3>
<p>El módulo añade un carrusel a las pestañas de la página principal.</p>
<h5>Instalación y Eliminación</h5>
<p>El módulo se puede instalar y eliminar como cualquier otro módulo de PrestaShop.</p>
<h5>Configuración del módulo</h5>
<p>La configuración del módulo instalado se puede configurar en la página de configuración del módulo.</p>
