<h3>TM Products Manager</h3>
<h4>Introducción</h4>
<p>El módulo se crea para gestionar tiendas en línea, especialmente tiendas con gran cantidad de productos. Usando este módulo usted puede filtrar fácilmente sus productos y hacer operaciones diferentes en ellos.</p>

<h4>Instalación y extracción del módulo</h4>
<p>El módulo se instala y se elimina como cualquier módulo de Prestashop.</p>
<p class="alert alert-info">
    <span>La eliminación o reinstalación del módulo no restablecerá los cambios realizados.</span>
</p>
<p>Cuando el módulo se haya instalado correctamente, vaya a Catalog -> TM Products Manager (1) para ver el menú principal del módulo.</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-1.png" alt=""/></figure>
<h4>Rendimiento</h4>
<p>El menú principal del módulo se divide en dos partes principales: 'Choose section' (1) y 'Choose option' (2).</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-2.png" alt=""/></figure>
<p>La primera parte se crea en forma de fichas - Información, Precio, Asociaciones y Cantidad.</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-3.png" alt=""/></figure>
<ul class="marked-list">
    <li><strong>Información</strong> - opciones relativas a la disponibilidad y descripción del producto.</li>
    <li><strong>Precio</strong> - precios y reglas de impuestos..</li>
    <li><strong>Asociaciones</strong> - capacidad de cambiar marca, categoría o accesorios de varios producto.</li>
    <li><strong>Cantidad</strong> - cambiando la cantidad de productos.</li>
</ul>
<ul class="index-list">
    <li>
        <p>Haga clic en cada pestaña para ver las opciones disponibles en esta sección. Se describirán más adelante. <br>
            Cada opción tiene una indirecta - descripción corta, que aparece en la derecha cuando usted pasa la opción (1).</p>
        <figure class="img-polaroid"><img src="img/tmproductsmanager-4.png" alt=""/></figure>
    </li>
    <li>
        <p>Elija la opción necesaria y haga clic en ella.</p>
    </li>
    <li>
        <p>El siguiente paso es filtrar los productos. Configure las opciones de filtrado según sus necesidades y haga clic en el botón 'Seleccionar' (1). Para borrar los filtros seleccionados, haga clic en el botón "Reiniciar" (2).</p>
        <figure class="img-polaroid"><img src="img/tmproductsmanager-5.png" alt=""/></figure>
        <p>También puede cancelar cada uno de los filtros después de haberlos seleccionados. Simplemente haga clic en la cruz (1) cerca del valor de la propiedad del producto y eliminar.</p>
        <figure class="img-polaroid"><img src="img/tmproductsmanager-6.png" alt=""/></figure>
    </li>
    <li>
        <p>Debajo de los filtros hay una lista de productos que coinciden con los criterios seleccionados. Puede editarlo Si necesita eliminar algunos de los productos de la lista. Cada producto se puede eliminar por separado haciendo clic en el botón "Eliminar de la lista" (1). Para eliminar todos los productos de la lista, haga clic en los botones "Seleccionar todo" (2) y "Eliminar seleccionados" (3). Si comprobó un gran número de productos y debe desmarcarlos, haga clic en el botón "Anular selección". (4).</p>
        <figure class="img-polaroid"><img src="img/tmproductsmanager-7.png" alt=""/></figure>
    </li>
    <li>
        <p>Cuando la lista de productos seleccionados cumpla con todos los requisitos, haga clic en el botón "Proceso" (1).</p>
        <figure class="img-polaroid"><img src="img/tmproductsmanager-8.png" alt=""/></figure>
    </li>
    <li>
        <p>Configure las opciones según sus necesidades y haga clic en el botón 'Guardar' (1). Si necesita hacer algunos cambios en la lista de productos, haga clic en el botón "Volver al filtro" (2).</p>
        <p class="alert alert-info">
            <span>Tenga en cuenta que no podrá deshacer estos cambios de la misma manera.</span>
        </p>
        <figure class="img-polaroid"><img src="img/tmproductsmanager-9.png" alt=""/></figure>
    </li>
</ul>

<h4>Opciones Disponibles</h4>
<h5>Información</h5>
<p>La sección permite configurar las siguientes opciones:</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-10.png" alt=""/></figure>
<ul class="marked-list">
    <li><strong>Activar/Desactivar productos</strong> - establecer el estado de encendido / apagado de sus productos.</li>
    <li><strong>Disponible para ordenar</strong> - hacer algunos productos disponibles o no disponibles para el orden
        <p class="alert alert-warning small">
            <span>NOTA: si 'Disponible para pedido' está establecido en 'Sí' para los productos con precios ocultos, sus precios volverán a ser visibles automáticamente.</span>
        </p>
    </li>
    <li><strong>Mostrar precio</strong> - mostrar u ocultar el precio del producto.
        <p class="alert alert-warning small">
            <span>NOTA: si establece 'Mostrar precio' en 'No' para los productos, los cuales están disponibles para el pedido, se cambiarán automáticamente a no disponibles.</span>
        </p>
    </li>
    <li><strong>Solo online</strong> - aplique la marca "Sólo en línea" a los elementos seleccionados.</li>
    <li><strong>Condición</strong> - Cambiar la condición de varios productos.</li>
    <li><strong>Breve descripción</strong> - añadir una breve descripción que se debe aplicar a todos los productos seleccionados.</li>
    <li><strong>Descripción completa</strong> - añadir una descripción a los productos seleccionados con la capacidad de colocarlo antes de la descripción principal, después de ella, o reemplazar toda la descripción por la nueva.</li>
    <li><strong>Etiquetas</strong> - añadir más etiquetas a los productos elegidos.</li>
</ul>

<h5>Precio</h5>
<p>En esta sección puede cambiar las opciones relativas al precio del producto.</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-11.png" alt=""/></figure>
<ul class="marked-list">
    <li><strong>Display "En rebajas"</strong> - mostrar los elementos seleccionados con la marca 'En rebajas'.</li>
    <li><strong>Cambiar las reglas de impuestos</strong> - elegir las normas fiscales que se aplicarán a varios productos.</li>
    <li><strong>Añadir nuevo precio específico</strong> - establecer el precio especial para los productos seleccionados con la capacidad de agregar el período de activación de precios.</li>
</ul>

<h5>Asociaciones</h5>
<p>Aquí puede cambiar las opciones relacionadas con las relaciones de producto.</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-12.png" alt=""/></figure>
<ul class="marked-list">
    <li><strong>Cambio de marca</strong> - cambiar la marca de los artículos seleccionados.</li>
    <li><strong>Cambiar categoría predeterminada</strong> - elija la categoría que se establecerá como predeterminada para algunos productos.
        <p class="alert alert-warning small">
            <span>NOTA: Sólo puede elegir una categoría predeterminada. Tenga en cuenta que su categoría predeterminada actual será reemplazada por la nueva.</span>
        </p>
    </li>
    <li><strong>Cambiar categorías relacionadas</strong> - establezca las categorías relacionadas con varios productos con la capacidad de guardar o eliminar las viejas relaciones.</li>
    <li><strong>Añadir accesorios</strong> - añadir los accesorios adecuados para los productos elegidos.</li>
</ul>

<h5>Cantidad</h5>
<p>Esta opción permite establecer la cantidad de producto o aumentar/disminuir la cantidad actual en una cantidad determinada.</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-13.png" alt=""/></figure>
<ul class="marked-list">
    <li><strong>Cambiar cantidad</strong> - establecer la cantidad requerida de algunos productos.</li>
</ul>
