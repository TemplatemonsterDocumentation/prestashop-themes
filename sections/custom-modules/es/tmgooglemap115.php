<h3>TM Google Map 1.1.5</h3>
<p>Este módulo permite añadir Google Map con ubicaciones de tiendas y almacenar descripciones breves en la Página principal. El mapa mostrará las direcciones de todas las tiendas conectadas con su tienda en línea.</p>
<h5>Instalación y extracción</h5>
<p>El módulo se instala y se retira como cualquier otro módulo de PrestaShop.</p>
<h5>Configuración del módulo</h5>
<p>Después de instalar el módulo, puede agregar almacenes al mapa. Después de presionar el botón 'Añadir nueva tienda' (1), será redirigido a la pestaña 'Almacenar contactos', donde puede editar sus tiendas.</p>
<p class="img-polaroid"><img src="img/tmgooglemap115-1.png" alt="" /></p>
<p>Para añadir un nuevo almacén, pulse el botón 'Añadir nuevo'.</p>
<p class="img-polaroid"><img src="img/tmgooglemap115-2.png" alt="" /></p>
<ul class="marked-list">
    <li><strong>Seleccione una tienda</strong> - seleccione la tienda que desea agregar al mapa.</li>
    <li><strong>Estado</strong> - el estado del elemento.</li>
    <li><strong>Marcador</strong> - agregando un marcador personalizado.</li>
    <li><strong>Texto personalizado</strong> - añadir texto personalizadot.</li>
</ul>
<h5>Configuración de visualización de Google Maps</h5>
<p class="img-polaroid"><img src="img/tmgooglemap115-3.png" alt="" /></p>
<ul class="marked-list">
    <li><strong>Estilo de Mapa</strong> - seleccione el estilo de visualización del mapa.</li>
    <li><strong>Tipo de Mapa</strong> - seleccione el tipo de visualización del mapa.</li>
    <li><strong>Nivel de zoom</strong> - especifique el nivel inicial del zoom del mapa (de 1 a 17).</li>
    <li><strong>Zoom en el desplazamiento</strong> - activar/desactivar el zoom de mapa en desplazamiento.</li>
    <li><strong>Controles de mapa</strong> - activar/desactivar los controles del mapa.</li>
    <li><strong>Vista de calle</strong> - activar/desactivar la vista de la calle.</li>
    <li><strong>Marcador de animación</strong> - activar/desactivar marcador de animación.</li>
    <li><strong>Popup</strong> - activar/desactivar ventanas emergentes con información de la tienda.</li>
</ul>