<h5>Ajout/Modification des options d'article</h5>
<div class="alert alert-info">Remarque: le nom de la catégorie, la description et l'image sont chargées à partir des propriétés de la catégorie
et on n'a pas besoin de les ajouter manuellement. Pour modifier ces options, accédez au Panneau d'administration -> Catalogue -> Catégories -> choisissez la catégorie -> Éditer.</div>
<p>Lorsque vous ajoutez un nouvel élément au module (ou modifiez celui de la liste des éléments), vous avez quelques options à choisir. La description détaillée des options que vous pouvez voir ci-dessous la capture d'écran. Une fois toutes les options définies, cliquez sur le bouton 'Enregistrer' (1).</p>
<figure class="img-polaroid">
    <img src="img/tmhomepagecategorygallery-4.png" alt=""/>
</figure>
<ul class="marked-list">
	<li><strong>Choisir une catégorie</strong> - ici, vous choisissez dans la liste déroulante la catégorie qui devrait être affichée sur votre page.</li>
	<li><strong>Classe spécifique</strong> - la classe spécifique peut être ajoutée à l'élément du module.</li>
	<li><strong>Afficher un nom</strong> - l'option pour afficher ou masquer le nom de la catégorie.</li>
	<li><strong>Longueur du nom de la catégorie</strong> - le nombre maximal de symboles à afficher dans le nom de la catégorie. Si la description est plus longue - le symbole '..' sera ajouté après le nombre de symboles défini.</li>
	<li><strong>Afficher la description</strong> - l'option pour afficher ou masquer la description de la catégorie.</li>
	<li><strong>Longueur de la description</strong> - le nombre maximal de symboles à afficher dans la description. Si la description est plus longue - le symbole '..' sera ajouté après le nombre de symboles défini.</li>
	<li><strong>Ordre de tri</strong> - l'ordre des articles dans la galerie des catégories.</li>
	<li><strong>Bouton d'affichage</strong> - l'option pour afficher ou masquer le bouton 'Achetez!' (le texte du bouton peut être différent selon le modèle).</li>
	<li><strong>Etat</strong> - l'option de montrer ou masquer la catégorie.</li>
	<li><strong>Contenu HTML</strong> - le champ où un contenu HTML peut être ajouté.</li>
</ul> 