<h5>Ajout du module Media Parallax</h5>
<ul class="index-list">
    <li>
        <p>Il est nécessaire d'ajouter l'ID ou la classe dans le contenu de votre page HTML.
		Vous pouvez aussi utiliser une balise unique, cet élément ne doit pas être répété.</p>
    </li>

    <li>
        <p>Créez l'image de la taille requise (il est recommandé de créer l'image de plus grande taille que le bloc où l'effet sera ajouté). 
		Dans le cas où vous souhaitez ajouter la vidéo, vous devrez la préparer en 3 formats:
            .mp4, .webm and .ogv, et aussi ajouter l'image de prévisualisation de même taille (.png, .jpg and .gif)</p>
    </li>
    <li>
        <p>Appuyez sur le bouton <strong>'Ajouter un conteneur'</strong> dans <strong>'Ajouter un nouveau bloc'</strong>.</p>
        <figure class="img-polaroid">
            <img src="img/parallax-1.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Dans l'écran apparu, ajoutez tous les paramètres requis.</p>
        <h6>
            <strong>Paramètres:</strong>
        </h6>
        <ul class="index-list">
            <li>
                <dl class="inline-term">
                    <dt>Sélecteur</dt>
                    <dd>l'élément où l'effet sera ajouté (id ou tag unique).</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Type de parallaxe</dt>
                    <dd>le type d'effet qui sera utilisé (image ou vidéo).</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Fichiers multimédia</dt>
                    <dd>Télécharger tous les fichiers requis.</dd>
                </dl>
            </li>
            <li>
                <p>Appuyez sur le bouton <strong>'Enregistrer'</strong>.</p>
            </li>
        </ul>
        <figure class="img-polaroid">
            <img src="img/parallax-2.png" alt=""/>
        </figure>
    </li>
</ul>
