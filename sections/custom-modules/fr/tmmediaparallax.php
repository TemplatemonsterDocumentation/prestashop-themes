<h3>Module Media Parallax</h3>
<h4>Qu'est-ce que l'effet de parallaxe?</h4>
<p>Il s’agit d’une méthode de web design qui consiste à donner l’illusion d’un effet 2D à une page web. 
Les éléments sont disposés en couches superposées qui se déplacent à des vitesses différentes lors d’une action de l’utilisateur, 
qui peut être le scroll ou le déplacement de la souris. Les concepteurs Web ont commencé à intégrer le défilement de la parallaxe en 2011, en utilisant HTML5 et CSS3.
Les sites Web avec le fond de parallaxe deviennent de plus en plus populaire, c'est un moyen simple d'embrasser la fluidité du Web.
Le défilement parallaxe permet la création de l'aspect visuel dynamique et interactive.</p>
<p>Ce module permet d'ajouter des effets de parallaxe à votre site sans aucune compétence et effort supplémentaire. C'est possible d'ajouter des images et une vidéo de fond.</p>
