<h3>Home Carrousel des Produits TM</h3>
<p>Le module ajoute un carrousel aux onglets de la page principale.</p>
<h5>Installation et suppression</h5>
<p>Le module est installé et supprimé de la même manière que tout autre module PrestaShop.</p>
<h5>Configuration du module</h5>
<p>Les paramètres du module installé peuvent être configurés sur la page de configuration du module.</p>
