<p>Ce module permet d'ajouter un nombre illimité de vidéos à tout produit dans n'importe quelle langue et magasin.</p>
<h5>Installation et suppression</h5>
<p>Le module est installé et supprimé comme tout autre module Prestashop. Il n'a pas de page de configuration et supporte les multi-magasins.</p>
<p>Après l'installation du module, vous verrez le nouvel onglet dans la page de configuration de votre produit
 (Catalogue -> Produit -> Sélectionner un produit - Vidéos du Produit TM).</p>
<p>Pour ajouter une vidéo à un produit, vous devez remplir les champs requis (le champ de lien est obligatoire). Le module est multilingue, alors vous devrez remplir les champs pour toutes les langues auxquelles cette vidéo est liée. Si le champ de lien n'est pas rempli pour aucune langue, la vidéo ne sera pas ajoutée pour cette langue. Le module est censé afficher des vidéos Youtube et Vimeo, alors vous devez ajouter des liens à partir de ces ressources. Après avoir effectué les modifications, cliquez sur le bouton <strong>'Enregistrer'</strong>/<strong>'Enregistrer & Rester'</strong>.</p>
