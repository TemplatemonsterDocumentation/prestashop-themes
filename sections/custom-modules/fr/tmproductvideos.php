<h6>Exemples de liens</h6>
<p>Pour afficher correctement la vidéo, les liens doivent avoir le format suivant:</p>
<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Youtube</dt>
            <dd><code>https://www.youtube.com/embed/K6VcTyaym90</code></dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Vimeo</dt>
            <dd><code>http://player.vimeo.com/video/66140585</code></dd>
        </dl>
    </li>
</ul>
<p>Si le produit a une vidéo liée à cette langue, vous verrez la liste des vidéos ci-dessous.</p>
<p>Pour enregistrer, supprimer ou modifier la liste, il n'y a pas besoin de cliquer sur le bouton <strong>'Enregistrer'</strong>/<strong>'Enregistrer & Rester'</strong>. Utilisez les boutons situés dans les blocs vidéo. Pour modifier l'ordre des vidéos, utilisez la méthode glisser-déposer pour changer leur position. Pour activer/désactiver la vidéo, utilisez le bouton à côté de la vidéo. Pour éditer des vidéos, remplissez les champs avec les données requises et appuyez sur le bouton <strong>'Actualiser'</strong>.</p>
<p>Pour supprimer la vidéo de la liste, cliquez sur le bouton  <strong>'Supprimer Vidéo'</strong>.</p>
<p>En changeant de langue, seule la liste des vidéos liées à la langue apparaîtra.</p>
<p>Pour afficher la vidéo en format agrandi, appuyez sur le bouton <strong>'Zoom'</strong> sous cette vidéo.</p>
