<h3>Produits de Catégorie TM 0.1.3</h3>
<p>Ce module affiche des produits de catégorie en crochets displayHome et displayHomeTabContent. 
Le module permet d'afficher des produits individuels, des produits d'une catégorie ou de tous les produits.
En outre, le module est livré avec un carrousel qui fonctionne, lorsque le module occupe la position displayHome.</p>
<h4>Installation du module</h4>
<p>Pour installer le module, accédez au panneau Administration  -> Modules et Services -> Modules et Services et trouver le module 'Produits de Catégorie TM ' dans les Fonctionnalités de Front Office, puis cliquez sur le bouton 'Installer' (1).</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryproducts013-1.png" alt=""/>
</figure>
<h4>Configuration du module</h4>
<ul class="index-list">
    <li>
        <p>Une fois le module a été installé avec succès, le bouton 'Configurer' (1) apparaît.</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts013-2.png" alt=""/>
        </figure>
    </li>
    <li>Dans la page de configuration du module, il existe des listes d'onglets et de blocs (1) (où vous pouvez voir les éléments créés)
	et deux boutons - 'Paramètres' (2) et 'Ajouter un élément' (3).</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts013-3.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Appuyez sur le bouton 'Paramètres' et choisissez les options du module selon vos besoins.</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts013-4.png" alt=""/>
        </figure>
        <ul class="marked-list">
            <li><p><strong>Eléments visibles</strong> - le nombre de fabricants visibles.</p></li>
            <li><p><strong>Défilement des éléments</strong> - le nombre de fabricants représentés lors de défilement du carrousel.</p></li>
            <li><p><strong>Largeur de diapositive</strong> - la largeur de l'élément dans le carrousel.</p></li>
            <li><p><strong>Marge de diapositive</strong> - la marge de l'élément dans le carrousel.</p></li>
            <li><p><strong>Défilement automatique</strong> - le défilement automatique.</p></li>
            <li><p><strong>Vitesse du carrousel</strong> - la vitesse de changement d'éléments.</p></li>
            <li><p><strong>Pause</strong> - la pause entre les défilements (si 'Défilement automatique' est activé).</p></li>
            <li><p><strong>Aléatoire</strong> - le carrousel commence par l'élément aléatoire.</p></li>
            <li><p><strong>Boucle de carrousel</strong> - le boucle: quand tous les objets du carrousel ont été affichés, le carrousel revient au début.</p></li>
            <li><p><strong>Masquer le contrôle à la fin</strong> - masquer les boutons de contrôle (Suivant/Précédent).</p></li>
            <li><p><strong>Pager</strong> - afficher la pagination.</p></li>
            <li><p><strong>Contrôle</strong> - afficher les contrôles Suivant/Précédent.</p></li>
            <li><p><strong>Contrôle automatique</strong> - afficher les contrôles (Lecture/Pause).</p></li>
            <li><p><strong>Hover automatique</strong> - une pause lors du survol des éléments du carrousel.</p></li>
        </ul>
    </li>
</ul>
<h4>Ajout d'un nouvel onglet/bloc</h4>
<p>'Onglet" est un bloc qui affiche les produits en position displayHomeTabContent.</p>
<p>Ce bloc permet de sélectionner des produits en position displayHome.</p>
<ul>
    <li>
        <p>Pour ajouter un nouvel onglet ou un bloc, cliquez sur le bouton 'Ajouter un élément'.</p>
        <p>Vous verrez la fenêtre où vous pouvez choisir les options suivantes:</p>
        <ul class="marked-list">
            <li><p><strong>Choisir une catégorie</strong> - choisissez une catégorie dont les produits seront affichés.</p>
                <figure class="img-polaroid">
                    <img src="img/tmcategoryproducts013-5.png" alt=""/>
                </figure>
            </li>
            <li>
                <p><strong>Nombre de produits à afficher</strong> - le nombre d'éléments à afficher.</p>
                <figure class="img-polaroid">
                    <img src="img/tmcategoryproducts013-6.png" alt=""/>
                </figure>
            </li>
            <li>
                <p><strong>Mode</strong> - Mode onglet/bloc.</p>
                <figure class="img-polaroid">
                    <img src="img/tmcategoryproducts013-7.png" alt=""/>
                </figure>
            </li>
            <li>
                <p><strong>Etat</strong> - permet d'activer/désactiver l'onglet ou le bloc.</p>
                <figure class="img-polaroid">
                    <img src="img/tmcategoryproducts013-8.png" alt=""/>
                </figure>
            </li>
        </ul>
        <p>Après avoir choisi les paramètres, cliquez sur le bouton 'Enregistrer'. L'onglet/bloc sera enregistré et apparaîtra dans la liste des onglets/blocs.</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts013-9.png" alt=""/>
        </figure>
    </li>
</ul>
<h4>Édition des onglets/blocs</h4>
<p>Pour modifier l'onglet ou le bloc, trouvez-le dans la liste et appuyez sur le bouton 'Editer' (1). <br>
Les options sont identiques à celles de la création d'un bloc ou d'un onglet.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryproducts013-10.png" alt=""/>
</figure>
<h4>Suppression des onglets/blocs</h4>
<p>Pour supprimer l'onglet ou le bloc, trouvez le bouton 'Supprimer' sous le bouton 'Editer'.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryproducts013-11.png" alt=""/>
</figure>
