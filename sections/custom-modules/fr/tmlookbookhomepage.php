<p class="alert alert-warning">Pour utiliser le module, vous devez installer le module TM LookBook.</p>
<p>LookBook Homepage TM est utilisé pour afficher le bloc du module TM LookBook sur la page d'accueil.
Ce module permet d'afficher à la fois les couvertures lookbook et les pages lookbook.</p>
<h5>Exigences:</h5>
<ul class="marked-list">
    <li>PHP 5.4+</li>
    <li>PrestaShop 1.6</li>
    <li>Module TM LookBook </li>
</ul>
<h5>Caractéristiques du module:</h5>
<ul class="marked-list">
    <li>Le module peut être affiché en 2 positions: displayHome et displayTopColumn.</li>
    <li>LookBook Homepage TM est un module enfant de TM LookBook, c'est pourquoi il ne peut être utilisé sans lui.</li>
    <li>The module can display the lookbook cover or its pages.</li>
</ul>
<h5>Panneau Principal de LookBook Homepage TM</h5>
<p>Au panneau principal du module, vous pouvez ajouter, supprimer et éditer des blocs, que le module affichera sur la page d'accueil. </p>
<figure class="img-polaroid">
    <img src="img/tmlookbookhomepage-main-panel.png" alt=""/>
</figure>
<h5>Ajout d'un nouveau bloc</h5>
<ul class="index-list">
    <li>
        <p>Pour ajouter un nouveau bloc, entrez le panneau principal du module et cliquez sur '+' (1) dans la table avec le crochet nécessaire.</p>
        <figure class="img-polaroid">
            <img src="img/tmlookbookhomepage-add-block-1.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Dans la forme apparue, configurez les options suivantes:</p>
        <ul class="marked-list">
            <li><strong>Etat</strong> - l'état du bloc. Ce paramètre permet d'activer ou de désactiver l'affichage du bloc.</li>
            <li><strong>Type</strong> - permet de sélectionner le type de bloc.</li>
            <li><strong>Lookbook</strong> - permet de sélectionner un look précédemment sélectionné.</li>
        </ul>
    </li>
    <li>
        <p>Après avoir configuré toutes les options, appuyez sur le bouton 'Enregistrer' (2).</p>
        <figure class="img-polaroid">
            <img src="img/tmlookbookhomepage-add-block-2.png" alt=""/>
        </figure>
    </li>
</ul>
<p>Une fois le nouveau bloc est créé, il sera affiché sur la page d'accueil.</p>