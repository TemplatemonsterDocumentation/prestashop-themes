<h3>Smart Blog</h3>
<p><strong>Smart blog -</strong> un ensemble de modules qui permet de créer un blog dans votre magasin. Il se compose des modules suivants:</p>
<ul class="marked-list">
    <li>Smart Blog</li>
    <li>Flux RSS Smart Blog</li>
    <li>Archive Smart Blog</li>
    <li>Catégories Smart Blog</li>
    <li>Dernières Nouvelles de Home Smart Blog</li>
    <li>Derniers Commentaires Smart Blog</li>
    <li>Module AddThis Smart Blog</li>
    <li>Articles Populaires Smart Blog</li>
    <li>Articles Récents Smart Blog</li>
    <li>Articles Connexes Smart Blog</li>
    <li>Recherche Smart Blog</li>
    <li>Mots Clés Smart Blog</li>
</ul>
<p>Il y a aussi des crochets où les modules de blog peuvent apparaître sur le site, ils peuvent également être affichés dans les crochets standard (seulement des colonnes).</p>
<h4>Smart Blog</h4>
<p>C'est le module principal qui contient de nombreux paramètres pour la fonctionnalité du blog.
Après son installation, l'onglet du menu Blog apparaîtra dans le menu principal de votre panneau d'administration, où vous pouvez ajouter/supprimer/modifier les articles/catégories ou sélectionnez le type d'images et leurs dimensions utilisées dans les posts 
(les types et les dimensions des images de blog sont différents des paramètres d'images de produits dans votre magasin). Pour ajouter les paramètres frontend du module, il faut naviger à <strong>Smart Blog (Modules -> Fonctionnalités Front Office -> Smart Blog)</strong> et définir tous les paramètres.</p>
<h6>Description des paramètres:</h6>
<ul class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Titre Méta</dt>
            <dd>le titre de votre blog qui apparaîtra sur toutes ses pages.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Méta Keywords</dt>
            <dd>les mots-clés qui seront utilisés par les moteurs de recherche pour une meilleure optimisation SEO.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Méta Description</dt>
            <dd>la description de votre blog utilisé par les moteurs de recherche pour une meilleure optimisation SEO.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>URL Principale du Blog</dt>
            <dd>l'URL de votre blog.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Utilisez .html avec URL convivial</dt>
            <dd>activer/désactiver l'ajout de .html aux URL des pages du blog en utilisant les URL convivials.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Nombre de publications par page</dt>
            <dd>le nombre de messages affichés par page (catégories/pages d'archives, etc.).</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Commentaire auto-accepté</dt>
            <dd>activer/désactiver les commentaires auto-acceptés; si activé, les commentaires seront disponibles sans modération de l'administrateur.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Activer Captcha</dt>
            <dd>activer/désactiver Captcha sous le formulaire Laissez un commentaire sur les pages des produits.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Activer Commentaire</dt>
            <dd>activer/désactiver l'jout de commentaires par les utilisateurs.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Afficher Nom D'auteur</dt>
            <dd>activer/désactiver le nom de l'auteur sur les pages du blog et les pages uniques.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Afficher Vues du Message</dt>
            <dd>activer/désactiver le nombre de fois que les utilisateurs ont vu la publication. Il apparaîtra sous la publication sur les pages du blog et les pages uniques.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Afficher Style du Nom d'Auteur</dt>
            <dd>modifier le format du nom de l'auteur.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>AVATAR Image</dt>
            <dd>l'ajout de l'image d'avatar standard pour tous les utilisateurs.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Pas D'image</dt>
            <dd>activer/désactiver l'image. Aucune image est définie, s'il n'y a pas d'image pour publication / catégorie, etc..</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Afficher Catégorie/dt>
            <dd>activer/désactiver l'image et la description de la catégorie sur les pages de la catégorie de blog.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Réglage des Colonnes de la page du blog</dt>
            <dd>la configuration de l'affichage des modules liés au blog. Ll nécessite souvent des paramètres supplémentaires. Par conséquent, il est recommandé d'installer des modules supplémentaires de la même manière que les modules PrestaShop standards.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>CSS personnalisé</dt>
            <dd>Vous pouvez ajouter des styles supplémentaires dans ce champ. 
			Faites attention en utilisant les classes déjà utilisées dans le thème, car certains conflits peuvent se produire.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Supprimer Vieux Tumblr</dt>
            <dd>sert à régénérer les images du blog dans le cas où les dimensions de l'image ont été modifiées ou le nouveau type d'image a été ajouté. 
			Pour se régénérer, sélectionnez <strong>'Qui'</strong> et cliquez sur <strong>'Régénérer'</strong>.
            </dd>
        </dl>
    </li>
</ul>
