<h4>Flux RSS Smart Blog</h4>
<h6>Le module génère le nouveau flux pour votre blog.</h6>
<p>Il a plusieurs paramètres dans le panneau d'administration:</p>
<ul class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Période de mise à jour</dt>
            <dd>permet d'afficher des messages à partir d'une période spécifique 
			(heure/jour/semaine/mois/année).
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Fréquence de mise à jour</dt>
            <dd>permet de définir la fréquence de mise à jour
			(1 semaine, 2 heures, etc.).
            </dd>
        </dl>
    </li>
</ul>
<p>Il existe un chemin de fichier en haut où les nouvelles seront générées.
 Il se compose de l'URL du site Web + /modules/smartblogfeed/rss.php.</p>
