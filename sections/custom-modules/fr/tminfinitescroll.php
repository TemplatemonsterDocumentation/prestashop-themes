<h5>Configuration du module</h5>
<ul class="index-list">
    <li>Pages - pages, où le module est censé fonctionner.</li>
    <li>Chargement automatique - si cette option est activée, les produits seront automatiquement chargés sur le défilement. Sinon, si l'option est désactivée, vous verrez le bouton 'Afficher plus' et les produits seront chargés, lorsque vous appuyez sur ce bouton.</li>
    <li>Décalage - cette option s'affiche lorsque 'Chargement automatique' est activé. Il permet de déterminer le décalage avant la zone de chargement des produits (ou après - si la valeur est négative).</li>
    <li>Pagination - cette option s'affiche lorsque 'Chargement automatique' est désactivé. Il permet d'ajouter une pagination à la page des produits.</li>
    <li>Display 'Show all' button - cette option s'affiche lorsque 'Chargement automatique' est désactivé. Après avoir appuyé sur ce bouton, tous les produits apparaissent.</li>
    <li>Préchargeur  - Dans ce champ, vous pouvez choisir une image de préchargeur qui sera affichée pendant le chargement des produits.</li>
</ul>