<h3>Défilement Infini TM</h3>
<p>Avec l'aide de ce module, vous pouvez ajouter des produits AJAX aux pages avec un bloc d'annonces. 
La fonctionnalité du module permet de configurer le chargement automatique des produits sur le défilement ou lorsque vous appuyez sur le bouton 'Afficher plus'.
En outre, le module fonctionne avec les filtres de produits par défaut de PrestaShop.</p>
