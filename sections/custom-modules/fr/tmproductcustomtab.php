<h3>Onglet Personnalisé du Produit TM</h3>
<p>Le module sert à créer un onglet supplémentaire sur la page du produit pour afficher des informations supplémentaires.
   Le module présente un avantage par rapport à la fonctionnalité existante, en raison de la capacité d'ajouter des images, du texte, de la vidéo, des liens, etc..</p>
<h4>Installation et suppression</h4>
<p>Le module peut être installé et supprimé comme tout autre module PrestaShop.</p>
<h4>Paramètres du module</h4>
<p>Après l'installation du module, vous pouvez ajouter de nouveaux onglets pour les produits à la page de configuration du module, ainsi que sur la page d'édition du produit.</p>
<h5>Ajout d'onglets dans la configuration du module:</h5>
<p>Pour ajouter un nouvel onglet, appuyez sur le bouton 'Ajouter un Nouveau' (1).</p>
<figure class="img-polaroid">
    <img src="img/tmcustomproducttab-1.png" alt=""/>
</figure>
<ul class="index-list">
    <li>
        <p>Ajout d'un onglet à un ou plusieurs produits d'une catégorie précédemment sélectionnée.</p>
        <figure class="img-polaroid">
            <img src="img/tmcustomproducttab-4.png" alt=""/>
        </figure>
        <ul class="marked-list">
            <li><strong>Titre D'onglet</strong> - le nom de l'onglet, requis.</li>
            <li><strong>Description D'onglet</strong> - les informations sur l'onglet, il peut inclure des données au format d'une image, texte, vidéo, liens, etc..</li>
            <li><strong>Etat</strong> - l'état de l'élément.</li>
            <li><strong>Paramètres Aupplémentaires</strong> - activation/désactivation des paramètres supplémentaires.</li>
            <li><strong>Choisir une Catégorie</strong> - le choix des catégories pour lesquelles l'onglet doit être affiché.</li>
            <li><strong>Produits à Afficher</strong> - la sélection des produits des catégories précédemment sélectionnés.</li>
        </ul>
    </li>
    <li>
        <p>Ajout de l'onglet aux produits appartenant aux catégories sélectionnées.</p>
        <figure class="img-polaroid">
            <img src="img/tmcustomproducttab-3.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Pour ajouter un onglet à tous les produits, désactivez l'option 'Paramètres Supplémentaires'.</p>
        <figure class="img-polaroid">
            <img src="img/tmcustomproducttab-2.png" alt=""/>
        </figure>
    </li>
</ul>
<h5>Modification de la position d'affichage de l'onglet</h5>
<p>Faites glisser et déposez l'onglet nécessaire pour changer sa position.</p>
<figure class="img-polaroid">
    <img src="img/tmcustomproducttab-5.png" alt=""/>
</figure>
<h5>Ajout d'onglets dans les paramètres du produit:</h5>
<p>Accédez à la page du produit et cliquez sur le lien 'Onglet Personnalisé du Produit TM' dans le menu de gauche.</p>
<figure class="img-polaroid">
    <img src="img/tmcustomproducttab-6.png" alt=""/>
</figure>
<p>Si l'option 'Paramètres Supplémentaires' est désactivée, l'onglet sera ajouté au produit qui a été édité plus tôt.
Vous pouvez également ajouter l'onglet aux produits qui appartiennent à certaines catégories, ou vous pouvez l'ajouter à plusieurs produits appartenant à la catégorie précédemment sélectionnée.</p>
<p>Pour changer la position d'affichage des onglets, faites glisser-déposer les blocs. Vous pouvez également modifier ou supprimer des onglets.</p>
