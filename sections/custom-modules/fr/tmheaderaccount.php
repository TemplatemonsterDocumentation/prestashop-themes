<h3>Header Compte TM</h3>
<p>Ce module affiche le formulaire de connexion et les éléments de compte de l'utilisateur disponibles dans l'en-tête du site.
Il permet de se connecter à votre compte en restant sur la page active.
Le module affiche sous la forme d'un bloc déroulant.</p>
<h5>Installation et suppression du module</h5>
<p>Le module est installé et supprimé comme tout autre module PrestaShop.</p>
<h5>Configuration du module</h5>
<p>Le module n'a pas de paramètres supplémentaires.</p>
