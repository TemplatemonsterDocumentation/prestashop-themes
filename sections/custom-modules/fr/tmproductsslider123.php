<h3>Slider de Produits TM 1.2.3</h3>
<p>Le module affiche les produits en magasin sous forme de curseur.</p>
<h4>Installation et suppression</h4>
<p>Le module peut être installé et supprimé comme tout autre module Prestashop.</p>
<p>Après l'installation, vous pouvez modifier ses paramètres sur la page de configuration du module.</p>
<p>Vous pouvez définir l'ordre des produits dans votre slider en utilisant la méthode de glisser-déposer. 
Vous pouvez également activer/désactiver l'affichage du produit sur les diapositives.</p>
<p>Vous pouvez définir la largeur du curseur, l'effet de glissement, la vitesse, la pause, la lecture automatique,
la pause en faisant passer votre souris sur les éléments du carrousel, le contrôle de glissière, les boutons suivant/précédent, les boutons de lecture/pause.
</p>
<figure class="img-polaroid">
    <img src="img/tmproductsslider-1.png" alt="">
</figure>
<p>Après l'installation du module, vous verrez le nouvel onglet dans la page de configuration de votre produit
    (Catalogue -> Produit -> sélectionnez un produit -> Slider de Produits TM).</p>
<p>Ici, vous pouvez définir si le produit apparaîtra dans le slider ou non.
 Après avoir effectué les modifications, appuyez sur le bouton Enregistrer/Enregistrer & Rester pour le sauvegarder.</p>
<p>Le module supporte aussi les multi-magasins. Vous pouvez modifier les paramètres pour chaque magasin, groupe de magasins ou tous les magasins à la fois.</p>
