<h3>TM Google Map 1.1.5</h3>
<p>Ce module permet d'ajouter Google Map avec les emplacements des magasins et leur brève description sur votre première page.
La carte affichera les adresses de tous les magasins liés à votre boutique en ligne.</p>
<h5>Installation et suppression</h5>
<p>Le module est installé et supprimé de la même manière que tout autre module PrestaShop.</p>
<h5>Paramètres du module</h5>
<p>Après l'installation du module, vous pouvez ajouter des magasins à la carte.
Après avoir appuyé sur le bouton "Ajouter Nouveau Magasin" (1),Vous serez redirigé vers l'onglet 'Contacts de magasin' où vous pouvez éditer vos magasins.</p>
<p class="img-polaroid"><img src="img/tmgooglemap115-1.png" alt="" /></p>
<p>Pour ajouter un nouveau magasin, appuyez sur le bouton 'Ajouter Un Nouveau'.</p>
<p class="img-polaroid"><img src="img/tmgooglemap115-2.png" alt="" /></p>
<ul class="marked-list">
    <li><strong>Sélectionnez un Magasin</strong> - sélectionnez le magasin que vous souhaitez ajouter à la carte.</li>
    <li><strong>Etat</strong> - l'état de l'élément.</li>
    <li><strong>Marqueur</strong> - l'ajout d'un marqueur personnalisé.</li>
    <li><strong>Texte personnalisé</strong> - l'ajout d'un texte personnalisé.</li>
</ul>
<h5>Paramètres d'affichage Google Map</h5>
<p class="img-polaroid"><img src="img/tmgooglemap115-3.png" alt="" /></p>
<ul class="marked-list">
    <li><strong>Style de Carte</strong> - sélectionnez le style d'affichage de la carte.</li>
    <li><strong>Type de Carte</strong> - sélectionnez le type d'affichage de la carte.</li>
    <li><strong>Niveau de Zoom</strong> - spécifiez le niveaudu zoom carte (de 1 à 17).</li>
    <li><strong>Zoom sur Défilement</strong> - activer/désactiver le zoom de la carte sur le défilement.</li>
    <li><strong>Contrôles de Carte</strong> - activer/désactiver les contrôles de la carte.</li>
    <li><strong>Street view</strong> - activer/désactiver l'outil Street View.</li>
    <li><strong>AMarqueur d'animation</strong> - activer/désactiver le marqueur d'animation.</li>
    <li><strong>Popup</strong> - activer/désactiver les fenêtres contextuelles avec des informations sur les magasins.</li>
</ul>