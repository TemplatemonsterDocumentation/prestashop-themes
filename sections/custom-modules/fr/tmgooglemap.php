<h3>TM Google Map</h3>
<p>Ce module permet d'ajouter la carte Google avec les emplacements de vos magasins et leur brève description sur votre première page. 
La carte affichera les adresses de tous les magasins liés à votre boutique en ligne. Vous pouvez modifier les détails de vos magasins dans l'onglet Préférences -> Contacts de magasin.</p>
<h5>Installation et suppression</h5>
<p>Le module est installé et supprimé de la même manière que tout autre module PrestaShop.</p>
<h5>Paramètres du module</h5>
<p>Après l'installation, vous pouvez modifier ses paramètres sur la page de configuration du module.</p>
<p>Vous pouvez configurer le style et les éléments de votre carte.</p>
<ul class="index-list"> 
    <li>Sélectionnez le style de carte dans le premier champ (tous les styles sont tirées des <a
        target="_blank" href="https://snazzymaps.com/">Snazzymaps</a>). Vous pouvez aussi ajouter le style à partir de ce site.
		Vous découvrirez comment les ajouter à la fin de cette instruction.</li>
    <li>Sélectionnez le type de vue dans le deuxième champ (Vue satellite ou Street view).</li>
    <li>Vous pouvez également sélectionner le niveau de zoom pour votre carte de 1 à 17.</li>
    <li>Option de zoom lors du défilement sur la zone de la carte.</li>
    <li>Vous pouvez désactiver le contrôle de la carte et la capacité de visualiser les rues.</li>
    <li>Le module supporte les multi-magasins. On peut changer le style de la carte pour chaque magasin.</li>
</ul>
