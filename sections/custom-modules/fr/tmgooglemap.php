<h5>Ajout de styles personnalisés</h5>
<p>Vous pouvez ajouter un style personnalisé à partir <a target="_blank" href="https://snazzymaps.com/">Snazzymaps</a>. 
Pour ce faire, vous devez choisir le style du site, copiez JavaScript Style Array dans le fichier et sauvegardez-le dans le dossier du module /js/styles/
(pour le nom du fichier, utilisez le nom du style en remplaçant les espaces par des caractères de soulignement et en supprimant tous les caractères spéciaux. 
Par exemple: pour le style Subtle Grayscale - le fichier subtle_grayscale.js, pour le style Maps-esque - le fichier apple_maps-esque.js).
Ensuite, allez à la page de configuration de votre module et sélectionnez le style requis dans la liste déroulante des styles.</p>