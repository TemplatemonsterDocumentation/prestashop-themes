<p>Ce module vous permet d'afficher des catégories avec des produits, des bannières, des vidéos (Youtube, Vimeo et une vidéo personnalisée), de contenu HTML, des divers sliders avec les nombreuses diapositives sur la page d'accueil.
</p>
<h5>Installation et suppression</h5>
<p>Le module est installé et supprimé comme tout autre module Prestashop.</p>
<h5>Paramètres du module</h5>
<p>Une fois le module installé, vous pouvez ajouter les blocs requis avec les produits de catégorie, les bannières, les vidéos et le contenu HTML précédemment ajoutés.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-1.png" alt="" /></figure>
<h5>Ajout/suppression/édition de blocs</h5>
<p>Pour ajouter une nouvelle catégorie, appuyez sur le bouton 'Ajouter un nouveau'.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-2.png" alt="" /></figure>
<ul class="marked-list">
    <li><strong>Choisir une catégorie</strong> - choisissez une catégorie pour ajouter des produits, des bannières, des vidéos et du contenu HTML.</li>
    <li><strong>Utilisez le nom personnalisé</strong> - l'état de catégorie personnalisée.</li>
    <li><strong>Nom personnalisé</strong> - le titre de catégorie.</li>
    <li><strong>Etat</strong> - l'état de l'élément.</li>
    <p>Pour ajouter la nouvelle ligne, appuyez sur le bouton 'Ajouter Ligne'. Ensuite, choisissez l'une des huit dispositions présentées dans la fenêtre suivante.</p>
</ul>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-3.png" alt="" /></figure>
<p>Pour ajouter la disposition, cliquez sur l'une des huit options pour la sélectionner, et elle sera ajoutée au formulaire.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-4.png" alt="" /></figure>
<p>Pour ajouter des éléments, cliquez sur l'une des cellules de disposition. Vous verrez cinq boutons, énumérant les éléments disponibles.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-5.png" alt="" /></figure>
<p>Après avoir appuyé sur l'un des boutons, vous verrez le formulaire pour sélectionner l'élément de type choisi.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-6.png" alt="" /></figure>
<p>Après avoir appuyé sur l'un des éléments, il sera ajouté à la cellule de disposition. Vous pouvez la remplir avec différents blocs de cette façon.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-7.png" alt="" /></figure>
<p>Après avoir enregistré la catégorie avec les disposition choisies, elle apparaîtra sous la forme de catégories ajoutées.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-8.png" alt="" /></figure>
<p>Pour supprimer l'élément, appuyez sur le bouton 'Supprimer'.</p>
<h5>Ajout/suppression/édition de bannière</h5>
<p>Pour ajouter une nouvelle bannière, cliquez sur le bouton 'Ajouter une nouvelle bannière'. Ensuite, remplissez les champs requis dans la forme qui apparaît.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-9.png" alt="" /></figure>
<ul class="marked-list">
    <li><strong>Choisir un fichier</strong> - choisissez un fichier, ce champ est requis (au moins pour la langue par défaut).</li>
    <li><strong>Titre</strong> - le titre de la bannière, ce champ est requis (au moins pour la langue par défaut).</li>
    <li><strong>Url</strong> - le chemin de lien, ce champ est requis (au moins pour la langue par défaut).</li>
    <li><strong>Description</strong> - la description de la bannière (au moins pour la langue par défaut).</li>
    <li><strong>Classe spécifique</strong> - la classe CSS du bloc.</li>
</ul>
<h5>Ajout/suppression/édition de vidéo</h5>
<p>Pour ajouter une nouvelle vidéo, cliquez sur le bouton 'Ajouter une nouvelle vidéo'. Ensuite, remplissez les champs requis dans la forme qui apparaît.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-10.png" alt="" /></figure>
<ul class="marked-list">
    <li><strong>Entrer Nom de Vidéo</strong> - le titre de vidéo, ce champ est requis (au moins pour la langue par défaut).</li>
    <li><strong>Lien de Vidéo/Chemin</strong> - le lien vers la vidéo (youtube, vimeo ou la vidéo personnalisée), ce champ est requis (au moins pour la langue par défaut).</li>
    <li><strong>Autoriser Lecture Automatique</strong> - définit si la vidéo démarre automatiquement, juste après le chargement du lecteur.</li>
    <li><strong>Autoriser Contrôles</strong> - définit si les contrôles du lecteur sont affichés.</li>
    <li><strong>Boucle</strong> - définit si la lecture vidéo est dans la boucle infinie.</li>
</ul>
<p>Pour ajouter une vidéo personnalisée, appuyez sur le bouton 'Fichier'. Ensuite, serez en mesure d'ajouter une vidéo via le gestionnaire de fichiers.</p>
<h5>Ajout/suppression/édition de contenu HTML</h5>
<p>Pour ajouter un nouveau contenu HTML, appuyez sur le bouton 'Ajouter un nouveau code HTML'. Ensuite, remplissez les champs requis dans la forme qui apparaît.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-11.png" alt="" /></figure>
<ul class="marked-list">
    <li><strong>Entrez Nom D'élément HTML</strong> - le titre de contenu HTML, ce champ est requis (au moins pour la langue par défaut).</li>
    <li><strong>Classe spécifique</strong> -la classe CSS du bloc.</li>
    <li><strong>Contenu HTML</strong> - le contenu.</li>
</ul>
<h5>Ajout/suppression/édition de slider</h5>
<p>Pour ajouter un nouveau slider, appuyez sur le bouton 'Ajouter un nouveau slider'. Ensuite, remplissez les champs requis dans la forme qui apparaît</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-12.png" alt="" /></figure>
<ul class="marked-list">
    <li><strong>Entrez Nom de slider</strong> - le titre slider, ce champ est obligatoire (au moins pour la langue par défaut).</li>
    <li><strong>Contrôle</strong> - afficher/masquer la navigation du slider.</li>
    <li><strong>Pager</strong> - afficher/masquer la pagination du curseur.</li>
    <li><strong>Défilement Automatique</strong> - active/désactive la lecture automatique du slider.</li>
    <li><strong>Classe spécifique</strong> -la classe CSS du bloc.</li>
    <li><strong>Vitesse du Carrousel</strong> - la durée de transition des diapositives (en ms), ce champ est requis.</li>
    <li><strong>Pause</strong> - la période entre la commutation des diapositives (en ms), ce champ est requis.</li>
</ul>
<h5>Ajout/suppression/édition de diapositives</h5>
<p>Pour ajouter une nouvelle diapositive, appuyez sur le bouton 'Afficher' dans le formulaire de curseur, puis appuyez sur le bouton 'Ajouter un nouveau' dans une fenêtre qui s'affiche. Ensuite, remplissez les champs requis.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-13.png" alt="" /></figure>
<ul class="marked-list">
    <li><strong>Type</strong> - choisissez le type de diapositive, ce champ est requis.</li>
    <li><strong>Liste de Bannière/Vidéo/HTML</strong> - choisissez l'élément du type précédemment sélectionné, ce champ est requis.</li>
    <li><strong>Ordre de Tri</strong> - le position de la diapositive.</li>
    <li><strong>Etat D'élément</strong> - l'état de l'élément.</li>
</ul>