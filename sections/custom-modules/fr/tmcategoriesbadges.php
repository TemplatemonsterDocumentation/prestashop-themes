<h3>Badges  de Catégories TM</h3>
<p>Ce module permet d'ajouter des badges aux catégories de la barre latérale du module par défaut Catégories ("Blockcategories"). 
Les badges peuvent être ajoutés à la catégorie de tout niveau et toute langue.
Le champ de texte de l'étiquette est obligatoire et doit être rempli dans toutes les langues.
Si ce champ est laissé vide, il prend automatiquement le texte de la langue par défaut.</p>
<h5>Installation et suppression du module</h5>
<p>Le module est installé et supprimé comme tout autre module PrestaShop.</p>
<h5>Configuration du module</h5>
<p>Après l'installation, vous pouvez modifier ses paramètres sur la page de configuration du module.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoriesbadges-1.png" alt=""/>
</figure>
<p>Vous pouvez ajouter des badges à n'importe quelle catégorie après l'avoir sélectionné dans la liste déroulante.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoriesbadges-2.png" alt=""/>
</figure>
<p class="alert alert-warning"><span> Attention! Vous pouvez ajouter un seul badge pour chaque catégorie.</span></p>
<p>Ensuite, vous pouvez ajouter du texte qui apparaîtra dans le badge et sélectionner le style dans la liste déroulante (les styles peuvent varier dans les différents modèles).</p>
<p>Puis appuyez sur le bouton 'Enregistrer' (1).</p>
<figure class="img-polaroid">
    <img src="img/tmcategoriesbadges-3.png" alt=""/>
</figure>

<p>Vous pouvez voir la liste des badges existants ci-dessous. Ici, vous pouvez éditer ou supprimer des badges avec les boutons correspondants.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoriesbadges-4.png" alt=""/>
</figure>
<p>Le module est multilangue et prend en charge les multi-magasins. Vous pouvez ajouter des badges pour un groupe de magasins ou tous les magasins à la fois. Ils apparaissent dans la liste des éléments séparés.</p>
