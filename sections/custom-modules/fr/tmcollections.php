<h6>Le module Collections TM est créé pour permettre aux visiteurs du magasin d'ajouter des produits aux collections et partager ces collections sur Facebook.</h6>
<h4>Installation du module</h4>
<p>Pour installer le module, accédez au panneau Administration  -> Modules et Services -> Modules et Services et trouver le module > Collections TM (2), puis cliquez sur le bouton 'Installer' (3).</p>
<figure class="img-polaroid">
    <img src="img/tmcollections1.png" alt=""/>
</figure>
<h4>Configuration du module</h4>
<p>Une fois le module installé, la page 'Paramètres' apparaîtra.<br>
   Ajoutez l'ID de l'application Facebook au champ requis (1) et appuyez sur le bouton 'Enregistrer' (2).<br>
   Les instructions sur la façon d'obtenir ID de l'application Facebook peuvent être trouvées ici: <a
        href="https://developers.facebook.com/docs/apps/register" target="blank">Creating an App ID</a>.</p>
<figure class="img-polaroid">
    <img src="img/tmcollections2.png" alt=""/>
</figure>
<h4>Utilisation du module Collections TM</h4>
<h5>Ajout de produit à la collection</h5>
<p class="alert alert-info">Les visiteurs du magasin doivent être connectés pour pouvoir ajouter les éléments aux collections.</p>
<ul class="index-list">
    <li>
        <p>Une fois le module installé, le bouton 'Ajouter à la collection' apparaît sur chaque page de produit. 
		Il devrait ressembler à la capture d'écran ci-dessous. (1).</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections3.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Après avoir appuyé sur ce bouton, un visiteur du magasin verra une notification: 'Le produit a été ajouté avec succès à votre collection.' 
		Il peut être fermé ou ignoré en cliquant sur le bouton 'Mes collections' (2).</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections4.png" alt=""/>
        </figure>
    </li>
</ul>
<h5>Parcourir les collections</h5>
<ul class="index-list">
    <li>
        <p>Dans la page "Mes collections", un client peut ajouter la nouvelle collection (1) ou afficher la liste des créés (2).</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections5.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>En cliquant sur le bouton 'Afficher la collection' (1), vous ouvrirez la page de la collection, le bouton 'Afficher les produits' (2) permet de voir la liste des produits de cette collection sur la page actuelle. <br>
           Le titre de la collection peut être modifié (3) ou supprimé (4). <br>
           Pour partager la collection, le visiteur doit appuyer sur le bouton 'Partager' (5).</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections6.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Tout en regardant les éléments de la collection, après avoir cliqué sur le bouton 'Afficher les produits',
		chaque élément peut être retiré de la liste (1).</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections7.png" alt=""/>
        </figure>
    </li>
</ul>
<h5>Partager des collections</h5>
<p>Pour partager la collection, le visiteur doit appuyer sur le bouton 'Partager'. 
Il y aura quelques étapes simples à suivre avant de publier la collection.
Au cours de chaque étape, il est possible de retourner en arrière et de modifier les options de partage.</p>
<ul class="index-list">
    <li>
        <p>La première étape consiste à choisir la disposition pour la collection qui sera affichée.</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections8.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Ensuite, les produits devraient être ajoutés. Chaque cellule avec le signe '+' est un bouton pour ajouter l'image du produit.</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections9.png" alt=""/>
        </figure>
        <p>Choisissez les images de chaque produit:</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections10.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Lorsque toutes les images sont ajoutées, le visiteur doit appuyer sur le bouton 'Partager'(1).</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections11.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Dans une petite fenêtre apparu, il y aura des options d'affichage standard pour choisir et la possibilité d'ajouter du texte à la publication (1).Après avoir cliqué sur le bouton 'Partager sur Facebook' (2), la publication sera publiée.</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections12.png" alt=""/>
        </figure>
    </li>
</ul>
<h5>Statistiques</h5>
<p>En utilisant ce module, vous pouvez également obtenir les statistiques collectées sur les produits sont ajoutés aux collections. 
Ainsi, vous saurez quels produits sont actuellement populaires. Cette information peut être très utile pour suggérer un reduction par exemple. <br>
Vous pouvez afficher les statistiques en naviguant vers le panneau d'administration -> Stats -> Stats -> Collections TM. <br>
</p>
<figure class="img-polaroid">
    <img src="img/tmcollections13.png" alt=""/>
</figure>
<ul class="marked-list">
    <li>Vous pouvez sélectionner la période de visualisation des statistiques à partir du moment spécifié (1).</li>
    <li>Dans la première colonne, il existe plusieurs ajouts de produit (2).</li>
    <li>La deuxième colonne est créée pour votre commodité. Vous pouvez l'utiliser pour voir combien de produits ajoutés ont été vendus (3).
    </li>
</ul>