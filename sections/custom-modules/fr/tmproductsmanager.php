<h3>Gestionnaire de Produits TM</h3>
<h4>Introduction</h4>
<p>Le module est créé pour gérer les magasins en ligne, en particulier les magasins avec une grande quantité de produits. En utilisant ce module, vous pouvez facilement filtrer vos produits et effectuer différents changements.</p>
<h4>Installation et suppression du module</h4>
<p>Le module est installé et supprimé comme tout autre module Prestashop.</p>
<p class="alert alert-info">
    <span>L'enlèvement ou la réinstallation du module ne réinitialisera pas les modifications que vous avez effectuées en l'utilisant.</span>
</p>
<p>Une fois le module a été installé avec succès, accédez à Catalogue -> Gestionnaire de produits TM (1) pour voir le menu principal du module.</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-1.png" alt=""/></figure>
<h4>Performance</h4>
<p>Le menu principal du module est divisé en deux parties principales 'Choisir la section' (1) and 'Choisir l'option' (2).</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-2.png" alt=""/></figure>
<p>La première partie est créée sous la forme d'onglets - Information, Prix, Associations et Quantité.</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-3.png" alt=""/></figure>
<ul class="marked-list">
    <li><strong>Information</strong> - les options concernant la disponibilité et la description des produits.</li>
    <li><strong>Prix</strong> - les paramètres des prix et des règles fiscales.</li>
    <li><strong>Associations</strong> -la possibilité de changer la marque, la catégorie ou les accessoires de plusieurs produits.</li>
    <li><strong>Quantité</strong> - le changement de quantité de produits.</li>
</ul>
<ul class="index-list">
    <li>
        <p>Ouvrez chaque onglet pour voir les options disponibles dans cette section. Ils seront décrits plus bas.<br>
            Chaque option a un soupçon - une brève description, qui apparaît à droite lorsque vous passez la souris sur l'option(1).</p>
        <figure class="img-polaroid"><img src="img/tmproductsmanager-4.png" alt=""/></figure>
    </li>
    <li>
        <p>Choisissez l'option nécessaire et appuyez.</p>
    </li>
    <li>
        <p>L'étape suivante est le filtrage des produits. Définissez les options de filtrage selon vos besoins et cliquez sur le bouton 'Sélectionner' (1). Pour effacer les filtres sélectionnés, cliquez sur le bouton 'Réinitialiser' (2).</p>
        <figure class="img-polaroid"><img src="img/tmproductsmanager-5.png" alt=""/></figure>
         Cliquez simplement sur la croix (1) près de la valeur de la propriété du produit et elle sera supprimée.</p>
        <figure class="img-polaroid"><img src="img/tmproductsmanager-6.png" alt=""/></figure>
    </li>
    <li>
        <p>Au-dessous des filtres, il existe une liste de produits correspondant aux critères sélectionnés. Vous pouvez le modifier si vous devez supprimer certains produits de la liste. Chaque produit peut être retiré séparément en cliquant sur le bouton 'Supprimer de la liste' (1). Pour supprimer tous les produits de la liste, appuyez sur les boutons 'Tout sélectionner' (2) et 'Supprimer sélectionné' (3). Si vous avez coché un grand nombre de produits et que vous souhaitez les décocher, cliquez sur le bouton 'Tout déselectionner' (4).</p>
        <figure class="img-polaroid"><img src="img/tmproductsmanager-7.png" alt=""/></figure>
    </li>
    <li>
        <p>Lorsque la liste des produits sélectionnés se conforme toutes les exigences, cliquez sur le bouton 'Processer' (1).</p>
        <figure class="img-polaroid"><img src="img/tmproductsmanager-8.png" alt=""/></figure>
    </li>
    <li>
        <p>Réglez l'option/options selon vos besoins et appuyez sur le bouton 'Enregistrer' (1). Si avez besoin de faire quelques modifications dans la liste des produits, appuyez sur le bouton 'Retour au filtre' (2).</p>
        <p class="alert alert-info">
            <span>Veuillez noter que vous ne pourrez pas annuler ces changements de la même manière.</span>
        </p>
        <figure class="img-polaroid"><img src="img/tmproductsmanager-9.png" alt=""/></figure>
    </li>
</ul>

<h4>Options disponibles</h4>
<h5>Information</h5>
<p>La section permet de configurer les options suivantes:</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-10.png" alt=""/></figure>
<ul class="marked-list">
    <li><strong>Activer/Désactiver les produits</strong> -  définissez l'état ON/OFF de vos produits.</li>
    <li><strong>Disponible pour la commande</strong> - rendre certains produits disponibles ou indisponibles pour la commande.
        <p class="alert alert-warning small">
            <span>REMARQUE: si 'Disponible pour la commande' est réglé sur 'OUI' pour les produits avec des prix cachés, leurs prix apparaîtront automatiquement à nouveau.</span>
        </p>
    </li>
    <li><strong>Afficher le prix</strong> - afficher ou masquer le prix du produit.
        <p class="alert alert-warning small">
            <span>REMARQUE: si vous définissez 'Afficher le prix' à 'NON' pour les produits, qui sont disponibles pour la commande, ils deviendront automatiquement indisponibles.</span>
        </p>
    </li>
    <li><strong>En ligne seulement</strong> - appliquez 'En ligne seulement' aux éléments sélectionnés.</li>
    <li><strong>Condition</strong> - modifiez l'état de plusieurs produits.</li>
    <li><strong>Brève description</strong> - ajoutez une brève description qui devrait s'appliquer à tous les produits sélectionnés.</li>
    <li><strong>Description complète</strong> - ajoutez une description aux produits sélectionnés avec la possibilité de le placer avant la description principale, après, ou remplacez la description complète par la nouvelle.
</li>
    <li><strong>Tags</strong> - ajoutez plus de tags aux produits choisis.</li>
</ul>

<h5>Prix</h5>
<p>dans cette section, vous pouvez modifier les options concernant le prix du produit.</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-11.png" alt=""/></figure>
<ul class="marked-list">
    <li><strong>Afficher 'En vente'</strong> - afficher les éléments sélectionnés avec la marque 'En vente'.</li>
    <li><strong>Modifier les règles fiscales</strong> - choisissez les règles fiscales qui s'appliqueront à plusieurs produits.</li>
    <li><strong>Ajouter un nouveau prix spécifique</strong> - définissez le prix spécial pour certains produits avec la possibilité d'ajouter la période d'activation du prix.</li>
</ul>

<h5>Associations</h5>
<p>Ici, vous pouvez modifier les options liées aux relations avec les produits.</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-12.png" alt=""/></figure>
<ul class="marked-list">
    <li><strong>Changer la marque</strong> - changer la marque des produits sélectionnés.</li>
    <li><strong>Changer la catégorie par défaut</strong> - choisissez la catégorie qui sera définie comme défaut pour certains produits.
        <p class="alert alert-warning small">
            <span>REMARQUE: vous ne pouvez choisir qu'une seule catégorie par défaut. Veuillez noter que votre catégorie par défaut actuelle sera remplacée par la nouvelle.</span>
        </p>
    </li>
    <li><strong>Modifier les catégories connexes</strong> - définissez les catégories liées à plusieurs produits avec la possibilité d'enregistrer ou de supprimer des anciennes relations.</li>
    <li><strong>Ajouter des accessoires</strong> - ajouter les accessoires adaptés aux produits choisis.</li>
</ul>

<h5>Quantité</h5>
<p>Cette option permet de définir la quantité de produit ou d'augmenter/diminuer la quantité actuelle.</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-13.png" alt=""/></figure>
<ul class="marked-list">
    <li><strong>Changer la quantité</strong> -  définissez la quantité requise de certains produits.</li>
</ul>
