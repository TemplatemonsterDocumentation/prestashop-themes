<p>Ce module vous permet d'afficher la liste des produits avec le compte à rebours de réduction.
Vous pouvez également ajouter une minuterie aux produits de la page d'accueil, sur les pages de catégories et les pages des produits complets.
</p>
<h5>Installation et suppression</h5>
<p>Le module est installé et supprimé de la même manière que tout autre module PrestaShop.</p>
<h5>Paramètres du module</h5>
<p>Après l'installation du module, vous pouvez ajouter une réduction de produit avec la période d'affichage et un compte à rebours.</p>
<figure class="img-polaroid"><img src="img/tmdaydeal-scr-1.png" alt="" /></figure>
<h5>Ajout/suppression/édition de blocs</h5>
<ul class="index-list">
    <li>
        <p>Pour ajouter un nouveau produit, appuyez sur le bouton "Ajouter un Nouveau"</p>
        <figure class="img-polaroid"><img src="img/tmdaydeal-scr-2.png" alt="" /></figure>
        <ul class="marked-list">
            <li><strong>Choisir un Produit</strong> - sélectionnez un produit pour appliquer la réduction et affichez une minuterie.</li>
            <li><strong>Etat</strong> - l'état de l'élément.</li>
            <li><strong>Date de Début</strong> - la date de début de la période de réduction.</li>
            <li><strong>Date de Fin</strong> - la date de fin de la période de réduction.</li>
            <li><strong>Label</strong> - l'étiquette du produit.</li>
            <li><strong>Prix Réduit</strong> - le prix Réduit(valeur absolue ou en pourcentage).</li>
        </ul>
    </li>
    <li>
        <p>Après avoir enregistré l'élément, il apparaît dans la liste des éléments de ce type.</p>
        <figure class="img-polaroid"><img src="img/tmdaydeal-scr-3.png" alt="" /></figure>
    </li>
    <li>
        <p>Pour modifier cet élément, appuyez sur le bouton <strong>'Éditer'</strong>.</p>
    </li>
    <li>
        <p>Pour supprimer cet élément, appuyez sur le bouton <strong>'Supprimer'</strong>.</p>
    </li>
    <li>
        <p>Si un prix spécial, valable pour une certaine période, a été fixé au produit précédemment, ce produit peut être ajouté à la liste des éléments du module.</p>
        <figure class="img-polaroid"><img src="img/tmdaydeal-scr-4.png" alt="" /></figure>
    </li>
</ul>
<h5>Configuration de l'affichage du produit</h5>
<ul class="index-list">
    <li>
        <p>Pour changer le mode d'affichage du produit, appuyez sur le bouton <strong>'Configuration'</strong>.</p>
        <figure class="img-polaroid"><img src="img/tmdaydeal-scr-5.png" alt="" /></figure>
    </li>
    <li>
        <p>TEnsuite, un formulaire avec la configuration de l'affichage du produit apparaîtra.</p>
        <figure class="img-polaroid"><img src="img/tmdaydeal-scr-6.png" alt="" /></figure>
        <ul class="marked-list">
            <li><strong>Produits à afficher</strong> - le nombre de produits à afficher dans le bloc de la page d'accueil.</li>
            <li><strong>Produits aléatoires</strong> - l'affichage des produits aléatoires.</li>
        </ul>
    </li>
</ul>

