<h5>Sélection des produits</h5>
<p>Si vous créez un élément et activez l'option 'Sélectionner les produits à afficher', vous pouvez choisir les produits que vous souhaitez afficher. Pour faire ça:</p>
<ul class="index-list">
    <li><p>Appuyez sur le bouton 'Ajouter des produits' (1):</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts-7.png" alt=""/>
        </figure>
    </li>
    <li><p>Dans la fenêtre pop-up, sélectionnez les produits nécessaires (2) et appuyez sur le bouton 'Ajouter' (3):</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts-8.png" alt=""/>
        </figure>
    </li>
    <li><p>Maintenant, vous pouvez placer les produits ajoutés où vous voulez avec l'aide de glisser-déposer. (4):</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts-9.png" alt=""/>
        </figure>
    </li>
    <li><p>Lorsque tous les produits nécessaires sont ajoutés et arrangés dans l'ordre souhaité, appuyez sur le bouton 'Enregistrer'.</p>
    </li>
</ul>
<h5>Suppression de blocs/onglets</h5>
<p>Pour supprimer des éléments, appuyez sur le menu déroulant à côté du bloc/onglet nécessaire (1) et appuyez sur Supprimer (2). L'élément sera supprimé.</p>
