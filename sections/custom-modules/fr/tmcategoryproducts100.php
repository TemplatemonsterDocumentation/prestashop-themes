<p>Ce module affiche des produits de catégorie en crochets displayHome et displayHomeTabContent. 
Le module permet d'afficher des produits individuels, des produits d'une catégorie ou de tous les produits.
En outre, le module est livré avec un carrousel qui fonctionne, lorsque le module occupe la position displayHome.</p>
<h5>Panneau principal de >Produits de Catégorie TM</h5>
<p>Le panneau principal. Ici, vous pouvez configurer le module. </p>
<figure class="img-polaroid">
    <img src="img/tmcategoryproducts-1.png" alt=""/>
</figure>
<h5>Ajout d'un onglet</h5>
<p>'Onglet" est un bloc qui affiche les produits en position displayHomeTabContent.</p>
<ul class="index-list">
    <li><p>Accédez au panneau principal du module et appuyez sur le bouton 'plus' (1) dans la liste des onglets.</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts-2.png" alt=""/>
        </figure>
    </li>
    <li><p>Dans la fenêtre apparue, vous verrez les options suivantes:</p>
        <ul class="marked-list">
            <li>Etat - cette option permet d'activer/désactiver l'onglet.</li>
            <li>Choisir une catégorie - la catégorie dont les produits seront affichés.</li>
            <li>Utiliser le nom propre - cette option permet de modifier le nom de catégorie affiché. 
			Si cette option est activée, le formulaire suivant sera affichéed:
                <ul class="marked-list">
                    <li>Nom - le nom de l'onglet propre, affiché sur le site.</li>
                </ul>
            </li>
            <li>Sélectionner les produits à afficher - si désactivé, affiche le champ 'Nombre de produits à afficher', qui définit le nombre de produits à afficher.
			Si activé, affiche le bouton 'Ajouter des produits' qui permet de sélectionner les produits à afficher dans l'onglet.
            </li>
        </ul>
    </li>
    <li><p>Après avoir configuré toutes les options nécessaires, appuyez sur le bouton 'Enregistrer' (2).</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts-3.png" alt=""/>
        </figure>
    </li>
</ul>
<h5>Ajout d'un bloc</h5>
<p>Ce bloc permet de sélectionner des produits dans la position displayHome.</p>
<ul class="index-list">
    <li><p>Accédez au panneau principal du module et appuyez sur le bouton 'plus' (1) dans la liste des blocs.</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts-4.png" alt=""/>
        </figure>
    </li>
    <li><p>Dans la fenêtre apparue, vous verrez les options suivantes:</p>
        <ul class="marked-list">
            <li><p>Etat - cette option permet d'activer/désactiver le bloc.</p></li>
            <li>Choisir une catégorie - la catégorie dont les produits seront affichés.</li>
            <li>Utiliser le nom propre - cette option permet de modifier le nom de catégorie affiché. 
			Si cette option est activée, le formulaire suivant sera affichéed:
                <ul class="marked-list">
                    <li>Nom - le nom de l'onglet propre, affiché sur le site.</li>
                </ul>
            </li>
            <li>Sélectionner les produits à afficher - si désactivé, affiche le champ 'Nombre de produits à afficher', qui définit le nombre de produits à afficher.
			Si activé, affiche le bouton 'Ajouter des produits' qui permet de sélectionner les produits à afficher dans l'onglet.
            </li>
            <li>
                <p>Utiliser le carrousel - cette option permet d'activer/désactiver le carrousel pour les produits de blocs.
				Après avoir activé l'option, vous verrez les paramètres suivants:v</p>
		<ul class="marked-list">
            <li><p><strong>Eléments visibles</strong> - le nombre de fabricants visibles.</p></li>
            <li><p><strong>Défilement des éléments</strong> - le nombre de fabricants représentés lors de défilement du carrousel.</p></li>
            <li><p><strong>Largeur de diapositive</strong> - la largeur de l'élément dans le carrousel.</p></li>
            <li><p><strong>Marge de diapositive</strong> - la marge de l'élément dans le carrousel.</p></li>
            <li><p><strong>Défilement automatique</strong> - le défilement automatique.</p></li>
            <li><p><strong>Vitesse du carrousel</strong> - la vitesse de changement d'éléments.</p></li>
            <li><p><strong>Pause</strong> - la pause entre les défilements (si 'Défilement automatique' est activé).</p></li>
            <li><p><strong>Aléatoire</strong> - le carrousel commence par l'élément aléatoire.</p></li>
            <li><p><strong>Boucle de carrousel</strong> - le boucle: quand tous les objets du carrousel ont été affichés, le carrousel revient au début.</p></li>
            <li><p><strong>Masquer le contrôle à la fin</strong> - masquer les boutons de contrôle (Suivant/Précédent).</p></li>
            <li><p><strong>Pager</strong> - afficher la pagination.</p></li>
            <li><p><strong>Contrôle</strong> - afficher les contrôles Suivant/Précédent.</p></li>
            <li><p><strong>Contrôle automatique</strong> - afficher les contrôles (Lecture/Pause).</p></li>
            <li><p><strong>Hover automatique</strong> - une pause lors du survol des éléments du carrousel.</p></li>
        </ul>
        </li>
      </ul>
    </li>
    <li>
        <p>Après avoir configuré toutes les options nécessaires, appuyez sur le bouton 'Enregistrer' (2).</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts-5.png" alt=""/>
        </figure>
    </li>
</ul>
<h5>Tri des blocs/onglets</h5>
<p>Vous pouvez trier les blocs du module avec l'aide de glisser-déposer. Cela permet de changer rapidement les blocs et les onglets.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryproducts-6.png" alt=""/>
