<h3>Curseur D'image de Categorie TM</h3>
<p>Module permet de créer des diapositives multilingues pour toute catégorie du magasin. Le curseur s'affiche sur les pages de la catégorie.</p>
<h4>Installation et suppression</h4>
<p>La procédure d'installation est habituelle, Le module peut être installé et supprimé comme tout autre module PrestaShop.</p>
<h4>Paramètres du module</h4>
<p>Après l'installation du module, vous pouvez configurer ou modifier les options nécessaires sur la page de configuration du module.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryimagesslider-1.png" alt=""/>
</figure>
<ul class="marked-list">
    <li><strong>Mode</strong> - le type d'animation (fondu, vertical, horizontal).</li>
    <li><strong>Vitesse</strong> - la vitesse d'animation.</li>
    <li><strong>Largeur</strong> - la largeur des glissières (pour la vue correcte, il est recommandé d'utiliser la largeur de l'image réelle).
    </li>
    <li><strong>Hauteur</strong> - la hauteur des glissières (pour la vue correcte, il est recommandé d'utiliser la hauteur de l'image réelle).
    </li>
    <li><strong>Auto</strong> - une lecture automatique lorsque la page est ouverte.</li>
    <li><strong>Pause</strong> - le temps avant la transition vers la diapositive suivante.</li>
    <li><strong>Hover Automatique</strong> - la pause sur hover.</li>
    <li><strong>Contrôles</strong> - les boutons "Suivant"/"Précédent" buttons.</li>
    <li><strong>Texte Suivantt</strong> - le texte du bouton "Suivantt".</li>
    <li><strong>Texte Précédent</strong> - le texte du bouton "Précédent".</li>
    <li><strong>Contrôles Automatiques</strong> -  les boutons "Play"/"Pause".</li>
    <li><strong>Pager</strong> - la pagination.</li>
</ul>
<h5>Ajout/suppression/édition de diapositives</h5>
<p>Après l'installation du module, vous verrez le nouveau champ "Diapositives" dans les paramètres de catégorie.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryimagesslider-2.png" alt=""/>
</figure>
<p>Pour ajouter une diapositive, cliquez sur le bouton "Ajouter Nouvelle Diapositive". Remplissez les champs requis dans le formulaire.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryimagesslider-3.png" alt=""/>
</figure>
<ul class="marked-list">
    <li><strong>Sélectionnez un fichier</strong> - sSélectionnez votre fichier. Vous pouvez télécharger différentes images pour chaque langue.
	S'il n'y a pas d'image sélectionnée pour la langue spécifique, un ensemble d'images pour la langue par défaut sera chargé.</li>
    <li><strong>Entrez Titre de Diapositive</strong> - tpez le titre de la diapositive.</li>
    <li><strong>Entrer URL de Diapositives</strong> -  fournissez l'URL de la diapositive.</li>
    <li><strong>Entrer Légende de Diapositive</strong> - tapez la légende de diapositive.</li>
    <li><strong>Ouvrir dans une Nouvelle Fenetre</strong> - ouvrez dans une nouvelle fenêtre.</li>
    <li><strong>Etat</strong> - activer/désactiver la diapositive.</li>
</ul>
<p>Lorsque la diapositive est enregistrée, elle apparaîtra dans une liste de diapositives de catégorie.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryimagesslider-4.png" alt=""/>
</figure>
<p>Pour modifier la diapositive, cliquez sur le bouton <strong>'Editer'</strong>.</p>
<p>Pour supprimer la diapositive, cliquez sur <strong>'Supprimer'</strong> sous le bouton <strong>'Editer'</strong>.</p>
<div class="alert alert-info">
    <span>Les diapositives ne peuvent être ajoutées qu'aux catégories enregistrées.</span>
</div>