<p>Ce module permet d'afficher tout contenu (image, HTML, liens, etc.) dans les positions suivantes:
 displayNav, home,top, topColumn, left and right (displayLeftColumn/displayRightColumn), footer. 
Il permet d'élargir les opportunités de conception et d'ajouter des blocs de publicité/informations à votre magasin. 
C'est un module multilingue.</p>
