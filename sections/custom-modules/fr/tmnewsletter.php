<p>Le module affiche une notification avec une proposition pour s'inscrire à la boutique dans une fenêtre pop-up.
La vérification par é-mail peut être activée dans les paramètres du module au moyen d'e-mail de confirmation. 
Vous pouvez également ajuster le délai entre les rappels pour les utilisateurs enregistrés et non enregistrés.
Les utilisateurs enregistrés peuvent abandonner définitivement les rappels.</p>
<h5>Installation/désinstallation du module</h5>
<p>Le module est installé et supprimé de la même manière que tout autre module PrestaShop.</p>
<h5>Paramètres du module</h5>
<p>Une fois le module est installé, vous pouvez le configurer sur la page de configuration du module.</p>
