<p>Le module permet de rendre la commande beaucoup plus rapide qu'avant.<br>
   Il simplifie la procédure de cinq étapes à une et rend la procédure de commande plus attrayante pour les clients qui souhaitent économiser leur temps.<br>
   Grâce à cette fonctionnalité, vous pouvez obtenir plus d'achats spontanés. qui auront un impact positif sur votre entreprise.
</p>
<h4>Installation</h4>
<p>Allez aux Modules et Services -> Modules et Services (1) dans votre panneau d'administration et trouvez le module Commande 1-Clic TM (2). Puis appuyez sur le bouton 'Installer' (3).</p>
<figure class="img-polaroid">
    <img src="img/tmoneclick-1.png" alt=""/>
</figure>
<h4>Configuration du module</h4>
<p>Une fois que le module a été installé avec succès, la page de 'Configuration' avec les paramètres du module sera ouverte. <br>
    Vous verrez le nouvel élément de menu 'Commandes Rapides' (1) et une nouvelle icône apparaîtra dans le panneau de navigation(2).
</p>
<figure class="img-polaroid">
    <img src="img/tmoneclick-2.png" alt=""/>
</figure>
<p>Les paramètres sont divisés en trois onglets  - Modèle de précommande (1), Message de réussite du précommande (2) et Paramètres (3).</p>
<figure class="img-polaroid">
    <img src="img/tmoneclick-3.png" alt=""/>
</figure>
<h5>Modèle de précommande </h5>
<ul class="index-list-type-2">
    <li><p>Ici, vous pouvez ajouter un nouveau champ en appuyant sur le bouton correspondant (1):</p>
        <figure class="img-polaroid">
            <img src="img/tmoneclick-4.png" alt=""/>
        </figure>
        <p>Choisissez les options pour le nouveau champ et cliquez sur le bouton 'Enregistrer' (1):</p>
        <figure class="img-polaroid">
            <img src="img/tmoneclick-5.png" alt=""/>
        </figure>
        <ul class="marked-list">
            <li><strong>Type</strong> - choisissez le type de champ.</li>
            <li><strong>Classe spécifique</strong> - sjoutez la classe personnalisée pour le champ actuel.</li>
            <li><strong>Nom</strong> - le nom du champ.</li>
            <li><strong>Description</strong> - la description du champ; apparaît sous l'entrée.</li>
        </ul>
    </li>
    <li>
        <p>Modifier le champ existant.</p>

        <figure class="img-polaroid">
            <img src="img/tmoneclick-6.png" alt=""/>
        </figure>
        <p>Cliquez sur le champ que vous souhaitez modifier. Les paramètres sont les mêmes que pour ajouter un nouveau champ.</p>
    </li>
    <li>
        <p>Supprimer les champs du formulaire.<br>
		Appuyez sur le bouton 'Supprimer' (1) dans le coin supérieur droit du champ.</p>
        <figure class="img-polaroid">
            <img src="img/tmoneclick-7.png" alt=""/>
        </figure>
    </li>
</ul>
<h5>Message de réussite du précommande</h5>
<p>L'onglet permet de modifier le message pour la fenêtre d'alerte Qui apparaît après avoir cliqué sur le bouton 'Envoyer'  du formulaire de commande rapide.</p>
<p>Ajoutez le texte / bouton que vous souhaitez afficher et cliquez sur le bouton 'Enregistrer' (1).</p>
<figure class="img-polaroid">
    <img src="img/tmoneclick-8.png" alt=""/>
</figure>
<h5>Paramètres</h5>
<p>L'onglet permet d'activer ou de désactiver l'option 'Ordre Ajax'.</p>
<p>Cela aide l'administrateur à voir les nouvelles commandes rapides qui ont été soumises lors du traitement des commandes existantes.</p>
<figure class="img-polaroid">
    <img src="img/tmoneclick-9.png" alt=""/>
</figure>
<p>Si l'option est activée, l'option supplémentaire 'Délai d'attente Ajax' apparaît. <br>
    La valeur est définie en millisecondes et signifie la période entre chaque vérification des nouveaux ordres rapides.
</p>
<figure class="img-polaroid">
    <img src="img/tmoneclick-10.png" alt=""/>
</figure>
<p>Cette option permet d'ouvrir de nouveaux précommandes sans rechargement de page. 
Pour les voir dans la liste des commandes rapides,  appuyez sur le lien "Charger Nouveaux Commandes"(1) dans le menu Commandes -> Commandes Rapides.</p>
<figure class="img-polaroid">
    <img src="img/tmoneclick-11.png" alt=""/>
</figure>
<h4>Traitement des commandes</h4>
<p>Allez vers Commandes -> Commandes Rapides (1) dans votre panneau d'administration.</p>
<figure class="img-polaroid">
    <img src="img/tmoneclick-12.png" alt=""/>
</figure>
<p>Il a quatre onglets - Nouveau, Créé, Supprimé et Recherche.</p>
<ul class="marked-list">
    <li>
        <p><strong>Nouveau</strong> - les nouvelles précommandes qui ont été soumis par les clients.</p>
        <p>Dans la partie gauche, il existe un bouton pour créer un nouveau pré-ordre(1) et une liste de préordres (2).</p>
        <p> droite, vous pouvez voir les détails de précommande (3) divisé en onglets
		(Informations de précommande, Client, Langues et devises, Panier, Bons, Adresses, Livraison, Résumé).</p>
        <figure class="img-polaroid">
            <img src="img/tmoneclick-13.png" alt=""/>
        </figure>
        <p>Si l'ordre vient d'être créé, vous ne verrez que trois - Informations de précommande, Client, Langues et devises.</p>
        <figure class="img-polaroid">
            <img src="img/tmoneclick-14.png" alt=""/>
        </figure>
        <p>Lorsque vous complétez les informations sur le client, trois autres onglets apparaîtront- Panier, Bons et Adresses. 
		Lorsque les informations d'adresse sont ajoutées, les onglets Livraison et Résumé s'affichent.
		Les onglets où toutes les informations requises ont été ajoutées avec succès, sont colorés en vert.
		Les onglets de couleur jaune nécessitent plus de détails sur les commandes. Les bleus sont informatifs, ils sont générés automatiquement.<br>
		Lorsque l'onglet Résumé apparaît, il y a un bouton 'Créer Commande' dans l'onglet Résumé (1).</p>
        <figure class="img-polaroid">
            <img src="img/tmoneclick-15.png" alt=""/>
        </figure>
        <p>Après avoir cliqué, le précommande sera transféré de 'Nouveau' à la liste des précommandes 'Créé' (1).</p>
        <figure class="img-polaroid">
            <img src="img/tmoneclick-16.png" alt=""/>
        </figure>
        <p>Si vous devez supprimer l'ordre, Il y a une icône de 'poubelle' dans le coin supérieur droit(1).</p>
        <p>Appuyez sur cette icône et le pré-ordre sera transféré dans l'onglet 'Supprimé'.</p>
    </li>
    <li>
        <p><strong>Créé</strong> - les précommandes déjà traités par l'administrateur.</p>
        <p>Choisissez la commande à gauche et cliquez pour voir les informations détaillées à droite.</p>
        <p>Vous pouvez aussi appuyer sur le lien 'Afficher' pour voir les informations complètes sur le client(1), l'ordre (2) et l'employé(3).</p>
        <figure class="img-polaroid">
            <img src="img/tmoneclick-17.png" alt=""/>
        </figure>
    </li>
    <li>
        <p><strong>Supprimé</strong> - les préordres annulés.</p>
        <p>La précommande  dans cet onglet peut encore être récupérée en cliquant sur le bouton '+' (1). Ensuite, il sera transféré à l'onglet 'Nouveau'.</p>
        <figure class="img-polaroid">
            <img src="img/tmoneclick-18.png" alt=""/>
        </figure>
    </li>

    <li>
        <p><strong>Recherche</strong> - la recherche de précommandes. 
		Entrez la recherche (par l'information du client, l'information de l'employé, ID de commande, ID de précommande); vous pouvez également entrer la date/la période spécifiquede quand les préordres ont été créés.</p>
        <figure class="img-polaroid">
            <img src="img/tmoneclick-19.png" alt=""/>
        </figure>
    </li>
</ul>
