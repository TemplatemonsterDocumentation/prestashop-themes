<h4>Panneau Principal de Combinaisons Homepage TM</h4>
<p>C'est le panneau principal du module qui vous permet d'ajouter, de modifier et de supprimer des éléments. 
Pour créer un nouvel élément, appuyez sur le bouton 'Ajouter un Nouvel Elément' (1) dans le menu du panneau principal.</p>
<figure class="img-polaroid">
	<img src="img/tmhomepagecombinations-1.png" alt=""/>
</figure>
<p>Remplissez les champs de formulaire.</p>
<figure class="img-polaroid">
	<img src="img/tmhomepagecombinations-2.png" alt=""/>
</figure>
<ul class="marked-list">
	<li><strong>Produit</strong> - sélectionnez le produit.</li>
	<li><strong>Hook</strong> - choisissez la position de l'élément.</li>
	<li><strong>Titre</strong> - spécifiez le titre de l'article.</li>
	<li><strong>Etat du titre</strong> - si cette option serait désactivée, le titre de l'élément ne sera pas affiché.</li>
	<li><strong>Etat d'objet</strong> - si cette option est désactivée, l'élément n'apparaîtra pas dans sa position.</li>
	<li><strong>Ordre de tri</strong> - l'ordre de tri de l'objet.</li>
</ul>
<p>Appuyez sur le bouton 'Enregistrer & Rester' lorsque tous les champs sont remplis. Attendez jusqu'à la page recharge et appuyez sur 'Ajouter une Nouvelle Combinaison'.</p>
<p>Choisissez la combinaison requise de la liste et cliquez sur le bouton 'Ajouter' (1).</p>
<figure class="img-polaroid">
	<img src="img/tmhomepagecombinations-3.png" alt=""/>
</figure>
<p>Remplissez les champs requis dans la forme qui apparaît.</p>
<figure class="img-polaroid">
	<img src="img/tmhomepagecombinations-4.png" alt=""/>
</figure>
<ul class="marked-list">
	<li><strong>Sélectionner une image</strong> - choisissez l'image désirée.</li>
	<li><strong>Classe spécifique</strong> - la classe personnalisée, définit le style d'élément (classe CSS).</li>
	<li><strong>Ordre de tri</strong> - l'ordre de tri de l'objet.</li>
</ul>
<p>Appuyez sur le bouton 'Enregistrer' lorsque tous les champs sont remplis.</p>
<p>Après avoir créé quelques combinaisons actives, la liste des combinaisons disponibles s'affichera de la manière suivante.</p>
