<p>Ce module charge les réseaux sociaux sur votre site. Il permet aux visiteurs du site d'être au courant des dernières nouvelles et publications partagées sur les réseaux sociaux.</p>
<h4>Installation et suppression</h4>
<p>Le module est installé et supprimé de la même manière que tout autre module PrestaShop.</p>
<h4>Configuration du module</h4>
<p>Après l'installation, vous pouvez modifier ses paramètres sur la page de configuration du module.</p>
<p>Dans la forme supérieure, vous devez ajouter des paramètres globaux pour chaque réseau social. Les différents réseaux sociaux ont des différents paramètres.</p>
