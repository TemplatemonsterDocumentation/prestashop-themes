<p>The module displays a notification with a proposal to subscribe to store newsletter
    in a pop-up window. The email check can be enabled in module settings by means of
    confirmation email. You can also adjust the timeout between reminders for registered
    and non-registered users. Registered users can permanently give up on reminders.</p>
<h5>Module install/uninstall</h5>
<p>This module can be installed/uninstalled as any other PrestaShop module.</p>
<h5>Module settings</h5>
<p>After module has been installed you can configure it on the module configuration
    page.</p>
<figure class="img-polaroid">
    <img src="img/tmnewsletter-1.png" alt=""/>
</figure>
<ul class="index-list">
    <li><strong>Live mode</strong> - enable/disable the module.</li>
    <li><strong>Would you like to send a verification email after subscription?</strong> -
        enable/disable confirmation email send.
    </li>
    <li><strong>Timeout for guests</strong> - set timeout for popup reminding subscription for
        guests.
    </li>
    <li><strong>Timeout for customers</strong> - set timeout for popup reminding subscription for
        customers.
    </li>
</ul>
<p class="alert alert-warning">
    It is necessary to install and enable 'Newsletter block' for proper work of module.</p>
