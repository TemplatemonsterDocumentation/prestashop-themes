<h3>TM Product Custom Tab</h3>
<p>The module serves to create an additional tab on the product page for displaying additional information.
    The module has an advantage over the existing functionality, due to the ability of adding images, text, video, links, etc.</p>
<h4>Installation and removal</h4>
<p>The module is installed and removed as any other PrestaShop module.</p>
<h4>Module settings</h4>
<p>After installing the module, you can add new tabs for the products at the module configuration page, as well as at the product editing page.</p>
<h5>Adding tabs through the module configuration:</h5>
<p>To add a new tab, press the 'Add new' button (1).</p>
<figure class="img-polaroid">
    <img src="img/tmcustomproducttab-1.png" alt=""/>
</figure>
<ul class="index-list">
    <li>
        <p>Adding a tab to one or several products of a previously selected category.</p>
        <figure class="img-polaroid">
            <img src="img/tmcustomproducttab-4.png" alt=""/>
        </figure>
        <ul class="marked-list">
            <li><strong>Tab Heading</strong> - name of the tab, required.</li>
            <li><strong>Tab Description</strong> - information about the tab, can include data in the format of an image, text, video, links, etc.</li>
            <li><strong>Status</strong> - the element status.</li>
            <li><strong>Additional settings</strong> - enabling/disabling additional settings.</li>
            <li><strong>Select category</strong> - choosing categories for which the tab should be displayed.</li>
            <li><strong>Products to display</strong> - selecting the products from the previously selected categories.</li>
        </ul>
    </li>
    <li>
        <p>Adding the tab to the products that belong to selected categories.</p>
        <figure class="img-polaroid">
            <img src="img/tmcustomproducttab-3.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>To add a tab to all products, disable the 'Additional settings' option.</p>
        <figure class="img-polaroid">
            <img src="img/tmcustomproducttab-2.png" alt=""/>
        </figure>
    </li>
</ul>
<h5>Changing the tab display position</h5>
<p>Drag and drop the needed tab to change its position.</p>
<figure class="img-polaroid">
    <img src="img/tmcustomproducttab-5.png" alt=""/>
</figure>
<h5>Adding tabs through the product settings:</h5>
<p>Navigate to the product page and click the 'TM Product Custom Tab' link in the left menu.</p>
<figure class="img-polaroid">
    <img src="img/tmcustomproducttab-6.png" alt=""/>
</figure>
<p>If the 'Additional settings' option is disabled, the tab will be added to the product that was edited earlier. You can also add the tab to the products that belong to selected categories, or you can add it to several products that belong to the previously selected category.</p>
<p>To change the tabs display position, drag-and-drop the blocks. You can edit or remove tabs as well.</p>
