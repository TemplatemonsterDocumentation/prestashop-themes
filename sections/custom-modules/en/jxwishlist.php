<h6>JX Wishlist module is created to give the ability to the visitors of online store to add products to wishlists
    and share these wishlists on Facebook.</h6>
<h4>Module Installation</h4>
<p>То install the module navigate to Admin panel -> Modules -> Modules and Services, find the JX Wishlist module and press 'Install' button (1).</p>
<figure class="img-polaroid">
    <img src="img/jxwishlist-1.png" alt=""/>
</figure>
<h4>Module Configuration</h4>
<p>After the module has been installed, the 'Settings' page will appear.<br>
    Add the Facebook App Id to the required field (1) and press 'Save' button (2).<br>
    The instruction how to get the Facebook App Id can be found here: <a
        href="https://developers.facebook.com/docs/apps/register" target="blank">Creating an App ID</a>.</p>
<figure class="img-polaroid">
    <img src="img/jxwishlist-2.png" alt=""/>
</figure>
<h4>Using JX Wishlist Module</h4>
<h5>Adding product to wishlist</h5>
<p class="alert alert-info">Shop visitors should be logged in to be able to add the items to the wishlists.</p>
<ul class="index-list">
    <li>
        <p>After the module is installed, the button 'Add to wishlist' appears on each product page. It should look
            like on the screenshot below (1).</p>
        <figure class="img-polaroid">
            <img src="img/jxwishlist-3.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>After clicking this button a shop visitor will see a notification: 'The product was successfully added to
            your wishlist.' It can be closed or skipped to wishlists view by clicking 'My wishlists' button
            (2).</p>
        <figure class="img-polaroid">
            <img src="img/jxwishlist-4.png" alt=""/>
        </figure>
    </li>
</ul>
<h5>Browsing Wishlists</h5>
<ul class="index-list">
    <li>
        <p>On 'My Wishlists' page a client can add the new wishlist (1) or view the list of created ones (2).</p>
        <figure class="img-polaroid">
            <img src="img/jxwishlist-5.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Clicking 'View wishlist' button (1) will open the wishlist page, the 'View products' button (2) gives the
            ability to see the products list of this wishlist on current page. <br>
            The title of wishlist can de edited (3) or deleted (4). <br>
            To share the wishlist the visitor should press the 'Share' button (5).</p>
        <figure class="img-polaroid">
            <img src="img/jxwishlist-6.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>While looking through the items in wishlist after clicking 'View products' button, each item can be removed
            from the list (1).</p>
        <figure class="img-polaroid">
            <img src="img/jxwishlist-7.png" alt=""/>
        </figure>
    </li>
</ul>
<h5>Sharing Wishlists</h5>
<p>To share the wishlist the visitor should press the 'Share' button. There will be a few easy steps to follow before
    posting the wishlist. During each step there's an ability to go back and change the sharing options.</p>
<ul class="index-list">
    <li>
        <p>First step is to choose the layout for wishlist that will be posted.</p>
        <figure class="img-polaroid">
            <img src="img/jxwishlist-8.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Then the products should be added. Each cell with a '+' is a button to add the product image.</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections9.png" alt=""/>
        </figure>
        <p>Choosing the images of each product:</p>
        <figure class="img-polaroid">
            <img src="img/jxwishlist-11.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>When all images are added, the visitor should press the 'Share' button (1).</p>
        <figure class="img-polaroid">
            <img src="img/jxwishlist-10.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>In a small window which is then appeared, there will be some standard posting options to choose and the
            ability to add some text to the post. After clicking 'Post to Facebook' button, the post will be
            published.</p>
    </li>
</ul>
<h5>Statistics</h5>
<p>Using this module you can also get the statistics collected while the products are added to wishlists. Thus, you
    will know which products are currently popular. This information can be very useful for suggesting a discount, for
    example. <br>
    You can view the statistics navigating to Admin panel -> Stats -> Stats -> JX Wishlist. <br>
</p>
<figure class="img-polaroid">
    <img src="img/jxwishlist-12.png" alt=""/>
</figure>
<ul class="marked-list">
    <li>You can select the period for viewing the statistics from the specified time (1).</li>
    <li>In the first column there's a number of product addings (2).</li>
    <li>The second column is created for your convenience. You can use it to see how many of added products were sold
        (3).
    </li>
</ul>