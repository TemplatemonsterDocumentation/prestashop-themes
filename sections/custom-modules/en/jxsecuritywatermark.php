<h3>JX Security Watermark</h3>
<h6>JX Security Watermark module is used to leave watermarks on the pictures in order to increase the level of
    protection of the store.</h6>
<h4>Module Installation</h4>
<p>То install the module navigate to Admin panel -> Modules -> Modules and Services, find the JX Security Watermark
    module
    and click ‘Install' button (1).</p>
<figure class="img-polaroid">
    <img src="img/jxsecuritywatermark-1.png" alt=""/>
</figure>
<h4>Module Configuration</h4>
<p>After the module has been installed, the 'Settings' page will appear.</p>
<figure class="img-polaroid">
    <img src="img/jxsecuritywatermark-2.png" alt=""/>
</figure>
<ul class="marked-list">
    <li><strong>Watermark file</strong> - choose the watermark file in GIF format only.</li>
    <li><strong>Watermark opacity (1-100)</strong> - the opacity of watermark file. The default setting, 60, is usually a
        good compromise.
    </li>
    <li><strong>Watermark X align</strong> - select where your watermark should appear on the images, here on the
        horizontal axis.
    </li>
    <li><strong>Watermark Y align</strong> - select where your watermark should appear on the images, here on the
        vertical axis.
    </li>
    <li><strong>Choose image types for watermark protection</strong> - you may choose different image types to which the
        watermark should be applied by ticking them.
    </li>
</ul>
<p class="alert alert-warning"><span>Once, you have saved the settings, go to <strong>Design -> Image Settings</strong> section. There, click the <strong>Regenerate
        thumbnails</strong> button at the bottom of the page.</span></p>
<p>Now, your watermark image will appear on the types of pictures that you have selected.</p>

