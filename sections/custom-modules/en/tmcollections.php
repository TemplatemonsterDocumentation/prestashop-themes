<h6>TM Collections module is created to give the ability to the visitors of online store to add products to collections
    and share this collections on Facebook.</h6>
<h4>Module Installation</h4>
<p>То install the module navigate to Admin panel -> Modules and Services -> Modules and Services (1), find the TM
    Collections module (2) and press 'Install' button (3).</p>
<figure class="img-polaroid">
    <img src="img/tmcollections1.png" alt=""/>
</figure>
<h4>Module Configuration</h4>
<p>After the module has been installed, the 'Settings' page will appear.<br>
    Add the Facebook App Id to the required field (1) and press 'Save' button (2).<br>
    The instruction how to get the Facebook App Id can be found here: <a
            href="https://developers.facebook.com/docs/apps/register" target="blank">Creating an App ID</a>.</p>
<figure class="img-polaroid">
    <img src="img/tmcollections2.png" alt=""/>
</figure>
<h4>Using TM Collections Module</h4>
<h5>Adding product to collection</h5>
<p class="alert alert-info">Shop visitors should be logged in to be able to add the items to collections.</p>
<ul class="index-list">
    <li>
        <p>After the module is installed, the button 'Add to collection' appears on each product page. It should look
            like on the screenshot below (1).</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections3.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>After clicking this button a shop visitor will see a notification: 'The product was successfully added to
            your collection.' It can be closed or skipped to collections view by clicking 'My collections' button
            (2).</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections4.png" alt=""/>
        </figure>
    </li>
</ul>
<h5>Browsing Collections</h5>
<ul class="index-list">
    <li>
        <p>On 'My Collections' page a client can add the new collection (1) or view the list of created ones (2).</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections5.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Clicking 'View collection' button (1) will open the colection page, the 'View products' button (2) gives the
            ability to see the products list of this collection on current page. <br>
            The title of collection can de edited (3) or deleted (4). <br>
            To share the collection the visitor should press the 'Share' button (5).</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections6.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>While looking through the items in collection after clicking 'View products' button, each item can be removed
            from the list (1).</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections7.png" alt=""/>
        </figure>
    </li>
</ul>
<h5>Sharing Collections</h5>
<p>To share the collection the visitor should press the 'Share' button. There will be a few easy steps to follow before
    posting the collection. During each step there's an ability to go back and change the sharing options.</p>
<ul class="index-list">
    <li>
        <p>First step is to choose the layout for collection that will be posted.</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections8.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Then the products should be added. Each cell with a '+' is a button to add the product image.</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections9.png" alt=""/>
        </figure>
        <p>Choosing the images of each product:</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections10.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>When all images are added, the visitor should press the 'Share' button (1).</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections11.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>In a small window which is then appeared there will be some standard posting options to choose and the
            ability to add some text to the post (1). After clicking 'Post to Facebook' button (2), the post will be
            published.</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections12.png" alt=""/>
        </figure>
    </li>
</ul>
<h5>Statistics</h5>
<p>Using this module you can also get the statistics collected while the products are added to collections. Thus you
    will know which products are currently popular. This information can be very useful for suggesting a discount, for
    example. <br>
    You can view the statistics navigating to Admin panel -> Stats -> Stats -> TM Collections. <br>
</p>
<figure class="img-polaroid">
    <img src="img/tmcollections13.png" alt=""/>
</figure>
<ul class="marked-list">
    <li>You can select the period for viewing the statistics from the specified time (1).</li>
    <li>In the first column there's a number of product addings (2).</li>
    <li>The second column is created for your convenience. You can use it to see how many of added products were sold
        (3).
    </li>
</ul>