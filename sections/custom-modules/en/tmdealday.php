<p>This module allows you to display the list of products with the discount countdown timer. You can also add a timer to
    products in the home page tabs,
    on the category pages and single product pages.
</p>
<h5>Installation and Deleting</h5>
<p>The module is installed and removed the same way as any other PrestaShop module.</p>
<h5>Module Settings</h5>
<p>After module installation you can add a product discount with the display period and a discount countdown timer.</p>
<figure class="img-polaroid"><img src="img/tmdaydeal-scr-1.png" alt=""/></figure>
<h5>Adding/removing/editing blocks</h5>
<ul class="index-list">
    <li>
        <p>In order to add a new product, press 'Add new' button</p>
        <figure class="img-polaroid"><img src="img/tmdaydeal-scr-2.png" alt=""/></figure>
        <ul class="marked-list">
            <li><strong>Select a product</strong> - select a product to apply the discount and display a timer.</li>
            <li><strong>Status</strong> - item status.</li>
            <li><strong>Start Date</strong> - start date of the product discount period.</li>
            <li><strong>End Date</strong> - end date of the product discount period.</li>
            <li><strong>Label</strong> - product label.</li>
            <li><strong>Discount price</strong> - discount price (absolute or percentage value).</li>
        </ul>
    </li>
    <li>
        <p>After saving the item it appears in the list of items of this type.</p>
        <figure class="img-polaroid"><img src="img/tmdaydeal-scr-3.png" alt=""/></figure>
    </li>
    <li>
        <p>In order to edit this item, press <strong>'Edit'</strong> button.</p>
    </li>
    <li>
        <p>In order to remove this item, press <strong>'Delete'</strong> button.</p>
    </li>
    <li>
        <p>If a special price valid for a certain period was set to the product previously, this product can be added to
            the module items list.</p>
        <figure class="img-polaroid"><img src="img/tmdaydeal-scr-4.png" alt=""/></figure>
    </li>
</ul>
<h5>Product display configuration</h5>
<ul class="index-list">
    <li>
        <p>In order to change product display mode, press <strong>'Configuration'</strong> button.</p>
        <figure class="img-polaroid"><img src="img/tmdaydeal-scr-5.png" alt=""/></figure>
    </li>
    <li>
        <p>Then a form with product display configuration will appear.</p>
        <figure class="img-polaroid"><img src="img/tmdaydeal-scr-6.png" alt=""/></figure>
        <ul class="marked-list">
            <li><strong>Products to display</strong> - number of products to display in the home page block.</li>
            <li><strong>Random products</strong> - random products display.</li>
        </ul>
    </li>
</ul>

