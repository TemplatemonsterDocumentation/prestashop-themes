<p>This module loads the social networks feeds on your website. It enables the site visitors to be aware of the latest
    news and posts shared on the social networks.</p>
<h4>Installation and Deleting</h4>
<p>The module is installed and deleted like any other Prestashop module.</p>
<h4>Module configuration</h4>
<p>After installation you can change its settings on the module configuration page.</p>
<p>In the upper form you should add global settings for each social network. Different social networks have different
    settings.</p>
