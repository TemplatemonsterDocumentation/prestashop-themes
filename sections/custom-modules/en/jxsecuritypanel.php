<h3>JX Security Panel</h3>
<h6>JX Security Panel module is used to increase the level of store protection: block the possibility of publishing
    content, edit the URL at attempt to copy it.</h6>
<h4>Module Installation</h4>
<p>То install the module navigate to Admin panel -> Modules -> Modules and Services, find the JX Security Panel module
    and click ‘Install' button (1).</p>
<figure class="img-polaroid">
    <img src="img/jxsecuritypanel-1.png" alt=""/>
</figure>
<h4>Module Configuration</h4>
<p>The main module menu is divided into to main parts: Main Settings and Hot Keys.</p>
<p>You can set the following main settings:</p>
<figure class="img-polaroid">
    <img src="img/jxsecuritypanel-content.png" alt=""/>
</figure>
<ul class="marked-list">
    <li><strong>Disable context menu by right click</strong> - the ability to disable the click with a right mouse
        button.
    </li>
    <li><strong>Disable text selection with mouse</strong> - the ability to disable text selection.</li>
    <li><strong>Disable Drag&Drop</strong> - enable/disable the drag and drop option.
    </li>
    <li><strong>Enable Hotlinking</strong> - the ability to prevent image hotlinking.
    </li>
    <li><strong>Copyright</strong> - add copyright(or simple text) to copied text from your resource.</li>
</ul>
<p>It is possible to enable/disable the following <strong>Hot Keys</strong>.</p>
