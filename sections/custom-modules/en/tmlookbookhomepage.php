<p class="alert alert-warning">To use the module, you should have the TM Look Book module installed.</p>
<p>TM Look Book Homepage is used to display the TM Look Book module block on the Homepage. This module allows to display
    both the lookbook covers and lookbook pages.</p>
<h5>Requirements:</h5>
<ul class="marked-list">
    <li>PHP 5.4+</li>
    <li>PrestaShop 1.6</li>
    <li>TM Look Book Module</li>
</ul>
<h5>Module features:</h5>
<ul class="marked-list">
    <li>The module can be displayed in 2 positions: displayHome and displayTopColumn.</li>
    <li>TM Look Book Homepage is a child module of TM Look Book, that's why it can't be used without it.</li>
    <li>The module can display the lookbook cover or its pages.</li>
</ul>
<h5>TM Look Book Main Panel</h5>
<p>At the module main panel, you can add, delete and edit blocks, that the module will display on the Homepage. </p>
<figure class="img-polaroid">
    <img src="img/tmlookbookhomepage-main-panel.png" alt=""/>
</figure>
<h5>Adding a new block</h5>
<ul class="index-list">
    <li>
        <p>To add a new block, enter the module main panel and click the '+' (1) in the table with the needed hook.</p>
        <figure class="img-polaroid">
            <img src="img/tmlookbookhomepage-add-block-1.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>In the appeared form, set up the following options:</p>
        <ul class="marked-list">
            <li><strong>Status</strong> - the block status. This setting allows to enable or disable the block display.
            </li>
            <li><strong>Type</strong> - allows to select the block type.</li>
            <li><strong>Lookbook</strong> - allows to select a previously selected look.</li>
        </ul>
    </li>
    <li>
        <p>After setting up all options, press the 'Save' (2) button.</p>
        <figure class="img-polaroid">
            <img src="img/tmlookbookhomepage-add-block-2.png" alt=""/>
        </figure>
    </li>
</ul>
<p>After the new block is created, it will be displayed on the Homepage.</p>