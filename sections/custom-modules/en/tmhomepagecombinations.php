<h4>TM Homepage Combinations Main Panel</h4>
<p>This is the main module panel that allows you to add, edit and delete items. In order to create new item, press 'Add
    new item' button (1) in main panel menu.</p>
<figure class="img-polaroid">
    <img src="img/tmhomepagecombinations-1.png" alt=""/>
</figure>
<p>Fill in the pop-up form fields.</p>
<figure class="img-polaroid">
    <img src="img/tmhomepagecombinations-2.png" alt=""/>
</figure>
<ul class="marked-list">
    <li><strong>Product</strong> - select the product.</li>
    <li><strong>Hook</strong> - choose item position.</li>
    <li><strong>Title</strong> - specify item title.</li>
    <li><strong>Title status</strong> - in case this option is disabled, item title won't be shown.</li>
    <li><strong>Item status</strong> - if this option is disabled, item will not appear in its position.</li>
    <li><strong>Sort order</strong> - item sorting order.</li>
</ul>
<p>Press on 'Save & Stay' button when all fields are filled in. Wait until page reloads and press on 'Add new
    combination'.</p>
<p>Choose the required combination from the list and click the 'Add' button (1).</p>
<figure class="img-polaroid">
    <img src="img/tmhomepagecombinations-3.png" alt=""/>
</figure>
<p>Fill in the required fields in the form that appears.</p>
<figure class="img-polaroid">
    <img src="img/tmhomepagecombinations-4.png" alt=""/>
</figure>
<ul class="marked-list">
    <li><strong>Select image</strong> - choose the desired image.</li>
    <li><strong>Specific class</strong> - custom class, defines item styling (CSS class).</li>
    <li><strong>Sort order</strong> - item sorting order.</li>
</ul>
