<h3>TM Slider</h3>
<p>The module is specially crafted to demonstrate your content efficiently. It features all you need to showcase your content and make a professionally looking slider. The module has plenty of customization tools, which make the slider super-easy to adjust.</p>

<h4>Module Installation</h4>
<p>This module can be installed/uninstalled like any other PrestaShop module.</p>

<h4>Module customization and managing</h4>
<h5>To implement a slider into your site, you need to follow the next steps:</h5>
<ul class="index-list">
    <li>Add new slides and slides' content (layers)</li>
    <li>Create slider from slides</li>
    <li>Manage the relations</li>
</ul>
<p>Detailed instructions are described further.</p>

<h5>Adding new slide</h5>
<ul class="index-list">
    <li>
        <p>Navigate to TM Slider -> TM Slides in Admin panel (1).</p>
        <figure class="img-polaroid"><img src="img/tmslider-1.png" alt="" /></figure>
    </li>
    <li>
        <p>Click the 'Add new' ('+') button (1).</p>
        <figure class="img-polaroid"><img src="img/tmslider-2.png" alt="" /></figure>
    </li>
    <li>
        <p>Choose the options for the new slide.</p>
        <figure class="img-polaroid"><img src="img/tmslider-3.png" alt="" /></figure>
        <ul class="marked-list">
            <li><strong>Slide name</strong> - enter the slide name</li>
            <li><strong>Multi link</strong> - set different links for each language</li>
            <li><strong>Link</strong> - enter the slide link</li>
            <li><strong>Link target</strong> - choose if the link should be opened in new or in the same window</li>
            <li><strong>Width</strong> - enter the slide width in px</li>
            <li><strong>Height</strong> - enter the slide height in px</li>
            <li><strong>Slide background type</strong> - choose the type of slide background (image / youtube video / no slide content)</li>
            <li><strong>*Multi images</strong> - use different images for each language</li>
            <li><strong>*Select an image (default)</strong> - this image will be displayed on the large screens and all other screens if images for smaller resolutions are not defined</li>
            <li>*<strong>Select an image (tablet)</strong> - this image will be displayed on tablet screens</li>
            <li>*<strong>Select an image (mobile)</strong> - this image will be displayed on mobile screens</li>
            <li>*<strong>Select an image (retina)</strong> - this image will be displayed on retina screens</li>
            <li>*<strong>Multi videos</strong> - use different videos for each language</li>
            <li>*<strong>Enter a youtube video code</strong> - enter youtube code</li>
            <li>*<strong>Select a poster</strong> - select the image that will cover video slide</li>
            <li><strong>Slide description</strong> - enter the slide description</li>
            <li><strong>Multi thumbnails</strong> - use different thumbnails for each language</li>
            <li><strong>Select a thumbnail image</strong> - choose the image for slide thumbnail</li>
            <li><strong>Thumbnail text</strong> - enter a thumbnail text</li>
        </ul>
        <p>* - options appear according to the selected background type.</p>
    </li>
    <li>
        <p>Then click the 'Save' button. To go back without saving click 'Cancel'.</p>
    </li>
    <li>
        <p>To edit the slide press the down arrow near the slide you want to edit and click the 'Edit' button (1). Options are the same as for creating a new slide.</p>
        <figure class="img-polaroid"><img src="img/tmslider-4.png" alt="" /></figure>
    </li>
    <li>
        <p>To preview the slide just click on the needed slide in the list of slides or the 'View' button (1) on the slide.</p>
        <figure class="img-polaroid"><img src="img/tmslider-5.png" alt="" /></figure>
    </li>
    <li>
        <p>To delete the slide press the down arrow near the slide you want to remove and click the 'Delete' button (1). Then confirm the deletion.</p>
        <figure class="img-polaroid"><img src="img/tmslider-6.png" alt="" /></figure>
    </li>
</ul>

<h5>Adding slide content</h5>
<ul class="index-list">
    <li>
        <p>Navigate to TM Slider -> TM Slides in Admin panel and click the 'View' button (1) or simply click on the slide (2).</p>
        <figure class="img-polaroid"><img src="img/tmslider-7.png" alt="" /></figure>
    </li>
    <li>
        <p>Here you will see the list of created layers (1) and the slide preview (2).</p>
        <figure class="img-polaroid"><img src="img/tmslider-8.png" alt="" /></figure>
    </li>
    <li>
        <p>To add a new content layer click the 'Add new' ('+') button (1).</p>
        <figure class="img-polaroid"><img src="img/tmslider-9.png" alt="" /></figure>
    </li>
    <li>
        <p>Fill in the fields and click the 'Save' button.</p>
        <figure class="img-polaroid"><img src="img/tmslider-10.png" alt="" /></figure>
        <ul class="marked-list">
            <li><strong>Item title</strong> - the title of the content layer</li>
            <li><strong>Specific class</strong> - specific class applied to the layer</li>
            <li><strong>Layer content</strong> - content of the layer</li>
            <li><strong>Active</strong> - option to show or hide the content</li>
            <li><strong>Item order</strong> - the order of layer (item with the highest number will be on top)</li>
            <li><strong>Custom position</strong> - set to 'Yes' if you want to define the precise position of this element (enter the coordinates of the item in px or %)</li>
            <li><strong>Predefined position</strong> - predefined position (choose one of 9 basic positions)</li>
            <li><strong>Show effect</strong> - effect while item is appearing</li>
            <li><strong>Hide effect</strong> - effect while the item is disappearing</li>
            <li><strong>Show delay</strong> - delay before the item appears</li>
            <li><strong>Hide delay</strong> - delay before the item disappears</li>
        </ul>
    </li>
    <li>
        <p>To edit the slide content layer, simply click on the layer or click the 'Edit' button (1).</p>
        <figure class="img-polaroid"><img src="img/tmslider-11.png" alt="" /></figure>
    </li>
    <li>
        <p>To delete the content layer press the down arrow near the layer you want to remove and click the 'Delete' button (1).</p>
        <figure class="img-polaroid"><img src="img/tmslider-12.png" alt="" /></figure>
    </li>
</ul>

