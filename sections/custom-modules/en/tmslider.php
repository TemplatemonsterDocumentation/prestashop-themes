<h5>Managing Relations</h5>
<ul class="index-list">
    <li>
        <p>Navigate to TM Slider -> Manage Relations in Admin panel (1).</p>
        <figure class="img-polaroid"><img src="img/tmslider-23.png" alt="" /></figure>
    </li>
    <li>
        <p>To add new relation click the 'Add new' ('+') button (1) in the top right corner of the screen.</p>
        <figure class="img-polaroid"><img src="img/tmslider-24.png" alt="" /></figure>
    </li>
    <li>
        <p>Choose options for displaying one of the sliders on your website.</p>
        <figure class="img-polaroid"><img src="img/tmslider-25.png" alt="" /></figure>
        <ul class="marked-list">
            <li><strong>Select page</strong> - choose the page for implementing the slider</li>
            <li><strong>Select hook</strong> - choose the hook within this page</li>
            <li><strong>Select slider</strong> - choose the slider</li>
            <li><strong>Slider order</strong> - the order of slider in current hook</li>
            <li><strong>Active</strong> - enable/disable the slider</li>
            <li><strong>Slide only</strong> - display slides without related layouts</li>
            <li><strong>Slider width</strong> - set the slider width in pixels</li>
            <li><strong>Slider height</strong> - set the slider height in pixels</li>
            <li><strong>Responsive</strong> - make the slider responsive (The slider can be responsive even if the "width" and/or "height" properties are set to fixed values. In this situation, "width" and "height" will act as the maximum width and height of the slides.)</li>
            <li><strong>Allow autoplay</strong> - indicate whether autoplay is enabled or not</li>
            <li><strong>Autoplay delay</strong> - set the delay/interval (in milliseconds) before the autoplay will run</li>
            <li><strong>Autoplay direction</strong> - indicate whether autoplay will navigate to the next slide or to the previous one</li>
            <li><strong>Autoplay on hover</strong> - indicate if the autoplay will be paused or stopped when the slider is hovered</li>
            <li><strong>Fade</strong> - indicate if fade effect will be used</li>
            <li><strong>Fade out previous slide</strong> - indicate if the previous slide will be faded out (in addition to the next slide being faded in)</li>
            <li><strong>Fade duration</strong> - set the duration of the fade effect</li>
            <li><strong>Image scale mode</strong> - choose the type of image scaling</li>
            <li><strong>Center image</strong> - indicate if the image will be centered</li>
            <li><strong>Allow scale up</strong> - indicate if the image can be scaled up more than its original size</li>
            <li><strong>Auto height</strong> - indicate if height of the slider will be adjusted to the height of the selected slide</li>
            <li><strong>Auto slide size</strong> - the option maintains all the slides at the same height but allows the width of the slides to vary if the orientation of the slides is horizontal</li>
            <li><strong>Start slide</strong> - set the slide that will be selected when the slider loads</li>
            <li><strong>Shuffle</strong> - indicate if the slides will be shuffled</li>
            <li><strong>Orientation</strong> - choose the slider orientation</li>
            <li><strong>Force size</strong> - indicate if the size of the slider will be forced to full width or full window</li>
            <li><strong>Loop</strong> - indicate if the slider will be loopable (infinite scrolling); requires more than 2 slides. </li>
            <li><strong>Slide distance</strong> - set the distance between the slides</li>
            <li><strong>Slide animation duration</strong> - set the duration of the slide animation</li>
            <li><strong>Height animation duration</strong> - set the size of the visible area, allowing for more slides to become visible near the selected slide</li>
            <li><strong>Visible size</strong> - set the size of the visible area, allowing for more slides to become visible near the selected slide</li>
            <li><strong>Center selected slide</strong> - indicate whether the selected slide will be in the center of the slider when there are more slides visible at a time (if set to false, the selected slide will be on the left side of the slider)</li>
            <li><strong>Right to left</strong> - indicate if the direction of the slider will be from right to left, instead of the default left to right</li>
            <li><strong>Arrows</strong> - indicate whether the arrow buttons will be created</li>
            <li><strong>Fade arrows</strong> - indicate whether the arrows will fade in only on hover</li>
            <li><strong>Buttons</strong> - indicate whether the arrows will fade in only on hover</li>
            <li><strong>Keyboard</strong> - indicate whether keyboard navigation will be enabled</li>
            <li><strong>Keyboard only on focus</strong> - indicate whether the slider will respond to keyboard input only when the slider is in focus</li>
            <li><strong>Touch swipe</strong> - indicate whether the touch swipe will be enabled for slides</li>
            <li><strong>Touch swipe threshold</strong> - set the minimum value that the slides should move</li>
            <li><strong>Fade caption</strong> - indicate whether the captions will be faded or not</li>
            <li><strong>Caption fade duration</strong> - set the duration of the fade animation</li>
            <li><strong>Full screen button</strong> - indicate whether the full screen button is enabled</li>
            <li><strong>Fade full screen button</strong> - indicate whether the full screen button is enabled</li>
            <li><strong>Wait for layers</strong> - indicate whether the slider will wait for the layers to disappear before going to a new slide</li>
            <li><strong>Auto scale layers</strong> - indicate whether the layers will be scaled automatically</li>
            <li><strong>Reach video action</strong> - set the action that the video will perform when its slide container is selected</li>
            <li><strong>Leave video action</strong> - set the action that the video will perform when another slide is selected</li>
            <li><strong>Play video action</strong> - set the action that the video will perform when its slide container is selected</li>
            <li><strong>End video action</strong> - set the action that the slider will perform when the video ends</li>
            <li><strong>Thumbnail</strong> - set the action that the slider will perform when the video ends</li>
        </ul>
    </li>
    <li>
        <p>To edit the relation, simply click on the relation or click the 'Edit' button (1).</p>
        <figure class="img-polaroid"><img src="img/tmslider-26.png" alt="" /></figure>
    </li>
    <li>
        <p>To delete the slide from current slider press the down arrow near the slide you want to remove and click the 'Delete' button (1).</p>
        <figure class="img-polaroid"><img src="img/tmslider-27.png" alt="" /></figure>
    </li>
</ul>