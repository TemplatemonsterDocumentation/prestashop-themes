<h5>Adding/Editing item options</h5>
<div class="alert alert-info">Please note: category name, description and image are loaded from the category properties and there's no need to add them manually. To edit these options, go to Admin panel -> Catalog -> Categories -> choose category -> Edit.</div>
<p>When you add new item to the module (or edit the one from the list of items), you have some options to choose. The detailed description of options you can see below the screenshot. After all options are set, click 'Save' button (1).</p>
<figure class="img-polaroid">
    <img src="img/tmhomepagecategorygallery-4.png" alt=""/>
</figure>
<ul class="marked-list">
	<li><strong>Select category</strong> - here you choose from the dropdown list the category which should be displayed on your page.</li>
	<li><strong>Specific class</strong> - the specific class can be added to the module item.</li>
	<li><strong>Display name</strong> - option to show or hide the category name.</li>
	<li><strong>Category name length</strong> - maximum number of symbols to be displayed in the category name. If the name is longer - the '..' symbol will be added after the number of symbols that has been set.</li>
	<li><strong>Display description</strong> - option to show or hide the category description.</li>
	<li><strong>Description length</strong> - maximum number of symbols to be displayed in the decription. If the decription is longer - the '..' symbol will be added after the number of symbols that has been set.</li>
	<li><strong>Sort order</strong> - the order of item in the category gallery.</li>
	<li><strong>Display button</strong> - the option to show or hide the button 'Shop now' (button text can be different depending on the template).</li>
	<li><strong>Status</strong> - the option to show or hide the category</li>
	<li><strong>HTML content</strong> - the field where some HTML content can be added.</li>
</ul> 