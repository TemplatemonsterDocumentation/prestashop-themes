</figure>
<p>You can add or remove related products for the main product in the product settings
    <strong>Catalog -> Products</strong>.</p>
<ul class="index-list">
    <li>Navigate to <strong>TM Related Products</strong>;
    </li>
    <li>In the product search field start input the title of the product, and choose
        it from the offered variants;
    </li>
    <li>In order to remove the product from the list click the X before the
        product;
    </li>
    <li>Once all the configuration changes have been made click <strong>'Save and
            Stay'</strong> or <strong>'Save'</strong> button.
    </li>
</ul>
