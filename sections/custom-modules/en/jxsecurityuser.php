<h3>JX Security User</h3>
<h6>JX Security User module is used to monitor the activity of the users and block them if it is needed.</h6>
<h4>Module Installation</h4>
<p>То install the module navigate to Admin panel -> Modules -> Modules and Services, find the JX Security User module
    and click ‘Install' button (1).</p>
<figure class="img-polaroid">
    <img src="img/jxsecurityuser-1.png" alt=""/>
</figure>
<h4>Module Configuration</h4>
<p>After the module has been installed, the 'Settings' page will appear.</p>
<p>The module includes 6 main tabs: Customers and Visitors online, Login fail, Black list, Ban list, and Settings.</p>
<figure class="img-polaroid">
    <img src="img/jxsecurityuser-2.png" alt=""/>
</figure>
<ul class="marked-list">
    <li><strong>Customers online</strong> - the ability to view the list of all users who are currently registred on
        your site.
    </li>
    <li><strong>Visitors online</strong> - the ability to see the information about the visitors of your site at the
        moment.
    </li>
    <li><strong>Login fail</strong> - monitoring of failed login attempts, with the ability to view the user's IP
        address, the user name and the date/time of the failed login attempt.
    </li>
    <li><strong>Black list</strong> - users ban by IP address or range of IP addresses.
    </li>
    <li><strong>Ban list</strong> - the ability to view a list of all blocked users in the table form, with the
        opporunity to unlock individual IPs or group of IP addresses.</li>
    <li><strong>Settings</strong> - additional settings to secure the store:
        <ul class="marked-list">
            <li><strong>Maximum number of attempts before blocking</strong>.</li>
            <li><strong>Maximum number of attempts before disconnecting</strong>.</li>
            <li><strong>Maximum number of attempts before sending a message to the administrator</strong>.</li>
            </li>
        </ul>
</ul>
