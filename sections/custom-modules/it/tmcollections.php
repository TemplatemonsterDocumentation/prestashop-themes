<h6>Modulo TM Collections è stato creato per offrire la possibilità ai visitatori del negozio online di aggiungere prodotti alle collezioni
    E condividere queste collezioni su Facebook.</h6>
<h4>Installazione del modulo</h4>
<p>Installare il modulo per passare al pannello Amministrazione -> Moduli e servizi -> Moduli e servizi (1), trovare la TM
    Collect module (2) e premere il tasto 'Install' (3).</p>
<figure class="img-polaroid">
    <img src="img/tmcollections1.png" alt=""/>
</figure>
<h4>Configurazione del modulo</h4>
<p>Dopo che il modulo è stato installato, verrà visualizzata la pagina 'Impostazioni'.<br>
    Aggiungere l'ID App di Facebook al campo obbligatorio (1) e premere il pulsante 'Salva' (2).<br>
    L'istruzione per ottenere l'ID di app di Facebook può essere trovata qui: <a
        href="https://developers.facebook.com/docs/apps/register" target="blank">Creazione di un ID di app</a>.</p>
<figure class="img-polaroid">
    <img src="img/tmcollections2.png" alt=""/>
</figure>
