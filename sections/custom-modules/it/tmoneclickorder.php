<h5>Predisporre il messaggio di successo</h5>
<p>La scheda consente di modificare il messaggio della finestra di avviso che compare dopo che l'utente fa clic sul pulsante "Invia" in forma rapida.</p>
<p>Aggiungere il testo/pulsante da visualizzare e fare clic sul pulsante 'Salva' (1).</p>
<figure class="img-polaroid">
    <img src="img/tmoneclick-8.png" alt=""/>
</figure>
<h5>Impostazioni</h5>
<p>La scheda consente di attivare e disattivare l'opzione 'Ajax order'.</p>
<p>Consente all'amministratore di visualizzare i nuovi ordini veloci presentati durante l'elaborazione degli ordini esistenti.</p>
<figure class="img-polaroid">
    <img src="img/tmoneclick-9.png" alt=""/>
</figure>
<p>Se l'opzione è abilitata, viene visualizzata l'opzione aggiuntiva "Timeout di ordine Ajax".<br>
    Il valore è impostato in millisecondi e significa il periodo di tempo tra ogni controllo per nuovi ordini veloci.
</p>
<figure class="img-polaroid">
    <img src="img/tmoneclick-10.png" alt=""/>
</figure>
<p>Questa opzione aiuta a aprire nuovi preordini senza la ricarica pagina. Per visualizzarli nell'elenco degli ordini veloci, fai clic sul link "Carica nuovi ordini" (1) in Ordini -> Ordini rapidi.</p>
<figure class="img-polaroid">
    <img src="img/tmoneclick-11.png" alt=""/>
</figure>
<h4>Elaborazione dell'ordine</h4>
<p>Passare agli Ordini -> Ordini rapidi (1) nel pannello di amministrazione.</p>
<figure class="img-polaroid">
    <img src="img/tmoneclick-12.png" alt=""/>
</figure>
<p>Ha quattro schede: Nuovo, Creato, Rimosso e Ricerca.</p>
<ul class="marked-list">
    <li>
        <p><strong>Nuovo</strong> - nuovi preordini che sono stati presentati dai clienti.</p>
        <p>Nella parte sinistra c'è un pulsante per creare un nuovo preorder (1) e un elenco di pre-ordini (2).</p>
        <p>Nel lato destro si possono vedere i dettagli del prefisso (3) suddivisi in schede (Preordine informazioni, Cliente, Lingue e valute, Carrello, Buoni, Indirizzi, Spedizione, Riepilogo).</p>
        <figure class="img-polaroid">
            <img src="img/tmoneclick-13.png" alt=""/>
        </figure>
        <p>Se l'ordine è stato appena creato, vedrete solo tre di essi - Preordine informazioni, Cliente, Lingue e valute.</p>
        <figure class="img-polaroid">
            <img src="img/tmoneclick-14.png" alt=""/>
        </figure>
        <p>Quando riempite le informazioni sul cliente, appariranno altre tre schede: carrello, buoni e indirizzo. Quando vengono aggiunte le informazioni di indirizzo, verranno visualizzate le schede Spedizione e Riepilogo. Le schede in cui tutte le informazioni richieste sono state aggiunte con successo, sono colorate con verde. Le schede colorate giallo richiedono alcuni dettagli di ordine. I blu sono informativi, vengono generati automaticamente.<br>
            Quando viene visualizzata la scheda Riepilogo, è presente un pulsante 'Crea l'ordine' nella scheda Riepilogo (1).</p>
        <figure class="img-polaroid">
            <img src="img/tmoneclick-15.png" alt=""/>
        </figure>
        <p>Dopo averlo scelto, il prefisso verrà trasferito da "Nuovo" all'elenco dei preordini "Creati" (1).</p>
        <figure class="img-polaroid">
            <img src="img/tmoneclick-16.png" alt=""/>
        </figure>
        <p>Se devi eliminare l'ordine, nell'angolo in alto a destra c'è un segno di spazzatura (1).</p>
        <p>Premere il pulsante e il prefisso verrà trasferito alla scheda 'Ordini rimossi'.</p>
    </li>
    <li>
        <p><strong>Creato</strong> - I preordini già elaborati dall'amministratore.</p>
        <p>Scegli l'ordine sul lato sinistro e fai clic su di esso per vedere le informazioni dettagliate sul lato destro.</p>
        <p>Puoi anche cliccare sul link 'Mostra' per visualizzare informazioni complete sul cliente (1), sull'ordine (2) e sul dipendente (3).</p>
        <figure class="img-polaroid">
            <img src="img/tmoneclick-17.png" alt=""/>
        </figure>
    </li>
    <li>
        <p><strong>Rimosso</strong> - i preordini che sono stati annullati.</p>
        <p>Il prefisso in questa scheda può ancora essere recuperato facendo clic sul pulsante '+' (1). Quindi verrà trasferito nuovamente alla scheda 'Nuovi'.</p>
        <figure class="img-polaroid">
            <img src="img/tmoneclick-18.png" alt=""/>
        </figure>
    </li>

    <li>
        <p><strong>Ricerca</strong> - ricerca dei preordini. Inserisci la ricerca (per informazioni sui clienti, informazioni sui dipendenti, id degli ordini, pre-ordina id); È inoltre possibile immettere il periodo specifico della data/data quando i pre-ordini sono stati creati.</p>
        <figure class="img-polaroid">
            <img src="img/tmoneclick-19.png" alt=""/>
        </figure>
    </li>
</ul>
