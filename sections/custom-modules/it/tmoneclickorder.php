<p>Il modulo è usato per rendere il processo di fare un ordine molto più veloce e più facile di quanto lo fosse prima
    Semplifica la procedura da cinque passi a uno e lo rende più attraente per i clienti che vogliono risparmiare
    Il loro tempo durante l'ordine.<br>
    Con questa funzione è possibile ottenere acquisti più spontanei che avranno un impatto positivo sulla tua attività commerciale.
</p>
<h4>Installazione</h4>
<p>Passare a Moduli e Impostazioni -> Moduli e Impostazioni (1) nel pannello di amministrazione e trovare l'ordinazione TM One Click
    Modulo (2). Quindi fare clic sul pulsante 'Installa' (3).</p>
<figure class="img-polaroid">
    <img src="img/tmoneclick-1.png" alt=""/>
</figure>
<h4>Configurazione del modulo</h4>
<p>Dopo che il modulo è stato installato correttamente, verrà aperta la pagina "Configura" con le impostazioni del modulo.<br>
    Vedrai anche la nuova voce di menu 'Ordini rapidi' (1) e una nuova icona comparirà nel pannello di navigazione (2).
</p>
<figure class="img-polaroid">
    <img src="img/tmoneclick-2.png" alt=""/>
</figure>
<p>Le impostazioni sono suddivise in tre schede - Predefinito modello (1), Predefinito messaggio di successo (2) e Impostazioni (3).</p>
<figure class="img-polaroid">
    <img src="img/tmoneclick-3.png" alt=""/>
</figure>
<h5>Predefinire il template</h5>
<ul class="index-list-type-2">
    <li><p>Qui è possibile aggiungere un nuovo campo premendo il pulsante corrispondente (1):</p>
        <figure class="img-polaroid">
