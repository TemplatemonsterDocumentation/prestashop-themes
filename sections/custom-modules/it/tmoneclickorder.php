        </figure>
        <p>Scegli le opzioni per il nuovo campo e fai clic sul pulsante 'Salva' (1):</p>
        <figure class="img-polaroid">
            <img src="img/tmoneclick-5.png" alt=""/>
        </figure>
        <ul class="marked-list">
            <li><strong>Tipo</strong> - Scegliere il tipo di campo.</li>
            <li><strong>Specifica classe</strong> - aggiungere la classe personalizzata per il campo corrente.</li>
            <li><strong>Nome</strong> - nome campo.</li>
            <li><strong>Descrizione</strong> - descrizione del campo; Appare sotto l'ingresso.</li>
        </ul>
    </li>
    <li>
        <p>Modificare il campo esistente.</p>

        <figure class="img-polaroid">
            <img src="img/tmoneclick-6.png" alt=""/>
