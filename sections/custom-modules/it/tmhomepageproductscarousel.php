<h3>TM Homepage Prodotti Carousel</h3>
<p>Il modulo aggiunge un carosello alle schede della pagina principale.</p>
<h5>Installazione e eliminazione</h5>
<p>Il modulo può essere installato e cancellato come qualsiasi altro modulo PrestaShop.</p>
<h5>Configurazione del modulo</h5>
<p>Le impostazioni del modulo installato possono essere configurate sulla pagina di configurazione del modulo.</p>
