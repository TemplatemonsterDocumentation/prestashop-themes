<h5>Installazione e eliminazione</h5>
<p>Il modulo è installato e rimosso allo stesso modo di qualsiasi altro modulo PrestaShop.</p>
<h5>Impostazioni del modulo</h5>
<p>Dopo l'installazione del modulo è possibile aggiungere uno sconto di prodotto con il periodo di visualizzazione e un timer di conto alla rovescia.</p>
