<h5>Installazione e eliminazione</h5>
<p>Il modulo viene installato e cancellato come qualsiasi altro modulo PrestaShop.</p>
<h5>Impostazioni del modulo</h5>
<p>Dopo che il modulo è stato installato, è possibile aggiungere i blocchi necessari con i prodotti di categoria, precedentemente aggiunti banner, video e contenuti HTML.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-1.png" alt="" /></figure>
<h5>Aggiunta/rimozione/modifica di blocchi</h5>
<p>Per aggiungere una nuova categoria, premi il pulsante "Aggiungi nuovo".</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-2.png" alt="" /></figure>
<ul class="marked-list">
    <li><strong>Seleziona categoria</strong> - scegli una categoria per aggiungere prodotti, banner, video e contenuti HTML.</li>
    <li><strong>Utilizza il nome personalizzato</strong> - stato della categoria personalizzata.</li>
    <li><strong>Nome personalizzato</strong> - titolo della categoria.</li>
    <li><strong>Stato</strong> - stato dell'elemento.</li>
    <p>Per aggiungere la nuova riga, premi il pulsante "Aggiungi riga". Quindi scegliere uno degli otto layout presentati nella finestra seguente.</p>
</ul>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-3.png" alt="" /></figure>
<p>Per aggiungere il layout, fai clic su una delle otto opzioni per selezionarla e verrà aggiunta al modulo.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-4.png" alt="" /></figure>
<p>Per aggiungere elementi, fai clic su una delle celle di layout. Vedrai cinque pulsanti che elencano gli elementi disponibili di un determinato tipo.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-5.png" alt="" /></figure>
<p>Dopo aver premuto uno dei pulsanti vedrai il modulo per scegliere l'elemento del tipo prescelto.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-6.png" alt="" /></figure>
<p>Dopo aver fatto clic su uno degli elementi, verrà aggiunto alla cella di layout. È possibile compilare il layout con diversi blocchi in questo modo.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-7.png" alt="" /></figure>
<p>Dopo aver salvato la categoria con i layout selezionati, verrà visualizzata sotto forma di categorie aggiunte.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-8.png" alt="" /></figure>
<p>Per rimuovere l'elemento, premere il pulsante 'Elimina'.</p>
<h5>Aggiunta/rimozione/modifica di banner</h5>
<p>Per aggiungere un nuovo banner, fai clic sul pulsante 'Aggiungi nuovo banner'. Successivamente, compilare i campi richiesti nel modulo che appare.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-9.png" alt="" /></figure>
<ul class="marked-list">
    <li><strong>Seleziona un file</strong> - scegli un file, questo campo è richiesto (almeno per la lingua predefinita).</li>
    <li><strong>Titolo</strong> - titolo banner, questo campo è richiesto (almeno per la lingua predefinita).</li>
    <li><strong>Url</strong> - percorso di collegamento, questo campo è richiesto (almeno per la lingua predefinita).</li>
    <li><strong>Descrizione</strong> - descrizione banner (almeno per la lingua predefinita).</li>
    <li><strong>Specifica classe</strong> - blocco CSS classe.</li>
</ul>
<h5>Aggiunta/rimozione/modifica del video</h5>
<p>Per aggiungere un nuovo video, fai clic sul pulsante 'Aggiungi nuovo video'. Successivamente, compilare i campi richiesti nel modulo che appare.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-10.png" alt="" /></figure>
<ul class="marked-list">
    <li><strong>Immettere il nome del video</strong> - titolo video, questo campo è richiesto (almeno per la lingua predefinita).</li>
    <li><strong>Collegamento video/percorso</strong> - collegamento al video (youtube, vimeo o video personalizzato), questo campo è richiesto (almeno per la lingua predefinita).</li>
    <li><strong>Consenti l'autoplay</strong> - definisce se il video si avvia automaticamente subito dopo il caricamento del lettore.</li>
    <li><strong>Consenti controlli</strong> - definisce se vengono visualizzati i controlli dei giocatori.</li>
    <li><strong>Loop</strong> - definisce se la riproduzione video è nel ciclo infinito.</li>
</ul>
<p>Per aggiungere video personalizzati, premi il pulsante 'file'. Successivamente, sarà possibile aggiungere un video tramite il gestore di file.</p>
<h5>Aggiunta/rimozione/modifica del contenuto HTML</h5>
<p>Per aggiungere nuovi contenuti HTML, premi il pulsante 'Aggiungi nuovo HTML'. Successivamente, compilare i campi richiesti nel modulo che appare.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-11.png" alt="" /></figure>
<ul class="marked-list">
    <li><strong>Inserisci il nome dell'elemento HTML</strong> - titolo del contenuto HTML, questo campo è richiesto (almeno per la lingua predefinita).</li>
    <li><strong>Classe specifica</strong> - blocco CSS classe.</li>
    <li><strong>HTML contenuto</strong> - il contenuto.</li>
</ul>
<h5>Aggiunta/rimozione/modifica di Slider</h5>
<p>Per aggiungere un nuovo cursore, premi il pulsante 'Aggiungi nuovo cursore'. Successivamente, compilare i campi richiesti nel modulo che appare.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-12.png" alt="" /></figure>
<ul class="marked-list">
    <li><strong>Inserisci il nome dell'elemento Slider</strong> - titolo di scorrimento, questo campo è richiesto (almeno per la lingua predefinita).</li>
    <li><strong>Controllo</strong> - mostrare/nascondere la navigazione slider.</li>
    <li><strong>Pager</strong> - mostrare/nascondere la paginazione del cursore.</li>
    <li><strong>Auto scroll</strong> - accende/spegne l'autoplay del dispositivo di scorrimento.</li>
    <li><strong>Specifica classe</strong> - blocco CSS classe.</li>
    <li><strong>Carousel velocità</strong> - scorre la durata della transizione (in ms), questo campo è necessario.</li>
    <li><strong>Pausa</strong> - il periodo compreso tra la scorrimento delle diapositive (in ms), questo campo è necessario.</li>
</ul>
<h5>Aggiunta/rimozione/modifica di diapositive</h5>
<p>Per aggiungere una nuova diapositiva, fai clic sul pulsante 'Visualizza' nel modulo di scorrimento e premi il pulsante 'Aggiungi nuovo' in una finestra che viene visualizzata. Quindi riempire i campi richiesti.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-13.png" alt="" /></figure>
<ul class="marked-list">
    <li><strong>Tipo</strong> - scegliere il tipo di diapositiva, questo campo è necessario.</li>
    <li><strong>Banner/Video/Html elenco</strong> - scegliere l'elemento del tipo selezionato in precedenza, questo campo è necessario.</li>
    <li><strong>Ordinamento</strong> - posizione di scorrimento.</li>
    <li><strong>Stato dell'oggetto</strong> - stato dell'elemento.</li>
</ul>