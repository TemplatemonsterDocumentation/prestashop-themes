<h3>TM Prodotti Slider 1.2.3</h3>
<p>Il modulo visualizza i prodotti di scorta nel dispositivo di scorrimento.</p>
<h4>Installazione e cancellazione</h4>
<p>Il modulo viene installato e cancellato come qualsiasi altro modulo Prestashop.</p>
<p>Dopo l'installazione è possibile modificare le impostazioni nella pagina di configurazione del modulo.</p>
<p>È possibile impostare l'ordine dei prodotti nel cursore utilizzando il metodo di trascinamento. Puoi
    Attivare/disattivare anche il display del prodotto nel cursore.</p>
<p>È possibile impostare la larghezza del cursore, l'effetto scorrevole, la velocità del cursore, la pausa, la riproduzione automatica,
    La pausa quando si passa sopra gli elementi carosello, il controllo slider, i pulsanti successivi/precedenti,
    Pulsanti di riproduzione/pausa.</p>
<figure class="img-polaroid">
    <img src="img/tmproductsslider-1.png" alt="">
</figure>
<p>Dopo l'installazione del modulo, verrà visualizzata la nuova scheda nella pagina di configurazione del prodotto
    (Catalogo -> Prodotto -> seleziona qualsiasi prodotto -> TM Products Slider).</p>
<p>Qui è possibile impostare se il prodotto apparirà nel cursore o meno. Dopo aver fatto
    Le modifiche, fare clic su Salva / Salva e Sospendi per salvarli.</p>
<p>Il modulo supporta i multi-store. È possibile modificare le impostazioni per ciascun negozio, gruppo di
    Negozi o tutti i negozi alla volta.</p>
