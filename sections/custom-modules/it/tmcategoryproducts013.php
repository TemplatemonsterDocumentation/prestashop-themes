<h3>TM Categoria Prodotti 0.1.3</h3>
<p>Questo modulo visualizza i prodotti di categoria in ganci displayHome e displayHomeTabContent. Il modulo consente di visualizzare
    Singoli prodotti, prodotti di una categoria o di tutti i prodotti. Inoltre, il modulo viene fornito con una giostra che funziona,
    Quando il modulo occupa la posizione di displayHome.</p>
<h4>Modulo Installazione</h4>
<p>Per installare il modulo, passare al pannello Amministrazione -> Moduli e servizi -> Moduli e servizi e trovare 'Categoria TM
    Modulo dei prodotti nelle funzioni Front Office, quindi fare clic sul pulsante 'Installa' (1).</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryproducts013-1.png" alt=""/>
</figure>
<h4>Modulo Configurazione</h4>
<ul class="index-list">
    <li>
        <p>Quando il modulo è stato installato correttamente, apparirà il pulsante 'Configura' (1).</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts013-2.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Nella pagina di configurazione del modulo principale si trovano elenchi di schede e blocchi (1) (dove è possibile visualizzare la creazione
            Elementi) e due pulsanti - 'Impostazioni' (2) e 'Aggiungi elemento' (3).</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts013-3.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Fai clic sul pulsante 'Impostazioni' e scegli le opzioni del modulo in base alle tue esigenze.</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts013-4.png" alt=""/>
        </figure>
        <ul class="marked-list">
            <li><p><strong>Elementi visibili</strong> - il numero di elementi visibili.</p></li>
            <li><p><strong>Scorrimento elementi</strong> - il numero di elementi modificati con un rotolo di carosello.</p></li>
            <li><p><strong>Larghezza di scorrimento</strong> - la larghezza dell'elenco carosello.</p></li>
            <li><p><strong>Margine di scorrimento</strong> - il margine dell'elemento dell'elenco carosello.</p></li>
            <li><p><strong>Scorrimento automatico</strong> - scorrimento automatico di oggetti di carosello.</p></li>
            <li><p><strong>Carousel velocità</strong> - velocità di cambiamento degli oggetti.</p></li>
            <li><p><strong>Pausa</strong> - mettere in pausa tra i rotoli (se è abilitato "Scorrimento automatico").</p></li>
            <li><p><strong>Casuale</strong> - il carosello inizia con un elemento casuale.</p></li>
            <li><p><strong>Carousel loop</strong> - il ciclo: quando sono stati visualizzati tutti gli elementi carosello, carosello
                    Inizia dall'inizio.</p></li>
            <li><p><strong>Nascondi il controllo alla fine</strong> - nascondere i pulsanti di controllo (Next/Prev).</p></li>
            <li><p><strong>Pager</strong> - visualizzazione paginazione.</p></li>
            <li><p><strong>Controllo</strong> - controlli di visualizzazione (Next / Prev).</p></li>
            <li><p><strong>Auto controllo</strong> - controlli di visualizzazione (Play / Pause).</p></li>
            <li><p><strong>Auto hover</strong> - carosello interrompe, se l'utente si posiziona nell'elenco.</p></li>
        </ul>
    </li>
</ul>
<h4>Aggiunta di nuova scheda/blocco</h4>
<p>La scheda è un blocco che visualizza i prodotti in posizione displayHomeTabContent.</p>
<p>Questo blocco consente di selezionare i prodotti nella posizione DisplayHome.</p>
<ul>
    <li>
        <p>Per aggiungere una nuova scheda o un blocco, clicca sul pulsante 'Aggiungi elemento'.</p>
        <p>Vedrai la schermata dove puoi scegliere le seguenti opzioni:</p>
        <ul class="marked-list">
            <li><p><strong>Seleziona categoria</strong> - Scegliendo una categoria, i cui prodotti saranno
                    Essere visualizzati.</p>
                <figure class="img-polaroid">
                    <img src="img/tmcategoryproducts013-5.png" alt=""/>
                </figure>
            </li>
            <li>
                <p><strong>Numero di prodotti da visualizzare</strong> - numero di elementi da visualizzare.</p>
                <figure class="img-polaroid">
                    <img src="img/tmcategoryproducts013-6.png" alt=""/>
                </figure>
            </li>
            <li>
                <p><strong>Modo</strong> - tab/modalità blocco.</p>
                <figure class="img-polaroid">
                    <img src="img/tmcategoryproducts013-7.png" alt=""/>
                </figure>
            </li>
            <li>
                <p><strong>Stato</strong> - consente di abilitare/disabilitare la scheda o il blocco.</p>
                <figure class="img-polaroid">
                    <img src="img/tmcategoryproducts013-8.png" alt=""/>
                </figure>
            </li>
        </ul>
        <p>Dopo aver selezionato i parametri, fai clic su "Salva". La scheda / blocco verrà salvata e apparirà nell'elenco di
            schede/blocchi.</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts013-9.png" alt=""/>
        </figure>
    </li>
</ul>
<h4>Modifica tab/blocco</h4>
<p>Per modificare la scheda o il blocco, trovarlo nell'elenco e premere il pulsante 'Modifica' (1). <br>
    Le opzioni sono uguali per la creazione di blocco o scheda.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryproducts013-10.png" alt=""/>
</figure>
<h4>Cancellazione tab/blocco</h4>
<p>Per rimuovere la scheda o il blocco, trovare il pulsante 'Elimina' sotto 'Modifica'.</p>
