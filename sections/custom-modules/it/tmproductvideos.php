<p>Questo modulo consente di aggiungere un numero illimitato di video a qualsiasi prodotto in qualsiasi lingua e memorizzare.</p>
<h5>Installazione e cancellazione</h5>
<p>l modulo viene installato e cancellato come qualsiasi altro modulo Prestashop. Non dispone di una pagina di configurazione e di supporti
    Multi-negozi.</p>
<p>Dopo l'installazione del modulo, verrà visualizzata la nuova scheda nella pagina di configurazione del prodotto (Catalogo -> Prodotto -> seleziona qualsiasi
    Prodotto - Video prodotti TM).</p>
<p>Per aggiungere un video a qualsiasi prodotto, è necessario compilare i campi obbligatori (il campo di collegamento è obbligatorio). Il modulo è
    Multilingue, quindi dovrai compilare i campi per tutte le lingue con cui questo video è collegato. Se il campo Link
    Non è riempito per qualsiasi lingua, quindi il video non verrà aggiunto per questa lingua. Il modulo dovrebbe apparire
    Up video da Youtube e Vimeo, quindi è necessario aggiungere link solo da queste risorse. Dopo aver apportato le modifiche,
    clic <strong>'Salva'</strong>/<strong>'Salva e Soggiorno'</strong> per salvarli.</p>
