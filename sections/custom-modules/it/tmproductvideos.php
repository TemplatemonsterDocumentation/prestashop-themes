<h6>Esempi di link</h6>
<p>Per visualizzare correttamente il video, i collegamenti devono avere il seguente formato:</p>
<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Youtube</dt>
            <dd><code>https://www.youtube.com/embed/K6VcTyaym90</code></dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Vimeo</dt>
            <dd><code>http://player.vimeo.com/video/66140585</code></dd>
        </dl>
    </li>
</ul>
<p>Se il prodotto ha un video relativo a questa lingua, vedrai l'elenco dei video qui sotto.</p>
<p>Per salvare, eliminare o modificare l'elenco, non è necessario cliccare <strong>'Salva'</strong>/<strong>'Salva e Soggiorno'</strong> pulsante. Utilizzare i pulsanti che si trovano nella schermata
    Blocchi video. Per modificare l'ordine dei video, utilizzare il metodo drag-and-drop per modificare la posizione. Per abilitare / disabilitare
    Il video, utilizza il pulsante accanto al video. Per modificare i video, compilare i campi con i dati necessari e fare clic su <strong>'Aggiorna'</strong>
    button.</p>
<p>Per rimuovere il video dall'elenco, fare clic <strong>'Elimina video'</strong> pulsante.</p>
<p>Durante la commutazione di lingue, verrà visualizzato solo l'elenco dei video relativi alla lingua.</p>
<p>Per visualizzare il video in formato zoomato, fare clic su <strong>'Zoom'</strong> Pulsante sotto questo video.</p>
