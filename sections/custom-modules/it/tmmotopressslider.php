<h4>Slide contenuto</h4>
<p>Qui puoi aggiungere testi, immagini, pulsanti e video a ciascuna diapositiva.<br>
    Viene suddiviso in due sezioni principali: 'Impostazioni livello' (1) e 'Livelli ordinamento' (2).
    Sopra l'area 'Impostazioni livello' sono presenti quattro schede per l'aggiunta del contenuto necessario (3). Dopo aver scelto cosa
    elemento che si desidera aggiungere, è possibile andare oltre alla personalizzazione dettagliata. <br>
    La grande caratteristica è che questi contenuti vengono aggiunti come singoli livelli, quindi è possibile trascinarli su e giù per impostare
    il loro ordine nella sezione 'Livelli ordinamento' (2).</p>
<p>I livelli possono essere eliminati separatamente (4) o tutti contemporaneamente (5).</p>
<figure class="img-polaroid">
    <img src="img/mpslider19_slide_add_text.png" alt=""/>
</figure>
<h5>Layer impostazioni</h5>
<h6>Aggiungi Testo</h6>
<p>Questa scheda viene creata per aggiungere, modificare e individuare i testi nella diapositiva.</p>
<figure class="img-polaroid">
    <img src="img/mpslider19_slide_add_text_2.png" alt=""/>
</figure>
<ul class="marked-list">
    <li>Utilizzando quadrati grigi è possibile impostare rapidamente la posizione del testo (1).</li>
    <li>Per adattarlo, aggiungere i valori 'X' e 'Y' in pixel e impostare la larghezza di questo elemento (2).</li>
    <li>Se 'Whitespace' è impostato su 'Normale', il testo verrà avvolto normalmente. Se viene scelto "No-wrap", l'involucro verrà eseguito
        essere ignorati (3).
    </li>
    <li>Nella seconda colonna è possibile controllare l'animazione dell'elemento - come, per quanto tempo e quando dovrebbe iniziare e terminare (4)
        All'interno della durata della diapositiva (che è Mostraren sotto per la tua comodità (5); per cambiarla andare a Slider
        impostazioni -> SlideMostrare ritardo). È inoltre possibile visualizzare in anteprima l'animazione nella finestra visualizzata dopo aver cliccato su
        pulsante 'Modifica'. Scegli la durata, la funzione di facilità, l'effetto di animazione e premi 'Play' per visualizzare l'anteprima. Quindi fare clic su
        'Applica' quando soddisfa le tue aspettative.
    </li>
</ul>
<p>Le impostazioni descritte in precedenza sono simili per ciascun elemento, per cui solo le opzioni della terza colonna saranno
    menzionato ulteriormente.</p>
<figure class="img-polaroid">
    <img src="img/mpslider19_slide_add_text_3.png" alt=""/>
</figure>
<ul class="marked-list">
    <li>Il testo stesso può essere aggiunto nella textarea che viene prossima (6).</li>
    <li>Lo stile del testo può essere scelto tra i preset nell'elenco a discesa (7), modificati dopo aver premuto il pulsante 'Modifica'
        (8) e disattivato facendo clic sul pulsante 'Rimuovi' (9). Dopo aver modificato lo stile del testo, premere il pulsante 'Applica'
        pulsante di stile. Se si desidera modificare e salvare lo stile esistente, premere il pulsante 'Duplica', modificarlo e salvarlo. A
        creare da uno scraping lo stile che verrà spesso applicato, fare clic sul pulsante 'Crea nuovo preset' e modificarlo.
    </li>
    <li>È inoltre possibile aggiungere una classe personalizzata a ciascun elemento (10).</li>
</ul>
