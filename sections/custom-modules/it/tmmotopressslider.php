<h4>Installazione del modulo</h4>
<p>Questo modulo offre la possibilità di aggiungere MotoPress Slider alla tua pagina HTML. Può essere installato come qualsiasi altro
    Modulo PrestaShop:</p>
<ul class="index-list">
    <li><p>Carica il modulo nella cartella/modules.</p></li>
    <li><p>Vai al pannello Amministrazione -> Moduli e servizi -> Moduli e servizi (1), trova il modulo (2) e fai clic su
            Pulsante 'Installa' (3).</p>
        <figure class="img-polaroid">
            <img src="img/mpslider00_install.png" alt=""/>
        </figure>
    </li>
    <li><p>Quando il modulo è stato installato correttamente è possibile visualizzare il menu 'MotoPress Slider' apparso in fondo a
            Il tuo pannello di amministrazione (1).<br>
            Fare clic sul pulsante 'MotoPress Slider' in questo menu (2).
        </p>
        <figure class="img-polaroid">
            <img src="img/mpslider01_open.png" alt=""/>
        </figure>
    </li>
