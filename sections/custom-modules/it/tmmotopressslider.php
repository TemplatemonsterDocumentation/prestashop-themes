<h4>Importa ed esporta Slider</h4>
<h6>Importa & Esporta Slider è molto utile se devi trasferire qualsiasi Slider a siti web diversi. Non hai bisogno di
    trascorrere molto tempo per ripristinare nuovamente i cursori da zero. Se sei uno sviluppatore puoi creare cursori sul tuo computer
    sito web, mostrarli ai tuoi clienti e importare i cursori sui siti web dei clienti.</h6>
<h5>Esportazione Slider</h5>
<h6>Seguire questi semplici passaggi per imparare a esportare il cursore Motopress.</h6>
<ul class="index-list">
    <li>
        <p>Vai a Motopress Slider nel pannello di amministrazione (1, 2).</p>
        <figure class="img-polaroid">
            <img src="img/mpslider01_open.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Fai clic su 'Esporta' (3).</p>
        <figure class="img-polaroid">
            <img src="img/mpslider25_export1.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Scegli i cursori che si desidera esportare (4) e fai clic su "Esporta cursori" (5).</p>
