<h4>Uso del modulo</h4>
<h5>Aggiunta/modifica del dispositivo di scorrimento</h5>
<p>Ora è possibile aggiungere un nuovo cursore (1) o modificare quello esistente (2).</p>
<figure class="img-polaroid">
    <img src="img/mpslider02_addedit.png" alt=""/>
</figure>
<h5>Aggiunta di un nuovo cursore</h5>
<p>Quando hai fatto clic sul pulsante "Aggiungi slider", puoi scegliere se dovresti essere "Slider personalizzato" o "Prodotto"
    Slider '.</p>
<figure class="img-polaroid">
    <img src="img/mpslider03_slider_add.png" alt=""/>
</figure>
<p>La differenza tra questi due tipi è la seguente: Il dispositivo di scorrimento Prodotto viene creato dai prodotti del tuo negozio,
    Anche se in Custom Slider è possibile aggiungere più diapositive come si desidera, personalizzandole in base alle proprie esigenze.</p>
<p>Le impostazioni del cursore personalizzato e del prodotto sono leggermente diverse. <br>
    Saranno menzionati ulteriormente.</p>

<h4>Configurazione del modulo</h4>
<h5>Slider impostazioni</h5>
<p>Le impostazioni principali del cursore. La descrizione dettagliata delle impostazioni è sotto lo screenshot.</p>
<figure class="img-polaroid">
    <img src="img/mpslider04_slider_slider_settings.png" alt=""/>
</figure>
<ul class="marked-list">
    <li><strong>Titolo di scorrimento</strong> - il titolo del cursore (esempio: Slider1); È necessario quando si hanno più
        Sliders e devono distinguerli.
    </li>
    <li><strong>Slider alias</strong> - l'alias unico che verrà utilizzato in codici a scacchi per incorporare il cursore
        (Esempio: cursore1); deve essere unico.
    </li>
    <li><strong>Slider shortcode</strong> - copia questo codice e incolla alla tua pagina.</li>
    <li><strong>Forza la larghezza</strong> - abilita questa opzione per rendere questo slider allungato a tutta la larghezza.</li>
    <li><strong>Forza l'altezza totale</strong> - abilitare questa opzione per rendere questo cursore a piena altezza.</li>
    <li><strong>Forza la griglia a pieno formato</strong> - rende la griglia allungata al contenitore genitore (ma è comunque necessario impostare la griglia
        larghezza e altezza).
    </li>
    <li><strong>Dimensioni griglia dei livelli</strong> - rermette di impostare la larghezza e l'altezza iniziali dei livelli.</li>
    <li><strong>Attiva la presentazione</strong> - abilitare/disabilitare la presentazione.</li>
    <li><strong>Ritardo diapositiva</strong> - il tempo in cui una diapositiva rimane sullo schermo; È misurato in millisecondi.</li>
    <li><strong>Animazione di presentazione</strong> - scegliere l'animazione per modificare le diapositive.</li>
    <li><strong>Durata della presentazione</strong> - durata animazione in millisecondi.</li>
    <li><strong>SlideMostrare easings</strong> - easings sul cambio di scorrimento.</li>
    <li><strong>Inizia con la diapositiva</strong> - primo indice di diapositiva.</li>
</ul>
