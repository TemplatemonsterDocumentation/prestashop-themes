<h5>Aggiunta di una scheda</h5>
<p>La scheda è un blocco che visualizza i prodotti in posizione displayHomeTabContent.</p>
<ul class="index-list">
    <li><p>Passare al pannello principale del modulo e premere il pulsante 'plus' (1) nell'elenco delle schede.</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts-2.png" alt=""/>
        </figure>
    </li>
    <li><p>Nella maschera apparsa, verranno visualizzate le seguenti opzioni:</p>
        <ul class="marked-list">
            <li>Stato - questa opzione consente di abilitare / disabilitare la scheda.</li>
            <li>Seleziona la categoria - la categoria, i cui prodotti saranno
                Essere visualizzati.
            </li>
            <li>Utilizza nome personalizzato - questa opzione consente di modificare il nome della categoria visualizzato. Se abilitato, il seguente
                Verranno visualizzati:
                <ul class="marked-list">
                    <li>Nome - il nome della scheda personalizzata, visualizzato sul sito Web.</li>
                </ul>
            </li>
            <li>Seleziona i prodotti da visualizzare - se disabilitato, visualizza il campo 'Numero di prodotti da visualizzare', che imposta
                Il numero di prodotti da visualizzare. Se abilitato, viene visualizzato il pulsante 'Aggiungi prodotti' che consente di selezionare
                Prodotti da visualizzare nella scheda.
            </li>
        </ul>
    </li>
    <li><p>Dopo aver configurato tutte le opzioni necessarie, premi il pulsante 'Salva' (2).</p>
