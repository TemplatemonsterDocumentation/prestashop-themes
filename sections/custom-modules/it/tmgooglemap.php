<h5>Aggiunta di stili personalizzati</h5>
<p>Puoi aggiungere qualsiasi stile personalizzato da <a target="_blank" href="https://snazzymaps.com/">Snazzymaps</a>
    Al tuo modulo. Per fare questo, è necessario selezionare lo stile dal sito web, copia
    Array di stile JavaScript in file .js e salvatelo nella cartella del modulo / js / styles / (per
    Il nome del file utilizza il nome di stile che sostituisce gli spazi con sottolineatura e rimozione
    Tutti i caratteri speciali. Ad esempio: per lo stile sottile in scala di grigi -
    File subtle_grayscale.js, per file di Apple Maps-esque - file apple_maps-esque.js).
    Quindi, vai alla pagina di configurazione del modulo e seleziona lo stile desiderato da
    L'elenco a discesa degli stili.</p>