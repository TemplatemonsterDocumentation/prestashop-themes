<h3>TM Google Map</h3>
<p>TIl suo modulo consente di aggiungere la mappa di Google con le posizioni dei tuoi negozi e la loro breve descrizione sulla tua pagina iniziale. La mappa mostrerà gli indirizzi di tutti i negozi relative al tuo negozio online. 
        È possibile modificare i dettagli dei tuoi negozi in Preferenze -> Scheda Contatti.</p>
<h5>Installazione e eliminazione</h5>
<p>Il modulo viene installato e cancellato come qualsiasi altro modulo Prestashop.</p>
<h5>Configurazione del modulo</h5>
<p>Dopo l'installazione è possibile modificare le impostazioni nella pagina di configurazione del modulo.</p>
<p>È possibile configurare lo stile e gli elementi della mappa.</p>
<ul class="index-list">
    <li>Seleziona lo stile della mappa nel primo campo (tutti gli stili vengono prelevati <a
        target="_blank" href="https://snazzymaps.com/">Snazzymaps</a>). È anche possibile aggiungere
    Lo stile di questo sito web. Scoprirete come aggiungerli alla fine di questo
    Istruzione.</li>
    <li>Seleziona il tipo di visualizzazione nel secondo campo (visualizzazione satellitare o vista strada).</li>
    <li>È anche possibile selezionare il rapporto di zoom per la mappa da 1 a 17.</li>
    <li>Opzione Zoom mentre scorri nell'area della mappa.</li>
    <li>È possibile disabilitare il controllo della mappa e la capacità di visualizzare le strade.</li>
    <li>Il modulo supporta i multi-store. È possibile modificare lo stile della mappa per ciascuno
    negozio.</li>
</ul>
