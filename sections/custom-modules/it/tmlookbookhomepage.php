<p>La Homepage di Lookbook TM è utilizzata per visualizzare il blocco di modulo di Lookbook TM sulla Homepage. Questo modulo consente di visualizzare sia le pagine lookbook che le pagine lookbook.</p>
<h5>Requisiti:</h5>
<ul class="marked-list">
    <li>PHP 5.4+</li>
    <li>PrestaShop 1.6</li>
    <li>TM Look Book Modulo</li>
</ul>
<h5>Modulo caratteristiche:</h5>
<ul class="marked-list">
    <li>Il modulo può essere visualizzato in 2 posizioni: displayHome e displayTopColumn.</li>
    <li>La pagina TM Look Book Homepage è un modulo figlio di TM Look Book, e per questo motivo non può essere utilizzato senza di essa.</li>
    <li>Il modulo può visualizzare la copertina del lookbook o le sue pagine.</li>
</ul>
<h5>TM Look Book Main Panello</h5>
<p>Al pannello principale del modulo è possibile aggiungere, eliminare e modificare i blocchi che il modulo verrà visualizzato nella pagina iniziale.</p>
<figure class="img-polaroid">
    <img src="img/tmlookbookhomepage-main-panel.png" alt=""/>
</figure>
<h5>Aggiunta di un nuovo blocco</h5>
<ul class="index-list">
    <li>
        <p>Per aggiungere un nuovo blocco, entra nel pannello principale del modulo e fai clic sul pulsante '+' (1) nella tabella con il gancio necessario.</p>
        <figure class="img-polaroid">
            <img src="img/tmlookbookhomepage-add-block-1.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Nel modulo visualizzato, impostare le seguenti opzioni:</p>
        <ul class="marked-list">
            <li><strong>Stato</strong> - lo stato del blocco. Questa impostazione consente di attivare o disattivare il blocco.</li>
            <li><strong>Tipo</strong> - consente di selezionare il tipo di blocco.</li>
            <li><strong>Lookbook</strong> - consente di selezionare un look precedentemente selezionato.</li>
        </ul>
    </li>
    <li>
        <p>Dopo aver impostato tutte le opzioni, premere il pulsante 'Salva' (2).</p>
        <figure class="img-polaroid">
            <img src="img/tmlookbookhomepage-add-block-2.png" alt=""/>
        </figure>
    </li>
</ul>
<p>Una volta creato il nuovo blocco, verrà visualizzato nella Pagina iniziale.</p>