<h3>TM Prodotto Custom Tab</h3>
<p>Il modulo serve a creare una scheda aggiuntiva nella pagina del prodotto per visualizzare ulteriori informazioni.
    Il modulo ha un vantaggio rispetto alla funzionalità esistente, grazie alla possibilità di aggiungere immagini, testo, video, collegamenti, ecc.</p>
<h4>Installazione e rimozione</h4>
<p>Il modulo viene installato e rimosso come qualsiasi altro modulo PrestaShop.</p>
<h4>Impostazioni del modulo</h4>
<p>Dopo l'installazione del modulo, è possibile aggiungere nuove schede per i prodotti nella pagina di configurazione del modulo e nella pagina di modifica del prodotto.</p>
<h5>Aggiunta di schede tramite la configurazione del modulo:</h5>
<p>Per aggiungere una nuova scheda, premi il pulsante 'Aggiungi nuovo' (1).</p>
<figure class="img-polaroid">
    <img src="img/tmcustomproducttab-1.png" alt=""/>
</figure>
<ul class="index-list">
    <li>
        <p>Aggiunta di una scheda a uno o più prodotti di una categoria selezionata in precedenza.</p>
        <figure class="img-polaroid">
            <img src="img/tmcustomproducttab-4.png" alt=""/>
        </figure>
        <ul class="marked-list">
            <li><strong>Intestazione Tab</strong> - nome della scheda, necessaria.</li>
            <li><strong>Tab Descrizione</strong> - le informazioni sulla scheda possono includere dati nel formato di un'immagine, di un testo, di un video, di collegamenti, ecc.</li>
            <li><strong>Stato</strong> - lo stato dell'elemento.</li>
            <li><strong>Altre impostazioni</strong> - abilitare/disabilitare altre impostazioni.</li>
            <li><strong>Seleziona categoria</strong> - scegliendo le categorie per cui la scheda deve essere visualizzata.</li>
            <li><strong>Prodotti da visualizzare</strong> - selezionando i prodotti delle categorie selezionate in precedenza.</li>
        </ul>
    </li>
    <li>
        <p>Aggiunta della scheda ai prodotti che appartengono a categorie selezionate.</p>
        <figure class="img-polaroid">
            <img src="img/tmcustomproducttab-3.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Per aggiungere una scheda a tutti i prodotti, disattivare l'opzione 'Impostazioni aggiuntive'.</p>
        <figure class="img-polaroid">
            <img src="img/tmcustomproducttab-2.png" alt=""/>
        </figure>
    </li>
</ul>
<h5>Modifica della posizione di visualizzazione scheda</h5>
<p>Trascinare e rilasciare la scheda necessaria per modificarne la posizione.</p>
<figure class="img-polaroid">
    <img src="img/tmcustomproducttab-5.png" alt=""/>
</figure>
<h5>Aggiunta di schede tramite le impostazioni del prodotto:</h5>
<p>Passare alla pagina del prodotto e fare clic sul link "TM Custom Product Product" nel menu a sinistra.</p>
<figure class="img-polaroid">
    <img src="img/tmcustomproducttab-6.png" alt=""/>
</figure>
<p>Se l'opzione "Impostazioni aggiuntive" è disattivata, la scheda verrà aggiunta al prodotto che è stato modificato in precedenza. È inoltre possibile aggiungere la scheda ai prodotti che appartengono a categorie selezionate oppure è possibile aggiungerlo a più prodotti appartenenti alla categoria selezionata in precedenza.</p>
<p>Per modificare la posizione di visualizzazione delle schede, trascinare e rilasciare i blocchi. È anche possibile modificare o rimuovere schede.</p>
