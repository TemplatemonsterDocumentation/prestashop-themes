    <img src="img/tmcategoriesbadges-1.png" alt=""/>
</figure>
<p>È possibile aggiungere distintivi a qualsiasi categoria dopo averlo selezionato dall'elenco a discesa.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoriesbadges-2.png" alt=""/>
</figure>
<p class="alert alert-warning"><span>Attenzione! Puoi aggiungere solo un distintivo per ogni categoria.</span></p>
<p>Quindi puoi aggiungere il testo visualizzato nel badge e selezionare lo stile dall'elenco a discesa (gli stili
    Possono differire in modelli diversi).</p>
<p>Quindi premere il pulsante 'Salva' (1).</p>
<figure class="img-polaroid">
    <img src="img/tmcategoriesbadges-3.png" alt=""/>
</figure>

<p>Puoi vedere l'elenco dei distintivi esistenti qui sotto. Qui puoi modificare o eliminare i distintivi
    Con l'aiuto di pulsanti corrispondenti.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoriesbadges-4.png" alt=""/>
</figure>
<p>Il modulo è multilingue e supporta i multi-store. Puoi aggiungere distintivi per
    Gruppo di negozi o tutti i negozi contemporaneamente. Saranno visualizzati nell'elenco come separati
    elementi.</p>
