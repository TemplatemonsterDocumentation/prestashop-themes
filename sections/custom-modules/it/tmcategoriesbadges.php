<h3>TM Categorie Badges</h3>
<p>Questo modulo consente di aggiungere distintivi alle categorie di barre laterali del Prestashop predefinito
    Modulo categorie ("Categorie di blocchi"). I distintivi possono essere aggiunti alla categoria di
    Qualsiasi livello e in qualsiasi lingua. Il campo di testo dell'etichetta è obbligatorio e dovrebbe
    Essere compilato in tutte le lingue. Se questo campo è lasciato vuoto in qualsiasi lingua, quindi
    Prenderà automaticamente il testo della lingua predefinita.</p>
<h5>Installazione e eliminazione</h5>
<p>Il modulo viene installato e cancellato come qualsiasi altro modulo Prestashop.</p>
<h5>Modulo Configurazione</h5>
<p>Dopo l'installazione è possibile modificare le impostazioni nella pagina di configurazione del modulo.</p>
