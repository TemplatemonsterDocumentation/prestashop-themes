<h3>TM Slider</h3>
<p>Il modulo è appositamente progettato per dimostrare in modo efficiente i contenuti. Dispone di tutto ciò che serve per mostrare i tuoi contenuti e fare un cursore professionale. Il modulo dispone di numerosi strumenti di personalizzazione che rendono il dispositivo di scorrimento super-facile da regolare.</p>

<h4>Module Installation</h4>
<p>Questo modulo può essere installato/disinstallato come qualsiasi altro modulo PrestaShop.</p>


<h4>Impostazioni del modulo</h4>
<p>Una volta installato il modulo è possibile configurarlo sulla configurazione del modulo
    pagina.</p>
<p>Per ottenere i dati necessari per la configurazione del modulo è necessario creare
    Applicazione corrispondente sulla risorsa necessaria.</p>
<p>Non è necessario utilizzare entrambe le varianti di accesso (Google, Facebook)
    Contemporaneamente, è possibile utilizzare solo uno di essi.</p>
<h4>Facebook Campi</h4>
<ul class="marked-list">
    <li>ID dell'App</li>
    <li>Chiave segreta dell'App</li>
</ul>
<h4>Google Fields</h4>
<ul class="marked-list">
    <li>ID dell'App</li>
    <li>Chiave segreta dell'App</li>
    <li>Percorso di indirizzi dopo l'accesso <strong>(tuo shop
            URL+index.php?fc=module&module=tmsociallogin&controller=googlelogin)</strong></li>
</ul>
<h4>VK Campi</h4>
<ul class="marked-list">
    <li>ID dell'App</li>
    <li>Chiave segreta dell'App</li>
    <li>Riga di reindirizzamento dopo l'accesso <strong>(tuo shop
            URL+index.php?fc=module&module=tmsociallogin&controller=vklogin)</strong></li>
</ul>
<p class="alert alert-info">Informazioni più dettagliate si possono ottenere seguendo i collegamenti: <a href="https://developers.facebook.com/docs/apps/register" target="_blank">Facebook App</a>, <a href="https://support.google.com/cloud/answer/6158849?hl=en&ref_topic=6262490" target="_blank">Google App</a> e <a href="https://vk.com/dev/web_how_to_start">VK App</a>.</p>
<p class="alert alert-warning">
    Il percorso di reindirizzamento deve essere lo stesso che è stato indicato durante la creazione di Google App o VK App.
</p>
