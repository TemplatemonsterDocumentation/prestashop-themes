<h3>TM Google Map 1.1.5</h3>
<p>Questo modulo consente di aggiungere Google Map alle posizioni dei negozi e di immettere brevi descrizioni nella Homepage. La mappa visualizzerà gli indirizzi di tutti i negozi collegati al tuo negozio online.</p>
<h5>Installazione e rimozione</h5>
<p>Il modulo viene installato e rimosso come qualsiasi altro modulo PrestaShop.</p>
<h5>Impostazioni del Modulo</h5>
<p>Dopo l'installazione del modulo, è possibile aggiungere i negozi alla mappa. Dopo aver premuto il pulsante 'Aggiungi nuovo negozio' (1), verrà reindirizzato alla scheda "Contatti memorizzare", in cui è possibile modificare i tuoi negozi.</p>
<p class="img-polaroid"><img src="img/tmgooglemap115-1.png" alt="" /></p>
<p>Per aggiungere un nuovo negozio, premi il pulsante 'Aggiungi nuovo'.</p>
<p class="img-polaroid"><img src="img/tmgooglemap115-2.png" alt="" /></p>
<ul class="marked-list">
    <li><strong>Seleziona un negozio</strong> - selezionare l'archivio che si desidera aggiungere alla mappa.</li>
    <li><strong>Stato</strong> - lo stato dell'elemento.</li>
    <li><strong>Marker</strong> - aggiungendo un marcatore personalizzato.</li>
    <li><strong>Custom testo</strong> - aggiungendo testo personalizzato.</li>
</ul>
<h5>Google Map display impostazioni</h5>
<p class="img-polaroid"><img src="img/tmgooglemap115-3.png" alt="" /></p>
<ul class="marked-list">
    <li><strong>Map Stile</strong> - selezionare lo stile di visualizzazione della mappa.</li>
    <li><strong>Map Tipo</strong> - selezionare il tipo di visualizzazione della mappa.</li>
    <li><strong>Zoom Livello</strong> - specificare il livello iniziale dello zoom della mappa (da 1 a 17).</li>
    <li><strong>Zoom on scroll</strong> - abilitare/disabilitare lo zoom della mappa durante lo scorrimento.</li>
    <li><strong>Map controllo</strong> - abilitare/disabilitare i controlli della mappa.</li>
    <li><strong>Strada visualizzazione</strong> - abilitare/disabilitare la vista stradale.</li>
    <li><strong>Animazione marker</strong> - abilitare/disabilitare l'indicatore di animazione.</li>
    <li><strong>Popup</strong> - abilitare/disabilitare finestre popup con informazioni sul negozio.</li>
</ul>