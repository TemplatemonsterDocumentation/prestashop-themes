<h5>Aggiunta/modifica delle opzioni di elemento</h5>
<div class="alert alert-info">Nota: il nome, la descrizione e l'immagine della categoria vengono caricati dalle proprietà delle categorie e non c'è bisogno di aggiungere manualmente. Per modificare queste opzioni, vai al pannello Amministrazione -> Catalogo -> Categorie -> scegli la categoria -> Modifica.</div>
<p>Quando si aggiunge un nuovo elemento al modulo (o si modifica uno dall'elenco degli elementi), è possibile scegliere alcune opzioni. La descrizione dettagliata delle opzioni che potete vedere sotto lo screenshot. Dopo aver impostato tutte le opzioni, fai clic sul pulsante "Salva" (1).</p>
<figure class="img-polaroid">
    <img src="img/tmhomepagecategorygallery-4.png" alt=""/>
</figure>
<ul class="marked-list">
	<li><strong>Seleziona categoria</strong> - qui si sceglie dall'elenco a discesa la categoria che dovrebbe essere visualizzata nella tua pagina.</li>
	<li><strong>Specifica classe</strong> - la classe specifica può essere aggiunta alla voce del modulo.</li>
	<li><strong>Display nome</strong> - opzione per visualizzare o nascondere il nome della categoria.</li>
	<li><strong>Categoria nome lunghezza</strong> - numero massimo di simboli da visualizzare nel nome della categoria. Se il nome è più lungo - il simbolo ".." verrà aggiunto dopo il numero di simboli che è stato impostato.</li>
	<li><strong>Display descrizione</strong> - opzione per visualizzare o nascondere la descrizione della categoria.</li>
	<li><strong>Descrizione lunghezza</strong> - numero massimo di simboli da visualizzare nella descrizione. Se la descrizione è più lunga, il simbolo '..' verrà aggiunto dopo il numero di simboli che sono stati impostati.</li>
	<li><strong>Ordina</strong> - l'ordine dell'oggetto nella galleria di categoria.</li>
	<li><strong>Display pulsante</strong> - l'opzione per mostrare o nascondere il pulsante 'Acquista ora' (il testo del pulsante può essere diverso a seconda del modello).</li>
	<li><strong>Stato</strong> - l'opzione per mostrare o nascondere la categoria</li>
	<li><strong>HTML contenuto</strong> - il campo in cui è possibile aggiungere alcuni contenuti HTML.</li>
</ul> 