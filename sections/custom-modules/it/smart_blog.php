<h4>RSS Smart Blog Feed</h4>
<h6>Il modulo che genera il nuovo feed per il tuo blog.</h6>
<p>Ha diverse impostazioni nel pannello di amministrazione:</p>
<ul class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Periodo di Aggiornamento</dt>
            <dd>consente di visualizzare i messaggi da tempo specifico
                Periodo (ora/giorno/settimana/mese/anno).
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Frequenza di Aggiornamento</dt>
            <dd>consente di impostare la frequenza dell'aggiornamento (1
                settimana, 2 ore, ecc.).
            </dd>
        </dl>
    </li>
</ul>
<p>Nella parte superiore c'è un percorso di file in cui verranno generate notizie. È costituito dalla
    sito web URL + /modules/smartblogfeed/rss.php.</p>
