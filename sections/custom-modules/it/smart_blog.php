<h3>Smart Blog</h3>
<p><strong>Smart blog -</strong> un insieme di moduli che consente di creare blog nel tuo
    negozio. È composto dai seguenti moduli:</p>
<ul class="marked-list">
    <li>Smart Blog</li>
    <li>RSS Smart Blog Feed</li>
    <li>Smart Blog Archivio</li>
    <li>Smart Blog Categorie</li>
    <li>Smart Blog Home Più recente</li>
    <li>Smart Blog Più recente Commenti</li>
    <li>Smart Blog AddThisModule</li>
    <li>Smart Blog Messaggi Popolari</li>
    <li>Smart Blog Messaggi Recenti</li>
    <li>Smart Blog Messaggio Correlato</li>
    <li>Smart Blog Search</li>
    <li>Smart Blog Tags</li>
</ul>
<p>Ha anche i suoi ganci dove i moduli blog possono essere visualizzati
    sul sito, possono anche apparire nei ganci standard (solo colonne).
</p>
<h4>Smart Blog</h4>
<p>Questo è il modulo principale che contiene molte impostazioni per la funzionalità del blog. Dopo
    la sua installazione la scheda Menu Blog verrà visualizzata nel menu principale del tuo amministratore
    pannello dove è possibile aggiungere/rimuovere/modificare i post/categorie o selezionare il tipo di
    le immagini e le loro dimensioni utilizzate nei posti (tipi e dimensioni del post
    le immagini sono diverse dalle impostazioni delle immagini del prodotto nel tuo negozio). Per aggiungere il file
        (Moduli -> Funzioni del Front Office -> Smart Blog)</strong> e aggiungere tutti
    parametri.</p>
<h6>Descrizione dei parametri:</h6>
<ul class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Meta Titolo</dt>
            <dd>il titolo del tuo blog che apparirà su tutte le sue pagine.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Meta Keyword</dt>
            <dd>keywords che verrà utilizzato dai motori di ricerca per ottimizzare SEO.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Meta Descrizione</dt>
            <dd>descrizione del tuo blog utilizzato dai motori di ricerca per una migliore ottimizzazione SEO.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Main Blog Url</dt>
            <dd>URL del tuo Blog.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Utilizza .html con Friendly Url</dt>
            <dd>abilitare/disabilitare l'aggiunta di .html agli URL di pagine del blog utilizzando URL amichevoli.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Numero di messaggi per pagina</dt>
            <dd>numero di messaggi che vengono visualizzati per pagina (pagine categoria/archivi, ecc.).</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Auto ha accettato il commento</dt>
            <dd>abilitare/disabilitare i commenti degli utenti accettati automaticamente; Se abilitato, i commenti saranno disponibili senza
                admin moderazione.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Attiva Captcha</dt>
            <dd>abilitare/disabilitare Captcha in Lascia il modulo di commento sulle pagine dei prodotti.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Attiva Commenti</dt>
            <dd>abilitare/disabilitare i commenti lasciati dagli utenti.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mostra il Nome dell'Autore</dt>
            <dd>abilitare/disabilitare la visualizzazione di autori postali sulle pagine del blog e inviare pagine singole</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mostra Post Visto</dt>
            <dd>abilitare/disabilitare la visualizzazione del numero di volte in cui gli utenti hanno visualizzato il post. Sarà visualizzato sotto il post sulle pagine del blog e pagine singole di blog.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mostra Stile Autore Nome</dt>
            <dd>cambiando il formato del nome dell'autore.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>AVATAR Immagine</dt>
            <dd>aggiungendo l'immagine avatar standard per tutti gli utenti.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mostra No Immagine</dt>
            <dd>abilitare/disabilitare l'immagine. Nessuna immagine è impostata se non esiste un'immagine per post / categoria, ecc.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mostra la Categoria</dt>
            <dd>abilitare/disabilitare l'immagine di categoria e la descrizione sulle pagine della categoria del blog.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Impostazione Colonna Pagina Blog</dt>
            <dd>impostando la visualizzazione dei moduli correlati al Blog. Spesso richiede impostazioni aggiuntive, quindi
                si consiglia di installare moduli aggiuntivi allo stesso modo dei moduli PrestaShop standard
                sono installati.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Personalizzato CSS</dt>
            <dd>è possibile aggiungere altri stili in questo campo. Fare attenzione utilizzando le classi già utilizzate
                nel tema, poiché potrebbero verificarsi alcuni conflitti.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Cancellare Vecchio Thumblr</dt>
            <dd>viene utilizzato per rigenerare le immagini del blog nel caso in cui le dimensioni dell'immagine siano state modificate o il nuovo tipo di immagine
                è stato aggiunto. Per rigenerare, seleziona <strong>'Si'</strong> e clicca <strong>'Rigenerare'</strong>.
            </dd>
        </dl>
    </li>
</ul>
