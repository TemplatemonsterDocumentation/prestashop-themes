<h3>TM Prodotti Manager</h3>
<h4>Introduzione</h4>
<p>Il modulo è creato per la gestione di negozi online, in particolare negozi con grande quantità di prodotti. Utilizzando questo modulo è possibile filtrare facilmente i tuoi prodotti e fare operazioni diverse su di loro.</p>

<h4>Installazione e rimozione del modulo</h4>
<p>Il modulo viene installato e rimosso come qualsiasi modulo Prestashop.</p>
<p class="alert alert-info">
    <span>La rimozione o la reinstallazione del modulo non ripristina le modifiche apportate.</span>
</p>
<p>Quando il modulo è stato installato correttamente, andare al catalogo -> TM Products Manager (1) per visualizzare il menu principale del modulo.</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-1.png" alt=""/></figure>
<h4>Prestazione</h4>
<p>Il menu principale del modulo è suddiviso in due parti principali: 'Scegli la sezione' (1) e 'Scegli opzione' (2).</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-2.png" alt=""/></figure>
<p>La prima parte viene creata sotto forma di schede - Informazioni, Prezzo, Associazioni e Quantità.</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-3.png" alt=""/></figure>
<ul class="marked-list">
    <li><strong>Informazione</strong> - Opzioni relative alla disponibilità e descrizione del prodotto.</li>
    <li><strong>Prezzo</strong> - le impostazioni dei prezzi e delle impostazioni fiscali.</li>
    <li><strong>Associazioni</strong> - capacità di cambiare marca, categoria o accessori di diversi prodotti.</li>
    <li><strong>Quantità</strong> - cambiando la quantità di prodotti.</li>
</ul>
<ul class="index-list">
    <li>
        <p>Fare clic su ciascuna scheda per visualizzare le opzioni disponibili in questa sezione. Saranno descritte ulteriormente. <br>
            Ogni opzione ha un suggerimento - una breve descrizione, che appare nella destra quando si posiziona l'opzione (1).</p>
        <figure class="img-polaroid"><img src="img/tmproductsmanager-4.png" alt=""/></figure>
    </li>
    <li>
        <p>Scegli l'opzione necessaria e fai clic su di esso.</p>
    </li>
    <li>
        <p>Il passo successivo è quello di filtrare i prodotti. Impostare le opzioni di filtraggio in base alle proprie esigenze e fare clic sul pulsante 'Seleziona' (1). Per cancellare i filtri selezionati, fare clic sul pulsante 'Reset' (2).</p>
        <figure class="img-polaroid"><img src="img/tmproductsmanager-5.png" alt=""/></figure>
        <p>Puoi anche annullare ciascuno dei filtri dopo averli selezionati. Basta cliccare sulla croce (1) vicino al valore della proprietà del prodotto e sarà rimosso.</p>
        <figure class="img-polaroid"><img src="img/tmproductsmanager-6.png" alt=""/></figure>
    </li>
    <li>
        <p>Sotto i filtri c'è un elenco di prodotti che corrispondono ai criteri selezionati. È possibile modificarlo se è necessario eliminare alcuni dei prodotti dall'elenco. Ogni prodotto può essere rimosso separatamente facendo clic sul pulsante "Rimuovi dall'elenco" (1). Per rimuovere tutti i prodotti dall'elenco, fare clic sui pulsanti "Seleziona tutto" (2) e "Rimuovi selezionati" (3). Se hai controllato un gran numero di prodotti e devi deselezionarli, fai clic sul pulsante "Annulla tutto" (4).</p>
        <figure class="img-polaroid"><img src="img/tmproductsmanager-7.png" alt=""/></figure>
    </li>
    <li>
        <p>Quando l'elenco dei prodotti selezionati è conforme a tutti i requisiti, fare clic sul pulsante 'Process' (1).</p>
        <figure class="img-polaroid"><img src="img/tmproductsmanager-8.png" alt=""/></figure>
    </li>
    <li>
        <p>Impostare l'opzione/le opzioni in base alle proprie esigenze e fare clic sul pulsante 'Salva' (1). Se è necessario apportare alcune modifiche all'elenco dei prodotti, fare clic sul pulsante "Indietro al filtro" (2).</p>
        <p class="alert alert-info">
            <span>Tieni presente che non potrai annullare queste modifiche allo stesso modo.</span>
        </p>
        <figure class="img-polaroid"><img src="img/tmproductsmanager-9.png" alt=""/></figure>
    </li>
</ul>

<h4>Opzioni disponibili</h4>
<h5>Informazione</h5>
<p>La sezione consente di configurare le seguenti opzioni:</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-10.png" alt=""/></figure>
<ul class="marked-list">
    <li><strong>Attiva/disattiva i prodotti</strong> - impostare lo stato di attivazione / disattivazione dei prodotti.</li>
    <li><strong>Disponibile per l'ordine</strong> - rendere alcuni prodotti disponibili o non disponibili per l'ordine.
        <p class="alert alert-warning small">
            <span>NOTA: se 'Disponibile per l'ordine' è impostato su 'Sì' per i prodotti con prezzi nascosti, i loro prezzi verranno automaticamente nuovamente visibili.</span>
        </p>
    </li>
    <li><strong>Mostra prezzo</strong> - mostrare o nascondere il prezzo del prodotto.
        <p class="alert alert-warning small">
            <span>NOTA: se impostate "Mostra prezzo" su "No" per i prodotti disponibili per l'ordine, verranno automaticamente cambiati come non disponibili.</span>
        </p>
    </li>
    <li><strong>Solo online</strong> - Applicare il segno 'Solo in linea' agli elementi selezionati.</li>
    <li><strong>Condizione</strong> - modificare la condizione di più prodotti.</li>
    <li><strong>Breve descrizione</strong> - aggiungere una breve descrizione che dovrebbe essere applicata a tutti i prodotti selezionati.</li>
    <li><strong>Descrizione completa</strong> - aggiungere una descrizione a prodotti selezionati con la possibilità di posizionarla prima della descrizione principale, dopo di essa o sostituire l'intera descrizione con quella nuova.</li>
    <li><strong>Tags</strong> - aggiungere altri tag ai prodotti scelti.</li>
</ul>

<h5>Prezzo</h5>
<p>In questa sezione è possibile modificare le opzioni relative al prezzo del prodotto.</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-11.png" alt=""/></figure>
<ul class="marked-list">
    <li><strong>Display "In vendita"</strong> - visualizzare gli elementi selezionati con il segno "In vendita".</li>
    <li><strong>Modifica delle regole fiscali</strong> - scegliere le regole fiscali che verranno applicate a più prodotti.</li>
    <li><strong>Aggiungere nuovo prezzo specifico</strong> - impostare il prezzo speciale per i prodotti selezionati con la possibilità di aggiungere il periodo di attivazione dei prezzi.
</li>
</ul>

<h5>Associazioni</h5>
<p>Qui è possibile modificare le opzioni relative alle relazioni con i prodotti.</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-12.png" alt=""/></figure>
<ul class="marked-list">
    <li><strong>Cambiare marca</strong> - cambiare marca di elementi selezionati.</li>
    <li><strong>Modifica la categoria predefinita</strong> - scegliere la categoria che verrà impostata come predefinita per alcuni prodotti.
        <p class="alert alert-warning small">
            <span>NOTA: è possibile scegliere una sola categoria predefinita. Tieni presente che la tua categoria di default corrente verrà sostituita con quella nuova.</span>
        </p>
    </li>
    <li><strong>Cambiare categorie correlate</strong> - impostare le categorie correlate a più prodotti con la possibilità di salvare o rimuovere le vecchie relazioni.</li>
    <li><strong>Aggiungi accessori</strong> - aggiungere gli accessori adatti ai prodotti scelti.</li>
</ul>

<h5>Quantità</h5>
<p>Questa opzione consente di impostare la quantità di prodotto o aumentare / diminuire la quantità corrente per un certo importo.</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-13.png" alt=""/></figure>
<ul class="marked-list">
    <li><strong>Cambia quantità</strong> - impostare la quantità richiesta di alcuni prodotti.</li>
</ul>
