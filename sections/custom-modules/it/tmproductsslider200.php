<h4>Tipi di galleria e impostazioni consigliate</h4>
<p>Esistono quattro tipi di visualizzazione della galleria (standard, elenco, griglia, larghezza totale). Ognuno di essi può essere configurato in modo indipendente
    E si può passare da un altro se necessario.</p>
<p>Per impostazione predefinita, la galleria utilizza i seguenti tipi di immagini di prodotto: large_default - per l'immagine principale; Medio_default -
    Per le miniature.</p>
