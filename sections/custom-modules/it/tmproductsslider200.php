<h4>Introduzione</h4>
<p>Questo modulo serve per creare gallerie, basandosi sui dati del prodotto e visualizzandoli in forma di diapositive sul sito web
    fine frontale. Il modulo fornisce 4 metodi di visualizzazione della galleria con impostazioni e aspetto individuali. Ogni
    Il metodo ha funzionalità predefinite. È inoltre disponibile la configurazione avanzata. Il modulo può visualizzare un numero illimitato
    Quantità di prodotti con diverse informazioni</p>
<h4>Installazione e rimozione del modulo</h4>
<p class="alert alert-warning">
    Se la versione 1.n di questo modulo è installata nel motore di prestashop, non si suggerisce di aggiornarlo con quello corrente. Innanzitutto è necessario rimuovere la vecchia versione del modulo per eseguire senza errori.
</p>
<p>Il modulo è installato e rimosso come qualsiasi modulo Prestashop predefinito.</p>
<p class="alert alert-info">
    <span>Dopo la rimozione o la reinstallazione del modulo, tutte le impostazioni ei filtri vengono scartati.</span>
</p>
<h4>Aggiunta/eliminazione/modifica di una diapositiva</h4>
<h5>Aggiunta di una diapositiva</h5>
<p>Per aggiungere un prodotto alla galleria, vai al prodotto desiderato nella finestra <strong>CAtalog
        -> Prodotti</strong> sezione. Quindi premere il tasto <strong>'Modifica'</strong> pulsante (1) accanto al prodotto desiderato.</p>
<figure class="img-polaroid"><img src="img/tmproductsslider2-1.png" alt=""/></figure>
<p>Nella finestra apparsa per modificare le informazioni sul prodotto, passare alla pagina <strong>TM Prodotti Slider</strong>
    scheda (2) e contrassegno (3) che si desidera che il prodotto venga visualizzato nel cursore.</p>
<figure class="img-polaroid"><img src="img/tmproductsslider2-2.png" alt=""/></figure>
<p>Salvare le modifiche premendo uno <strong>'Salva e rimani'</strong> (4) o <strong>'Salva'</strong> pulsante (5).</p>
<h5>Rimozione di diapositive</h5>
<p>Ci sono due modi per eliminare una diapositiva.</p>
<ul class="index-list">
    <li>
        <p>Per escludere un prodotto dalla galleria, vai al prodotto desiderato <strong>Catalogo -> Prodotti</strong>
            sezione. Quindi premere il tasto <strong>'Modifica'</strong> pulsanti (1) accanto al prodotto necessario.</p>
        <figure class="img-polaroid"><img src="img/tmproductsslider2-1.png" alt=""/></figure>
        <p>Nella finestra apparsa per modificare le informazioni sul prodotto, passare alla pagina <strong>TM Prodotti
                Slider</strong> (2) e rimuovere il segno di spunta (3), il che significa che non si desidera che il prodotto sia
            Visualizzato nel cursore:</p>
        <figure class="img-polaroid"><img src="img/tmproductsslider2-3.png" alt=""/></figure>
        <p>Salvare le modifiche premendo uno <strong>'Salva e rimani'</strong> (4) or <strong>'Save'</strong> pulsante (5).
        </p>
    </li>
    <li>
        <p>Il secondo modo è quello di navigare alla pagina di configurazione del modulo, trovare il prodotto desiderato nella pagina <strong>Slides
                lista</strong> o premi il tasto <strong>Elimina</strong> pulsante (1) vicino a esso.</p>
        <figure class="img-polaroid"><img src="img/tmproductsslider2-4.png" alt=""/></figure>
        <p>Confermare la cancellazione.</p>
    </li>
</ul>
