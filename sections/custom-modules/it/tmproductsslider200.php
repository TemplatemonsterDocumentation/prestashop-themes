    Pagina di configurazione del modulo e trascinare la diapositiva nel punto desiderato <strong>Slides lista</strong> tabella.</p>
<figure class="img-polaroid"><img src="img/tmproductsslider2-5.png" alt=""/></figure>
<h5>Disattivare le diapositive</h5>
