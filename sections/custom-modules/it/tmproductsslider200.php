<h4>Impostazioni avanzate</h4>
<p>Per accedere a Impostazioni estese, spostare il tasto <strong>Estese
        Impostazioni</strong> Scorrimento verso la posizione Sì.</p>
<figure class="img-polaroid"><img src="img/tmproductsslider2-11.png" alt=""/></figure>
<ul class="marked-list">
    <li><strong>Gallery Tipo</strong> - tipo di galleria attiva</li>
    <li><strong>Gallery dimensione</strong> - active gallery dimensione</li>
    <li><strong>Gallery altezza</strong> - active gallery altezza</li>
    <li><strong>Impostazioni estese</strong> - impostazioni avanzate; Se l'opzione viene spostata in posizione "No", tutte le
        Le impostazioni avanzate verranno salvate e potrete ripristinarle in qualsiasi momento. Allo stesso tempo, in frontend sarà
        visualizzato come per impostazione predefinita.
    </li>
    <li><strong>Slider Durata</strong> - la velocità di cambiamento delle diapositive</li>
    <li><strong>Usa Navigazione</strong> - la visualizzazione dei tasti Prev / Next per la commutazione delle diapositive</li>
    <li><strong>Usa Thumbnails</strong> - utilizzando le miniature con informazioni aggiuntive sulle diapositive come elemento per
        Commutando la diapositiva corrente
    </li>
    <li><strong>Usa Paginazione</strong> - utilizzando la paginazione come elemento per commutare la diapositiva corrente</li>
    <li><strong>Usa Immagine Galleria</strong> - utilizzando immagini di prodotto aggiuntive all'interno della diapositivas</li>
    <li><strong>Consentire autoplay</strong> - consentono la commutazione automatica delle diapositive</li>
    <li><strong>Slider Intervallo</strong> - l'intervallo di tempo, dopo il quale la diapositiva passerà ad un altro (solo disponibile
        se 'Consenti autoplay' è attivo)
    </li>
    <li><strong>Consentire Loop</strong> - consentono di ripetere le diapositive dall'inizio dopo che tutte le diapositive sono state mostrate</li>
</ul>
<p class="alert alert-info">
    <span>Dopo aver commutato la galleria, viene salvata tutta la configurazione della galleria utilizzata in precedenza.</span>
</p>
<p class="alert alert-info">
    <span>Dopo aver disattivato/attivato le impostazioni avanzate, le impostazioni vengono salvate e potrete tornare in qualsiasi momento.</span>
</p>
