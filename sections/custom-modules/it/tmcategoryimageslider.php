<h3>TM Category Immagine Slider</h3>
<p>Il modulo consente di creare diapositive multilingue per qualsiasi categoria del negozio. Sullo schermo verrà visualizzato il cursore
    Pagine.</p>
<h4>Installazione e rimozione</h4>
<p>La procedura di installazione è standard, il modulo può essere installato e eliminato come un normale modulo PrestaShop.</p>
<h4>Impostazioni del modulo</h4>
<p>Dopo l'installazione del modulo, è possibile impostare o modificare le opzioni necessarie nella pagina di configurazione del modulo.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryimagesslider-1.png" alt=""/>
</figure>
<ul class="marked-list">
    <li><strong>Modo</strong> - tipo di animazione (dissolvenza, verticale, orizzontale).</li>
    <li><strong>Velocità</strong> - velocità di animazione.</li>
    <li><strong>Larghezza</strong> - larghezza diapositive (per la vista corretta, si consiglia l'utilizzo della larghezza di immagine effettiva).
    </li>
    <li><strong>Altezza</strong> - altezza di scorrimento (per la visualizzazione corretta, si consiglia l'utilizzo dell'altezza reale dell'immagine).
    </li>
    <li><strong>Auto</strong> - autoplay quando la pagina viene aperta.</li>
    <li><strong>Pausa</strong> - tempo prima di passare alla diapositiva successiva.</li>
    <li><strong>Auto Hover</strong> - metti in pausa sulla levetta.</li>
    <li><strong>Controlli</strong> - pulsanti "Successivo" / "Precedente".</li>
    <li><strong>Next Testo</strong> - "Next" pulsante testo.</li>
    <li><strong>Prev Testo</strong> -"Precedente" pulsante testo.</li>
    <li><strong>Auto Controlli</strong> - "Play"/"Pause" pulsanti.</li>
    <li><strong>Pager</strong> - paginazione.</li>
</ul>
<h5>Aggiunta/rimozione/modifica di diapositive</h5>
<p>Dopo l'installazione del modulo, verrà visualizzato il nuovo campo 'Slides' nelle impostazioni di categoria.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryimagesslider-2.png" alt=""/>
</figure>
<p>Per aggiungere una diapositiva, fai clic sul pulsante Aggiungi nuovo diapositiva. Compila i campi necessari nel modulo.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryimagesslider-3.png" alt=""/>
</figure>
<ul class="marked-list">
    <li><strong>Seleziona un file</strong> - seleziona il tuo file. È possibile caricare immagini diverse per ogni lingua. Nel caso
        Non è selezionata alcuna immagine per la lingua specifica, vengono caricate le immagini impostate per la lingua predefinita.
    </li>
    <li><strong>Inserisci il titolo Slide</strong> - digitare il titolo della diapositiva.</li>
    <li><strong>Inserisci Url diapositiva</strong> - fornire l'URL di diapositive.</li>
    <li><strong>Inserisci la didascalia</strong> - digitare la didascalia.</li>
    <li><strong>Apri in una nuova finestra</strong> - apri in una nuova finestra.</li>
    <li><strong>Stato</strong> - attivare/disattivare la diapositiva.</li>
</ul>
<p>Quando la diapositiva viene salvata, apparirà in un elenco di diapositive di categoria.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryimagesslider-4.png" alt=""/>
</figure>
<p>Per apportare modifiche alla diapositiva, fai clic sul pulsante <strong>'Modifica'</strong> pulsante.</p>
<p>Per eliminare la diapositiva, fare clic su <strong>'Delete'</strong> sotto il <strong>'Modifica'</strong> pulsante.</p>
<div class="alert alert-info">
    <span>Le diapositive possono essere aggiunte solo alle categorie salvate.</span>
</div>