<h5>Aggiungere il modulo Parallax Media</h5>
<ul class="index-list">
    <li>
        <p>È necessario aggiungere l'id o la classe nel contenuto della pagina HTML. È anche possibile utilizzare un tag univoco, questo
            Elemento non deve essere ripetuto.</p>
    </li>

    <li>
        <p>Crea l'immagine della dimensione richiesta (è auspicabile creare l'immagine di dimensione più grande del blocco dove
            L'effetto verrà aggiunto). Se vuoi aggiungere il video, dovrai prepararlo in tre formati:
            .mp4, .webm e .ogv e anche aggiungere l'anteprima della stessa dimensione (.png, .jpg e .gif)</p>
    </li>
    <li>
        <p>Click <strong>'Aggiungi container'</strong> pulsante in <strong>'Aggiungi nuovo blocco'</strong>.</p>
