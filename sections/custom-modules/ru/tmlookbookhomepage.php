<p class="alert alert-warning"> Чтобы использовать модуль, у Вас должен быть установлен модуль TM Look Book.</p>
<p>TM Look Book Homepage используется для отображения модуля TM Look Book на Домашней странице. Этот модуль позволяет отображать как обложки лукбука, так и его страницы.</p>
<h5>Требования:</h5>
<ul class="marked-list">
    <li>PHP 5.4+</li>
    <li>PrestaShop 1.6</li>
    <li>Модуль TM Look Book</li>
</ul>
<h5>Свойства модуля:</h5>
<ul class="marked-list">
    <li>Модуль можно отобразить в двух позициях: displayHome и displayTopColumn.</li>
    <li>TM Look Book Homepage – это дочерний модуль TM Look Book, поэтому его невозможно без него использовать.</li>
    <li> Модуль может отображать обложку лукбука или его страницы.</li>
</ul>
<h5>Основная панель TM Look Book</h5>
<p> В основной панели модуля Вы можете добавлять, удалять и редактировать блоки, которые модуль будет отображать на Домашней странице. </p>
<figure class="img-polaroid">
    <img src="img/tmlookbookhomepage-main-panel.png" alt=""/>
</figure>
<h5>Добавление нового блока</h5>
<ul class="index-list">
    <li>
        <p>Для того чтобы добавить новый блок, войдите в панель модуля и нажмите на '+' (1) в таблице с нужным хуком.</p>
        <figure class="img-polaroid">
            <img src="img/tmlookbookhomepage-add-block-1.png" alt=""/>
        </figure>
    </li>
    <li>
        <p> В появившейся форме настройте следующие параметры:</p>
        <ul class="marked-list">
            <li><strong>Статус</strong> (Status) – статус блока. Эта настройка позволяет включить или отключить отображение блока.</li>
            <li><strong>Тип</strong> (Type) – позволяет выбрать тип блока.</li>
            <li><strong>Лукбук</strong> (Lookbook) – позволяет выбрать ранее выбранный лук.</li>
        </ul>
    </li>
    <li>
        <p>Когда Вы указали значения всех настроек, нажмите на кнопку 'Сохранить' (2).</p>
        <figure class="img-polaroid">
            <img src="img/tmlookbookhomepage-add-block-2.png" alt=""/>
        </figure>
    </li>
</ul>
<p> После создания нового блока он будет отображаться на Домашней странице.</p>
