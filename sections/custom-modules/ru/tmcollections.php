<h5>Статистика</h5>
<p>С помощью этого модуля Вы можете также получить статистику, собранную в то время, когда товары добавляются в коллекции. Таким образом, Вы будете знать, какие товары в настоящее время популярны. Эта информация может быть очень полезной, например, для предоставления скидок. <br>
 Вы можете просмотреть статистику, нажав на кнопку Админ панель -> Статистика -> Статистика -> TM Collections. <br>
</p>
<figure class="img-polaroid">
    <img src="img/tmcollections13.png" alt=""/>
</figure>
<ul class="marked-list">
    <li> Вы можете выбрать период для просмотра статистики с указанного времени (1).</li>
    <li> В первой колонке указано количество добавлений товара (2).</li>
    <li> Вторая колонка создана для вашего удобства. Вы можете использовать её, чтобы узнать, сколько добавленных товаров было продано (3).
