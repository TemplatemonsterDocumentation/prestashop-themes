<h6> Модуль TM Collections создан, чтобы дать возможность посетителям интернет-магазина добавлять товары в коллекции и делиться этими коллекциями на Facebook.</h6>
<h4>Установка модуля</h4>
<p>Для того чтобы установить модуль, перейдите на вкладку Админ панель -> Модули и службы -> Модули и службы (1), найдите модуль TM Collections (2) и нажмите на кнопку 'Установить' (3).</p>
<figure class="img-polaroid">
    <img src="img/tmcollections1.png" alt=""/>
</figure>
<h4>Настройка модуля</h4>
<p>После того как модуль установлен, отобразится страница 'Настройки'.<br>
  Добавьте Идентификатор профиля Facebook в нужное поле (1) и нажмите на кнопку 'Сохранить' (2).<br>
    Вы найдете инструкцию, как получить Идентификатор профиля Facebook здесь: <a
        href="https://developers.facebook.com/docs/apps/register" target="blank">Создание Идентификатора приложения</a>.</p>
<figure class="img-polaroid">
    <img src="img/tmcollections2.png" alt=""/>
</figure>
<h4>Использование модуля TM Collections</h4>
<h5>Добавление товара в коллекцию</h5>
<p class="alert alert-info">Гости сайта должны войти в систему, для того чтобы добавить элементы в коллекцию.</p>
<ul class="index-list">
    <li>
        <p>После того как модуль установлен, отобразится кнопка 'Добавить в коллекцию' на каждой странице товара. Она будет выглядеть, как показано на скриншоте ниже (1).</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections3.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Нажав на эту кнопку, гости сайта увидят уведомление: 'Товар успешно добавлен в коллекцию.' Это уведомление можно закрыть или пропустить, нажав на кнопку 'Мои коллекции'            (2).</p>
        <figure class="img-polaroid">
            <img src="img/tmcollections4.png" alt=""/>
        </figure>
    </li>
</ul>
