<h3>Smart Blog</h3>
<p><strong>Smart blog</strong> - это набор модулей, которые позволяют создать блог в вашем магазине. Он состоит из следующих модулей:</p>
<ul class="marked-list">
    <li>Smart Blog</li>
    <li>RSS Smart Blog Feed</li>
    <li>Smart Blog Archive</li>
    <li>Smart Blog Categories</li>
    <li>Smart Blog Home Latest</li>
    <li>Smart Blog Latest Comments</li>
    <li>Smart Blog AddThisModule</li>
    <li>Smart Blog Popular Posts</li>
    <li>Smart Blog Recent Posts</li>
    <li>Smart Blog Related Post</li>
    <li>Smart Blog Search</li>
    <li>Smart Blog Tags</li>
</ul>
<p> У этого модуля также есть свои хуки, в которых модули блога можно отобразить на сайте. Они также могут отображаться в стандартных хуках (только колонки).</p>
<h4>Smart Blog</h4>
<p>Это основной модуль, который содержит множество параметров для работы с блогами. После его установки в главном меню вашей админ панели появится вкладка меню «Блог», где Вы можете добавить / удалить / редактировать записи/категории или выбрать тип изображений, используемых в сообщениях,  и их размеры (типы и размеры изображений записей отличаются от настроек изображений товаров в вашем магазине). Чтобы добавить параметры фронтенда модуля, перейдите на вкладку <strong>Smart Blog (Модули -> Свойства фронтенда -> Smart Blog) </ strong> и добавьте все параметры.</p>
<h6>Описание параметров:</h6>
<ul class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Мета-название</dt>
            <dd> Название вашего блога, которое будет отображаться на всех его страницах.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Мета-ключевые слова</dt>
            <dd> Ключевые слова, которые будут использоваться поисковыми системами для лучшей SEO-оптимизации.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Мета-описание</dt>
            <dd> Описание вашего блога, используемое поисковыми системами для лучшей SEO-оптимизации.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Основная ссылка блога</dt>
            <dd>ссылка на ваш Блог.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Использование .html с ЧПУ</dt>
            <dd> Включить / отключить добавление .html в ссылки страниц блога с использованием ЧПУ.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Количество записей на странице</dt>
            <dd>количество сообщений, отображаемых на страницах (категории / страницы архивов и т. д.)</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Автоматически одобрять комментарии</dt>
            <dd> Включить / отключить автоматическое одобрение пользовательских комментариев. Если этот параметр включен, комментарии будут доступны без модерации админа.          </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Включить Captcha</dt>
            <dd> Включить / отключить Captcha в разделе Оставить комментарий на страницах товаров.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Включить комментарий</dt>
            <dd> Включить / отключить комментарии пользователей.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Отобразить имя автора</dt>
            <dd> Включить / отключить отображение сообщений на страницах блога и на страницах отдельных записей </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Отображать количество просмотров записей</dt>
            <dd>Включить / отключить отображение количества просмотров записей пользователями. Оно отобразится под записью на страницах блога и на единичных страницах записей.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Отобразить стиль имени автора</dt>
            <dd> Изменение формата имени автора.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Изображение AVATAR</dt>
            <dd>Добавление стандартного изображения аватара для всех пользователей.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Отображать «Нет изображения»</dt>
            <dd>Включить / отключить изображение. «Нет изображения» отображается, если отсутствует изображение для записи/категории и т.д.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Отображение категории</dt>
            <dd>Включить / отключить изображение категории и описание на страницах категорий блога.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Настройки колонки страницы блога</dt>
            <dd> Настройка отображения модулей, связанных с блогами. Они часто требует дополнительных настроек, поэтому рекомендуется устанавливать дополнительные модули так же, как и стандартные модули PrestaShop.            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Пользовательский CSS</dt>
            <dd> Вы можете добавить дополнительные стили в это поле. Будьте осторожны, используя классы, которые уже используются в шаблоне, так как могут возникнуть конфликты.            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Удалить старый Thumblr</dt>
            <dd> Используется для пересоздания изображений блога в случае изменения размеров изображений или добавления нового типа изображений. Для того чтобы пересоздать изображения, выберите <strong>'Да'</strong> (Yes) и нажмите на кнопку <strong>'Пересоздать'</strong> (Regenerate).
            </dd>
        </dl>
    </li>
</ul>
