<h4> RSS Smart Blog Feed</h4>
<h6>Модуль создания новостной ленты для вашего Блога.</h6>
<p>Модуль имеет несколько настроек в админ панели:</p>
<ul class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Период обновления</dt>
            <dd>Позволяет отобразить записи определенного периода времени (час/день/неделя/месяц/год).            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Частота обновления</dt>
            <dd> Позволяет установить частоту обновления (1 неделя, 2 часа и т. д.) </dd>
        </dl>
    </li>
</ul>
<p> Вверху есть путь к файлу, где будут генерироваться новости. Он состоит из ссылки на сайт + /modules/smartblogfeed/rss.php.</p>
