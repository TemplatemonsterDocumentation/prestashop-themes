<h4>Настройки формы для добавления эффекта parallax</h4>
<ul class="index-list">
    <li>Селектор – селектор CSS элемента, к которому применён эффект parallax. Например, '#header .nav'</li>
    <li>Статус элемента – статус элемента (parallax не будет отображаться, если он отключен)</li>
    <li>Скорость – цифровое значение от 0 до 2, которое определяет скорость основного блока parallax </li>
    <li>Отступ – отступ основного слоя по оси Y </li>
    <li>Обратно – направление parallax</li>
    <li>Выцветание - эта настройка позволяет настроить постепенное исчезновение основного блока при прокрутке страницы </li>
    <li> Принудительная полная ширина – принудительная полная ширина блока parallax </li>
</ul>
<p>Нажмите на кнопку 'Сохранить', заполнив все нужные поля.</p>
