<h4> Предупреждения и рекомендации </h4>
<ul class="index-list">
    <li> Для мобильных устройств (смартфоны, планшеты) эффект parallax по умолчанию отключен, а верхний слой изображения блока используется вместо этого как фон.</li>
    <li> Принудительная ширина блока parallax больше влияет на скорость загрузки страницы, чем обычная загрузка полноширинного блока.</li>
    <li> Чем меньше размер загружаемого видео, тем лучше..</li>
</ul>
