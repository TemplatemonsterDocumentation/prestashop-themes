<h4>Главная панель TM Homepage Combinations</h4>
<p> Это основная панель модуля, которая позволяет добавлять, редактировать и удалять элементы. Для создания нового элемента, нажмите на кнопку 'Добавить новый элемент' (1) в меню основной панели.</p>
<figure class="img-polaroid">
	<img src="img/tmhomepagecombinations-1.png" alt=""/>
</figure>
<p> Заполните поля всплывающей формы.</p>
<figure class="img-polaroid">
	<img src="img/tmhomepagecombinations-2.png" alt=""/>
</figure>
<ul class="marked-list">
	<li><strong>Товар</strong> (Product) – выберите товар.</li>
	<li><strong>Хук</strong> (Hook) – выберите позицию элемента.</li>
	<li><strong>Название</strong> (Title) – укажите название элемента.</li>
	<li><strong>Статус названия</strong> (Title status) – если эта настройка отключена, название элемента не будет отображаться.</li>
	<li><strong>Статус элемента</strong> (Item status) – если эта настройка отключена, элемент не отобразится в позиции.</li>
	<li><strong>Порядок отображения</strong> (Sort order) – порядок отображения элемента.</li>
</ul>
<p>Нажмите на кнопку 'Сохранить и остаться', когда Вы заполните все поля. Подождите пока страница перезагрузится и нажмите на кнопку 'Добавить новую комбинацию'.</p>
<p>Выберите нужную комбинацию из списка и нажмите на кнопку 'Добавить' (1).</p>
<figure class="img-polaroid">
	<img src="img/tmhomepagecombinations-3.png" alt=""/>
</figure>
<p> Заполните обязательные поля в появившейся форме.</p>
<figure class="img-polaroid">
	<img src="img/tmhomepagecombinations-4.png" alt=""/>
</figure>
<ul class="marked-list">
	<li><strong>Выбрать изображение</strong> (Select image) – выберите нужное изображение.</li>
	<li><strong>Специфический класс</strong> (Specific class) – пользовательский класс, определяющий стили элемента (класс CSS).</li>
	<li><strong>Порядок отображения</strong> (Sort order) – порядок отображения элемента.</li>
</ul>
<p>Нажмите на кнопку 'Сохранить', когда Вы заполните все поля.</p>
<p> После создания нескольких активных комбинаций, список доступных комбинаций отобразится следующим образом.</p>
<figure class="img-polaroid">
	<img src="img/tmhomepagecombinations-5.png" alt=""/>
</figure>
<p>Нажмите на кнопку 'Сохранить' (1) в форме элемента, когда Вы добавите все комбинации.</p>
