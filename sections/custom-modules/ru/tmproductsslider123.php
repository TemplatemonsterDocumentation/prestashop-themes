<p>После установки модуля Вы увидите новую вкладку на странице настройки товара (Каталог -> Товар -> выберите любой товар -> TM Products Slider).</p>
<p> Здесь Вы можете указать, будет товар отображаться в слайдере или нет. После внесения изменений нажмите кнопку Сохранить/Сохранить и остаться, чтобы сохранить изменения.</p>
<p> Модуль поддерживает мультимагазин. Вы можете изменить настройки для каждого магазина, группы магазинов или всех магазинов одновременно.</p>
