<p> Этот модуль позволяет добавить неограниченное количество видеозаписей в любой товар на любом языке и в любом магазине.</p>
<h5>Установка и удаление</h5>
<p> Модуль устанавливается и удаляется как любой другой модуль PrestaShop. Модуль не имеет страницы настройки и поддерживает мультимагазин.</p>
<p> После установки модуля Вы увидите новую вкладку на странице настройки товара (Каталог -> Товар -> выберите любой товар - TM Product Videos).</p>
<p> Чтобы добавить видео для товара, заполните нужные поля (поле ссылки обязательно для заполнения). Модуль является многоязычным, поэтому Вам необходимо заполнить поля для всех языков, с которыми связано это видео. Если поле «Ссылка» не заполнено для одного из языков, видео не будет добавлено для этого языка. Модуль отображает видео с Youtube и Vimeo, поэтому Вам нужно добавлять ссылки только из этих ресурсов. После внесения изменений нажмите на кнопку <strong>'Сохранить'</strong> (Save)/<strong>'Сохранить и остаться'</strong> (Save and Stay), для того чтобы сохранить изменения.</p>
