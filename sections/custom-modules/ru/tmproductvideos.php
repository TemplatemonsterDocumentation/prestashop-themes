<h6>Примеры ссылок</h6>
<p> Для правильного отображения видео ссылки должны иметь следующий формат:</p>
<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Youtube</dt>
            <dd><code>https://www.youtube.com/embed/K6VcTyaym90</code></dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Vimeo</dt>
            <dd><code>http://player.vimeo.com/video/66140585</code></dd>
        </dl>
    </li>
</ul>
<p> Если у видео уже есть видеозаписи, связанные с выбранным языком, Вы увидите их список ниже.</p>
<p> Чтобы сохранить, удалить или изменить список, не нужно нажимать на кнопки <strong>'Сохранить'</strong> (Save)/<strong>'Сохранить и остаться'</strong> (Save and Stay). Используйте кнопки, расположенные в видео-блоках. Чтобы изменить порядок видео, используйте способ перетаскивания и измените их позицию. Чтобы включить / отключить видео, нажмите кнопку рядом с ним. Чтобы редактировать видео, заполните поля необходимыми данными и нажмите на кнопку <strong>'Обновить'</strong> (Update).</p>
<p> Чтобы удалить видео из списка, нажмите на кнопку <strong>'Удалить видео'</strong> (Remove video).</p>
<p> При переключении языка будет отображаться только список видеороликов, связанных с языком.</p>
<p> Чтобы просмотреть видео в увеличенном формате, нажмите на кнопку <strong>'Масштабирование'</strong> (Zoom) под видео.</p>
