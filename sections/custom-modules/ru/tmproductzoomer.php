<p> Этот модуль является альтернативным решением вместо стандартного масштабирования изображения товара на странице одного товара. Он полностью совместим со стандартными дополнительными каруселями миниатюр и изменениями атрибутов товара. Он позволяет выбрать один из трех доступных вариантов (окно / линза / внутренний) для увеличения изображения с помощью дополнительных настроек плагина. Модуль основан на плагине <a href="//igorlino.github.io/elevatezoom-plus/" target="_blank">Elevatezoom Plus</a>.</p>
<h4>Установка и удаление</h4>
<p> Модуль устанавливается и удаляется, как и любой другой модуль Prestashop. После установки модуля стандартный эффект масштабирования товара будет автоматически отключен. После удаления стандартного эффекта модуля необходимо включить его вручную (если необходимо) в разделе <strong>Предпочтения -> Товары -> Включить JqZoom вместо Fancybox на странице товара</strong> (Preferences -> Products -> Enable JqZoom instead of Fancybox on the product page).</p>
<h4>Настройка модуля</h4>
<p> После установки Вы можете изменить параметры модуля на странице его настройки.</p>
<p>По умолчанию, страница настроек отображает <strong>упрощённый</strong> набор настроек.</p>
