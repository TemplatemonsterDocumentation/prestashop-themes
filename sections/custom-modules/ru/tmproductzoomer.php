<ul class="index-list">
    <li><strong>Живой режим</strong> (Live mode) – модуль включен/отключён.</li>
    <li><strong>Fancybox</strong> - показывать ли увеличенное изображение в Fancybox при нажатии.</li>
    <li><strong>Изменить изображение на ховере</strong> (Change image on hover) – переключаться на основное изображение при наведении курсором мышки на миниатюру (по умолчанию, задано значение "переключаться при нажатии").</li>
    <li><strong>Адаптивный</strong> (Responsive) – позволяет использовать адаптивную функциональность плагина для использования на мобильных устройствах.</li>
    <li><strong>Тип зума</strong> (Zoom Type) – выберите режим отображения увеличенного изображения        <ul>
            <li><strong>Окно</strong> (window) - увеличенное изображение появляется рядом с основным изображением в одном из доступных (16) положений </li>
            <li><strong>Лупа </strong> (lens) - изображение масштабируется в контейнере объектива поверх него </li>
            <li><strong>Внутренний</strong> (inner) - изображение масштабируется внутри основного контейнера изображения, полностью закрывая его.</li>
        </ul>
        <div class="alert alert-info"><span>Для мобильных устройств (&lt; 768) моментально активируется режим "Лупа".</span></div>
    </li>
    <li><strong>Расширенные настройки</strong> (Extended settings) – включение расширенных настроек для опытных пользователей. Вы найдёте ссылку на официальную <a href="//igorlino.github.io/elevatezoom-plus/api.htm" target="_blank">документацию для получения более детальной информации</a> рядом с папаметром.</li>
</ul>
