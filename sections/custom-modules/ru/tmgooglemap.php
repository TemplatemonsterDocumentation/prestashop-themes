<h5> Добавление пользовательских стилей </h5>
<p>Вы можете добавить пользовательский стиль с сайта <a target="_blank" href="https://snazzymaps.com/">Snazzymaps</a> в ваш модуль. Чтобы сделать это, выберите стиль с веб-сайта, скопируйте массив стилей JavaScript в файл .js и сохраните его в папке /js/styles/ модуля  (для названия файла используйте имя стиля, заменяя пробелы подчеркиванием и удаляя все специальные символы, например: для стиля Subtle Grayscale - файл subtle_grayscale.js, для стиля Apple Maps-esque - файл apple_maps-esque.js). Затем перейдите на страницу настройки вашего модуля и выберите нужный стиль из выпадающего списка стилей.</p>
