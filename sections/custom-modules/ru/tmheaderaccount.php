<h3>TM Header Account</h3>
<p>Этот модуль отображает форму входа в систему и некоторые доступные элементы учетной записи пользователя в заголовке сайта. Это позволяет войти в вашу учетную запись, оставаясь на активной странице. Модуль является выпадающим блоком.</p>
<h5> Установка и удаление </h5>
<p> Модуль может быть установлен и удален как любой другой модуль PrestaShop.</p>
<h5> Настройка модуля </h5>
<p> Модуль не имеет дополнительных настроек.</p>
