<h3>TM Infinite Scroll</h3>
<p>С помощью этого модуля Вы можете добавлять товары AJAX на страницы с блоком списка товаров. Функциональность модуля позволяет настраивать автоматическую загрузку товаров при прокрутке или при нажатии кнопки «Показать больше». Кроме того, модуль работает с фильтрами товаров по умолчанию PrestaShop.</p>
