<h5>Добавление модуля Media parallax </h5>
<ul class="index-list">
    <li>
        <p> Требуется добавить идентификатор или класс в содержимое вашей HTML-страницы. Вы также можете использовать уникальный тег, этот элемент не должен повторяться.</p>
    </li>

    <li>
        <p> Создайте изображение требуемого размера (желательно создать изображение большего размера, чем блок, в который будет добавлен эффект). В случае если Вы хотите добавить видео, Вам нужно будет подготовить его в трех форматах: .mp4, .webm и .ogv, а также добавить изображения для предварительного просмотра такого же размера (.png, .jpg и .gif).</p>
    </li>
    <li>
        <p>Нажмите на кнопку <strong>'Добавить контейнер'</strong> (Add container) в разделе <strong>'Добавить новый блок'</strong> (Add new block).</p>
        <figure class="img-polaroid">
