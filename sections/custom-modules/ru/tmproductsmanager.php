<h3>TM Products Manager</h3>
<h4>Вступление</h4>
<p> Модуль создан для работы с интернет-магазинами, особенно с магазинами с большим количеством товаров. Используя этот модуль, Вы можете легко фильтровать свои товары и выполнять различные операции над ними.</p>

<h4>Установка и удаление модуля </h4>
<p>Модуль устанавливается и удаляется как любой модуль Prestashop.</p>
<p class="alert alert-info">
    <span> Удаление или повторная установка модуля не сбрасывает изменения, которые Вы внесли в данный модуль.</span>
</p>
<p>Если модуль успешно установлен, перейдите на вкладку Каталог -> TM Products Manager (1), для того чтобы видеть основное меню модуля.</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-1.png" alt=""/></figure>
<h4>
</h4>
<p>Основное меню модуля разделено на две основные части: 'Выберите раздел' (1) и 'Выберите настройку' (2).</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-2.png" alt=""/></figure>
<p>Первая часть создана в форме вкладок - Информация, Цена, Связи и Количество.</p>
