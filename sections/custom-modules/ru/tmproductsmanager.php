<h4>Доступные настройки</h4>
<h5>Информация</h5>
<p>Этот раздел позволяет изменить  следующие настройки:</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-10.png" alt=""/></figure>
<ul class="marked-list">
    <li><strong>Включить/отключить товары</strong> (Enable/Disable products) - включить/отключите ваш товар.</li>
    <li><strong>Доступно для заказа</strong> (Available for order) – сделать товары доступными или недоступными для заказа.
        <p class="alert alert-warning small">
            <span>Обратите внимание: если для параметра 'Доступен для заказа' задано значение 'Да' для товаров со скрытыми ценами, их цены снова автоматически станут видимыми.</span>
        </p>
    </li>
    <li><strong>Отобразить цену</strong> (Show price) – отобразить или скрыть цену товара.
        <p class="alert alert-warning small">
            <span>Обратите внимание: если Вы зададите для параметра 'Отображать цену' значение 'Нет' для товаров, доступных для заказа, они автоматически станут недоступными.</span>
        </p>
    </li>
    <li><strong>Только Online</strong> (Online only) – примените значение 'Только Online' для выбранных элементов.</li>
    <li><strong>Состояние</strong> (Condition) – измените состояние нескольких товаров.</li>
    <li><strong>Краткое описание</strong> (Short description) – добавьте краткое описание, которое будет применено ко всем выбранным товарам.</li>
    <li><strong>Полное описание</strong> (Full description) – добавьте описание в выбранные товары с возможностью разместить его перед основным описанием, после его, или замените это описание на новое.</li>
    <li><strong>Тэги</strong> (Tags) – добавьте больше тегов в выбранный товар.</li>
</ul>

<h5>Цена</h5>
<p>В этом разделе Вы можете изменить настройки, которые качаются цены товара.</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-11.png" alt=""/></figure>
<ul class="marked-list">
    <li><strong>Отобразить "На распродаже"</strong> (Display "On sale") – отобразить выбранные элементы с пометкой 'На распродаже'.</li>
    <li><strong>Изменить правила налогов</strong> (Change tax rules) – выбрать правила налогов, которые будут применены к нескольким товарам.</li>
    <li><strong>Добавить новую особую цену</strong> (Add new specific price) – задать особую цену для выбранных товаров с возможностью добавить период активации цены.</li>
</ul>

<h5>Связи</h5>
<p> Здесь Вы можете изменить параметры, связанные с отношениями товаров.</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-12.png" alt=""/></figure>
<ul class="marked-list">
    <li><strong>Изменить марку</strong> (Change brand) – изменить марку выбранных элементов.</li>
    <li><strong>Изменить стандартную категорию</strong> (Change default category) – выбрать категорию, которая будет стандартной для некоторых товаров.
        <p class="alert alert-warning small">
            <span>Обратите внимание: Вы можете выбрать только категорию по умолчанию. Помните, что ваша стандартная категория будет заменена новой.</span>
        </p>
    </li>
    <li><strong>Изменить связанные категории</strong> (Change related categories) – задать связанные категории для многих товаров с возможностью сохранить или удалить старые связи.</li>
    <li><strong>Добавить аксессуары</strong> (Add accessories) – добавить аксессуары, которые подходят для выбранного товара.</li>
</ul>

<h5>Количество</h5>
<p> Эта настройка позволяет задать количество товара или увеличить/уменьшить текущее количество на определенную величину.</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-13.png" alt=""/></figure>
<ul class="marked-list">
    <li><strong>Изменить количество</strong> (Change quantity) – задайте нужное количество товаров.</li>
</ul>
