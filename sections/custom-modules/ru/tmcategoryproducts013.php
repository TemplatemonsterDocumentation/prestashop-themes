<h3>TM Category Products 0.1.3</h3>
<p>Этот модуль отображает товары категории в хуках displayHome и displayHomeTabContent. Модуль позволяет отображать единичные товары, товары из категории или все товары. Более того, в модуле есть карусель, которая работает, когда модуль размещен в позиции displayHome.</p>
<h4>Установка модуля</h4>
<p>Для того чтобы установить модуль, перейдите на вкладку Админ панель -> Модули и службы -> Модули и службы и найдите модуль 'TM Category Products' в свойствах фронтенда, затем, нажмите на кнопку 'Установить' (1).</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryproducts013-1.png" alt=""/>
</figure>
