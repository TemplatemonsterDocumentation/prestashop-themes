            <li><p><strong>Автоматический ховер </strong> (Auto hover) - паузы карусели, если пользователь наводит курсор мышки на элемент списка.</p></li>
        </ul>
    </li>
</ul>
<h4>Добавление новой вкладки/блока</h4>
<p> Вкладка - это блок, который отображает товары в позиции displayHomeTabContent.</p>
<p>Блок позволяет выбрать товары в позиции displayHome.</p>
<ul>
    <li>
        <p> Чтобы добавить новую вкладку или блок, нажмите на кнопку 'Добавить элемент'.</p>
        <p> Вы увидите экран, на котором вы можете выбрать следующие настройки:</p>
        <ul class="marked-list">
            <li><p><strong>Выбрать категорию</strong> (Select category) – выбор категории, товары которой будут отображаться.</p>
                <figure class="img-polaroid">
                    <img src="img/tmcategoryproducts013-5.png" alt=""/>
                </figure>
            </li>
            <li>
                <p><strong>Количество отображаемых товаров</strong> (Number of products to display) – количество отображаемых товаров.</p>
                <figure class="img-polaroid">
                    <img src="img/tmcategoryproducts013-6.png" alt=""/>
                </figure>
            </li>
            <li>
                <p><strong>Режим</strong> (Mode) – режим вкладки/блока.</p>
                <figure class="img-polaroid">
                    <img src="img/tmcategoryproducts013-7.png" alt=""/>
                </figure>
            </li>
            <li>
                <p><strong>Статус</strong> (Status) – позволяет включение/отключение вкладки или блока.</p>
                <figure class="img-polaroid">
                    <img src="img/tmcategoryproducts013-8.png" alt=""/>
                </figure>
            </li>
        </ul>
        <p> После выбора параметров нажмите кнопку "Сохранить". Вкладка/блок будет сохранена и появится в списке вкладок/блоков.</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts013-9.png" alt=""/>
        </figure>
    </li>
</ul>
