<h3>TM Product Custom Tab</h3>
<p>Модуль служит для создания дополнительной вкладки на странице товара для отображения дополнительной информации. Модуль имеет преимущество над существующей функциональностью, благодаря возможности добавления изображений, текста, видео, ссылок и т. д.</p>
<h4> Установка и удаление</h4>
<p> Модуль устанавливается и удаляется, как и любой другой модуль PrestaShop.</p>
<h4>Настройки модуля</h4>
<p> После установки модуля Вы можете добавлять новые вкладки для товаров на странице конфигурации модуля, а также на странице редактирования товара.</p>
<h5> Добавление вкладок через настройки модуля:</h5>
<p>Для того чтобы добавить новую вкладку, нажмите на кнопку 'Добавить новый' (1).</p>
<figure class="img-polaroid">
