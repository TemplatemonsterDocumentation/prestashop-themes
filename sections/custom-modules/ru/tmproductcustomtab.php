</figure>
<ul class="index-list">
    <li>
        <p> Добавление вкладки для одного или нескольких товаров ранее выбранной категории.</p>
        <figure class="img-polaroid">
            <img src="img/tmcustomproducttab-4.png" alt=""/>
        </figure>
        <ul class="marked-list">
            <li><strongНазвание вкладки</strong> (Tab Heading) – название вкладки, обязательное поле.</li>
            <li><strong>Описание вкладки</strong> (Tab Description) - информация о вкладке, может включать данные в формате изображения, текста, видео, ссылки и т. д.</li>
            <li><strong>Статус</strong> (Status) – статус элемента.</li>
            <li><strong>Дополнительные настройки</strong> (Additional settings) - включение/отключение дополнительных настоек.</li>
            <li><strong>Выбор категории</strong> (Select category) - выбор категорий, для которых должна отображаться вкладка.</li>
            <li><strong>Отображаемые товары</strong> (Products to display) - выбор товаров из ранее выбранных категорий.</li>
