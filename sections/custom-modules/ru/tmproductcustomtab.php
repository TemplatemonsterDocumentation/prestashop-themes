<h5> Изменение позиции отображения вкладки </h5>
<p> Перетащите нужную вкладку, чтобы изменить ее расположение.</p>
<figure class="img-polaroid">
    <img src="img/tmcustomproducttab-5.png" alt=""/>
</figure>
<h5> Добавление вкладок через настройки товара:</h5>
<p>Перейдите на страницу товара и нажмите на ссылку 'TM Product Custom Tab' в меню слева.</p>
<figure class="img-polaroid">
    <img src="img/tmcustomproducttab-6.png" alt=""/>
</figure>
<p>Если 'Дополнительные настройки' отключены, вкладка будет добавлена к товару, который был ранее изменён. Вы также можете добавить вкладку к товарам, относящимся к выбранным категориям, или добавить ее к нескольким товарам, которые принадлежат к ранее выбранной категории.</p>
<p> Чтобы изменить позицию отображения вкладок, перетащите блоки. Вы также можете редактировать или удалять вкладки.</p>
