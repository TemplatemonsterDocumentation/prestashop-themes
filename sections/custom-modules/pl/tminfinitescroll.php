<h5>Ustawienia modułu</h5>
<ul class="index-list">
    <li>Strony - strony na których ten moduł powinien działać.</li>
    <li>Autoładowanie - jeżeli aktywować tą opcję, produkty będą automatycznie ładowane po przewijaniu. W przeciwnym
        razie, jeżeli ta opcja będzie wyłączona, zobaczysz przycisk 'Pokaż więcej' oraz wszystkie produkty będą ładowane
        kiedy wciśniesz ten przycisk.
    </li>
    <li>Odstęp - ta opcja jest pokazywana po włączeniu 'Autoładowania'. Pozwala ona wyznaczyć odstęp przed przestrzenią
        ładowania produktów (albo za nią - jeżeli wprowadzona wartość jest ujemna).
    </li>
    <li>Numeracja - to ustawienie jest pokazywane po wyłączeniu 'Autoładowania'. Pozwala ono dodawać numerację do stron
        produktów.
    </li>
    <li>Pokazuj przycisk 'Pokaż wszystkie' - ta opcja jest dostępna tylko w wypadku jeżeli wyłączone jest
        'Autoładowanie'. Po wcisnięciu tego przycisku pokazane będą wszystkie produkty.
    </li>
    <li>Wstępny Załadunek- w tym polu możesz wybrać obrazek wstępnego załądunku który będzie wyświetlany w trakcie
        ładowania produktów.
    </li>
</ul>