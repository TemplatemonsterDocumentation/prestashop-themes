<h3>TM-Nieskończone Przewijanie</h3>
<p>Za pomocą tego modułu możesz dodać ładowanie produktów AJAX do stron zawierających zestawienie produktów.
    Funkcjonalność modułu pozwala ustawić autoładowanie produktów po przewinięciu albo po wcisnięciu przycisku 'Pokaż
    więcej'. Ponadto, ten moduł działa z domyślnymi filtrami produktów PrestaShop.</p>
<h5>TM-Nieskończone Przewijanie—Panel Główny</h5>
<p>Panel Główny: Tu możesz nastawić moduł:</p>
