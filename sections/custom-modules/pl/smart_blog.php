<p><strong>Inteligentny blog </strong> jest zestawem modułów który pozwala stwarzać pewien blog w swoim sklepie. Składa
    się z następujących modułów:</p>
<ul class="marked-list">
    <li>Inteligentny Blog</li>
    <li>RSS-Transmisje Inteligentnego Blogu</li>
    <li>Archiwum Inteligentnego Blogu</li>
    <li>Kategorie Inteligentnego Blogu</li>
    <li>Najnowsze Wiadomości ze Strony Domowej Inteligentnego Blogu</li>
    <li>Najnowsze Komentarze ze Strony Domowej Inteligentnego Blogu</li>
    <li>Inteligentny Blog: ModułDodajTo</li>
    <li>Popularne Wpisy Inteligentnego Blogu</li>
    <li>Niedawne Wpisy Inteligentnego Blogu</li>
    <li>Związane Wpisy Inteligentnego Blogu</li>
    <li>Wyszukiwanie w Inteligentnym Blogu</li>
    <li>Znaczniki Inteligentnego Blogu</li>
</ul>
<p>Ma również zaczepach znajdujące się w miejscach gdzie moduły blogu mogą pojawiać się na stronie; mogą również
    pojawiać się w standardowych zaczepach (tylko kolumny). </p>
<h4>Inteligentny Blog</h4>
<p>To jest główny moduł zawierający wiele ustawień niezbędnych dla funkcjonalności blogu. Po zakończeniu instalacji
    zakładka menu Blog pojawi się w głównym menu twojego panelu administracji gdzie będziesz mógł
    dodawać/usuwać/edytować wpisy/kategorie lub wybierać typ obrazków oraz ich rozmiary wykorzystywane we wpisach (typy
    oraz rozmiary obrazków we wpisach różnią się od ustawień dla obrazków produktów wykorzystywanych w twoim sklepie).
    Żeby dodać ustawienia modułu po stronie użytkownika, przejdź do <strong>Inteligentny Blog (Moduły -> Funkcje Biura
        Obsługi Klienta -> Inteligentny Blog)</strong> oraz dodaj wszystkie parametry.</p>
<h6>Opis parametrów:</h6>
<ul class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Meta-Tytuł</dt>
            <dd>tytuł Twojego blogu który pojawia się na wszystkich jego stronach.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Metawyraz Kluczowy</dt>
            <dd>wyrazy kluczowe które będą wykorzystywane przez wyszukiwarki dla lepszej optymizacji SEO.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Meta-Opis</dt>
            <dd>opis Twojego blogu wykorzystywany przez wyszukiwarki dla lepszej optymizacji SEO.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Główny Adres URL Blogu</dt>
            <dd>adres URL Twojego Blogu.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Korzystaj z .html za pomocą przyjaznego URL</dt>
            <dd>włącz/wyłącz dodawanie .html do adresó URL stron blogu za pomocą przyjaznych URL.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Liczba wpisów na 1 stronie</dt>
            <dd>liczba wpisów pokazywanych na stronie (strony kategorii/archiwów etc.).</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Automatycznie akceptowany komentarz</dt>
            <dd>włącz/wyłącz automatycznie akceptowane komentarze użytkowników; Jeżeli aktywować tą opcję, komentarze
                będą dostępne bez moderowania przez administratora.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Włącz Captcha</dt>
            <dd>włącz/wyłącz Captcha pod formularzem dodawania komentarzy na stronach produktów.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Włącz Komentarze</dt>
            <dd>włącz/wyłącz możliwość zostawiania komentarzy przez użytkowników.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Pokaż Imię Autora</dt>
            <dd>włącz/wyłącz pokazywanie autora wpisu na stronach blogu oraz pojedyńczych stronach wpisów</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Pokazuj Liczbę Przeglądów</dt>
            <dd>włącz/wyłącz wyświetlanie liczby przeglądów wybranego wpisu przez użytkowników. Pojawi się pod wpisem na
                stronie blogu oraz pojedyńczych stronach blogu.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Pokazuj Styl Imienia Autora</dt>
            <dd>zmieniaj format wyświetlania imienia autora.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Obrazek AVATAR</dt>
            <dd>dodaj standardowy obrazek avatar dla wszystkich użytkowników.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Nie Pokazuj Obrazku</dt>
            <dd>włącz/wyłącz pokazywanie obrazku. Nie wyświetla się żaden obrazek jeżeli nie ma ustawionego obrazku dla
                wpisu/kategorii etc.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Pokazuj Kategorię</dt>
            <dd>włącz/wyłącz obrazek kategorii oraz opis stron kategorii blogu.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Ustawienia Kolumn na Stronie Blogu</dt>
            <dd>ustawienie wyświetlania powiązanych z Blogiem modułów. Często wymagane są dodatkowe ustawienia, dlatego
                zaleca się instalowanie dodatkowych modułów w taki samy sposób jak instalowane były standardowe moduły
                PrestaShop.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Niestandardowe Arkuszy CSS</dt>
            <dd>w tym polu możesz dodawać uzupełniające style. Należy zachować ostrożność w przyparku korzystania z klas
                które już są wykorzystywane w tym motywie, ponieważ moż to spowodować pewne konflikty.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Usuń Stary Tumblr</dt>
            <dd>wykorzystuje się dla regenerowania obrazków z blogu w wypadku gdy rozmiary obrazków zostały zmienione
                lub dodano nowy typ obrazka. Aby rozpocząć regenerowanie, wybierz <strong>'Tak'</strong> i wciśnij
                przycisk <strong>'Regeneruj'</strong>.
            </dd>
        </dl>
    </li>
</ul>
