<h4>RSS-Transmisje Inteligentnego Blogu</h4>
<h6>Ten moduł generuje nową transmisję dla Twojego Blogu.</h6>
<p>Posiada on kilka ustawień na panelu administracyjnym:</p>
<ul class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Okres Aktualizacji</dt>
            <dd>pozwala wyświetlać wpisy z wybranego okresu czasowego (godzina/dzień/tydzień/miesiąc/rok).</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Częstotliwość Aktualizacji</dt>
            <dd>pozwala ustawić częstotliwość aktualizacji (co tydzień, co 2 godziny itp.).</dd>
        </dl>
    </li>
</ul>
<p>W części górnej jest ścieżka do pliku w którym będą generowane aktualne informacje. Składa się z adresu URL witryny
    internetowej + /modules/smartblogfeed/rss.php.</p>
