<h3>TM-Slajder Produktów 1.2.3</h3>
<p>Ten moduł wyświetla produkty w slajderze.</p>
<h4>Instalowanie oraz Usuwanie</h4>
<p>Moduł instaluje się oraz usuwa się w taki sam sposób jak każdy inny moduł PrestaShop.</p>
<p>Po zakończeniu instalowania możesz zmienić jego ustawienia w modułu konfiguracji strony.</p>
<p>Możesz ustawić porządek produktów w swoim slajderze, stosując metodę przeciągnia i upuszczenia (drag-and-drop).
    Możesz również włączyć/wyłączyć pokazywanie produktu w slajderze.</p>
<p>Możesz ustawić szerokość slajderu, efekt slajdera, szybkość slajdera, pauzę, autoodtwarzanie, pauzę przy zawisaniu
    nad elementami karuzeli, kontrole slajdera, przyciski następny/poprzedni, przyciski odtwarzanie/pauza.</p>
<figure class="img-polaroid">
    <img src="img/tmproductsslider-1.png" alt="">
</figure>
<p>Po zakończeniu instalacji zobaczysz nową zakładkę na swojej stronie konfiguracji (Katalog -> Produkt -> wybierz
    dowolny produkt -> TM-Slajder Produktów).</p>
<p>Tutaj możesz ustalić czy będzie produkt pokazywany w slajderze czy nie. Po dokonaniu zmian, wciśnij przycisk
    Zachowaj/Zachowaj i Zostań aby ich zachować.</p>
<p>Moduł wspiera możliwość zarządzania kilkoma sklepami. Możesz zmienić ustawienia dla każdego sklepu, grupę sklepów
    albo wszystkie sklepy jednocześnie.</p>
