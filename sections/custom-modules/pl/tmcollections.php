<h6>Moduł TM-zbiory stworzony jest żeby pozwolić gościom sklepu internetowego dodawać produkty do zbiorów i udostępniać
    swoje zbiory na Facebook.</h6>
<h4>Instalowanie modułu</h4>
<p>Żeby zainstalować ten moduł, przejdź do Panelu Administracyjnego -&gt; Moduły i Serwisy -&gt; Moduły i Serwisy (1),
    znajdź moduł 'TM-Zbiory' (2) i wciśnij przycisk 'Instaluj' (3).</p>
<figure class="img-polaroid">
    <img src="img/tmcollections1.png" alt=""/>
</figure>
<h4>Konfiguracja modułu</h4>
<p>Po zakończeniu instalacji modułu pojawi się strona 'Ustawienia'.<br>
    Wpisz numer ID aplikacji Facebook w odpowiednie pole (1) i wciśnij przycisk 'Zachowaj' (2).<br>
    Aby dowiedzieć się, jak otrzymać numer ID aplikacji Facebook, klinkij na poniższy link: <a
            href="https://developers.facebook.com/docs/apps/register" target="blank">Stworzenie numeru ID aplikacji</a>.
</p>
