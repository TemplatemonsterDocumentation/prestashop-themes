<h3>TM-Specjalna Zakładka Produktu</h3>
<p>Ten moduł służy dla stworzenia dodatkowej zakładki na stronie produktu dla pokazywania dodatkowej informacji. Ten
    moduł ma przewagę nad już istniejącym zakresem funkcjonalności ponieważ pozwala dodawać obrazki, tekst, video, linki
    itp.</p>
<h4>Instalowanie i usuwanie</h4>
<p>Moduł instaluje się oraz usuwa się w taki sam sposób jak każdy inny moduł PrestaShop.</p>
<h4>Ustawienia modułu</h4>
<p>Po zakończeniu instalowania modułu możesz dodać nowe zakładkl dla produktów na stronie konfigurowania modułu, jak
    również na stronie edytowania produktu.</p>
<h5>Dodawanie zakładek za pośrednictwem konfigurowania modułu:</h5>
<p>Ąby dodać nową zakładkę, wciśnij przycisk 'Dodaj Nową' (1).</p>
<figure class="img-polaroid">
    <img src="img/tmcustomproducttab-1.png" alt=""/>
</figure>
<ul class="index-list">
    <li>
        <p>Dodawanie zakładki do jednego lub kilku produktów wcześniej wybranej kategorii.</p>
        <figure class="img-polaroid">
            <img src="img/tmcustomproducttab-4.png" alt=""/>
        </figure>
        <ul class="marked-list">
            <li><strong>Nagłówek Zakładki</strong> - nazwa zakładki (wymagalne).</li>
            <li><strong>Opis Zakładki</strong> - informacja dotycząca zakładki, może zawierać dane w formacie obrazku,
                tekstu, video, linków itp.
            </li>
            <li><strong>Status</strong> -status elementu.</li>
            <li><strong>Dodatkowe ustawienia</strong> - włączanie/wyłączanie dodatkowych ustawień.</li>
            <li><strong>Wybierz kategorię</strong> - wybieranie kategorii dla których należy wyświetlać zakładkę.</li>
            <li><strong>Wyświetlane Produkty</strong> - wybór produktów z wcześniej wybranych kategorii.</li>
        </ul>
    </li>
    <li>
        <p>Dodawanie zakłądki do produktów które należą do wybranych kategorii.</p>
        <figure class="img-polaroid">
            <img src="img/tmcustomproducttab-3.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Aby dodać zakładkę do każdego produktu, deaktywuj opcję 'Dodatkowe Ustawienia'.</p>
        <figure class="img-polaroid">
            <img src="img/tmcustomproducttab-2.png" alt=""/>
        </figure>
    </li>
</ul>
<h5>Zmiana pozycji wyświetlania zakładki</h5>
<p>Przeciągnij i upuść potrzebną Ci zakładkę, aby zmienić jej usytuowanie.</p>
<figure class="img-polaroid">
    <img src="img/tmcustomproducttab-5.png" alt=""/>
</figure>
<h5>Dodawanie zakładek za pośrednictwem ustawień produktu:</h5>
<p>Przejdź na stronę produktu i kliknij link 'TM- Specjalna Zakładka Produktu' w menu po lewej stronie.</p>
<figure class="img-polaroid">
    <img src="img/tmcustomproducttab-6.png" alt=""/>
</figure>
<p>Jeżeli opcja 'Dodatkowe Ustawienia' jest deaktywowana, zakładka będzie dodana do produktu który był edytowany
    wcześniej. Możesz również dodać zakładkę do produktów które należą do wybranych kategorii lub też dodać je do kilku
    produktów które należą do poprzednio wybranej kategorii.</p>
<p>Aby zmienić pozycję wyświetlania zakładek, przeciągnij i upuść bloki. Możesz również edytować albo usuwać
    zakładki.</p>
<figure class="img-polaroid">
