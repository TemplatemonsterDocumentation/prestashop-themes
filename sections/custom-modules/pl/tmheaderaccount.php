<h3>TM-Konto w Nagłówku</h3>
<p>Ten moduł wyświetla formularz logowania oraz niektóe dostępne elementy konta użytkownika w nagłówku witryny. Pozwala
    zalogować się do swojegp konta zostając przy tym na aktywnej stronie. Moduł działa jako blok rozwijany.</p>
<h5>Instalowanie oraz Usuwanie</h5>
<p>Moduł instaluje się oraz usuwa się w taki sam sposób jak każdy inny moduł PrestaShop.</p>
<h5>Konfiguracja modułu</h5>
<p>Moduł nie zawiera żadnych dodatkowych ustawień.</p>
