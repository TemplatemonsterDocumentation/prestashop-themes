<h3>TM-Menadżer Produktów</h3>
<h4>Wstęp</h4>
<p>Ten moduł jest stworzony dla zarządzania sklepami internetowymi, zwłaszcza sklepami zawierającymi duże iłości
    produktów. Korzystając z tego modułu, będziesz mógł w sposób prosty filtrować swoje produkty i dokonywać różnych
    operacji z nimi.</p>
<h4>Instalowanie i usuwanie niniejszego modułu</h4>
<p>Moduł instaluje się oraz usuwa się w taki sam sposób jak każdy inny moduł PrestaShop.</p>
<p class="alert alert-info">
    <span>Usunięcie lub ponowne instalowanie tego modułu nie spowoduje zresetowanie zmian dokonanych w trakcie korzystania z niego.</span>
</p>
<p>Po tym jak moduł zostanie pomyślnie zainstalowany, przejdź do Katalog -> TM-Menadżer Produktów (1) aby zobaczyć menu
    główne modułu.</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-1.png" alt=""/></figure>
<h4>Wydajność</h4>
<p>Główne menu modułu jest podzielone na dwie główne części: 'Wybierz sekcję' (1) oraz 'Wybierz opcję' (2).</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-2.png" alt=""/></figure>
<p>Pierwsza część jest sprojektowana w kształcie zakładek - Informacja, Cena, Powiązania oraz Liczba/Iłość.</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-3.png" alt=""/></figure>
<ul class="marked-list">
    <li><strong>Informacja</strong> - opcje dotyczące dostępności oraz opisu produktu.</li>
    <li><strong>Cena</strong> - ustawienia ceny oraz regułów podatkowych.</li>
    <li><strong>Powiązania</strong> - możliwość zmiany marki, kategorii lub akcesorii kilku produktów.</li>
    <li><strong>Liczba/Iłość</strong> - zmiana iłości/liczby produktów.</li>
</ul>
<ul class="index-list">
    <li>
        <p>Kliknij każdą zakładkę aby zobaczyć dostępne opcje w tej sekcji. Będą opisane dalej. <br>
            Każda opcja zawiera wskazówkę krótki opis który pojawia się po stronie prawej kiedy zawiśniesz nad wybraną
            opcją (1).</p>
        <figure class="img-polaroid"><img src="img/tmproductsmanager-4.png" alt=""/></figure>
    </li>
    <li>
        <p>Wybierz potrzebną Ci opcję i kliknij ją.</p>
    </li>
    <li>
        <p>Następnym krokiem jest filtrowanie produktów. Ustaw opcje filtrowania zgodnie ze swoimi potrzebami i wciśnij
            przycisk 'Wybierz' (1). Aby usunąć wybrane filtry, wciśnij przycisk 'Zresetuj' (2).</p>
        <figure class="img-polaroid"><img src="img/tmproductsmanager-5.png" alt=""/></figure>
        <p>Możesz również skasować jakikolwiek z wybranych filtrów po tym jak je wybrałeś. Po prostu kliknij krzyżyk (1)
            znajdujący się obok wartości produktu, i ten produkt zostanie usunięty.</p>
        <figure class="img-polaroid"><img src="img/tmproductsmanager-6.png" alt=""/></figure>
    </li>
    <li>
        <p>Poniżej filtrów znajduje się lista produktów odpowiadających wybranym kryteriom. Możesz ją edytować jeżeli
            potrzebujesz usunąć niektóre produkty z listy. Każdy produkt można usunąć osobnie, wciskając przycisk 'Usuń
            z Listy' (1). Aby usunąć wszystkie produkty z listy, wciśnij 'Wybierz wszystkie' (2) oraz 'Usuń wybrane'
            (3). Jeżeli wybrałeś dużą liczbę produktów i potrzebujesz anulować ich wybór, wciśnij przycisk 'Odznacz
            wszystkie' (4).</p>
        <figure class="img-polaroid"><img src="img/tmproductsmanager-7.png" alt=""/></figure>
    </li>
    <li>
        <p>Jeżeli lista wybranych produktów odpowiada wszystkim wymaganiom, wciśnij przycisk 'Przetwarzanie' (1).</p>
        <figure class="img-polaroid"><img src="img/tmproductsmanager-8.png" alt=""/></figure>
    </li>
    <li>
        <p>Ustaw opcje zgodnie ze swoimi potrzebami i wciśnij przycisk 'Zachowaj' (1). Jeżeli musisz dokonać pewnych
            zmian w liście produktów, wciśnij przycisk 'Wróć do filtru' (2)</p>
        <p class="alert alert-info">
            <span>Proszę odnotować że nie będziesz mógł cofnąć dokonane zmiany w ten sam sposób.</span></p>
        <figure class="img-polaroid"><img src="img/tmproductsmanager-9.png" alt=""/></figure>
    </li>
</ul>
<h4>Dostępne opcje</h4>
<h5>Informacja</h5>
<p>Ta sekcja pozwala Ci skonfigurować następujące opcje:</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-10.png" alt=""/></figure>
<ul class="marked-list">
    <li><strong>Włącz/Wyłącz produkty</strong> - ustaw status włączony/wyłączony (on/off) dla swoich produktów.</li>
    <li><strong>Dostępny dla zamówienia</strong> - udostępnij te lub inne produkty lub uczyń je niedostępnymi dla
        zamówień. <p class="alert alert-warning small">
            <span>UWAGA: Jeżeli 'Dostępny dla Zamówienia' jest ustawiona w pozycję 'Tak' ('Yes') dla produktów z ukrytymi cenami, to ceny na te produkty zostaną ponownie widoczne.</span>
        </p>
    </li>
    <li><strong>Pokaż cenę</strong> - pokaż lub ukryj cenę produktu. <p class="alert alert-warning small">
            <span>UWAGA: Jeżeli ustawisz 'Pokaż Cenę' w pozycję 'Nie' ('No') dla produktów które są dostępne dla zamówienia, zostaną automatycznie zmienione na niedostępne.</span>
        </p>
    </li>
    <li><strong>Tylko Online</strong>- stosuj oznakowanie 'Tylko Online' dla wybranych elementów.</li>
    <li><strong>Warunek</strong> - zmień warunek dla kilku produktów.</li>
    <li><strong>Krótki Opis</strong> - dodaj krótki opis który należy zastosować do wszystkich wybranych produktów.</li>
    <li><strong>Pełny Opis</strong> - dodaj opis do wybranych produktów z możliwością umieścić go przed opisem głównym,
        po opisie głównym lub zastępując cały istniejący opis nowym opisem.
    </li>
    <li><strong>Znaczniki</strong> - dodaj więcej znaczników dla wybranych produktów.</li>
</ul>
<h5>Cena</h5>
<p>W tej sekcji można zmieniać opcje dotyczące ceny produktu.</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-11.png" alt=""/></figure>
<ul class="marked-list">
    <li><strong>Pokaż "W Sprzedaży"</strong> - pokaż wybrane elementy z oznakowaniem 'W sprzedaży'.</li>
    <li><strong>Zmień reguły podatkowe</strong> - wybierz reguły podatkowe którę będą stosowane do kilku produktów.</li>
    <li><strong>Dodaj nową cenę specjalną</strong> - ustaw cenę specjalną dla wybranych produktów z możliwością dodania
        okresu aktywacji ceny.
    </li>
</ul>
<h5>Powiązania</h5>
<p>Tutaj możesz zmienić opcje dotyczące powiązań produktu.</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-12.png" alt=""/></figure>
<ul class="marked-list">
    <li><strong>Zmień markę</strong> - zmień markę wybranych elementów.</li>
    <li><strong>Zmień kategorię domyślną</strong> - wybierz kategorię która będzie ustawiona jako domyślna dla
        niektórych produktów. <p class="alert alert-warning small">
            <span>UWAGA: Możes ustalić tylko jedną kategorię jako domyślną. Pamiętaj o tym, że Twoja bieżąca kategoria domyślna będzie zastąpiona przez nową.</span>
        </p>
    </li>
    <li><strong>Zmień powiązane kategorie</strong> - ustaw powiązane kategorie dla wielu produktów z możliwością
        zachowywać lub usuwać stare powiązania.
    </li>
    <li><strong>Dodaj Akcesorie</strong> - dodaj akcesorie odpowiednie dla wybranych produktów.</li>
</ul>
<h5>Iłość</h5>
<p>Ta opcja pozwala ustalić iłość produktów lub zwiększyć/zmniejszyć obecną iłość o pewną kwotę.</p>
<figure class="img-polaroid"><img src="img/tmproductsmanager-13.png" alt=""/></figure>
<ul class="marked-list">
    <li><strong>Zmień iłość</strong> - ustaw wymaganą iłość dla niektórych produktów.</li>
</ul>
