<h4>Widok Panoramiczny</h4>
<p>Ten moduł jest również wykorzystywany dla dodawania widoku panoramicznego na Stronę Domową.</p>
<p class="alert alert-warning">Możesz dodawać tyle bloków panoramicznych ile potrzebujesz.</p>
<h5>Dodawanie widoku panoramicznego</h5>
<ul class="index-list">
    <li>
        <p>Żeby dodać nowy widok panoramiczny, otwórz stronę konfiguracji modułu i wciśnij przycisk '+' ('Dodaj nowy
            element') (1).</p>
        <figure class="img-polaroid">
            <img src="img/360view-8.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Zobaczysz ustawienia widoku panoramicznego opisane niżej.</p>
        <figure class="img-polaroid">
            <img src="img/360view-9.png" alt=""/>
        </figure>
        <ul class="marked-list">
            <li><strong>Wybierz plik</strong> - wybierz pole widoku panoramicznego; to pole jest wymagane (przynajmniej
                dla języka domyślnego).
            </li>
            <li><strong>Tytuł</strong> - wprowadź tytuł widoku panoramicznego; to pole jest wymagane (przynajmniej dla
                języka domyślnego).
            </li>
            <li><strong>Szerokość</strong> - szerokość bloku panoramicznego; to pole jest wymagane.</li>
            <li><strong>Wysokość</strong> - wysokość bloku panoramicznego; to pole jest wymagane.</li>
            <li><strong>Status</strong> - włączyć/wyłączyć element panoramy na Stronie Domowej.</li>
            <li><strong>Pętla</strong> - rozpocznij ponowne obracanie obrazku od początku jak tylko skończy się bieżacy
                cykl obracania.
            </li>
            <li><strong>Animacja</strong> - opcja wykorzystywana dla wyświetlania widoku panoramicznego automatycznie
                obracanego w momencie załadowania strony.
            </li>
            <li><strong>Interwał Obrazków</strong>(pojawia się jeżeli 'Animacja' ustawiona w pozycji 'Tak') - okres
                czasu za jaki obrazek panoramiczny zatoczy pełny obrót; wartość należy wpisać w milisekundach; to pole
                jest wymagane.
            </li>
        </ul>
    </li>
    <li>
        <p>Wciśnij przycisk 'Zachowaj' i twój obrazek panoramiczny pojawi się na Stronie Domowej.</p>
    </li>
</ul>
