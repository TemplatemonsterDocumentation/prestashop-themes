<p>Ten moduł wyświetla obrazki z efektem 3D oraz pozwala użytkownikom obracać jego widok o 360 stopni. Obracanie może
    zacząć się automatycznie w momencie załadowania strony; lub też klient może samodzielnie uruchomić obracanie
    'obrazu', przeciągając lewy przycisk myszy.</p>
<p>Ponadto, ten moduł może być wykorzystywany dla dodawania widoku panoramicznego (jegdnego lub kilku) na stronę
    domową.</p>
<p>Tryb Przeglądanie 360° jest również dostępny na stronie produktu.</p>
<h4>Instalacja</h4>
<p>Przejdź do Moduły i Ustawienia -> Moduły i Ustawienia (1) na swoim panelu administracyjnym i znajdź moduł
    Przeglądanie 360° (2). Następnie wciśnij przycisk 'Instaluj' (3).</p>
<figure class="img-polaroid">
    <img src="img/360view-1.png" alt=""/>
</figure>
<h4>Konfiguracja Modułu</h4>
<p>Po tym jak moduł został pomyślnie zainstalowany, zobaczysz stronę 'Skonfiguruj' z wyświetlanymi domyślnymi
    ustawieniami modułu.</p>
<p>Możesz znaleźć ustawienia dla trybu Przeglądanie 360° (1) jak również dla widoku panoramicznego (2).</p>
<figure class="img-polaroid">
    <img src="img/360view-2.png" alt=""/>
</figure>
<h4>Przeglądanie 360°</h4>
<p>Ustawienia opisane są poniżej na zrzucie ekranu. Wybierz ustawienia odpowiadające Twoim potrzebom oraz wciśnij
    przycisk 'Zachowaj' (1).</p>
<figure class="img-polaroid">
    <img src="img/360view-3.png" alt=""/>
</figure>
<ul class="marked-list">
    <li><strong>Włącz na Stronie Domowej</strong> - ta opcja pozwala aktywować wyświetlanie trybu Przeglądanie 360°
        produktu na Stronie Domowej.
    </li>
    <li><strong>Wybierz Produkt</strong>(pojawia się jeżeli 'Włączyć na Stronie Domowej' ustawiono w pozycję 'Tak') -
        wybierz produkt który należy pokazywać w trybie Przeglądanie 360°.
    </li>
    <li><strong>Animacja</strong> - opcja wykorzystywana dla wyświetlania produktu automatycznie obracanego w momencie
        załadowania strony.
    </li>
    <li><strong>Interwał Obrazków</strong> (pojawia się jeżeli 'Animacja' ustawiona w pozycji 'Tak') - okres czasu który
        mija zanim bieżący obrazek zostaje zastąpiony przez następujący; wartość należy wpisać w milisekundach.
    </li>
</ul>
