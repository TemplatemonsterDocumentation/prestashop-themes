<h3>TM-Mapa Google 1.1.5</h3>
<p>Ten moduł pozwala Ci dodać Mapę Google pokazującą lokalizację sklepów oraz ich ścisły opis na Stronie Domowej. Mapa
    zawierać będzie adresy wszystkich sklepów powiązanych z Twoim sklepem internetowym..</p>
<h5>Instalowanie i usuwanie</h5>
<p>Moduł instaluje się oraz usuwa się w taki sam sposób jak każdy inny moduł PrestaShop.</p>
<h5>Ustawienia modułu</h5>
<p>Po zakończeniu instalowania modułu możesz dodać sklepy do mapy. Po wciśnięciu przycisku 'Dodaj nowy sklep' (1)
    zostaniesz przekierowany do zakładki 'Dane Kontaktowe Sklepu' gdzie możesz edytować swoje sklepy</p>
<p class="img-polaroid"><img src="img/tmgooglemap115-1.png" alt=""/></p>
<p>Ąby dodać nowy sklep, wciśnij przycisk 'Dodaj Nowy'.</p>
<p class="img-polaroid"><img src="img/tmgooglemap115-2.png" alt=""/></p>
<ul class="marked-list">
    <li><strong>Wybierz sklep</strong> - wybierz sklep który chciałbyś dodać na mapę.</li>
    <li><strong>Status</strong> -status elementu.</li>
    <li><strong>Marker</strong> - dodawanie specjalnego markera.</li>
    <li><strong>Specjalny tekst</strong> - dodawanie specjalnego tekstu.</li>
</ul>
<h5>Ustawienia przeglądania Mapy Google</h5>
<p class="img-polaroid"><img src="img/tmgooglemap115-3.png" alt=""/></p>
<ul class="marked-list">
    <li><strong>Styl Mapy</strong> - wybierz styl pokazywanej mapy.</li>
    <li><strong>Typ Mapy</strong> - wybierz typ pokazywanej mapy.</li>
    <li><strong>Poziom Powiększenia</strong> - doprecyzuj początkowy poziom powiększenia mapy (od 1 do 17).</li>
    <li><strong>Powiększenie kółkiem</strong> - włączyć/wyłączyć powiększenie mapy kółkiem myszy.</li>
    <li><strong>Sterowanie Mapą</strong> - włączyć/wyłączyć sterowanie mapą.</li>
    <li><strong>Widok Ulic</strong> - włączyć/wyłączyć widok ulic.</li>
    <li><strong>Marker Animacji</strong> - włączyć/wyłączyć marker animacji.</li>
    <li><strong>Wyskakujące Okna</strong> - włączyć/wyłączyć wyskakujące okna zawierające informacje ze sklepów.</li>
</ul>