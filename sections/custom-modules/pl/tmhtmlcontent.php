<p>Ten moduł pozwala pokazywać dowolnie wybraną treść (obrazki, HTML, linki itp) w następujących pozycjach: displayNav,
    strona domowa, na górze, topColumn, lewa strona i prawa strona (displayLeftColumn/displayRightColumn), stopka.
    Wykorzystywany jest dla rozszerzenia możliwości oraz dodawania bloków reklamowych/informacyjnych do Twojego sklepu.
    Jest to moduł wielojęzyczny i może działać na witrynie zarządzającej kilkoma sklepami.</p>
<h5>Aby dodać treść, powinieneś dokonać następujących kroków opisanych poniżej:</h5>
<h6>Przejdź do <strong>Moduły -> Funkcje po Stronie Użytkownika -> TM-Treść HTML </strong>(jeżeli nie został poprzednio
    zainstalowany, należy go zainstalować). </h6>
<ul class="index-list">
    <li>Wciśnij przycisk <strong>'Dodaj nowy'</strong>.</li>
    <li>Lista dostępnych języków. Możesz przejrzeć dostępne elementy dla wszystkich języków, przełączając zakładki
        języków.
    </li>
    <li>Lista elementów dodawanych dla specjalnej pozycji oraz języka.</li>
</ul>
