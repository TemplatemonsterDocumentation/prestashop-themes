<h5>Dodawanie/usuwanie/edytowanie Wideo</h5>
<p>Aby dodać nowe wideo, wciśnij przycisk 'Dodaj nowe wideo' Następnie, wypełnij wymagane pola w formularzu który się
    pojawi.</p>
<figure class="img-polaroid"><img src="img/tmmosaicproducts-10.png" alt=""/></figure>
<ul class="marked-list">
    <li><strong>Wprowadź Nazwę Wideo</strong> - tytuł wideo—to pole jest wymagane (przynajmniej dla języka domyślnego).
    </li>
