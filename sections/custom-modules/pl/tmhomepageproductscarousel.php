<h3>TM-Karuzela Produktów Strony Domowejl</h3>
<p>Ten moduł dodaje karuzelę do zakładek strony głównej.</p>
<h5>Instalowanie oraz Usuwanie</h5>
<p>Moduł instaluje się oraz usuwa się w taki sam sposób jak każdy inny moduł PrestaShop.</p>
<h5>Konfiguracja modułu</h5>
<p>Ustawienia zainstalowanego modułu można skonfigurować na stronie konfiguracji modułu.</p>
