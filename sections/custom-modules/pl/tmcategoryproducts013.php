<h3>TM-Produkty Kategorii 0.1.3</h3>
<p>Ten moduł pokazuje produkty kategorii w zaczepkach displayHome oraz displayHomeTabContent. Ten moduł pozwala
    pokazywać pojedyńcze produkty, produkty z wybranej kategorii lub wszystkie produkty. Ponadto, moduł ten dostarczany
    jest z karuzelą która włącza się kiedy moduł zajmuje pozycję displayHome.</p>
<h4>Instalowanie modułu</h4>
<p>Żeby zainstalować ten moduł, przejdź do Panelu Administracyjnego -> Moduły i Serwisy -> Moduły i Serwisy i znajdź
    moduł 'TM Produkty Kategorii' wśród Funkcji po Stronie Użytkownika, następnie wciśnij przycisk 'Instaluj' (1).</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryproducts013-1.png" alt=""/>
</figure>
<h4>Konfiguracja modułu</h4>
<ul class="index-list">
    <li>
        <p>Po tym jak moduł zostanie pomyślnie zainstalowany, pojawi się przycisk 'Skonfiguruj' (1).</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts013-2.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Na głównej stronie konfiguracji modułu są listy zakładek i bloków (1) (tam możesz zobaczyć stworzone
            elementy) oraz dwa przyciski - 'Ustawienia' (2) i 'Dodaj element' (3).</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts013-3.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Wciśnij przycisk 'Ustawienia' oraz wybierz opcje moduły odpowiadające Twoim potrzebom.</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts013-4.png" alt=""/>
        </figure>
        <ul class="marked-list">
            <li><p><strong>Widoczne elementy</strong> - liczba widocznych elementów.</p></li>
            <li><p><strong>Przewijanie elementów</strong> - liczba elementów które się zmieniają za jedne przewijanie
                    karuzeli.</p></li>
            <li><p><strong>Szerokość Slajdu</strong> - szerokość elementu listy karuzeli.</p></li>
            <li><p><strong>Margines Slajdu</strong> - margines elementu listy karuzeli.</p></li>
            <li><p><strong>Autoprzewijanie</strong> - automatyczne przewijanie elementów karuzeli.</p></li>
            <li><p><strong>Szybkość karuzeli</strong> - szybkość zmiany elementów.</p></li>
            <li><p><strong>Pauza</strong> - pauza pomiędzy przewijaniami (jeżeli włączono "Autoprzewijanie").</p></li>
            <li><p><strong>Losowo</strong> - karuzela zaczyna się od losowo wybranego elementu.</p></li>
            <li><p><strong>Pętla Karuzeli</strong> - tryb pętli: po tym, jak wszystkie elementy karuzeli zostały
                    wyświetlone, karuzela zaczyna ponownie od początku.</p></li>
            <li><p><strong>Ukryj konrole po zakończeniu</strong> - ukryj przyciski sterujące (Nast/Poprzed).</p></li>
            <li><p><strong>Strony</strong> - pokazuje numerację stron.</p></li>
            <li><p><strong>Kontrole</strong> - pokazuje przyciski sterujące (Nast/Poprzed).</p></li>
            <li><p><strong>Autokontrole</strong> - pokazuje przyciski sterowania (Grać/Pauza).</p></li>
            <li><p><strong>Autozawisanie</strong> - karusela zatrzymuje się jeżeli użytkownik zawiśnie nad elementem
                    listy.</p></li>
        </ul>
    </li>
</ul>
<h4>Dodawanie nowej zakładki/bloku</h4>
<p>Zakładka jest blokiem który pokazuje produkty w pozycji displayHomeTabContent.</p>
<p>Ten blok pozwala wybierać produkty w pozycji displayHome .</p>
<ul>
    <li>
        <p>Aby dodać nową zakładkę lub blok, kliknij 'Dodaj element'.</p>
        <p>Na ekranie, który się pojawi, będziesz mógł wybrać jedną z następujących opcji:</p>
        <ul class="marked-list">
            <li><p><strong>Wybierz kategorię</strong> - wybierz kategorię, produkty z której będą pokazywane.</p>
                <figure class="img-polaroid">
                    <img src="img/tmcategoryproducts013-5.png" alt=""/>
                </figure>
            </li>
            <li>
                <p><strong>Liczba produktów pokazywanych</strong> - liczba artykułów, które mają być pokazywane.</p>
                <figure class="img-polaroid">
                    <img src="img/tmcategoryproducts013-6.png" alt=""/>
                </figure>
            </li>
            <li>
                <p><strong>Tryb</strong> - tryb zakładka/blok.</p>
                <figure class="img-polaroid">
                    <img src="img/tmcategoryproducts013-7.png" alt=""/>
                </figure>
            </li>
            <li>
                <p><strong>Status</strong> - pozwala włączać/wyłączać zakładkę lub blok.</p>
                <figure class="img-polaroid">
                    <img src="img/tmcategoryproducts013-8.png" alt=""/>
                </figure>
            </li>
        </ul>
        <p>Po wybraniu parametrów, wciśnij przycisk 'Zachowaj'. Zakładka/blok będą zachowane i pojawią się w liście
            zakładek/bloków.</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts013-9.png" alt=""/>
        </figure>
    </li>
</ul>
<h4>Edytowanie zakładki/bloku</h4>
<p>Zeby zacząć edytowanie zakładki albo bloku, znajdź je w liście i wciśnij przycisk 'Edytuj' (1). <br>
    Opcje są takie same jak dla stwarzania bloku albo zakładki.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryproducts013-10.png" alt=""/>
</figure>
