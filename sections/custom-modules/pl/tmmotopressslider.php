<h5>Ustawienia warstwy</h5>
<h6>Dodaj tekst</h6>
<p>Ta zakładka jest stworzona dla dodawania, edytowania tudzież ulokowania tekstów w slajdzie.</p>
<figure class="img-polaroid">
    <img src="img/mpslider19_slide_add_text_2.png" alt=""/>
</figure>
<ul class="marked-list">
    <li>Stosując szarew kwadraty, można szybko ustalić pozycję tekstu (1).</li>
    <li>Aby dostosować, dodaj wartości 'X' oraz 'Y' w pikselach i ustal szerokość tego elementu (2).</li>
    <li>Jeżeli 'Wolne Miejsce' ('Whitespace') jest ustalone w pozycji 'Zwykła' ('Normal'), tekst będzie ogarnięty w
        sposób zwykły. Jeżeli wybrano 'Bez Ogarnięcia' ('No-wrap'), ogarnięcie będzie ignorowane (3).
    </li>
    <li>W drugiej kolumnien możesz sterować animacją elementu - w jaki sposób, ile ma trwać, kiedy ma być uruchomiona i
        kiedy ma się skończyć (4) w ramach czasu trwania slajdu (który jest pokazywany poniżej dla Twojej wygody (5);
