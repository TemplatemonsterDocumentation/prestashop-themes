</figure>
<ul class="marked-list">
    <li><strong>Tytuł Slajdera</strong> - tytuł slajdera (na przykłąd: Slajder1/ Slider1); ta opcja jest wymagana,
        ponieważ kiedy masz kilka slajderów, trzeba je rozróżniac.
    </li>
    <li><strong>Alias Slajderu</strong> - niepowtarzalny alias który będzie wykorzystywany w krótkich kodach dla
