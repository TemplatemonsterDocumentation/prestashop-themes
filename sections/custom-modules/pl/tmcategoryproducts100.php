<h5>Dodawanie Zakładki</h5>
<p>Zakładka jest blokiem który pokazuje produkty w pozycji displayHomeTabContent.</p>
<ul class="index-list">
    <li><p>Przejdź do modułu na panelu głównym i wciśnij przycisk 'plus' (1) w liście zakładek.</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts-2.png" alt=""/>
        </figure>
    </li>
    <li><p>W formularze który się pojawi, zobaczysz następne opcje:</p>
        <ul class="marked-list">
            <li>Status - ta opcja pozwala włączać/wyłączać zakładkę.</li>
            <li>Wybierz kategorię - kategoria, produkty z której będą pokazywane.</li>
            <li>Zastosuj specjalną nazwę - ta opcja pozwala zmieniać nazwę kategorii. Jeżeli włączyć tą opcję, wyświedli
                się następujący formularz:
                <ul class="marked-list">
                    <li>Nazwa - specjalna (niestandardowa) nazwa zakładki która będzie pokazywana na witrynie
                        internetowej.
                    </li>
                </ul>
            </li>
            <li>Wybierz wyświetlane produkty - jeżeli wyłączyć tą opcję, pokazywane będzie pole 'Liczba wyświetlanych
                produktów' które wyznacza liczbę produktów które mają być pokazywane. Jeżeli włączyć tą opcję,
                pokazywany będzie przycisk 'Dodaj produkty' który pozwala wybierać produkty które mają być pokazywane w
                zakładce.
            </li>
        </ul>
    </li>
    <li><p>Po zakończeniu konfigurowania wszystkich niezbędnych opcji, wciśnij przycisk 'Zachowaj' (2).</p>
        <figure class="img-polaroid">
            <img src="img/tmcategoryproducts-3.png" alt=""/>
        </figure>
    </li>
</ul>
