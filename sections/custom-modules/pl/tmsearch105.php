<h3>TM-Wyszukiwanie 1.0.5</h3>
<p>Ten moduł pozwala Ci skonfigurować funkcje wyszukiwania twojego sklepu. Zawiera Wyszukiwanie Ajax (które pozwala
    pokazywać listę wyników od momentu w którym Twoi klienci wpiszą kilka liter w pasek wyszukiwania, tuż poniżej pasku
    wyszukiwania) oraz Wyszukiwanie Błyskawiczne (które pozwala pokazywać listę wyników od momentu w którym Twoi klienci
    wprowadzą kilka liter do pasku wyszukiwania, tuż poniżej przestrzeni głównej treści Twojego sklepu).</p>
<p class="alert alert-warning"><span>Wyszukiwanie Błyskawiczne może być domyślnie wyłączone, ponieważ goście nie zawsze są przyzwyczajeni do tego żeby cała ich treść zmieniała się jedynie wskutek wpisywania kilku liter do pasku wyszukiwania sklepu. Zatem, sugerujemy stosowanie z ostrożnością.</span>
</p>
<p>Oferuje również możliwość ustawienia wyświetlania dodatkowej informacji o produkcie (opis/cena/wytwórca etc).
    Wszystkie ustawienia modułu są dostępne na głównym panelu administracyjnym.</p>
<h4>Instalowanie oraz Usuwanie</h4>
<p>Moduł instaluje się oraz usuwa się w taki sam sposób jak każdy inny moduł PrestaShop.</p>
<h4>Konfiguracja modułu</h4>
<p>Po instalowaniu modułu możesz go nastawić na stronie konfiguracji modułu. Przejdź do Moduły i Serwisy -> Moduły i
    Serwisy -> Wyszukiwanie TemplateMonster.</p>
