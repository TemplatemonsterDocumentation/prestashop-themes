<h3>TM-Identyfikatory Kategorii</h3>
<p>Ten moduł pozwala Ci dodawać identyfikatory do kategorii pasków bocznych w domyślnym modułu Kategorii Prestashop
    ("Blok Kategorii"). Identyfikatory można dodawać do kategorii dowolnie wybranego poziomu oraz w dowolnie wybranym
    języku. Pole tekstowe oznakowania jest obowiązkowe; należy go wypełnić we wszystkich językach. Jeżeli zostawisz to
    pole puste w jakimkolwiek języku, będzie automatycznie wyświetlało tekst w języku domyślnym.</p>
<h5>Instalowanie oraz Usuwanie</h5>
<p>Moduł instaluje się oraz usuwa się w taki sam sposób jak każdy inny moduł PrestaShop.</p>
<h5>Konfiguracja modułu</h5>
<p>Po zakończeniu instalowania możesz zmienić jego ustawienia w modułu konfiguracji strony.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoriesbadges-1.png" alt=""/>
</figure>
<p>Możesz dodawać identyfikatory do dowolnie wybranej kategorii, wybierając je z listy rozwijanej.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoriesbadges-2.png" alt=""/>
</figure>
<p class="alert alert-warning"><span>Uwaga! Możesz dodawać tylko jeden identyfikator dla każdej kategorii.</span></p>
<p>Następnie, możesz dodać tekst który pojawi się na identyfikatorze oraz wybrać styl z listy rozwijanej (style w
    różnych szablonach mogą się różnić).</p>
<p> Następnie wciśnij przycisk 'Zachowaj' (1).</p>
<figure class="img-polaroid">
    <img src="img/tmcategoriesbadges-3.png" alt=""/>
</figure>
<p>Poniżej możesz zobaczyć listę istniejących identyfikatorów. Tutaj możesz edytować oraz usuwać identyfikatory,
    korzystając z odpowiednich przycisków.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoriesbadges-4.png" alt=""/>
</figure>
<p>Moduł jest wielojęzyczny oraz wspiera zarządzanie kilkoma sklepami. Możesz dodawać identyfikatory dla grupy sklepów
    albo dla wszystkich sklepów na raz. Będą wyświetlane w liście jako elementy osobne.</p>
