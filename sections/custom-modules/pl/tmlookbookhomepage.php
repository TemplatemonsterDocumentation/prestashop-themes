<p>TM Look Book—Strona Domowa wykorzystywany jest dla pokazywania bloku TM LookBook na Stronie Domowej. Ten moduł
    umożliwia pokazywanie zarówno okładki lookbook jak i stron lookbook.</p>
<h5>Wymagania:</h5>
<ul class="marked-list">
    <li>PHP 5.4+</li>
    <li>PrestaShop 1.6</li>
    <li>Moduł TM-Look Book</li>
</ul>
<h5>Funkcje modułu:</h5>
<ul class="marked-list">
    <li>Moduł może być pokazywany w dwóch pozycjach: displayHome oraz displayTopColumn.</li>
    <li>TM Look Book—Strona Domowa jest modułem potomnym TM Look Book, a więc nie może być wykorzystywany bez modułu
        głównego.
    </li>
    <li>Ten moduł pozwala pokazywać okładkę lookbook albo jego strony.</li>
</ul>
<h5>TM-Look Book—Panel Główny</h5>
<p>Na głównym panelu modułu możesz dodawać, usuwać oraz edytować bloki które będą pokazywane na Stronie Domowej. </p>
