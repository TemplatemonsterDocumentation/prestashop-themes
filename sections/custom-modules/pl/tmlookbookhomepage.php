<h5>Dodawanie nowego bloku</h5>
<ul class="index-list">
    <li>
        <p>Aby dodać nowy blok, wejdź na panel główny modułu oraz klilknij '+' (1) w tablicy z potrzebną ci
            zaczepką.</p>
        <figure class="img-polaroid">
            <img src="img/tmlookbookhomepage-add-block-1.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>W formularze który się pojawi, ustaw następne opcje:</p>
        <ul class="marked-list">
            <li><strong>Status</strong> - status bloku. To ustawienie pozwoli Ci włączać albo wyłączać pokazywanie
                bloku.
            </li>
            <li><strong>Typ</strong> - pozwala wybrać tyb bloku.</li>
            <li><strong>Lookbook</strong> - pozwala wybrać wcześniej wybrany look.</li>
        </ul>
    </li>
