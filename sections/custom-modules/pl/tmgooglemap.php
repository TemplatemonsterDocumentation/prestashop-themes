<h5>Dodawanie specjalnych stylów</h5>
<p>Możesz dodać jakikolwiek potrzebny Ci styl pobierając go ze <a target="_blank" href="https://snazzymaps.com/">Snazzymaps</a>
    i ładując go do swojego modułu. Aby to osiągnąć, należy wybrać styl z witryny internetowej, skopiować Szereg Styli
    Java Script (JavaScript Style Array) do pliku .js oraz zachować go w folderze modułu /js/styles/ (jako nazwę pliku
    wykorzystaj nazwę stylu zastępując wszystkie spacje podkreślnikami oraz usuń wszystkie specjalne znaki. Na przykład:
    dla stylu Subtle Grayscale (Delikatne Odcienie Szarego) - plik subtle_grayscale.js file, dla stylu Apple Maps-esque
    style (podobizna Map Apple) - plik apple_maps-esque.js). Następnie, przejdź do strony konfiguracji swojego modułu
    oraz wybierz tam styl z listy rozwijanej.</p>