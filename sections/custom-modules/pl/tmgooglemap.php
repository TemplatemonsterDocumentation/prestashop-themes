<h3>TM-Mapa Google</h3>
<p>Ten moduł pozwala Ci dodać mapę Google Google pokazującą lokalizację Twoich sklepów oraz ich ścisły opis na Twojej
    stronie głównej. Mapa zawierać będzie adresy wszystkich sklepów powiązanych z Twoim sklepem internetowym. Możesz
    edytować szczególne dane Twoich sklepów, przechodząc do wkładki Preferencje -> Dane Kontaktowe Sklepu.</p>
<h5>Instalowanie oraz Usuwanie</h5>
<p>Moduł instaluje się oraz usuwa się w taki sam sposób jak każdy inny moduł PrestaShop.</p>
<h5>Konfiguracja modułu</h5>
<p>Po zakończeniu instalowania możesz zmienić jego ustawienia w modułu konfiguracji strony.</p>
<p>Możesz skonfigurować styl oraz elementy swojej mapy.</p>
<ul class="index-list">
    <li>Wybierz styl mapy w pierwszym polu (wszystkie style pobrane z <a target="_blank" href="https://snazzymaps.com/">Snazzymaps</a>).
        Możesz również dodawać style z bieżącej strony internetowej. Procedura dodawania styli jest opisana w końcu tej
        instrukcji.
    </li>
    <li>Wybierz typ przeglądania w drugim polu (Widok Satelitarny lub Widok Ulic).</li>
    <li>Można również wybrać współczynnik zbliżenia dla swojej mapy: od 1 do 17.</li>
    <li>Opcja zbliżenia podczas przewijania obszaru mapy.</li>
    <li>Możesz wyłączyć sterowanie mapą oraz możliwość przeglądania w trybie Widok Ulic.</li>
    <li>Moduł wspiera możliwość zarządzania kilkoma sklepami. Możesz wybrać/zmienić styl mapy dla każdego sklepu.</li>
</ul>
