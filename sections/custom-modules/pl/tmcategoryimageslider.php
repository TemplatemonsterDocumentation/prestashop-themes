<h3>TM-Slajder Zdjęć Kategorii</h3>
<p>Ten moduł pozwala stwarzać slajdy wielojęzyczne dla każdej kategorii w tym sklepie. Slajder będzie pokazywany na
    stronach kategorii.</p>
<h4>Instalowanie i usuwanie</h4>
<p>Procedura instalacji jest standardową; moduł można instalować oraz usunąć tak samo, jak każdy zwykły moduł
    PrestaShop.</p>
<h4>Ustawienia modułu</h4>
<p>Po zakończeniu instalacji modułu, możesz ustawić lub zmienić potrzebne Ci opcje na stronie konfiguracji modułu.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryimagesslider-1.png" alt=""/>
</figure>
<ul class="marked-list">
    <li><strong>Tryb</strong> - typ animacji (zanikanie, pionowa, pozioma).</li>
    <li><strong>Szybkość </strong> - szybkość animacji.</li>
    <li><strong>Szerokość</strong> - szerokość slajdów (dla zapewnienia poprawnego przeglądania zalecamy stosowanie
        faktycznej szerokości obrazu).
    </li>
    <li><strong>Wysokość</strong> - wysokość slajdów (dla zapewnienia poprawnego przeglądania zalecamy stosowanie
        faktycznej wysokości obrazu).
    </li>
    <li><strong>Auto</strong> - autoodtwarzanie przy otwieraniu nowej strony.</li>
    <li><strong>Pauza</strong> - czas który mija zanim slajd zostanie zastąpiony przez następny.</li>
    <li><strong>Autozawisanie</strong> - pauza przy zawisaniu.</li>
    <li><strong>Kontrole</strong> - przyciski "Następny"/"Poprzedni" buttons.</li>
    <li><strong>Nast tekst</strong> - tekst przycisku "Następny".</li>
    <li><strong>Poprz Text</strong> - tekst przycisku "Poprzedni".</li>
    <li><strong>Autokontrole</strong> - przyciski "Grać"/"Pauza".</li>
    <li><strong>Strony</strong> - numeracja stron.</li>
</ul>
<h5>Dodawanie/usuwanie/edytowanie slajdów</h5>
<p>Po zakończeniu instalacji modułu zobaczyśz nowe pole 'Slajdy' w ustawieniach kategorii.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryimagesslider-2.png" alt=""/>
</figure>
<p>Aby dodać slajd, wciśnij przycisk 'Dodaj nowy slajd'. Wypełnij wymagane pola w formularzu.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryimagesslider-3.png" alt=""/>
</figure>
<ul class="marked-list">
    <li><strong>Wybierz plik</strong> - wybierz swój plik. Możesz przesłać różne obrazki dla każdego języka. W wypadku
        jeśli nie wybrano żadnego obrazku dla konkretnego języka, będą załadowane obrazki dla domyślnego języka.
    </li>
    <li><strong>Wprowadź tytuł Slajdu</strong> - wpisz tytuł slajdu.</li>
    <li><strong>Wpisz URL Slajdu</strong> - podaj URL-adres slajdu.</li>
    <li><strong>Wprowadź podpis Slajdu</strong> - wprowadź tytuł slajdu.</li>
    <li><strong>Otwórz w nowym oknie</strong> - otworzyć w nowym oknie.</li>
    <li><strong>Status</strong> - włącz/wyłącz slajd.</li>
</ul>
<p>Po zachowaniu slajdu, pojawi się on w liście slajdów kategorii.</p>
<figure class="img-polaroid">
    <img src="img/tmcategoryimagesslider-4.png" alt=""/>
</figure>
<p>Aby dokonać zmian w slajdzie, wciśnij przycisk <strong>'Edytuj'</strong>.</p>
<p>Aby usunąć slajd, wciśnij przycisk <strong>'Usuń'</strong> znajdujący się poniżej przycisku <strong>'Edytuj'</strong>.
</p>
<div class="alert alert-info">
    <span>Slajdy można dodawać tylko do zachowanych kategorii.</span></div>