<p>Ten moduł pozwala Ci dodawać nieograniczoną liczbę wideo do jakiegokolwiek produktu w jakimkolwiek języku i
    sklepie.</p>
<h5>Instalowanie oraz Usuwanie</h5>
<p>Moduł instaluje się oraz usuwa się w taki sam sposób jak każdy inny moduł PrestaShop. Nie posiada strony konfiguracji
    oraz podtrzymuje zarządzanie kilkoma sklepami.</p>
<p>Po zakończeniu instalacji zobaczysz nową zakładkę na swojej stronie konfiguracji (Katalog -&gt; Produkt -&gt; wybierz
    dowolny produkt -&gt; TM-Wideo Produktów).</p>
<p>Aby dodać wideo do jakiegokolwiek produktu, należy wypełnić wymagane pola (pole linku jest obowiązkowe). Moduł jest
    wielojęzykowy, a więc będziesz musiał wypełnić pola dla wszystkich języków których się stosuje to wideo. Jeżeli pole
    linku dla pewnego języka nie jest wypełnione, wideo dla tego języka nie będzie dodane. Moduł jest sprojektowany dla
    pokazywania wideo z Youtube oraz Vimeo, musisz więc dodawać linki wyłącznie z tych zasobów. Po dokonaniu zmian,
    wciśnij przycisk <strong>'Zachowaj'</strong>/<strong>'Zachowaj i Zostań'</strong> aby ich zachować.</p>
