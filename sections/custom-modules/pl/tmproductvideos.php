<h6>Przykłady linków</h6>
<p>Dla poprawnego odtwarzania wideo linki muszą mieć następujący format:</p>
<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Youtube</dt>
            <dd><code>https://www.youtube.com/embed/K6VcTyaym90</code></dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Vimeo</dt>
            <dd><code>http://player.vimeo.com/video/66140585</code></dd>
        </dl>
    </li>
</ul>
<p>Jeżeli produkt posiada wideo powiązane z bieżącym językiem, zobaczysz go w liście wideo poniżej.</p>
<p>Aby zachować, usunąć lub zedytować listę nie musisz wciskać przycisk <strong>'Zachowaj'</strong>/<strong>'Zachowaj i
        Zostań'</strong>. Skorzystaj z przycisków znajdujących się w blokach wideo. Aby zmienić porządek wideo,
    skorzystaj z metody przeciągnia i upuszczenia (drag-and-drop) żeby zmienić ich pozycję. Aby włączyć/wyłaczyć wideo,
    skorzystaj z przycisków obok wideo. Aby zedytować wideo, wypełnij pola wymaganymi danymi oraz wciśnij przycisk
    <strong>'Aktualizuj'</strong>.</p>
<p>Aby usunąć wideo z listy, wciśnij przycisk <strong>'Usuń wideo'</strong>.</p>
<p>W trakcie przełączania języków wyświetlana będzie tylko lista wideo powiązanych z obecnie wybranym językiem.</p>
<p>Aby oglądać wideo w powiększonym formacie, wciśnij przycisk <strong>'Powiększ'</strong> poniżej tego wideo.</p>
