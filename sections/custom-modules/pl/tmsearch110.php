<ul class="marked-list">
    <li><strong>Włącz Wyszukiwanie Ajax </strong>- uaktywnia wyszukiwanie na żywo w liście rozwijanej.</li>
    <li><strong>Włącz Błyskawiczne Wyszukiwanie</strong> - uaktywnia błyskawiczne wyszukiwanie.</li>
    <li><strong>Minimalna długość pytania</strong> - minimalna liczba symboli (znaków) potrzebna dla wyświetlania
        wyników.
    </li>
    <li><strong>Liczba pokazywanych wyników</strong> - liczba produktów pokazywanych w sekcji wyników (liczba produktów
        na stronie w trybie "strony" lub "sterowanie").
    </li>
    <li><strong>Wyświetlaj przycisk "Pokaż Wszystkie" </strong>- pokazuje przycisk 'Pokaż wszystkie' poniżej w sekcji
        wyników; jeżeli go wcisnąć, pokazane będą wszystkie odnalezione wyniki.
    </li>
    <li><strong>Pokaż strony</strong> - dzieli wyniki na strony (zależnie od wartości pola 'Liczba pokazywanych
        wyników') oraz wyświetla linki prowadzące do nich.
    </li>
    <li><strong>Wyświetlaj sterowanie</strong> - pokazuje przyciski Poprzedni/Następny.</li>
    <li><strong>Położenie sterowania</strong> - zaczepka/zaczepki, dla pokazywania sterowania (dostępne opcje na
        górze/na dole/w obu miejscach).
    </li>
    <li><strong>Wyróźnij wynik zapytania</strong> - wyróźnia tekst odpowiadający zapytaniu wyszukiwania.</li>
    <li><strong>Pokazuj obrazek w wyszukiwaniu Ajax </strong>- pokazuje zdjęcia produktu w sekcji wyników.</li>
    <li><strong>Pokazuj opis w wyszukiwaniu Ajax </strong>- pokazuje opis produktu w sekcji wyników.</li>
    <li><strong>Pokazuj ceny w wyszukiwaniu Ajax </strong>- pokazuje ceny produktu w sekcji wyników.</li>
    <li><strong>Pokazuj referencje w wyszukiwaniu Ajax </strong>- pokazuje referencje produktu w sekcji wyników.</li>
    <li><strong>Pokazuj wytwórcę w wyszukiwaniu Ajax </strong>- pokazuje wytwórcę produktu w sekcji wyników.</li>
    <li><strong>Pokazuj dostawców w wyszukiwaniu Ajax </strong>- pokazuje dostawców produktu w sekcji wyników.</li>
</ul>
