<h5>Dodawanie modułu Paralaksy Mediaplików</h5>
<ul class="index-list">
    <li>
        <p>Należy obowiązkowo dodać ID albo klasę w treści Twojej strony HTML. Możesz również skorzystać z
            niepowtarzalnego znacznika; ten element nie powinien się powtarzać.</p>
    </li>
    <li>
        <p>Stwórz obrazek wymaganego rozmiaru (zaleca się stworzyć obrazek większego rozmiaru, aniż blok w którym dodany
            będzie ten efekt). W wypadku jeżeli chcesz dodać plik wideo, będziesz musiał zadbać o to, żeby został on
            przygotowany w trzech formatach: .mp4, .webm oraz .ogv, a także dodać obrazek podglądu tego samego rozmiaru
            (.png, .jpg oraz .gif)</p>
    </li>
    <li>
        <p>Wciśnij przycisk <strong>'Dodaj pojemnik'</strong> w <strong>'Dodaj nowy blok'</strong>.</p>
        <figure class="img-polaroid">
            <img src="img/parallax-1.png" alt=""/>
        </figure>
    </li>
    <li>
        <p>Na ekranie, który się pojawi, wpisz wszystkie wymagane ustawienia.</p>
        <h6>
            <strong>Ustawienia:</strong></h6>
        <ul class="index-list">
            <li>
                <dl class="inline-term">
                    <dt>Selektor</dt>
                    <dd>element w którym będzie dodawany efekt (id albo niepowtarzalny znacznik).</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Typ Paralaksy</dt>
                    <dd>typ efektu który będzie wykorzystywany (obrazek albo wideo).</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Mediapliki</dt>
                    <dd>prześlij wszystkie wymagalne pliki.</dd>
                </dl>
            </li>
            <li>
                <p>Wciśnij przycisk <strong>'Zachowaj'</strong>.</p>
            </li>
        </ul>
        <figure class="img-polaroid">
            <img src="img/parallax-2.png" alt=""/>
        </figure>
    </li>
</ul>
