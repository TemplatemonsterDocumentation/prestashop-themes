<h3>Moduł Paralaksa Mediaplików</h3>
<h4>Czym jest efekt paralaksy?</h4>
<p>Przewijanie z efektem paralaksy jest specjalną metodą przewijania w grafice komputerowej kiedy obrazki w tle posuwają
    się przez kamerę wolniej aniżeli obrazki na pierwszym planie, tworząc tym samym iluzję głębi w grze wideo 2D oraz
    wzmacniając efekt zanurzenia. Projektanci Web zaczęli wdrażać przewijanie z efektem paralaksy od 2011 roku,
    wykorzystując HTML5 oraz CSS3. Witryny internetowe mające tło z efektem paralaksy zaczynają być coraz bardziej
    popularną strategią rozwoju; zwolennicy twierdzą że jest to prosty sposób na ogarnięcie płynności środowiska
    internetowego. Ponadto, zwolennicy wykorzystują tło z efektem paralaksy jako narzędzie pozwalające skuteczniej
    angażować użytkowników i doskonalić ogólne doświadczenie korzystania z witryny internetowej. Przewijanie z efektem
    paralaksy pozwala zapewnić dynamiczną, interaktywną całość wrażeń użytkownikom z możliwością udoskonalić estetykę
    wizualną.</p>
<p>Ten moduł pozwoli dodać efekt paralaksy do Twojej strony internetowej bez dodatkowych specjalnych umiejętności oraz
    wysiłku. Można dodać obrazki do wideo w tle.</p>
