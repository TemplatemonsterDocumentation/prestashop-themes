<h3>TM Olark Chat</h3>

<p>Module to display Olark Chat on your store front-end.</p>

<h5>Module install/uninstall</h5>

<p>This module can be installed/uninstalled as any other PrestaShop module.</p>

<h5>Module settings</h5>

<p>After module has been installed you can configure it on the module configuration
    page.</p>

<p>In order to configure Olark Chat, which you can see on the live demo of the template,
    you need to register on the <a href="http://www.olark.com/" target="_blank"
                                   title="Olark Registration">official website</a> and
    to get the ID for your website. Input your ID to the field "Olark site ID" and set
    module under "Live mode". Now save settings by clicking <strong>Save</strong>
    button.</p>

