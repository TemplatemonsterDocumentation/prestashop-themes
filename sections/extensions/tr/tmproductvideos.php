<h6>Examples of links</h6>

<p>To display video properly, the links should have the following format:</p>

<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Youtube</dt>
            <dd><code>https://www.youtube.com/v/K6VcTyaym90</code></dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Vimeo</dt>
            <dd><code>http://player.vimeo.com/video/66140585</code></dd>
        </dl>
    </li>
</ul>
<p>If the product has video related to this language, you will see the list of videos below.</p>

<p>To save, delete or edit the list, there is no need to click Save/Save and Stay button. Use the buttons located in the
    video blocks. To change the order of videos, use drag-and-drop method to change their position. To enable/disable
    the video, use the button next to video. To edit the videos, fill in the fields with required data and click Update
    button.</p>

<p>To remove the video from the list, click Remove video button.</p>

<p>While switching languages, only the list of videos related to the language will show up.</p>

<p>To view the video in zoomed format, click Zoom button under the video you want.</p>


