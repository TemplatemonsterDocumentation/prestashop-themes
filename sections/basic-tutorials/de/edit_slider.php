<h3>Wie man Änderungen an dem Slider vornimmt</h3>
<p>Finden Sie vor allem das Modul <strong>Image slider for your homepage</strong> in der <strong>Modulliste</strong> (Modules list) Menü-Tab <strong>Module und Dienste</strong> (Modules and services) Ihres  PrestaShop Shops (1) (er befindet sich in dem Bereich Module <strong>Frontend Eigenschaften</strong> (Front Office Features)). Danach drücken Sie auf den Button 'Einstellen' (2), um dieses Modul zu ändern.
</p>
<figure class="img-polaroid">
    <img src="img/12-slider-1.png" alt=""/>
</figure>
<p>In dem DropDown Fenster können Sie Werte der Slider-Grundeigenschaften wie  maximale Bildbreite, Umschaltgeschwindigkeit, Pause zwischen zwei Slides festlegen und automatische Wiedergabe einstellen. <br> Nachdem Sie die Änderungen vorgenommen haben, drücken Sie auf den Button 'Speichern' (1).</p>
<figure class="img-polaroid">
    <img src="img/12-slider-2.png" alt=""/>
</figure>
<p>Um ein neues Slide hinzufügen, finden Sie den Button '+' in der oberen rechten Ecke in dem Bereich Slide-Liste (1) und drücken Sie auf ihn. <br> Wenn Sie eines der bestehenden Slides ändern möchten, drücken Sie auf den Button 'Bearbeiten' (2).</p>
<figure class="img-polaroid">
    <img src="img/12-slider-3.png" alt=""/>
</figure>
<p>In dem Bereich 'Information über das Slide' füllen Sie alle Pflichtfelder aus und drücken Sie auf den Button 'Speichern' (1).</p>
<figure class="img-polaroid">
    <img src="img/12-slider-4.png" alt=""/>
</figure>
