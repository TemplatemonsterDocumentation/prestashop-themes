<h3>Wie man Informationen über Ihren Online-Shops ändert</h3>
<p>Öffnen Sie das PrestaShop Control Panel und finden Sie den Tab <strong>Präferenzen > Kontakte des Shops</strong> (Preferences > Store Contacts).</p>
<p>Zuerst sehen Sie die Liste des Shops in dem Bereich <strong>Kontakte des Shops</strong> (Store Contacts). Hier können Sie alle Änderungen an jedem Shop vornehmen, sie aktivieren/deaktivieren, hinzufügen/löschen.</p>
<figure class="img-polaroid">
    <img src="img/10-shop_info-1.png" alt=""/>
</figure>
<h4>Shop-Map</h4>
<p>Unter der Shop-Liste gibt es den Bereich <strong>Parameter</strong> (Parameters). Hier können Grundeinstellungen des Shop-Map ändern. Ändern Sie sie nach Ihren Bedürfnissen und drücken Sie auf den Button 'Speichern' (1).</p>
<figure class="img-polaroid">
    <img src="img/10-shop_info-2.png" alt=""/>
</figure>
<h4>Wie man einen Shopnamen ändert</h4>
<p>Im Bereich <strong>Kontaktdaten</strong> (Contact Details) können Sie Basisinformationen über Ihren Shop – seinen Namen (1) und andere Kontaktdaten ändern. Vergessen Sie nicht auf den Button 'Speichern' (2) zu drücken, nachdem Sie die Änderungen vorgenommenen haben.</p>
<figure class="img-polaroid">
    <img src="img/10-shop_info-3.png" alt=""/>
</figure>
<p>Um die Informationenen eines bestimmten Shops zu ändern, wählen Sie den Shop aus, an dem Sie die Änderungen vornehmen möchten. Finden Sie den Bereich <strong>Kontakte des Shops</strong> (Store Contacts) und drücken Sie auf den Button 'Ändern' (1).</p>
<figure class="img-polaroid">
    <img src="img/10-shop_info-4.png" alt=""/>
</figure>
<p>Hier können Sie alle Kontaktinformationen Ihres Shops (wie Namen, Adresse, Status, Bild, Arbeitszeit usw.) ändern.<br>
Nachdem Sie die Änderungen vorgenommen haben, drücken Sie auf den Button 'Speichern' (1).</p>
