<h3>Wie man mit dem Modul Theme Configurator arbeitet</h3>
<p>Das jeweilige Tutorial zeigt, wie man mit dem Modul Theme Configurator in Ihrer Prestashop 1.6 Vorlage arbeitet.</p>
<p>Dieses Modul spielt eine wichtige Rolle in Ihrer Vorlage. Vor allem ermöglicht es Ihnen, einige Eigenschaften (Buttons «In den sozialen Netzwerken teilen», das obere Banner, Logos der Zahlungssysteme  usw.) zu aktivieren oder zu deaktivieren. Darüber hinasu gibt es Ihnen die Möglichtkeit ganz einfach Bilder zur Startseite hinzuzufügen/zu ändern.</p>
<ul class="index-list">
	<li>
		<span>Loggen Sie sich ins Prestashop Control Panel ein und finden Sie den Tab <strong>Module und Dienste</strong> (Modules and services) (1) -> <strong>Module und Dienste</strong> (Modules and services) (2). </span>
		<figure class="img-polaroid">
    		<img src="img/13-configurator-1.png" alt=""/>
		</figure>
	</li>
	<li>
		<span>Danach finden Sie das Modul 'Theme Configurator' in der  <strong>Modulliste</strong> (Modules list) (1). Es wird in der rechten Spalte dargestellt. Wenn dieses Modul noch nicht installiert ist, drücken Sie auf den Button 'Installieren'. <br>
		Nachdem Sie das Modul installiert haben, wird der Button 'Installieren' durch den Button 'Einstellen' (2) ersetzt. Drücken Sie auf diesen Button, um die Einstellungen zu ändern.</span>
		<figure class="img-polaroid">
    		<img src="img/13-configurator-2.png" alt=""/>
		</figure>
	</li>
	<li>
		<span>Auf der Seite Einstellungen sehen Sie verschiedene Blöcke, die Sie aktivieren oder deaktivieren können, indem Sie auf den Buttons 'Ja' oder 'Nein' (1) drücken. Drücken Sie auf den Button 'Einstellen' (2), um die Einstellungen eines Blocks zu ändern:</span>
		<figure class="img-polaroid">
    		<img src="img/13-configurator-3.png" alt=""/>
		</figure>
		<ul class="marked-list">
			<li>
				<span><strong>Links zu Shop Profile in den sozialen Netzwerken darstellen</strong> (Display links to your store's social accounts): geben Sie die Zeichen der Links ein, die Sie in diesem Block darstellen möchten und drücken Sie auf den Button 'Speichern' (1), um die vorgenommenen Änderungen zu speichern.</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-4.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Ihre Kontaktdaten darstellen</strong> (Display links to your store's social accounts): geben Sie den Namen, Adresse, Telefonnummer und E-Mail-Adresse des Unternehmens in die entsprechenden Felder ein und drücken Sie auf den Button 'Speichern' (1).</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-5.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Buttons «In den sozialen Netzwerken teilen» auf den Produktseiten darstellen</strong> (Display social sharing buttons on the product's page): stellt Buttons Facebook, Twitter, Google, Pinterest auf den Produktseiten dar.</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-6.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Den Block Facebook auf der Starseite darstellen</strong> (Display the Facebook block on the home page): stellt den Block 'Folgen Sie uns auf Facebook' dar.</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-7.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Den benutzerfreundlichen CMS Informationsblock</strong> darstellen: Sie können einen oder einige Informationsblöcke für jede Sprache Ihrer Website hinzufügen. Auf der Seite Einstellungen drücken Sie '+' rechts oben (1), um einen neuen benutzerdefinierten CMS Block hinzuzufügen. Geben Sie einen Text ins Textfeld ein und drücken Sie auf den Button 'Speichern'. Oder drücken Sie auf den Button 'Bearbeiten' (2), um die Änderungen an einem von den Blöcken vorzunehmen.</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-8.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Das Schnellansicht-Fenster auf der Startseite und den Kategorieseiten darstellen</strong> (Display quick view window on homepage and category pages )</span>
			</li>
			<li>
				<span><strong>Kategorien als Produktlisten anstatt der Standarddarstellung als Grid darstellen</strong> (Display categories as a list of products instead of the default grid-based display)</span>
			</li>
			<li>
				<span><strong>Das obere Banner darstellen</strong> (Display top banner): Drücken Sie auf den Button 'Einstellen'. In 'Einstellungen' können Sie ein anderes Banner (1) hinzufügen, seinen Link oder seine Beschreibung ändern. Nach der Bearbeitung drücken Sie auf den Button 'Speichern' (2).</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-9.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Logos der verfügbaren Zahlungssysteme darstellen</strong> (Display logos of available payment methods): ermöglicht es Ihnen, Bilder hochzuladen und einen Namen/Link für die Logos der Zahlungssysteme auf den Produktseiten hinzuzufügen. Drücken Sie auf den Button 'Datei hinzufügen' (1), um die Datei des Logos des Zahlungssystems hochzuladen oder geben Sie den Link zum Bild ins Feld 'Link zum Bild' (2) ein. Danach drücken Sie auf den Button 'Speichern' (3).</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-10.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Live Configurator darstellen</strong> (Display Live Configurator): Nur Sie können Live Configurator auf der Startseite Ihrer Website sehen, wenn ihn aktiviert ist.</span>
			</li>
			<li>
				<span><strong>Unterkategorien darstellen</strong> (Display subcategories)</span>
			</li>
			<li>
				<span>Scrollen Sie nach unten zum Bereich <strong>ändern/löschen/html Inhaltselemente hinzufügen</strong> (change/remove/add html content items). Standardelemente, die zu diesem Modul gehören, können nur dann gelöscht werden, wenn das Modul Theme Configurator deaktiviert ist. Als Alternative können Sie die Veröffentlichung der Elemente abbrechen, indem Sie das Häkchen neben 'Aktiv' auf der Seite Elementeinstellungen entfernen. Geben Sie den Namen des Elementes ein, fügen Sie ihn zum Hook an einer von den vorgeschlagenen Stellen (home, top, left, right, footer) hinzu, drücken Sie auf den Button 'Übersicht' um ein Bild hochzuladen, geben Sie manuell die Werte Breite/Hohe des Bildes in Pixel ein, legen Sie die Links fest, zu denen dieses Element verlinkt wird und fügen Sie Ihren Inhalt zum Feld 'HTML' hinzu. Danach drücken Sie auf den Button 'Speichern'. </span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-11.png" alt=""/>
				</figure>
				<span>Wenn Sie eine mehrsprachige Website haben, drücken Sie auf den Tab mit der entsprechenden Sprache und nehmen Sie die Änderungen vor. Drücken Sie auf den Button 'Element hinzufügen', um ein neues Element hinzuzufügen.</span>
			</li>
		</ul>
	</li>
	<li>
		<span>Aktualisieren Sie Ihre Website, um die vorgenommenen Änderungen zu sehen.</span>
	</li>
</ul>
