<h3>Come modificare il dispositivo di scorrimento</h3>
<p>Innanzitutto trovare la <strong>Slider immagine per la tua home page</strong> modulo in <strong>Moduli lista</strong> dei <strong>Moduli e servizi</strong> Scheda menu del tuo negozio PrestaShop (1) (si trova nella finestra di dialogo <strong>Front Office Caratteristiche</strong> moduli sezione). Quindi premere il pulsante 'Configura' (2) per modificare il modulo.</p>
<figure class="img-polaroid">
    <img src="img/12-slider-1.png" alt=""/>
</figure>
<p>Sullo schermo che appare successivamente sarai in grado di impostare le proprietà principali del cursore come la larghezza massima delle immagini, la velocità delle diapositive, il ritardo tra due diapositive e l'opzione di riproduzione automatica. <br> Dopo averlo cambiato, premi il pulsante 'Salva' (1).</p>
<figure class="img-polaroid">
    <img src="img/12-slider-2.png" alt=""/>
</figure>
<p>Per aggiungere una nuova diapositiva, trovare il pulsante '+' nell'angolo in alto a destra della sezione Elenchi di diapositive (1) e fare clic su di esso. <br> Se si desidera modificare la diapositiva esistente, fare clic sul pulsante 'Modifica' (2).</p>
<figure class="img-polaroid">
    <img src="img/12-slider-3.png" alt=""/>
</figure>
<p>L'ulteriore processo è abbastanza semplice. Nella sezione "Informazioni sulla diapositiva" compilare tutti i campi obbligatori e salvare (1).</p>
