<h3>Come gestire il modulo Theme Configurator</h3>
<p>Questo tutorial ti mostrerà come gestire il modulo Theme Configurator sul modello Prestashop 1.6.</p>
<p>Il modulo gestisce molte cose sul tuo tema. Principalmente, ti permette di accendere e spegnere alcune funzioni (pulsanti di social sharing, banner top, loghi di pagamento, ecc.) E forse più importante, ti consente di aggiungere / modificare facilmente le immagini sulla tua home page.</p>
<ul class="index-list">
	<li>
		<span>Accedi al tuo pannello di amministrazione di Prestashop e vai a <strong>Moduli e Servizi</strong> (1) -> <strong>Moduli e Servizi</strong> (2). </span>
		<figure class="img-polaroid">
    		<img src="img/13-configurator-1.png" alt=""/>
		</figure>
	</li>
	<li>
		<span>Quindi trovare il modulo 'Theme Configurator' in <strong>Moduli lista</strong> (1). Apparirà nella colonna di destra. Se non è ancora installato, fai clic sul relativo pulsante 'Installa'.<br>
		Una volta installato il modulo, il pulsante 'Installa' verrà cambiato nel pulsante 'Configura' (2) che è necessario fare clic per configurare le impostazioni.</span>
		<figure class="img-polaroid">
    		<img src="img/13-configurator-2.png" alt=""/>
		</figure>
	</li>
	<li>
		<span>Nella pagina di configurazione delle impostazioni, è possibile visualizzare diversi blocchi che puoi accendere e spegnere facendo clic sui pulsanti "Sì" e "No" (1). Fai clic su "Configura" (2) per configurare le impostazioni di un blocco particolare:</span>
		<figure class="img-polaroid">
    		<img src="img/13-configurator-3.png" alt=""/>
		</figure>
		<ul class="marked-list">
			<li>
				<span><strong>Mostra link ai conti sociali del tuo store</strong>: Immettere indirizzi web per le icone che si desidera visualizzare in questo blocco e fare clic su "Salva" (1) al termine.</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-4.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Visualizza le tue informazioni di contatto</strong>: Inserisci il tuo nome, indirizzo, numero di telefono e email nei campi corrispondenti e fai clic su "Salva" (1).</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-5.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Mostra i pulsanti di social sharing nella pagina del prodotto</strong>: displays Facebook, Twitter, Google, Pinterest buttons on your product pages.</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-6.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Visualizzare il blocco Facebook nella home page</strong>: visualizza il blocco "Seguici su Facebook".</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-7.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Visualizzare il blocco informazioni CMS personalizzato</strong>: ti permette di aggiungere uno o più blocchi di informazioni per ogni lingua del tuo sito. Nella pagina di configurazione, fai clic su "+" in alto a destra (1) per aggiungere un nuovo blocco CMS personalizzato. Inserisci il testo nel campo di testo e fai clic su 'Salva'. Oppure fare clic su 'Modifica' (2) per modificare qualsiasi blocco.</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-8.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Visualizzare la finestra di visualizzazione rapida sulle pagine di homepage e di categoria</strong></span>
			</li>
			<li>
				<span><strong>Visualizzare le categorie come un elenco di prodotti anziché il display basato su griglia predefinito</strong></span>
			</li>
			<li>
				<span><strong>Visualizza banner superiore</strong>: Fai clic sul pulsante 'Configura'. In 'Impostazioni' puoi scegliere un altro banner (1), modificarne il collegamento e la descrizione. Dopo aver modificato cliccare su 'Salva' (2).</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-9.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Visualizzare i loghi dei metodi di pagamento disponibili</strong>: ti consente di caricare l'immagine e di aggiungere un titolo / link per i loghi di pagamento nella pagina del prodotto. Fai clic su "Aggiungi file" (1) per caricare il proprio file per il logo di pagamento o inserire un collegamento con l'immagine nel campo "Collegamento immagine" (2). Quindi fare clic sul pulsante "Salva" (3).</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-10.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Display Live Configurator</strong>: solo tu puoi vedere sulla pagina anteriore del tuo sito quando è abilitata.</span>
			</li>
			<li>
				<span><strong>Visualizza sottocategorie</strong></span>
			</li>
			<li>
				<span>Scorri verso il basso <strong>change/remove/add html contenuto elementi</strong>. Gli elementi predefiniti che vengono originariamente con questo modulo possono essere rimossi solo quando il modulo Theme Configurator è disabilitato oppure è possibile disattivarli semplicemente deselezionando 'Active' nella pagina di configurazione dell'elemento. Inserisci il titolo dell'articolo, lo aggancia in una delle posizioni suggerite (home, top, left, right, footer), fai clic su "Browse" per caricare un'immagine, immetti manualmente i valori di larghezza / altezza dell'immagine in pixel, specifica l'URL Questa voce verrà indicata e inserire i tuoi contenuti nel file HTML. Quindi fare clic su "Salva".</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-11.png" alt=""/>
				</figure>
				<span>Se si esegue un sito multilingue, fare clic sulla linguetta corrispondente e ripetere le stesse correzioni. Fai clic sul pulsante "Aggiungi elemento" per aggiungere un nuovo elemento.</span>
			</li>
		</ul>
	</li>
	<li>
		<span>Apri l'estremità anteriore del tuo sito per vedere le modifiche.</span>
	</li>
</ul>