<h3>Come modificare informazioni sul tuo negozio</h3>
<p>Apri il pannello di amministrazione di PrestaShop e vai a <strong>Preferences > Store Contacts</strong>.</p>
<p>In primo luogo vedrai l'elenco dei negozi in <strong>Store Contacts</strong> sezione. Qui puoi abilitare/disabilitare, aggiungere/eliminare o modificare ogni negozio.</p>
<figure class="img-polaroid">
    <img src="img/10-shop_info-1.png" alt=""/>
</figure>
<h4>Memorizza mappatura</h4>
<p>Sotto l'elenco dei negozi c'è un <strong>Parameters</strong> Sezione dove è possibile modificare le impostazioni principali relative alla mappatura del tuo negozio. Cambiarli secondo il tuo bisogna premere il pulsante 'Salva' (1).</p>
<figure class="img-polaroid">
    <img src="img/10-shop_info-2.png" alt=""/>
</figure>
<h4>Come modificare il nome del negozio</h4>
<p>In <strong>Contact Details</strong> blocco puoi cambiare le informazioni principali sul tuo negozio - il suo nome (1) e altri dettagli di contatto. Non dimenticare di premere il pulsante 'Salva' (2) dopo la modifica.</p>
<figure class="img-polaroid">
    <img src="img/10-shop_info-3.png" alt=""/>
</figure>
<p>Per modificare le informazioni su un determinato archivio, scegliere l'archivio in cui si desidera modificare <strong>Store Contacts</strong> E fai clic su 'Modifica' (1).</p>
<figure class="img-polaroid">
    <img src="img/10-shop_info-4.png" alt=""/>
</figure>
<p>Qui puoi modificare tutte le informazioni relative ai tuoi contatti del negozio (ad esempio il nome del negozio, l'indirizzo, lo stato, l'immagine, il programma di lavoro ecc.)<br>
Al termine, fai clic sul pulsante 'Salva' (1).</p>
