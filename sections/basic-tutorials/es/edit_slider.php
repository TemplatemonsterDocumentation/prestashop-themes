<h3>Cómo editar el slider</h3>
<p>Primero encuentre el módulo <strong>Image slider for your homepage</strong> en <strong>la lista de Módulos</strong> de la pestaña  del menú <strong>Módulos y Servicios</strong> su tienda de PrestaShop (1) (se encuentra en la sección de modulos <strong>Front Office Features</strong>). Then press the 'Configure' button (2) to edit the module.
</p>
<figure class="img-polaroid">
    <img src="img/12-slider-1.png" alt=""/>
</figure>
<p>En la pantalla que aparece a continuación, podrá establecer las propiedades principales de su slider como el ancho máximo de imágenes, la velocidad de cambio de diapositivas, el retardo entre dos diapositivas y la opción de reproducción automática. <br> Después de cambiarlo, presione el botón "Guardar" (1).</p>
<figure class="img-polaroid">
    <img src="img/12-slider-2.png" alt=""/>
</figure>
<p>Para agregar una nueva diapositiva, busque el botón '+' en la esquina superior derecha de la sección de la lista de diapositivas (1) y haga clic en él. <br> Si desea editar la diapositiva existente, haga clic en el botón "Editar" (2).</p>
<figure class="img-polaroid">
    <img src="img/12-slider-3.png" alt=""/>
</figure>
<p>Proceso adicional es bastante simple. En la sección 'Información de la Diapositiva' llene todos los campos requeridos y pulse 'Guardar' (1).</p>
