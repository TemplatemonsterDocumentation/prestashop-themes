<h3>Cómo administrar el módulo Theme Configurator</h3>
<p>Este tutorial le mostrará cómo administrar el módulo Theme Configurator en su plantilla de Prestashop 1.6.</p>
<p>El módulo maneja muchas cosas en su tema. Principalmente, le permite activar y desactivar algunas funciones (botones de compartimiento social, banner superior, logotipos de pago, etc.), y quizás lo más importante, le permite añadir/modificar fácilmente imágenes en su página de inicio.</p>
<ul class="index-list">
	<li>
		<span>Inicie sesión en el panel de administración de Prestashop y vaya a <strong>Módulos y servicios</strong> (1) -> <strong>Módulos y servicios</strong> (2). </span>
		<figure class="img-polaroid">
    		<img src="img/13-configurator-1.png" alt=""/>
		</figure>
	</li>
	<li>
		<span>A continuación, busque el módulo 'Theme Configurator' en <strong>lista de Módulos</strong> (1). Aparecerá en la columna de la derecha. Si todavía no está instalado, haga clic en el botón 'Instalar'. <br>
		Una vez instalado el módulo, el botón 'Instalar' cambiará al botón 'Configurar' (2) que debe hacer clic para configurar la configuración.</span>
		<figure class="img-polaroid">
    		<img src="img/13-configurator-2.png" alt=""/>
		</figure>
	</li>
	<li>
		<span>En la página de ajustes de configuración, puede ver diferentes bloques que puede activar y desactivar haciendo clic en los botones 'Sí' y 'No' (1). Haga clic en 'Configurar' (2) para configurar los ajustes de un bloque en particular:</span>
		<figure class="img-polaroid">
    		<img src="img/13-configurator-3.png" alt=""/>
		</figure>
		<ul class="marked-list">
			<li>
				<span><strong>Mostrar vínculos a las cuentas sociales de su tienda</strong>: introduzca direcciones web para los iconos que desea mostrar en este bloque y haga clic en "Guardar" (1) cuando haya terminado.</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-4.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Mostrar su información de contacto</strong>: introduzca el nombre de su empresa, dirección, número de teléfono y correo electrónico en los campos correspondientes y haga clic en "Guardar" (1).</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-5.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Mostrar los botones de compartimiento social en la página del producto</strong>: muestra botones Facebook, Twitter, Google, Pinterest en las página de su producto.</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-6.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Mostrar el bloque de Facebook en la página principal</strong>: muestra bloque de 'Follow us on Facebook'.</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-7.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Mostrar el bloque de información de CMS personalizado</strong>: le permite agregar uno y más bloques de información para cada idioma de su sitio. En la página de configuración, haga clic en '+' en la parte superior derecha (1) para agregar un nuevo bloque CMS personalizado. Introduzca su texto en el campo de texto y haga clic en "Guardar". O bien, haga clic en "Editar" (2) para cambiar cualquier bloque.</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-8.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Mostrar ventana de vista rápida en la página principal y en las páginas de categoría</strong></span>
			</li>
			<li>
				<span><strong>Mostrar categorías como una lista de productos en lugar de la pantalla predeterminada basada en la cuadrícula</strong></span>
			</li>
			<li>
				<span><strong>Mostrar el banner superior</strong>: Haga clic en el botón "Configurar". En 'Configuración' puede elegir otro banner (1), cambiar su enlace y descripción. Después de la edición, haga clic en el botón "Guardar" (2).</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-9.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Mostrar logotipos de métodos de pago disponibles</strong>: le permite cargar su imagen y agregar un título/enlace para los logotipos de pago en la página del producto. Haga clic en "Agregar archivo" (1) para cargar su propio archivo para el logotipo de pago o inserte un vínculo con la imagen en el campo "Enlace de imagen" (2). A continuación, haga clic en el botón "Guardar" (3).</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-10.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Mostrar Live Configurator</strong>: sólo se puede ver en la página principal de su sitio cuando está activado.</span>
			</li>
			<li>
				<span><strong>Mostrar subcategorías</strong></span>
			</li>
			<li>
				<span>Desplácese hacia abajo hasta los elementos de contenido <strong>change/remove/add html </strong>. Los elementos predeterminados que vienen inicialmente con este módulo sólo se pueden quitar cuando el módulo del Configurador de temas está desactivado o simplemente puede no publicarlos desmarcando "Activo" en la página de configuración del elemento. Introduzca el título del artículo, conéctelo a una de las posiciones sugeridas (inicio, arriba, izquierda, derecha, pie de página), haga clic en "Examinar" para cargar una imagen, ingrese manualmente los valores de ancho/alto de imagen en píxeles, especifique la URL Este punto será señalado e ingrese su contenido en el 'HTML' archivado. A continuación, haga clic en "Guardar". </span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-11.png" alt=""/>
				</figure>
				<span>Si está ejecutando un sitio multilingüe, haga clic en la pestaña de idioma correspondiente y repita los mismos ajustes para ello. Haga clic en el botón "Agregar elemento" para agregar un nuevo elemento.</span>
			</li>
		</ul>
	</li>
	<li>
		<span>Abra la parte frontal de su sitio para ver los cambios.</span>
	</li>
</ul>