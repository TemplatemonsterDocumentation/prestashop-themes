<h3>Cómo cambiar la información de su tienda</h3>
<p>Abra el panel de Administración de PrestaShop y vaya a <strong>Preferencias > Contactos de la Tienda</strong>.</p>
<p>Primero verá la lista de tiendas en secciones <strong>Contactos de la Tienda</strong>. Aquí puede activar/desactivar, agregar/eliminar o editar cada tienda.</p>
<figure class="img-polaroid">
    <img src="img/10-shop_info-1.png" alt=""/>
</figure>
<h4>Mapeo de tiendas</h4>
<p>Debajo de la lista de tiendas hay una sección <strong>Parámetros</strong> donde puede editar los ajustes principales relacionados con la asignación de su tienda. Cambiarlos de acuerdo a sus necesidades y pulse el botón "Guardar" (1).</p>
<figure class="img-polaroid">
    <img src="img/10-shop_info-2.png" alt=""/>
</figure>
<h4>Cómo cambiar el nombre de la tienda</h4>
<p>En el bloque <strong>Detalles de Contacto</strong> usted puede cambiar la información principal sobre su tienda - su nombre (1) y otros detalles de contacto. No olvide presionar el botón 'Guardar' (2) después de editar.</p>
<figure class="img-polaroid">
    <img src="img/10-shop_info-3.png" alt=""/>
</figure>
<p>Para editar la información de una tienda en particular, elija la tienda que desea editar en <strong>Contactos de la Tienda</strong>  haga clic en "Editar" (1).</p>
<figure class="img-polaroid">
    <img src="img/10-shop_info-4.png" alt=""/>
</figure>
<p>Aquí puede editar toda la información relativa a los contactos de su tienda (como el nombre de la tienda, la dirección, el estado, la imagen, el horario de trabajo, etc.)<br>
Cuando termine, haga clic en el botón "Guardar" (1).</p>
