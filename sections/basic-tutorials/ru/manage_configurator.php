<h3>Как работать с модулем Theme Configurator</h3>
<p>Из этого туториала Вы узнаете, как работать с модулем Theme Configurator в вашем шаблоне Prestashop 1.6.</p>
<p>Этот модуль отвечает за многое в вашем шаблоне. В основном, он позволяет Вам включить или выключить некоторые свойства (кнопки «Поделиться в социальных сетях», верхний баннер, логотипы платёжных систем и т.д.), и, что, возможно, более важно, он позволяет Вам легко добавить/изменить изображения на вашей Домашней странице.</p>
<ul class="index-list">
	<li>
		<span>Войдите в вашу админ панель Prestashop и перейдите на вкладку <strong>Модули и службы</strong> (Modules and services) (1) -> <strong>Модули и службы</strong> (Modules and services) (2). </span>
		<figure class="img-polaroid">
    		<img src="img/13-configurator-1.png" alt=""/>
		</figure>
	</li>
	<li>
		<span>Затем, найдите модуль 'Theme Configurator' в <strong>Списке модулей</strong> (Modules list) (1). Он отобразится в правой колонке. Если он ещё не установлен, нажмите на кнопку 'Установить'. <br>
		После установки модуля, кнопка 'Установить' заменится кнопкой 'Настроить' (2), на которую нужно нажать, для того чтобы изменить настройки.</span>
		<figure class="img-polaroid">
    		<img src="img/13-configurator-2.png" alt=""/>
		</figure>
	</li>
	<li>
		<span>На странице изменения настроек, Вы увидите разные блоки, которые Вы можете включать или отключать, нажимая на кнопки 'Да' или 'Нет' (1). Нажмите на кнопку 'Настроить' (2), для того чтобы изменить настройки того или иного блока:</span>
		<figure class="img-polaroid">
    		<img src="img/13-configurator-3.png" alt=""/>
		</figure>
		<ul class="marked-list">
			<li>
				<span><strong>Отображать ссылки на профили магазина в социальных сетях</strong> (Display links to your store's social accounts): введите ссылки значков, которые Вы хотите отобразить в этом блоке, и нажмите на кнопку 'Сохранить' (1), закончив внесение изменений.</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-4.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Отображать ваши контактные данные</strong> (Display links to your store's social accounts): введите название компании, адрес, телефонный номер и адрес электронной почты в соответствующих полях и нажмите на кнопку 'Сохранить' (1).</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-5.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Отображать кнопки «Поделиться в социальных сетях» на страницах товаров</strong> (Display social sharing buttons on the product's page): отображает кнопки Facebook, Twitter, Google, Pinterest на страницах товаров.</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-6.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Отображать блок Facebook на домашней странице</strong> (Display the Facebook block on the home page): отображает блок 'Подпишитесь на нас в Facebook'.</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-7.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Отображать пользовательский информационный CMS блок</strong>: позволяет Вам добавить один или несколько информационных блоков для каждого языка вашего сайта. На странице настройки, нажмите на '+' справа вверху (1), для того чтобы добавить новый пользовательский CMS блок. Введите ваш текст в текстовое поле и нажмите на кнопку 'Сохранить'. Или нажмите на кнопку 'Редактировать' (2), для того чтобы внести изменения в один из блоков.</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-8.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Отобразить окно быстрого просмотра на домашней странице и на страницах категорий</strong> (Display quick view window on homepage and category pages )</span>
			</li>
			<li>
				<span><strong>Отображать категории как списки товаров вместо стандартного отображения в виде сетки</strong> (Display categories as a list of products instead of the default grid-based display)</span>
			</li>
			<li>
				<span><strong>Отображать верхний баннер</strong> (Display top banner): Нажмите на кнопку 'Настроить'. В 'Настройках' Вы можете выбрать другой баннер (1), изменить его ссылку и описание. Закончив редактирование, нажмите на кнопку 'Сохранить' (2).</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-9.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Отображать логотипы доступных способов оплаты</strong> (Display logos of available payment methods): позволяет загрузить изображения и добавить название/ссылку для логотипов способов оплаты на страницах товаров. Нажмите на кнопку 'Добавить файл' (1), для того чтобы загрузить ваш файл логотипа способа оплаты или введите ссылку на изображение в поле 'Ссылка изображения' (2). Затем, нажмите на кнопку 'Сохранить' (3).</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-10.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Отобразить Live Configurator</strong> (Display Live Configurator): Только Вы можете видеть Live Configurator на домашней странице вашего сайта, если он включен.</span>
			</li>
			<li>
				<span><strong>Отображать подкатегории</strong> (Display subcategories)</span>
			</li>
			<li>
				<span>Прокрутите вниз к разделу <strong>изменить/удалить/добавить элементы содержимого html</strong> (change/remove/add html content items). Стандартные элементы, которые входят в состав данного модуля, могут быть удалены, только когда модуль Theme Configurator отключен. В качестве альтернативы, Вы можете отменить публикацию элементов, убрав галочку рядом с 'Активен' на странице настройки элемента. Введите название элемента, добавьте его в хук одной из предложенных позиций (home, top, left, right, footer), нажмите на 'Обзор' для того чтобы загрузить изображение, вручную введите значения ширины/высоты изображения в пикселях, укажите ссылки, на которые будет указывать этот элемент, и добавьте ваше содержимое в поле 'HTML'. Затем, нажмите на кнопку 'Сохранить'. </span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-11.png" alt=""/>
				</figure>
				<span>Если у Вас многоязычный сайт, нажмите на вкладку с соответствующим языком и внесите для неё те же изменения. Нажмите на кнопку 'Добавить элемент' для добавления нового элемента.</span>
			</li>
		</ul>
	</li>
	<li>
		<span>Откройте фронтенд вашего сайта для просмотра изменений.</span>
	</li>
</ul>
