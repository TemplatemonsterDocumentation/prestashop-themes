<h3>Jak edytować slajder</h3>
<p>Najpierw znajdź moduł <strong>Slajder zdjęć dla Twojej strony domowej</strong> w <strong>Liście modułów</strong> w zakładce menu <strong>Moduły i Serwisy</strong> Twojego sklepu PrestaShop (1) (Znajduje się w sekcji modułów <strong>Funkcje Biura Obsługi Klienta</strong> ). Następnie wciśnij przycisk 'Skonfiguruj' (2) aby edytować moduł. </p>
<figure class="img-polaroid">
<img src="img/12-slider-1.png" alt=""/>
</figure>
<p>Na ekranie, który się pojawi, będziesz mógł ustawić główne właściwości Twojego slajderu, na przykład maksymalna szerokość zdjęć, szybkość przesuwania slajdów, opóźnienie między dwoma slajdami oraz funkcję autoodtwarzania. <br> Po tym, jak je zmienisz, wciśnij przycisk 'Zachowaj' (1).</p>
<figure class="img-polaroid">
<img src="img/12-slider-2.png" alt=""/>
</figure>
<p>Aby dodać nowy slajd, znajdź przycisk '+' w górnym prawym kącie sekcji lista Slajdów (1) i wciśnij go.<br> Jeżeli chcesz edytować istniejący slajd, wciśnij przycisk 'Edytuj' (2).</p>
<figure class="img-polaroid">
<img src="img/12-slider-3.png" alt=""/>
</figure>
<p>Ciąg dalszy procesu jest dość prosty. W sekcji 'Informacja o slajdzie' wypełnij wszystkie wymagane pola i 'Zachowaj' (1) je.</p>
