<h3>Jak zmienić informację o swoim sklepie</h3>
<p>Otwórz panel administracyjny PrestaShop i przejdź do <strong>Preferencje > Dane Kontaktowe Sklepu</strong>.</p>
<p>Najpierw zobaczysz listę sklepów w sekcji <strong>Dane Kontaktowe Sklepu</strong> . Tutaj możesz włączać lub wyłączać, dodawać i usuwać, jak również edytować ustawienia każdego sklepu.</p>
<figure class="img-polaroid">
<img src="img/10-shop_info-1.png" alt=""/>
</figure>
<h4>Mapowanie sklepu</h4>
<p>Pod listą sklepów zobaczysz sekcję <strong>Parametery</strong>, w której zmożesz edytować najważniejsze ustawienia dotyczące mapowania Twojego sklepu. Zmieniaj ich wedle własnych potrzeb i wciśnij przycisk 'Zachowaj' (1).</p>
<figure class="img-polaroid">
<img src="img/10-shop_info-2.png" alt=""/>
</figure>
<h4>Jak zmieniać nazwę sklepu</h4>
<p>W bloku <strong>Dane Kontaktowe</strong> możesz zmieniać najważniejszą informację o swoim sklepie: jego nazwę (1) jak również inne szczegóły kontaktowe. Nie zapomnij o tym, że po ukończeniu edytowania trzeba wcisnąć przycisk 'Zachowaj' (2) .</p>
<figure class="img-polaroid">
<img src="img/10-shop_info-3.png" alt=""/>
</figure>
<p>Aby przystąpić do edytowania jednego wybranego sklepu, wybierz sklep, który chcesz edytować w sekcji <strong>Dane Kontaktowe Sklepu</strong> i kliknij 'Edytuj' (1).</p>
<figure class="img-polaroid">
<img src="img/10-shop_info-4.png" alt=""/>
</figure>
<p>Tutaj możesz edytować wszystkie informacje dotyczące danych kontaktowych Twojego sklepu (m.in. nazwę sklepu, adres, status, zdjęcie, godziny pracy itp.)<br>
Po zakończeniu wciśnij przycisk 'Zachowaj' (1).</p>
