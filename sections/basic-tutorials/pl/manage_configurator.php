<h3>Jak zarządzać modułem Konfigurator Motywów</h3>
<p>Niniejszy samouczek pomoże Ci zarządzać modułem Konfigurator Motywów w Twoim szablonie Prestashop 1.6.</p>
<p>Ten modul obsługuje wiele różnych rzeczy w Twoim motywie. Głównie pozwala Ci włączać i wyłączać niektóre funkcje
    (przyciski dzielenia się za pomocą sieci społecznościowych, logotypy płatności etc), co więcej, pozwala Ci łatwo
    dodawać i usuwać zdjęcia na swojej stronie domowej.</p>
<ul class="index-list">
    <li>
        <span>Zaloguj się do swojego panelu administracyjnego PrestaShop i przejdź do <strong>Moduły i Serwisy</strong> (1) -> <strong>Moduły i Serwisy</strong> (2). </span>
        <figure class="img-polaroid">
            <img src="img/13-configurator-1.png" alt=""/>
        </figure>
    </li>
    <li>
<span>Następnie znajdź moduł 'Konfigurator Motywów' w <strong>Liście Modułów</strong> (1). Pojawi się on w prawej kolumnie. Jeżeli nie zainstalowano go jeszcze, wciśnij przy nim przycisk 'Instaluj'. <br>
Po zakończeniu instalacji modułu, przycisk 'instaluj' zmieni się w przycisk 'Skonfiguruj' (2) z którego należy skorzystać w razie potrzeby skonfigurowania ustawień.</span>
        <figure class="img-polaroid">
            <img src="img/13-configurator-2.png" alt=""/>
        </figure>
    </li>
    <li>
        <span>Na stronie konfiguracji ustawień, możesz zobaczyć różne bloki które można włączać i wyłączać wciskając przyciski 'Tak' oraz 'Nie' (1). Kliknij 'Konfiguruj' (2) aby skonfigurować ustawienia wybranego bloku:</span>
        <figure class="img-polaroid">
            <img src="img/13-configurator-3.png" alt=""/>
        </figure>
        <ul class="marked-list">
            <li>
                <span><strong>Wyświetlaj linki prowadzące do kont Twojego sklepu w sieciach społecznościowych</strong>: wprowadź adresy stron internetowych dla ikonek które chciałbyś umieścić w tym bloku i kliknij 'Zachowaj' (1) po wprowadzeniu wszystkich danych.</span>
                <figure class="img-polaroid">
                    <img src="img/13-configurator-4.png" alt=""/>
                </figure>
            </li>
            <li>
                <span><strong>Wyświetlaj swoją informację kontaktową</strong>: wprowadź nazwę swojej kompanii, numer telefonu i adres poczty elektronicznej w odpowiednich polach, po czym klinkij 'Zachowaj' (1).</span>
                <figure class="img-polaroid">
                    <img src="img/13-configurator-5.png" alt=""/>
                </figure>
            </li>
            <li>
                <span><strong>Wyświetlaj przyciski stron w sieciach społecznościowych na stronie swojego produktu</strong>: wyświetla przyciski Facebook, Twitter, Google, oraz Pinterest na twoich stronach produktów.</span>
                <figure class="img-polaroid">
                    <img src="img/13-configurator-6.png" alt=""/>
                </figure>
            </li>
            <li>
                <span><strong>Wyświetla blok Facebook na twojej stronie domowej</strong>: wyświetla blok 'Zapraszamy do czytania nas na Facebook'.</span>
                <figure class="img-polaroid">
                    <img src="img/13-configurator-7.png" alt=""/>
                </figure>
            </li>
            <li>
                <span><strong>Wyświetla dowolny blok informacyjny z systemu CMS</strong>: pozwala Ci dodawać jeden lub więcej bloków dla każdego języka na Twojej witrynie internetowej. Na stronie konfiguracji, wciśnij przycisk '+' w górnej prawej części (1) aby dodać nowy specjalny blok CMS. Wprowadź swój tekst w polu tekstowym i kliknij 'Zachowaj'. Albo wciśnij przycisk 'Edytuj' (2) żeby zmienić dowolny blok.</span>
                <figure class="img-polaroid">
                    <img src="img/13-configurator-8.png" alt=""/>
                </figure>
            </li>
            <li>
                <span><strong>Wyświetla okno szybkiego podglądu na stronie domowej i stronach kategorii</strong></span>
            </li>
            <li>
                <span><strong>Wyświetla kategorie jako listę produktów zamiast domyślnego widoku w trybie siatki</strong></span>
            </li>
            <li>
                <span><strong>Wyświetlaj baner górny</strong>: Wciśnij przycisk 'Skonfiguruj'. W 'Ustawieniach' możesz wybrać inny baner (1), zmienić treść linku oraz jego opis. Po zakończeniu edytowania wciśnij przycisk 'Zachowaj' (2).</span>
                <figure class="img-polaroid">
                    <img src="img/13-configurator-9.png" alt=""/>
                </figure>
            </li>
            <li>
                <span><strong>Pokazuje logotypy dostępnych metod płatności</strong>: pozwala Ci przesłać swój obrazek zdjęcie oraz dodać tytuł/link do logotypów opcji dokonania płatności na stronie produktu. Kliknij 'Dodaj plik' (1) żeby przesłać swój własny plik dla logotypu płatności lub wstaw link ze zdjęcięm w pole 'Link z Obrazkiem' (2). Następnie wciśnij przycisk 'Zachowaj' (3).</span>
                <figure class="img-polaroid">
                    <img src="img/13-configurator-10.png" alt=""/>
                </figure>
            </li>
            <li>
                <span><strong>Wyświetlaj Konfigurator Na Żywo</strong>: po włączeniu, tylko Ty możesz widzić go na Stronie Głównej swojej witryny internetowej.</span>
            </li>
            <li>
                <span><strong>Wyświetlaj Podkategorie</strong></span></li>
            <li>
                <span>Przewiń stronę do dołu żeby <strong>zmieniać/usuwać/dodawać elementy treści html </strong>. Domyślne elementy które początkowo były dostarczone razem z niniejszym modułem można usunąć tylko jeżeli moduł Konfiguratora Motywów jest wyłączony; opcjonalnie można ich po prostu deaktywować ich publikację poprzez odznaczenie opcji 'Aktywny' na stronie konfiguracji elementu. Wprowadź nazwę elementu, umieść go w jednej z proponowanych pozycji (strona domowa, w części górnej, dolnej, prawej, lewej), klinkij 'Przegląd' aby przesłać obrazek, ręcznie wprowadź wartości szerokości/wysokości w pikselach, podaj URL do którego będzie prowadził ten element i wprowadź swoją treść w polu 'HTML'. Następnie kliknij 'Zachowaj'. </span>
                <figure class="img-polaroid">
                    <img src="img/13-configurator-11.png" alt=""/>
                </figure>
                <span>Jeżeli prowadzisz stronę wielojęzyczną, kliknij zakładkę odpowiedniego języka i wprowadź analogiczne dostosowania dla wybranego języka. Kliknij 'Dodaj element' żeby dodać nowy element.</span>
            </li>
        </ul>
    </li>
    <li>
        <span>Otwórz swoją witrynę po stronie użytkownika żeby zobaczyć zmiany.</span></li>
</ul>