<h3>Comment modifier le slider</h3>
<p>Trouvez d'abord le module <strong>Slider D'images pour Page D'accueil</strong> dans la <strong>Liste des Modules</strong> de l'onglet du menu  
<strong>Modules et services</strong> de votre magasin PrestaShop(1) (il se trouve dans la section des modules <strong>Fonctionnalite de front-office</strong>). 
Ensuite, appuyez sur le bouton 'Configurer' (2) pour editer le module.
</p>
<figure class="img-polaroid">
    <img src="img/12-slider-1.png" alt=""/>
</figure>
<p>Sur l'ecran qui apparait ensuite, vous pourrez definir les proprietes principales de votre slider comme une largeur maximale des images, la vitesse de changement des diapositives, le delai entre deux diapositives et l'option de lecture automatique.
<br> Apres l'avoir change, appuyez sur le bouton 'Enregistrer' (1).</p>
<figure class="img-polaroid">
    <img src="img/12-slider-2.png" alt=""/>
</figure>
<p>Pour ajouter une nouvelle diapositive, trouvez le bouton '+' dans le coin superieur droit de la section Liste des diapositives (1) et appuyez-le.<br> 
Si vous souhaitez modifier la diapositive existante, cliquez sur le bouton 'Modifier'(2).</p>
<figure class="img-polaroid">
    <img src="img/12-slider-3.png" alt=""/>
</figure>
<p>Le processus supplementaire est assez simple. Dans la section «Informations sur la diapositive» remplissez tous les champs requis et 'Enregistrer' (1) les modifications.</p>
