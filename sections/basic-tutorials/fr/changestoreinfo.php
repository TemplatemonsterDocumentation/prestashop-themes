<h3>Comment changer d'information sur votre magasin</h3>
<p>Ouvrez le panneau d'administration de PrestaShop et accédez à <strong Préférences>Contacts de Magasin</strong>.</p>
<p>D'abord, vous verrez la liste des magasins dans la section <strong>Contacts de Magasin</strong>. Ici, vous pouvez activer/désactiver, ajouter/supprimer ou modifier chaque magasin.<p>
<figure class="img-polaroid">
    <img src="img/10-shop_info-1.png" alt=""/>
</figure>
<h4>Mappage du Magasin</h4>
<p>Au-dessous de la liste des magasins, il y a une section <strong>Paramètres</strong> où vous pouvez modifier les paramètres principaux concernant le mappage de votre magasin. Modifiez les paramètres selon vos besoins et appuyez sur le bouton 'Enregistrer' (1).</p>
<figure class="img-polaroid">
    <img src="img/10-shop_info-2.png" alt=""/>
</figure>
<h4>Comment changer le nom du magasin</h4>
<p>Dans le bloc <strong>Détails du Contact</strong> vous pouvez modifier les informations principales sur votre magasin - son nom (1) et autres coordonnées. N'oubliez pas d'appuyer sur le bouton 'Enregistrer' (2) après l'édition.</p>
<figure class="img-polaroid">
    <img src="img/10-shop_info-3.png" alt=""/>
</figure>
<p>Pour modifier l'information sur un magasin en particulier, choisissez le magasin dans lequel vous souhaitez modifier <strong>Contacts de Magasin</strong> et cliquez sur "Éditer" (1).</p>
<figure class="img-polaroid">
    <img src="img/10-shop_info-4.png" alt=""/>
</figure>
<p>Ici vous pouvez éditer toutes les informations concernant les contacts de votre magasin (tels que le nom de la boutique, l'adresse, l'état , l'image, le calendrier de travail, etc.)<br>
Après cela, cliquez sur le bouton 'Enregistrer' (1).</p>
