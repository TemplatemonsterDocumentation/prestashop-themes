<h3>Comment gérer le module de Configuration de Thème</h3>
<p>Ce tutoriel vous montrera comment gérer le module Configuration de Thème dans votre modèle Prestashop 1.6.</p>
<p>Le module gère beaucoup de choses de votre thème. Principalement, il vous permet d'activer ou de désactiver certaines fonctionnalités (boutons de partage social, bannière supérieure, logos de paiement, etc.) et, peut-être, plus important il vous permet d'ajouter / modifier facilement des images sur votre page d'accueil.</p>
<ul class="index-list">
	<li>
		<span>Connectez-vous su panneau d'administration Prestashop et accédez à  <strong>Modules et services</strong> (1) -> <strong>Modules et services</strong> (2). </span>
		<figure class="img-polaroid">
    		<img src="img/13-configurator-1.png" alt=""/>
		</figure>
	</li>
	<li>
		<span>Ensuite, trouvez le module 'Configuration de Thème' dans la <strong>Liste des modules</strong> (1). Il apparaîtra dans la colonne de droite. S'il n'est pas encore installé, appuyez sur le bouton 'Installer'.<br>
		Une fois le module est installé, le bouton 'Installer' changera en 'Configurer' (2) Dont vous devez appuyer pour configurer les paramètres.</span>
		<figure class="img-polaroid">
    		<img src="img/13-configurator-2.png" alt=""/>
		</figure>
	</li>
	<li>
		<span>Sur la page de configuration du module, vous verez les différents blocs que vous pouvez activer ou désactiver en cliquant sur les boutons 'Oui' et 'Non' (1). 
		Cliquez sur 'Configurer' (2) pour configurer les paramètres d'un bloc particulier:</span>
		<figure class="img-polaroid">
    		<img src="img/13-configurator-3.png" alt=""/>
		</figure>
		<ul class="marked-list">
			<li>
				<span><strong>Afficher des comptes de réseau social</strong>: entrez les adresses Web pour les icônes que vous souhaitez afficher dans ce bloc et cliquez sur 'Enregistrer' (1).</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-4.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Afficher vos coordonnées</strong>: Entrez le nom, l'adresse, le numéro de téléphone et le courriel de votre entreprise dans les champs correspondants et cliquez sur 'Enregistrer' (1).</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-5.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Afficher les boutons de partage social sur la page du produit</strong>: affiche les boutons Facebook, Twitter, Google, Pinterest sur les pages de vos produits.</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-6.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Afficher le bloc Facebook sur la page d'accueil</strong>: affiche le bloc 'Suivez-nous sur Facebook'.</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-7.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Afficher le bloc CMS d'informations personnalisé</strong>: vous permet d'ajouter un bloc d'informations pour chaque langue sur votre site.
				Sur la page de configuration, cliquez sur '+' en haut à droite (1) pour ajouter un nouveau bloc CMS personnalisé. Entrez votre texte dans le champ de texte et cliquez sur 'Enregistrer'. Ou appuyez sur 'Modifier'(2) changer un bloc.</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-8.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Afficher le vue rapide sur la page d'accueil et les pages de catégorie</strong></span>
			</li>
			<li>
				<span><strong>Afficher les catégories comme la liste des produits au lieu de l'affichage par défaut basé sur une grille</span>
			</li>
			<li>
				<span><strong>Afficher la bannière supérieure</strong>: Cliquez sur le bouton 'Configurer'. Dans 'Paramètres', vous pouvez choisir une autre bannière (1), modifiez son lien et sa description. Après l'édition, appuyez sur le bouton 'Enregistrer' (2).</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-9.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Afficher les logos des méthodes de paiement disponibles</strong>: vous permet de télécharger votre image et d'ajouter un titre/lien pour les logos de paiement sur la page du produit. Appuyez sur 'Ajouter un fichier' (1) pour télécharger votre propre fichier pour le logo de paiement ou insérez un lien avec l'image dans le champ 'Lien d'image' (2). Ensuite, cliquez sur le bouton 'Enregistrer' (3).</span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-10.png" alt=""/>
				</figure>
			</li>
			<li>
				<span><strong>Afficher le Configurateur en direct</strong>: vous seul pouvez le voir sur la page avant de votre site lorsqu'il est activé.</span>
			</li>
			<li>
				<span><strong>Afficher les sous-catégories</strong></span>
			</li>
			<li>
				<span>Défilez jusqu'à <strong>modifier/supprimer/ajouter des éléments de contenu html</strong>. Les éléments par défaut, qui viennent avec ce module, peuvent être retirés lorsque le module Configurateur Theme est désactivé ou vous pouvez simplement les dépublier en décochant 'Active' sur la page de configuration de l'élément. Entrez le titre de l'élément,  accrochez-le à l'une des positions suggérées (accueil, haut, gauche, droite, bas de page), appuyez sur 'Parcourir' pour télécharger une image, entrez manuellement les valeurs de largeur/hauteur de l'image en pixels, spécifiez l'URL pour laquelle cet élément sera lié et entrez votre contenu dans le champ 'HTML'. Ensuite, cliquez sur 'Enregistrer'. </span>
				<figure class="img-polaroid">
    				<img src="img/13-configurator-11.png" alt=""/>
				</figure>
				<span>Si vous utilisez un site multilingue, cliquez sur l'onglet de langue correspondant et répétez les mêmes ajustements pour une autre langue
				Cliquez sur le bouton 'Ajouter un élément' pour ajouter un nouvel élément.</span>
			</li>
		</ul>
	</li>
	<li>
		<span>Actualisez et visitez votre site pour voir les modifications.</span>
	</li>
</ul>